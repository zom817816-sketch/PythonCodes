"""
给定两个整数 n 和 k，返回范围 [1, n] 中所有可能的 k 个数的组合。
你可以按 任何顺序 返回答案。

解题思路：

本题是回溯算法的经典入门题目，属于「组合问题」。

核心思想：
  使用 DFS（深度优先搜索）遍历所有可能的组合。
  通过「选择-递归-回溯」三步走，探索所有解空间。

算法流程：
  1. 从 1 开始，依次尝试将每个数字加入当前路径
  2. 递归进入下一层，从当前数字的下一个位置开始选择（避免重复）
  3. 当路径长度达到 k 时，记录一个有效组合
  4. 回溯：撤销当前选择，尝试其他分支

示例（n=4, k=2）的搜索树：

        1
       /|\
      2 3 4        → 得到 [1,2], [1,3], [1,4]
     / \
    3   4          → 得到 [2,3], [2,4]
   /
  4                → 得到 [3,4]

回溯算法三要素：

1. 递归参数：start 表示当前从哪个数字开始选（保证不重复选）
2. 终止条件：当 path 长度等于 k 时，找到一个有效组合
3. 单层逻辑：遍历可选数字，选择→递归→回溯

关键注意点：

1. res.append(path[:]) 必须使用切片复制！
   - path 是引用，如果不复制，回溯后 path 会变空
   - path[:] 创建新列表，保存当前状态

2. backtracking(i + 1) 不是 backtracking(start + 1)！
   - i 是当前选择的数字，下一层从 i+1 开始
   - 保证每个数字只用一次，且组合内有序

3. 剪枝优化（可选）：
   - 当剩余数字不够选出 k 个数时，提前终止
   - 例如：n=4, k=4，选完 1 后，只能从 2 开始选，否则凑不够 4 个

复杂度分析：

时间复杂度：O(C(n, k) × k)
  - C(n, k) = n! / (k! × (n-k)!) 是组合数，表示搜索树叶子节点数量
  - 每个叶子节点需要 O(k) 时间复制 path[:]
  - 最坏情况（k ≈ n/2）接近 O(2^n / √n)

空间复杂度：O(k)（不计结果存储）
  - 递归调用栈深度最多为 k
  - path 数组同时只存一条路径，大小为 k
  - 结果数组 res 需要 O(C(n, k) × k)，但不计入算法辅助空间
"""

from typing import List


def combine(n: int, k: int) -> List[List[int]]:
    """
    主函数：返回从 [1, n] 中选 k 个数的所有组合

    参数：
        n: 可选数字范围的上界（范围是 1 到 n）
        k: 每个组合需要的数字个数

    返回：
        所有可能的 k 数组合的列表
    """
    res = []  # 存储所有满足条件的组合结果
    path = []  # 存储当前正在构建的组合路径（单条分支）

    def backtracking(start: int):
        """
        回溯函数：递归搜索所有组合

        参数：
            start: 当前层可以选择的数字起始位置
        从 start 开始选择，保证组合内数字递增，避免重复
        """
        #  终止条件
        # 当 path 中已有 k 个数字，说明找到一个有效组合
        if len(path) == k:
            # 注意：必须用 path[:] 切片复制！
            # 如果直接 append(path)，res 中保存的是 path 的引用
            # 后续 path.pop() 会导致 res 中的结果也被清空
            res.append(path[:])
            return

        #  单层搜索逻辑
        # 遍历从 start 到 n 的所有可选数字
        for i in range(start, n + 1):
            # 1. 处理节点：将当前数字加入路径
            path.append(i)

            # 2. 递归搜索：进入下一层
            #    下一层从 i+1 开始，避免重复使用 i
            backtracking(i + 1)

            # 3. 回溯操作：撤销当前选择，恢复现场
            #    这一步非常重要！保证尝试其他分支时状态正确
            path.pop()

        #  剪枝优化版（可选）
        # 已经选择的元素个数：len(path);
        # 所需需要的元素个数为: k - len(path);
        # 列表中剩余元素（n-i） >= 所需需要的元素个数（k - len(path)）
        # 在集合n中至多要从该起始位置 : i <= n - (k - len(path)) + 1，开始遍历
        #
        # for i in range(start, n - (k - len(path)) + 2):
        #     path.append(i)
        #     backtracking(i + 1)
        #     path.pop()

    # 从数字 1 开始搜索
    backtracking(1)
    return res
