"""
找出所有相加之和为 n 的 k 个数的组合，且满足下列条件：

只使用数字1到9
每个数字 最多使用一次
返回 所有可能的有效组合的列表 。该列表不能包含相同的组合两次，组合可以以任何顺序返回。
"""

from typing import List


def combinationSum3(k: int, n: int) -> List[List[int]]:
    """
    找出所有相加之和为 n 的 k 个数的组合

    解题思路：

    本题是「77. 组合」的进阶版本（01_Combinations.py）。
    在组合问题的基础上增加了「和为 n」的约束条件。

    与组合问题的异同：
    - 相同点：都是从 1~9 中选 k 个数，每个数只能用一次
    - 不同点：本题要求选出的 k 个数之和必须等于 n

    核心思想：
      使用回溯（DFS）遍历所有可能的 k 数组合。
      在到达终止条件（选满 k 个）时，额外判断和是否为 n。

    算法流程：
      1. 从 1 开始，依次尝试将每个数字加入当前路径
      2. 递归进入下一层，从当前数字的下一个位置开始选择
      3. 当路径长度达到 k 时，检查路径和是否等于 n
      4. 若等于 n，记录结果；否则回溯，尝试其他分支

    回溯算法三要素：

      1. 递归参数：
        - start: 当前层从哪个数字开始选（避免重复组合）
        - current_sum: 当前累加和（作为参数传递，避免每次求和）

      2. 终止条件：
        - 当 path 长度达到 k 时，检查 sum == n
        - 额外剪枝：如果当前和已经超过 n，提前终止（因为数字都是正数）

      3. 单层逻辑：
        - 遍历可选数字，执行 选择 → 递归 → 回溯

    关键注意点：

      1. 剪枝优化可做两层：
        - 组合剪枝：剩余数字不够凑 k 个时终止（同 01_Combinations）
        - 和剪枝：当前和超过 n 时终止（因为数字为正，后续只会更大）

      2. 和作为参数传递：
        - 每次递归时传入更新后的和，避免在终止条件中重复计算 sum(path)
        - 时间复杂度从 O(C(9,k) × k) 降到 O(C(9,k))

      3. path[:] 切片复制：
        - 与组合问题一样，必须复制 path，否则回溯后结果会被清空

    复杂度分析：

      时间复杂度：O(C(9, k) × k)
        - 最坏情况下需要枚举所有 C(9,k) 个组合
        - 和剪枝可以大幅减少实际搜索量（n 较小时效果显著）

      空间复杂度：O(k)
        - 递归调用栈深度最多为 k
        - path 数组同时只存一条路径
    """
    res = []  # 存储所有满足条件的组合
    path = []  # 存储当前正在构建的组合路径

    def backtracking(start: int, current_sum: int):
        """
        回溯搜索所有满足和条件的组合

        参数：
            start: 当前层可以选择的数字起始位置
            current_sum: 当前路径中数字的累加和
        """
        #  剪枝：和已经超过 n
        # 因为数字全是正数，加上更大的数只会更大，没必要继续
        if current_sum > n:
            return

        #  终止条件：选够了 k 个数
        if len(path) == k:
            if current_sum == n:
                # 找到一个有效组合，切片复制保存
                res.append(path[:])
            return

        #  组合剪枝（可选优化）
        # 还需要 k - len(path) 个数
        # 最多只能从 9 - (k - len(path)) + 1 的位置开始选
        # 例如 k=3, len(path)=0, 最多从 9-3+1=7 开始
        max_start = 9 - (k - len(path)) + 1

        #  单层搜索逻辑
        for i in range(start, max_start + 1):
            # 1. 处理节点：选择当前数字
            path.append(i)
            current_sum += i

            # 2. 递归搜索下一层
            backtracking(i + 1, current_sum)

            # 3. 回溯操作：撤销选择，恢复现场
            current_sum -= i
            path.pop()

    # 从数字 1 开始搜索，初始和为 0
    backtracking(1, 0)
    return res
