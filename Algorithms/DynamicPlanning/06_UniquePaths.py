"""
不同路径 II（Unique Paths II）

题目描述：
一个机器人位于一个 m x n 网格的左上角（起始点在下图中标记为 "Start"）。
机器人每次只能向下或者向右移动一步。机器人试图达到网格的右下角（在下图中标记为 "Finish"）。
现在考虑网格中有障碍物的情况。网格中的障碍物和空位置分别用 1 和 0 来表示。
机器人的移动路径中不能包含任何有障碍物的方格。
问总共有多少条不同的路径？

注意：
    - m 和 n 的值均不超过 100
    - 障碍物用 1 表示，空位置用 0 表示
    - 测试用例保证答案小于等于 2 × 10^9

示例 1：
    输入：obstacleGrid = [[0,0,0],[0,1,0],[0,0,0]]
    输出：2
    解释：
        3x3 网格的正中间有一个障碍物。
        从左上角到右下角一共有 2 条不同的路径：
        1. 向右 -> 向右 -> 向下 -> 向下
        2. 向下 -> 向下 -> 向右 -> 向右

示例 2：
    输入：obstacleGrid = [[0,1],[0,0]]
    输出：1
    解释：
        2x2 网格的第一行第二列有一个障碍物。
        从左上角到右下角只有 1 条路径：
        1. 向下 -> 向右

示例 3：
    输入：obstacleGrid = [[1,0,0]]
    输出：0
    解释：
        起点位置有障碍物，无法移动。

示例 4：
    输入：obstacleGrid = [[0,0,0],[0,0,0],[0,0,1]]
    输出：0
    解释：
        终点位置有障碍物，无法到达。

解题思路总览：
───────────────────────────────────────────────────────────────────────
解法                          时间复杂度    空间复杂度    说明
───────────────────────────────────────────────────────────────────────
暴力递归                       O(2^(m+n))   O(m+n)      超时
记忆化搜索（Top-Down DP）       O(m × n)     O(m × n)    ⭐⭐
动态规划（Bottom-Up DP）         O(m × n)     O(m × n)    ⭐⭐⭐
动态规划（空间优化）             O(m × n)     O(n)        ⭐⭐⭐⭐ 最优
───────────────────────────────────────────────────────────────────────

核心思想（动态规划）：
───────────────────────────────────────────────────────────────────────
关键洞察：
1. 与"不同路径"问题类似，但要额外考虑障碍物：
   - 如果当前位置 (i, j) 有障碍物，则 dp[i][j] = 0（无法到达）
   - 如果当前位置 (i, j) 无障碍物，则 dp[i][j] = dp[i-1][j] + dp[i][j-1]
2. 初始化时，遇到障碍物就停止（后面的位置无法到达）

状态定义：
    dp[i][j] = 到达位置 (i, j) 的不同路径数

状态转移：
    如果 obstacleGrid[i][j] == 1：
        dp[i][j] = 0（有障碍物，无法到达）
    否则：
        dp[i][j] = dp[i-1][j] + dp[i][j-1]

初始条件：
    - 第一行：从左向右遍历，遇到障碍物后，后面的位置都为 0
    - 第一列：从上向下遍历，遇到障碍物后，下面的位置都为 0

特殊情况：
    - 如果起点 obstacleGrid[0][0] == 1，返回 0（无法出发）
    - 如果终点 obstacleGrid[m-1][n-1] == 1，返回 0（无法到达）

与"不同路径"的区别：
───────────────────────────────────────────────────────────────────────
1. 需要判断障碍物：遇到障碍物时，路径数为 0
2. 初始化时需要分段：遇到障碍物后，后面的位置都无法到达
3. 无法使用组合数学公式：障碍物破坏了路径的对称性
"""

from typing import List


# ══════════════════════════════════════════════════════════
# 解法一：暴力递归（Brute Force）
# ══════════════════════════════════════════════════════════


def uniquePathsWithObstacles_brute(obstacleGrid: List[List[int]]) -> int:
    """
    暴力递归解法（Brute Force）

    核心思路：
    ────────────────────────────────────────────────────────
    直接根据递推关系递归求解，但要额外判断障碍物：
        - 如果当前位置有障碍物，返回 0（无法到达）
        - 否则：paths(i, j) = paths(i-1, j) + paths(i, j-1)

    算法步骤：
    1. Base Case：
        a. 如果起点或终点有障碍物，返回 0
        b. 如果 i = 0 或 j = 0，返回 1（只有一条路径）
        c. 如果当前位置有障碍物，返回 0
    2. 递归求解：paths(i-1, j) + paths(i, j-1)

    时间复杂度：O(2^(m+n)) — 递归树呈指数级增长
    空间复杂度：O(m+n)   — 递归调用栈的最大深度

    问题分析：
    ────────────────────────────────────────────────────────
    与"不同路径"类似，但增加了障碍物判断。
    遇到障碍物时，直接返回 0，表示无法通过该位置。

    注意：这个解法在 m, n > 10 时就会明显变慢，仅用于理解问题。
    """
    m = len(obstacleGrid)
    n = len(obstacleGrid[0])

    # 特殊情况：起点或终点有障碍物
    if obstacleGrid[0][0] == 1 or obstacleGrid[m - 1][n - 1] == 1:
        return 0

    def dfs(i: int, j: int) -> int:
        """递归求解到达位置 (i, j) 的路径数"""
        # 如果当前位置有障碍物，返回 0
        if obstacleGrid[i][j] == 1:
            return 0

        # Base Case：起点位置
        if i == 0 and j == 0:
            return 1

        # 第一行：只能从左边来，但要检查左边是否有障碍物
        if i == 0:
            return dfs(0, j - 1)

        # 第一列：只能从上面来，但要检查上面是否有障碍物
        if j == 0:
            return dfs(i - 1, 0)

        # 递归求解：从上方或左方来
        return dfs(i - 1, j) + dfs(i, j - 1)

    return dfs(m - 1, n - 1)


# ══════════════════════════════════════════════════════════
# 解法二：记忆化搜索（Top-Down DP）
# ══════════════════════════════════════════════════════════


def uniquePathsWithObstacles_memo(obstacleGrid: List[List[int]]) -> int:
    """
    记忆化搜索解法（Top-Down DP）⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    在暴力递归的基础上，增加一个缓存（二维数组），
    记录已经计算过的子问题结果，避免重复计算。

    算法步骤：
    1. 创建缓存 memo，存储已计算的 paths(i, j) 值
    2. 递归时先查缓存：
        a. 如果缓存中有结果，直接返回（命中）
        b. 如果缓存中没有，计算后存入缓存，再返回
    3. Base Case：
        a. 如果起点或终点有障碍物，返回 0
        b. 如果当前位置有障碍物，返回 0
        c. 如果 i = 0 或 j = 0，返回 1

    时间复杂度：O(m × n) — 每个子问题只计算一次
    空间复杂度：O(m × n) — 缓存 + 递归调用栈

    图解示例：obstacleGrid = [[0,0,0],[0,1,0],[0,0,0]]
    ────────────────────────────────────────────────────────
    网格：
        [0, 0, 0]
        [0, 1, 0]  ← (1,1) 有障碍物
        [0, 0, 0]

    DP 表：
            j=0   j=1   j=2
        ────────────────────
    i=0    1     1     1
    i=1    1     0     1      ← dp[1][1] = 0（障碍物）
    i=2    1     1     2      ← dp[2][2] = dp[1][2] + dp[2][1] = 1 + 1 = 2

    结果：2 ✅

    状态定义：
    - memo[i][j] = 到达位置 (i, j) 的路径数

    状态转移：
    - 如果 obstacleGrid[i][j] == 1：memo[i][j] = 0
    - 否则：memo[i][j] = memo[i-1][j] + memo[i][j-1]
    """
    m = len(obstacleGrid)
    n = len(obstacleGrid[0])

    # 特殊情况：起点或终点有障碍物
    if obstacleGrid[0][0] == 1 or obstacleGrid[m - 1][n - 1] == 1:
        return 0

    # 创建缓存（二维数组）
    memo = [[-1 for _ in range(n)] for _ in range(m)]

    def dfs(i: int, j: int) -> int:
        """递归求解到达位置 (i, j) 的路径数"""
        # 如果当前位置有障碍物，返回 0
        if obstacleGrid[i][j] == 1:
            return 0

        # Base Case：起点位置
        if i == 0 and j == 0:
            return 1

        # 命中缓存，直接返回
        if memo[i][j] != -1:
            return memo[i][j]

        # 第一行：只能从左边来
        if i == 0:
            memo[i][j] = dfs(0, j - 1)
            return memo[i][j]

        # 第一列：只能从上面来
        if j == 0:
            memo[i][j] = dfs(i - 1, 0)
            return memo[i][j]

        # 计算并存入缓存
        memo[i][j] = dfs(i - 1, j) + dfs(i, j - 1)
        return memo[i][j]

    return dfs(m - 1, n - 1)


# ══════════════════════════════════════════════════════════
# 解法三：动态规划（Bottom-Up DP）
# ══════════════════════════════════════════════════════════


def uniquePathsWithObstacles_dp(obstacleGrid: List[List[int]]) -> int:
    """
    动态规划解法（Bottom-Up DP）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    从底向上计算：先初始化第一行和第一列，
    再按行或列依次填充 DP 表。
    需要额外处理障碍物的情况。

    算法步骤：
    1. 创建 DP 数组 dp，dp[i][j] 表示到达位置 (i, j) 的路径数
    2. 初始化第一行和第一列：
        a. 遇到障碍物后，后面的位置都为 0
    3. 按行填充：
        a. 如果当前位置有障碍物，dp[i][j] = 0
        b. 否则 dp[i][j] = dp[i-1][j] + dp[i][j-1]
    4. 返回 dp[m-1][n-1]

    时间复杂度：O(m × n) — 一次遍历
    空间复杂度：O(m × n) — DP 数组

    图解示例：obstacleGrid = [[0,0,0],[0,1,0],[0,0,0]]
    ────────────────────────────────────────────────────────
    网格：
        [0, 0, 0]
        [0, 1, 0]  ← (1,1) 有障碍物
        [0, 0, 0]

    DP 表填充过程：
    ────────────────────────────────────────────────────────
    初始化第一列：
        dp[0][0] = 1
        dp[1][0] = 1
        dp[2][0] = 1

    初始化第一行：
        dp[0][0] = 1
        dp[0][1] = 1
        dp[0][2] = 1

    填充第二行：
        dp[1][1] = 0（障碍物）
        dp[1][2] = dp[0][2] + dp[1][1] = 1 + 0 = 1

    填充第三行：
        dp[2][1] = dp[1][1] + dp[2][0] = 0 + 1 = 1
        dp[2][2] = dp[1][2] + dp[2][1] = 1 + 1 = 2

    结果：2 ✅

    状态定义：
    - dp[i][j] = 到达位置 (i, j) 的路径数

    状态转移：
    - 如果 obstacleGrid[i][j] == 1：dp[i][j] = 0
    - 否则：dp[i][j] = dp[i-1][j] + dp[i][j-1]

    初始条件：
    - 第一列：从上向下遍历，遇到障碍物后停止
    - 第一行：从左向右遍历，遇到障碍物后停止

    最终答案：
    - dp[m-1][n-1]（到达右下角的路径数）
    """
    m = len(obstacleGrid)
    n = len(obstacleGrid[0])

    # 特殊情况：起点或终点有障碍物
    if obstacleGrid[0][0] == 1 or obstacleGrid[m - 1][n - 1] == 1:
        return 0

    # 创建 DP 数组
    dp = [[0 for _ in range(n)] for _ in range(m)]

    # 初始化第一列
    for i in range(m):
        if obstacleGrid[i][0] == 1:
            break  # 遇到障碍物，后面的位置都无法到达
        dp[i][0] = 1

    # 初始化第一行
    for j in range(n):
        if obstacleGrid[0][j] == 1:
            break  # 遇到障碍物，后面的位置都无法到达
        dp[0][j] = 1

    # 从第二行第二列开始填充
    for i in range(1, m):
        for j in range(1, n):
            if obstacleGrid[i][j] == 1:
                dp[i][j] = 0  # 有障碍物，无法到达
            else:
                dp[i][j] = dp[i - 1][j] + dp[i][j - 1]

    return dp[m - 1][n - 1]


# ══════════════════════════════════════════════════════════
# 解法四：动态规划（空间优化版，推荐解）
# ══════════════════════════════════════════════════════════


def uniquePathsWithObstacles(obstacleGrid: List[List[int]]) -> int:
    """
    空间优化的动态规划解法 ⭐⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    观察状态转移方程：dp[i][j] = dp[i-1][j] + dp[i][j-1]
    计算第 i 行时，只需要第 i-1 行的数据和当前行已经计算的数据。
    可以用一维数组代替二维数组，空间复杂度降为 O(n)。

    算法步骤：
    1. 创建一维 DP 数组 dp，长度为 n
    2. 初始化第一行：
        a. 遇到障碍物后，后面的位置都为 0
    3. 外层循环遍历行，内层循环更新 dp[j]：
        a. 如果当前位置有障碍物，dp[j] = 0
        b. 否则 dp[j] = dp[j] + dp[j-1]
    4. 返回 dp[-1]

    时间复杂度：O(m × n) — 一次遍历
    空间复杂度：O(n) — 一维 DP 数组

    为什么可以用一维数组？
    ────────────────────────────────────────────────────────
    在计算 dp[i][j] 时：
        - dp[j]（更新前）= dp[i-1][j]（上一行的值）
        - dp[j-1]（更新后）= dp[i][j-1]（当前行左边的值）
    所以：dp[j] = dp[j] + dp[j-1]

    需要额外处理障碍物：
        - 如果 obstacleGrid[i][j] == 1，则 dp[j] = 0

    图解示例：obstacleGrid = [[0,0,0],[0,1,0],[0,0,0]]
    ────────────────────────────────────────────────────────
    初始化（第一行）：dp = [1, 1, 1]

    i=1（第二行）：
        obstacleGrid[1][0] = 0 → dp[0] = 1（不变）
        obstacleGrid[1][1] = 1 → dp[1] = 0（障碍物）
        obstacleGrid[1][2] = 0 → dp[2] = dp[2] + dp[1] = 1 + 0 = 1
        → dp = [1, 0, 1]

    i=2（第三行）：
        obstacleGrid[2][0] = 0 → dp[0] = 1（不变）
        obstacleGrid[2][1] = 0 → dp[1] = dp[1] + dp[0] = 0 + 1 = 1
        obstacleGrid[2][2] = 0 → dp[2] = dp[2] + dp[1] = 1 + 1 = 2
        → dp = [1, 1, 2]

    结果：2 ✅

    特殊处理：
    ────────────────────────────────────────────────────────
    1. 第一列的处理：每行开始时，如果 obstacleGrid[i][0] == 1，则 dp[0] = 0
    2. 障碍物的处理：如果 obstacleGrid[i][j] == 1，则 dp[j] = 0
    """
    m = len(obstacleGrid)
    n = len(obstacleGrid[0])

    # 特殊情况：起点或终点有障碍物
    if obstacleGrid[0][0] == 1 or obstacleGrid[m - 1][n - 1] == 1:
        return 0

    # 创建一维 DP 数组，初始化第一行
    dp = [1] * n

    # 初始化第一行：遇到障碍物后，后面的位置都为 0
    for j in range(n):
        if obstacleGrid[0][j] == 1:
            # 从障碍物位置开始，后面的位置都为 0
            for k in range(j, n):
                dp[k] = 0
            break

    # 外层循环遍历行（从第二行开始）
    for i in range(1, m):
        # 特殊处理：第一列有障碍物时，dp[0] = 0
        if obstacleGrid[i][0] == 1:
            dp[0] = 0

        # 内层循环遍历列（从第二列开始）
        for j in range(1, n):
            if obstacleGrid[i][j] == 1:
                dp[j] = 0  # 有障碍物，无法到达
            else:
                dp[j] = dp[j] + dp[j - 1]

    return dp[-1]


# ══════════════════════════════════════════════════════════
# 重要说明：算法的正确性证明
# ══════════════════════════════════════════════════════════
#
# 为什么动态规划解法是正确的？
# ────────────────────────────────────────────────────────
# 1. 最优子结构（Optimal Substructure）：
#    到达位置 (i, j) 的最后一步只有两种可能：
#        a) 从上方 (i-1, j) 向下移动一步
#        b) 从左方 (i, j-1) 向右移动一步
#    如果当前位置有障碍物，则无法到达，路径数为 0。
#    否则：dp[i][j] = dp[i-1][j] + dp[i][j-1]
#
# 2. 重叠子问题（Overlapping Subproblems）：
#    在暴力递归中，很多子问题会被重复计算，
#    用缓存或 DP 数组可以消除重复计算。
#
# 3. 无后效性（No Aftereffect）：
#    计算 dp[i][j] 时，只需要知道 dp[i-1][j] 和 dp[i][j-1]，
#    不需要知道是如何到达这些位置的。
#    这保证了状态转移的正确性。
#
# 为什么初始化时要分段处理？
# ────────────────────────────────────────────────────────
# 第一行和第一列只能从一个方向来（左边或上面）。
# 如果遇到障碍物，后面的位置都无法到达，因为只有一条路径。
# 所以初始化时，遇到障碍物就停止，后面的位置都为 0。
#
# 为什么空间优化是正确的？
# ────────────────────────────────────────────────────────
# 因为计算第 i 行时，只需要第 i-1 行的数据。
# 用一维数组时：
#    - dp[j]（更新前）= 上一行第 j 列的值
#    - dp[j-1]（更新后）= 当前行第 j-1 列的值
# 所以可以用 dp[j] = dp[j] + dp[j-1] 来更新。
#
# 与"不同路径"的区别：
# ────────────────────────────────────────────────────────
# 1. 需要判断障碍物：遇到障碍物时，路径数为 0
# 2. 初始化时需要分段：遇到障碍物后，后面的位置都无法到达
# 3. 无法使用组合数学公式：障碍物破坏了路径的对称性
#    组合数学公式假设所有路径都是等价的，但障碍物打破了这种假设
# ══════════════════════════════════════════════════════════