"""
整数拆分（Integer Break）

题目描述：
给定一个正整数 n，将其拆分为 k 个正整数的和（k >= 2），并使这些整数的乘积最大化。
返回你可以获得的最大乘积。

注意：
    - 2 <= n <= 58
    - 至少拆分成两个正整数

示例 1：
    输入: n = 2
    输出: 1
    解释: 2 = 1 + 1, 1 × 1 = 1。

示例 2：
    输入: n = 10
    输出: 36
    解释: 10 = 3 + 3 + 4, 3 × 3 × 4 = 36。
    注意：3 × 3 × 4 = 36 比 2 × 2 × 2 × 2 × 2 = 32 更大。

示例 3：
    输入: n = 8
    输出: 18
    解释: 8 = 2 + 3 + 3, 2 × 3 × 3 = 18。

示例 4：
    输入: n = 3
    输出: 2
    解释: 3 = 1 + 2, 1 × 2 = 2。

解题思路总览：
───────────────────────────────────────────────────────────────────────
解法                          时间复杂度    空间复杂度    说明
───────────────────────────────────────────────────────────────────────
暴力枚举                       O(2^n)       O(n)        超时
记忆化搜索（Top-Down DP）       O(n^2)       O(n)        ⭐⭐
动态规划（Bottom-Up DP）         O(n^2)       O(n)        ⭐⭐⭐
动态规划（空间优化）             O(n^2)       O(1)        ⭐⭐⭐⭐
数学解法                       O(n)         O(1)        ⭐⭐⭐⭐⭐ 最优
───────────────────────────────────────────────────────────────────────

核心思想（动态规划）：
───────────────────────────────────────────────────────────────────────
关键洞察：
1. 对于整数 i，我们可以将其拆分成 j 和 (i-j)，其中 1 <= j < i
2. 对于 (i-j)，我们有两种选择：
   - 不再拆分：乘积为 j × (i-j)
   - 继续拆分：乘积为 j × dp[i-j]（其中 dp[i-j] 是 i-j 的最大乘积）
3. 因此，dp[i] = max(j × (i-j), j × dp[i-j])，其中 j 从 1 到 i-1

状态定义：
    dp[i] = 将正整数 i 拆分成至少两个正整数之和的最大乘积

状态转移：
    dp[i] = max(dp[i], j × (i-j), j × dp[i-j])
    其中 j 从 1 到 i-1

初始条件：
    dp[2] = 1（2 只能拆分成 1 + 1，乘积为 1）

最终答案：
    dp[n]

数学视角（最优解）：
───────────────────────────────────────────────────────────────────────
数学结论：
1. 当 n >= 2 时，尽可能多地拆分出 3，可以使乘积最大
2. 如果余数为 1，应该拆分成两个 2（而不是 3 + 1）
3. 如果余数为 2，保留一个 2

原因：
    - 3 是最优的拆分单位：对于任意 x >= 4，都有 x <= 2 × (x-2) 或 x <= 3 × (x-3)
    - 3 × 3 > 2 × 2 × 2，所以优先拆 3
    - 但 3 + 1 应该变成 2 + 2，因为 2 × 2 > 3 × 1

公式：
    - 如果 n <= 3，返回 n - 1（因为必须拆分）
    - 如果 n % 3 == 0，返回 3^(n/3)
    - 如果 n % 3 == 1，返回 3^((n-4)/3) × 4
    - 如果 n % 3 == 2，返回 3^((n-2)/3) × 2
"""


# ══════════════════════════════════════════════════════════
# 解法一：暴力枚举（Brute Force）
# ══════════════════════════════════════════════════════════


def integerBreak_brute(n: int) -> int:
    """
    暴力枚举解法（Brute Force）

    核心思路：
    ────────────────────────────────────────────────────────
    使用回溯法枚举所有可能的拆分方式，计算每种拆分的乘积，取最大值。

    算法步骤：
    1. 定义回溯函数 backtracking(remaining, path_product, count)
        - remaining: 剩余待拆分的数值
        - path_product: 当前路径的乘积
        - count: 已经拆分出的数字个数
    2. Base Case：
        - remaining == 0 且 count >= 2：更新最大乘积
        - remaining < 0：剪枝，返回
    3. 枚举：从 1 到 remaining，递归拆分

    时间复杂度：O(2^n) — 指数级增长
    空间复杂度：O(n)   — 递归调用栈深度

    问题分析：
    ────────────────────────────────────────────────────────
    对于 n = 10，可能的拆分方式非常多：
        10 = 1 + 9, 1 + 1 + 8, 1 + 2 + 7, ...
    每种拆分都需要计算乘积并比较，效率极低。

    注意：这个解法在 n > 20 时就会明显变慢，仅用于理解问题。
    """
    max_product = 0

    def backtracking(remaining, path_product, count):
        nonlocal max_product
        # Base Case：拆分完成，且至少拆分成两个数
        if remaining == 0 and count >= 2:
            max_product = max(max_product, path_product)
            return
        # 剪枝：剩余数值为负
        if remaining < 0:
            return

        # 枚举所有可能的拆分
        for i in range(1, remaining + 1):
            backtracking(remaining - i, path_product * i, count + 1)

    backtracking(n, 1, 0)
    return max_product


# ══════════════════════════════════════════════════════════
# 解法二：记忆化搜索（Top-Down DP）
# ══════════════════════════════════════════════════════════


def integerBreak_memo(n: int) -> int:
    """
    记忆化搜索解法（Top-Down DP）⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    在暴力递归的基础上，增加一个缓存（一维数组），
    记录已经计算过的子问题结果，避免重复计算。

    算法步骤：
    1. 创建缓存 memo，存储已计算的最大乘积
    2. 定义递归函数 dfs(i)：返回整数 i 的最大乘积
    3. 对于每个 i，枚举拆分点 j（1 到 i-1）：
        a. 不继续拆分：j × (i-j)
        b. 继续拆分：j × dfs(i-j)
    4. 取所有情况的最大值存入缓存

    时间复杂度：O(n^2) — 每个子问题只计算一次，每次计算 O(n)
    空间复杂度：O(n) — 缓存 + 递归调用栈

    图解示例：n = 10
    ────────────────────────────────────────────────────────
    计算 dfs(10)：
        j = 1: max(1 × 9, 1 × dfs(9)) = max(9, dfs(9))
        j = 2: max(2 × 8, 2 × dfs(8)) = max(16, 2 × dfs(8))
        j = 3: max(3 × 7, 3 × dfs(7)) = max(21, 3 × dfs(7))
        ...
        j = 6: max(6 × 4, 6 × dfs(4)) = max(24, 6 × dfs(4))

    子问题重叠：
        dfs(10) 需要 dfs(9), dfs(8), ..., dfs(4)
        dfs(9) 需要 dfs(8), dfs(7), ...
        这些子问题会被重复计算，用缓存避免。

    状态定义：
    - memo[i] = 将正整数 i 拆分的最大乘积

    状态转移：
    - memo[i] = max(memo[i], j × (i-j), j × memo[i-j])
    """
    # 创建缓存，-1 表示未计算
    memo = [-1] * (n + 1)

    def dfs(i: int) -> int:
        """返回整数 i 拆分的最大乘积"""
        # Base Case：2 只能拆分成 1 + 1
        if i == 2:
            return 1

        # 命中缓存
        if memo[i] != -1:
            return memo[i]

        # 计算最大乘积
        max_product = 0
        for j in range(1, i):
            # 情况 1：不继续拆分 (i-j)
            product1 = j * (i - j)
            # 情况 2：继续拆分 (i-j)
            product2 = j * dfs(i - j)
            # 取最大值
            max_product = max(max_product, product1, product2)

        memo[i] = max_product
        return max_product

    return dfs(n)


