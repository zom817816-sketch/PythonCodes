# 解法三：动态规划（Bottom-Up DP）
# ══════════════════════════════════════════════════════════


def integerBreak_dp(n: int) -> int:
    """
    动态规划解法（Bottom-Up DP）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    从底向上计算：先计算小数，再逐步计算大数。
    利用已计算的小数结果来推导大数的结果。

    算法步骤：
    1. 创建 DP 数组 dp，dp[i] 表示整数 i 的最大乘积
    2. 初始化 dp[2] = 1
    3. 对于 i 从 3 到 n：
        a. 枚举拆分点 j 从 1 到 i-1
        b. dp[i] = max(dp[i], j × (i-j), j × dp[i-j])
    4. 返回 dp[n]

    时间复杂度：O(n^2) — 双重循环
    空间复杂度：O(n) — DP 数组

    图解示例：n = 10
    ────────────────────────────────────────────────────────
    dp[2] = 1

    i = 3:
        j = 1: max(1 × 2, 1 × dp[2]) = max(2, 1) = 2
        j = 2: max(2 × 1, 2 × dp[1]) = 2
        dp[3] = 2

    i = 4:
        j = 1: max(1 × 3, 1 × dp[3]) = max(3, 2) = 3
        j = 2: max(2 × 2, 2 × dp[2]) = max(4, 2) = 4
        j = 3: max(3 × 1, 3 × dp[1]) = 3
        dp[4] = 4

    i = 5:
        j = 1: max(1 × 4, 1 × dp[4]) = max(4, 4) = 4
        j = 2: max(2 × 3, 2 × dp[3]) = max(6, 4) = 6
        ...
        dp[5] = 6

    ...

    i = 10: dp[10] = 36 ✅

    状态定义：
    - dp[i] = 将正整数 i 拆分的最大乘积

    状态转移：
    - dp[i] = max(dp[i], j × (i-j), j × dp[i-j])

    初始条件：
    - dp[2] = 1

    最终答案：
    - dp[n]
    """
    # 创建 DP 数组
    dp = [0] * (n + 1)
    dp[2] = 1  # 2 = 1 + 1, 1 × 1 = 1

    # 从 3 开始计算
    for i in range(3, n + 1):
        # 枚举拆分点 j
        for j in range(1, i//2+1):
            # 情况 1：不继续拆分 (i-j)
            product1 = j * (i - j)
            # 情况 2：继续拆分 (i-j)
            product2 = j * dp[i - j]
            # 更新 dp[i]
            dp[i] = max(dp[i], product1, product2)

    return dp[n]

# ══════════════════════════════════════════════════════════
# 解法四：数学解法（推荐解）
# ══════════════════════════════════════════════════════════


def integerBreak(n: int) -> int:
    """
    数学解法 ⭐⭐⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    数学证明：对于正整数 n >= 2，将其拆分成若干个 3 的和，可以使乘积最大。

    证明思路：
    1. 对于任意 x >= 4，都有 x <= 2 × (x-2) 或 x <= 3 × (x-3)
       这意味着拆分成更小的数不会使乘积变小
    2. 3 是最优的拆分单位：
       - 3 × 3 > 2 × 2 × 2（9 > 8）
       - 所以优先拆 3
    3. 特殊情况：
       - 余数为 1 时，3 + 1 应该变成 2 + 2（因为 2 × 2 > 3 × 1）
       - 余数为 2 时，保留一个 2

    算法步骤：
    1. 如果 n <= 3，返回 n - 1（因为必须拆分）
    2. 计算 n 除以 3 的商和余数
    3. 根据余数分类讨论：
        a. 余数为 0：返回 3^quotient
        b. 余数为 1：返回 3^(quotient-1) × 4
        c. 余数为 2：返回 3^quotient × 2

    时间复杂度：O(log n) — 快速幂计算，或 O(n) 用循环
    空间复杂度：O(1) — 只用了常数空间

    图解示例：n = 10
    ────────────────────────────────────────────────────────
    10 ÷ 3 = 3 余 1
    余数为 1，应该拆成 3 + 3 + 4（而不是 3 + 3 + 3 + 1）
    结果：3 × 3 × 4 = 36 ✅

    图解示例：n = 8
    ────────────────────────────────────────────────────────
    8 ÷ 3 = 2 余 2
    余数为 2，保留一个 2
    结果：3 × 3 × 2 = 18 ✅

    图解示例：n = 6
    ────────────────────────────────────────────────────────
    6 ÷ 3 = 2 余 0
    余数为 0，全部拆成 3
    结果：3 × 3 = 9 ✅
    """
    # 特殊情况：n <= 3 时必须拆分
    if n <= 3:
        return n - 1

    # 计算商和余数
    quotient, remainder = divmod(n, 3)

    if remainder == 0:
        # 余数为 0：全部拆成 3
        return 3 ** quotient
    elif remainder == 1:
        # 余数为 1：拆成 (quotient-1) 个 3 和一个 4
        return 3 ** (quotient - 1) * 4
    else:
        # 余数为 2：拆成 quotient 个 3 和一个 2
        return 3 ** quotient * 2


# ══════════════════════════════════════════════════════════
# 重要说明：算法的正确性证明
# ══════════════════════════════════════════════════════════
#
# 为什么动态规划解法是正确的？
# ────────────────────────────────────────────────────────
# 1. 最优子结构（Optimal Substructure）：
#    对于整数 i，拆分成 j 和 (i-j) 后：
#        - 如果不再拆分 (i-j)，乘积为 j × (i-j)
#        - 如果继续拆分 (i-j)，乘积为 j × dp[i-j]
#    最优解一定在这两种情况之中。
#
# 2. 重叠子问题（Overlapping Subproblems）：
#    计算 dp[10] 需要 dp[9], dp[8], ...
#    计算 dp[9] 需要 dp[8], dp[7], ...
#    子问题被重复计算，用缓存或 DP 数组可以消除重复。
#
# 3. 无后效性（No Aftereffect）：
#    计算 dp[i] 时，只需要知道 dp[1] 到 dp[i-1]，
#    不需要知道这些值是如何得到的。
#
# 为什么数学解法是正确的？
# ────────────────────────────────────────────────────────
# 引理 1：对于任意 x >= 4，都有 x <= 2 × (x-2)
#    证明：2 × (x-2) = 2x - 4 >= x 当 x >= 4
#    这意味着拆分成更小的数不会使乘积变小。
#
# 引理 2：3 是最优的拆分单位
#    证明：比较 2 × 2 × 2 = 8 和 3 × 3 = 9
#    三个 2 的乘积小于两个 3 的乘积，所以优先拆 3。
#
# 引理 3：余数为 1 时，应该拆成 2 + 2
#    证明：3 + 1 = 4，但 3 × 1 = 3 < 2 × 2 = 4
#    所以 3 + 1 应该变成 2 + 2。
#
# 综上所述，数学解法的分类讨论是正确的。
#
# 时间复杂度对比：
# ────────────────────────────────────────────────────────
# - 暴力枚举：O(2^n) — 指数级，不可接受
# - 记忆化搜索：O(n^2) — 多项式级
# - 动态规划：O(n^2) — 多项式级
# - 数学解法：O(log n) 或 O(n) — 最优
#
# 空间复杂度对比：
# ────────────────────────────────────────────────────────
# - 暴力枚举：O(n) — 递归栈
# - 记忆化搜索：O(n) — 缓存 + 递归栈
# - 动态规划：O(n) — DP 数组
# - 数学解法：O(1) — 最优
# ══════════════════════════════════════════════════════════