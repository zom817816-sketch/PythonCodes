"""
买卖股票的最佳时机 III（Best Time to Buy and Sell Stock III）

题目描述：
给定一个数组，它的第 i 个元素是一支给定的股票在第 i 天的价格。
设计一个算法来计算你所能获取的最大利润。你最多可以完成 两笔 交易。
注意：你不能同时参与多笔交易（你必须在再次购买前出售掉之前的股票）。

输入描述：
一个整数数组 prices，其中 prices[i] 表示第 i 天的股票价格。

输出描述：
输出一个整数，表示最大利润。

输入示例：
[3,3,5,0,0,3,1,4]
输出示例：
6

════════════════════════════════════════════════════════════════════════
核心联系：股票系列问题的本质关系
════════════════════════════════════════════════════════════════════════

维度              股票 I（k=1）        股票 II（k=∞）        股票 III（k=2）       股票 IV（k=任意）
────────────────────────────────────────────────────────────────────────────────────────────────
交易次数限制      1 次                 无限次                2 次                  k 次
是否持有股票      2 种状态             2 种状态              2 种状态              2 种状态
交易维度          不需要               不需要               需要（区分第1/2笔）    需要（区分第1~k笔）
状态数            2                   2                    4                    2k
────────────────────────────────────────────────────────────────────────────────────────────────

为什么需要"交易次数"这个维度？
────────────────────────────────────────────────────────────────────────
当交易次数有限制时，我们必须区分当前处于"第几笔交易"，
因为第 1 笔的买入/卖出决策，与第 2 笔的买入/卖出决策是不同的。

本题（k=2）的四种状态：
    1. buy1   — 第 1 次买入后的最大利润（持有股票）
    2. sell1  — 第 1 次卖出后的最大利润（不持有股票）
    3. buy2   — 第 2 次买入后的最大利润（持有股票）
    4. sell2  — 第 2 次卖出后的最大利润（不持有股票）

状态之间的转换关系：
    buy1  ← 第1次买入：-prices[i]（花钱买股票）
    sell1 ← 第1次卖出：buy1 + prices[i]（卖掉股票，完成第1笔交易）
    buy2  ← 第2次买入：sell1 - prices[i]（用第1笔的利润，再买股票）
    sell2 ← 第2次卖出：buy2 + prices[i]（卖掉股票，完成第2笔交易）

类比理解：
    - 股票 I  像是"只有一次机会的赌博"，要么买要么不买
    - 股票 II 像是"无限次低买高卖"，抓住每一次上涨
    - 股票 III 像是"两次翻盘机会"，需要在两次交易中最大化收益

解题思路总览：
────────────────────────────────────────────────────────────────────────
解法                          时间复杂度       空间复杂度       说明
────────────────────────────────────────────────────────────────────────
暴力枚举分割点                  O(n²)           O(n)            超时，仅用于理解
动态规划（二维状态）            O(n)            O(n)            ⭐⭐⭐
动态规划（滚动变量优化）        O(n)            O(1)            ⭐⭐⭐⭐  推荐
────────────────────────────────────────────────────────────────────────

核心思想（动态规划）：
────────────────────────────────────────────────────────────────────────
关键洞察（状态机模型）：
1. 每天只有两种动作：买入或卖出
2. 两次交易形成 4 个状态：buy1 → sell1 → buy2 → sell2
3. 每个状态可以从"保持不变"或"从上一状态转换"而来
4. 最终答案为 sell2（完成两次交易后的最大利润）

状态定义（4 个变量）：
    buy1[i]  = 第 i 天结束时，处于"第1次买入"状态的最大利润
    sell1[i] = 第 i 天结束时，处于"第1次卖出"状态的最大利润
    buy2[i]  = 第 i 天结束时，处于"第2次买入"状态的最大利润
    sell2[i] = 第 i 天结束时，处于"第2次卖出"状态的最大利润

状态转移：
    buy1[i]  = max(buy1[i-1],  -prices[i])           保持第1次买入 / 今天第1次买入
    sell1[i] = max(sell1[i-1], buy1[i-1] + prices[i]) 保持第1次卖出 / 今天第1次卖出
    buy2[i]  = max(buy2[i-1],  sell1[i-1] - prices[i])保持第2次买入 / 今天第2次买入
    sell2[i] = max(sell2[i-1], buy2[i-1] + prices[i]) 保持第2次卖出 / 今天第2次卖出

初始条件：
    buy1[0]  = -prices[0]   第1天买入
    sell1[0] = 0            第1天买入后立刻卖出（利润为0，相当于没交易）
    buy2[0]  = -prices[0]   第1天买入、卖出、再买入（利润为 -prices[0]）
    sell2[0] = 0            第1天完成两笔交易（利润为0，相当于没交易）

最终答案：
    sell2（最多两笔交易的最大利润）
"""


# ══════════════════════════════════════════════════════════
# 解法一：暴力枚举分割点
# ══════════════════════════════════════════════════════════


def maxProfit_brute(prices: list[int]) -> int:
    """
    暴力枚举分割点

    核心思路：
    ────────────────────────────────────────────────────────
    两笔交易一定是一前一后完成的，因此存在一个"分割点"：
    - 第一笔交易在分割点左侧完成
    - 第二笔交易在分割点右侧完成
    枚举每一个分割点，分别计算左右两侧的最大单笔利润，取总和最大的。

    算法步骤：
    1. 预处理 left[i] = prices[0..i] 区间内的最大单笔利润
    2. 预处理 right[i] = prices[i..n-1] 区间内的最大单笔利润
    3. 枚举分割点 i，答案为 max(left[i] + right[i+1])

    时间复杂度：O(n²) — 枚举分割点 + 每次计算左右利润
    空间复杂度：O(n)   — 存储左右两侧的最大利润

    问题分析：
    ────────────────────────────────────────────────────────
    每次枚举分割点后需要重新计算左右利润，导致 O(n²)。
    可以通过预处理优化为 O(n)，但不如直接用 DP 优雅。
    仅用于理解"两笔交易一定存在分割点"这一核心性质。
    """
    n = len(prices)
    if n < 2:
        return 0

    result = 0

    for split in range(n):
        # 左侧 [0..split] 的最大单笔利润
        left_profit = 0
        left_min = prices[0]
        for i in range(split + 1):
            left_min = min(left_min, prices[i])
            left_profit = max(left_profit, prices[i] - left_min)

        # 右侧 [split+1..n-1] 的最大单笔利润
        right_profit = 0
        if split + 1 < n:
            right_min = prices[split + 1]
            for i in range(split + 1, n):
                right_min = min(right_min, prices[i])
                right_profit = max(right_profit, prices[i] - right_min)

        result = max(result, left_profit + right_profit)

    return result


# ══════════════════════════════════════════════════════════
# 解法二：动态规划（二维状态数组）
# ══════════════════════════════════════════════════════════


def maxProfit_dp(prices: list[int]) -> int:
    """
    动态规划解法（二维状态数组）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    用状态机模型描述两次交易的完整过程：
    buy1 → sell1 → buy2 → sell2

    算法步骤：
    1. 创建 4 个一维数组，分别对应 4 个状态
    2. 逐天更新每个状态
    3. 最终返回 sell2 的最大值

    时间复杂度：O(n) — 遍历一次价格数组
    空间复杂度：O(n) — 4 个一维数组

    图解示例：
    ────────────────────────────────────────────────────────
    prices = [3, 3, 5, 0, 0, 3, 1, 4]

    初始化：buy1[0]=-3, sell1[0]=0, buy2[0]=-3, sell2[0]=0

    i=0, price=3:
        buy1  = max(-3,  -3)  = -3     保持第1次买入
        sell1 = max(0,  -3+3) = 0      保持第1次卖出
        buy2  = max(-3, 0-3)  = -3     保持第2次买入
        sell2 = max(0, -3+3)  = 0      保持第2次卖出

    i=1, price=3:
        buy1  = max(-3,  -3)  = -3     不变
        sell1 = max(0,  -3+3) = 0      不变
        buy2  = max(-3, 0-3)  = -3     不变
        sell2 = max(0, -3+3)  = 0      不变

    i=2, price=5:
        buy1  = max(-3,  -5)  = -3     保持（5比3贵，不买）
        sell1 = max(0,  -3+5) = 2      第1次卖出！利润2
        buy2  = max(-3, 2-5)  = -3     保持
        sell2 = max(0, -3+5)  = 2      第2次卖出

    i=3, price=0:
        buy1  = max(-3,   0)  = 0      第1次买入！价格更低
        sell1 = max(2,  -3+0) = 2      保持
        buy2  = max(-3, 2-0)  = 2      第2次买入！用第1笔利润买
        sell2 = max(2, -3+0)  = 2      保持

    i=4, price=0:
        buy1  = max(0,    0)  = 0      不变
        sell1 = max(2,  0+0)  = 2      不变
        buy2  = max(2,  2-0)  = 2      不变
        sell2 = max(2,  2+0)  = 2      不变

    i=5, price=3:
        buy1  = max(0,  -3)  = 0       保持
        sell1 = max(2, 0+3)  = 3       第1次卖出！利润3
        buy2  = max(2, 3-3)  = 2       保持
        sell2 = max(2, 2+3)  = 5       第2次卖出！利润5

    i=6, price=1:
        buy1  = max(0,  -1)  = 0       保持
        sell1 = max(3, 0+1)  = 3       保持
        buy2  = max(2, 3-1)  = 2       保持
        sell2 = max(5, 2+1)  = 5       保持

    i=7, price=4:
        buy1  = max(0,  -4)  = 0       保持
        sell1 = max(3, 0+4)  = 4       第1次卖出！利润4
        buy2  = max(2, 3-4)  = 2       保持
        sell2 = max(5, 2+4)  = 6       第2次卖出！利润6 ✓

    结果：sell2 = 6
    对应：第1笔（第4天买0，第6天卖3，利润3）+ 第2笔（第7天买1，第8天卖4，利润3）= 6
    """
    n = len(prices)
    if n < 2:
        return 0

    buy1 = [0] * n
    sell1 = [0] * n
    buy2 = [0] * n
    sell2 = [0] * n

    buy1[0] = -prices[0]
    sell1[0] = 0
    buy2[0] = -prices[0]
    sell2[0] = 0

    for i in range(1, n):
        # 第1次买入：保持之前的买入，或今天买入
        buy1[i] = max(buy1[i - 1], -prices[i])
        # 第1次卖出：保持之前的卖出，或今天卖出（用昨天的买入价格）
        sell1[i] = max(sell1[i - 1], buy1[i - 1] + prices[i])
        # 第2次买入：保持之前的买入，或今天买入（用昨天第1次卖出的利润）
        buy2[i] = max(buy2[i - 1], sell1[i - 1] - prices[i])
        # 第2次卖出：保持之前的卖出，或今天卖出（用昨天的买入价格）
        sell2[i] = max(sell2[i - 1], buy2[i - 1] + prices[i])

    return sell2[-1]


# ══════════════════════════════════════════════════════════
# 解法三：动态规划（滚动变量优化）
# ══════════════════════════════════════════════════════════


def maxProfit(prices: list[int]) -> int:
    """
    动态规划解法（滚动变量优化）⭐⭐⭐⭐ 推荐

    核心思想：
    ────────────────────────────────────────────────────────
    观察 DP 状态转移方程可以发现：
    buy1[i] 只依赖 buy1[i-1] 和 prices[i]
    sell1[i] 只依赖 sell1[i-1] 和 buy1[i-1]
    ...

    因此可以用 4 个变量代替 4 个数组，将空间从 O(n) 优化到 O(1)。

    注意事项：
    ────────────────────────────────────────────────────────
    更新顺序必须严格按照 buy1 → sell1 → buy2 → sell2，
    因为 sell1 依赖更新前的 buy1，buy2 依赖更新前的 sell1，以此类推。
    但如果用"同一天更新"的方式，需要用临时变量保存旧值，
    或者利用数学等价性直接写（如下面代码所示）。

    这里利用了一个巧妙的等价关系：
        sell1 = max(sell1, buy1 + price)
    中的 buy1 可以用"当天更新后的 buy1"，因为：
        - 如果 buy1 更新了（今天买入），则 buy1 + price = -price + price = 0 ≤ sell1
        - 如果 buy1 没更新（保持昨天的值），则与用 buy1_old 等价

    时间复杂度：O(n) — 遍历一次价格数组
    空间复杂度：O(1) — 只用 4 个变量

    状态定义：
        buy1  = 当前处于"第1次买入"状态的最大利润
        sell1 = 当前处于"第1次卖出"状态的最大利润
        buy2  = 当前处于"第2次买入"状态的最大利润
        sell2 = 当前处于"第2次卖出"状态的最大利润

    初始条件：
        buy1  = -prices[0]   第1天买入
        sell1 = 0            未完成第1笔交易
        buy2  = -prices[0]   同一天买入卖出再买入
        sell2 = 0            未完成第2笔交易

    最终答案：
        sell2（最多两笔交易后的最大利润）
    """
    if len(prices) < 2:
        return 0

    buy1 = -prices[0]
    sell1 = 0
    buy2 = -prices[0]
    sell2 = 0

    for price in prices[1:]:
        # 第2次卖出：保持 或 今天卖出
        sell2 = max(sell2, buy2 + price)
        # 第2次买入：保持 或 今天买入（用第1笔利润）
        buy2 = max(buy2, sell1 - price)
        # 第1次卖出：保持 或 今天卖出
        sell1 = max(sell1, buy1 + price)
        # 第1次买入：保持 或 今天买入
        buy1 = max(buy1, -price)

    return sell2


# ══════════════════════════════════════════════════════════
# 重要说明：算法的正确性证明和易错点提示
# ══════════════════════════════════════════════════════════
#
# 算法正确性证明：
# ────────────────────────────────────────────────────────
#
# 定理：上述 DP 算法返回的 sell2 等于最多完成两笔交易的最大利润。
#
# 证明：
# （1）状态完备性：
#     任何一天结束时，投资者必然处于以下 4 种状态之一：
#     - 已完成0笔交易，持有1股（第1次买入后未卖出）     → buy1
#     - 已完成1笔交易，持有0股（第1次卖出后未再买入）    → sell1
#     - 已完成1笔交易，持有1股（第2次买入后未卖出）      → buy2
#     - 已完成2笔交易，持有0股（第2次卖出后）           → sell2
#     此外还有一种"未进行任何操作"的初始状态，利润为0，
#     已被 sell1=0 和 sell2=0 的初始值覆盖。
#     因此 4 个状态覆盖了所有可能。
#
# （2）状态转移的正确性：
#     对于每个状态，当天只有两种选择：保持不变 或 从上一状态转换。
#     - buy1: 保持（不操作）或 从初始状态买入（-prices[i]）
#     - sell1: 保持（不操作）或 从 buy1 卖出（buy1 + prices[i]）
#     - buy2: 保持（不操作）或 从 sell1 买入（sell1 - prices[i]）
#     - sell2: 保持（不操作）或 从 buy2 卖出（buy2 + prices[i]）
#     每种选择都取最大值，保证了局部最优。
#
# （3）全局最优性（由最优子结构保证）：
#     由于第 i 天的状态仅依赖第 i-1 天的状态（无后效性），
#     且每天的状态转移取了所有可能选择的最大值，
#     根据数学归纳法，第 n-1 天的 sell2 就是全局最优解。
#
# （4）"最多两笔"的保证：
#     sell2 ≥ sell1 ≥ 0，因为：
#     - sell1 ≥ 0：可以不交易，利润为0
#     - sell2 ≥ sell1：第2笔交易可以选择不做（buy2保持，sell2≥sell1）
#     所以即使只做一笔或不做交易，答案也是正确的。
#
#
# 为什么 sell1 = max(sell1, buy1 + price) 中可以用"当天更新后的 buy1"？
# ────────────────────────────────────────────────────────
# 情况1：buy1 今天被更新为 -price（今天买入）
#   则 buy1 + price = -price + price = 0
#   sell1 = max(sell1, 0) = sell1（因为 sell1 ≥ 0）
#   这等价于"今天买入又立刻卖出"，利润为0，不影响结果。
#
# 情况2：buy1 今天没更新（保持昨天的值）
#   则 buy1 + price 与用 buy1_old 完全等价。
#
# 因此两种情况都正确，可以省去临时变量。
# buy2 → sell2 同理。
#
#
# 为什么 buy2[0] = -prices[0]？不是应该先完成第1笔才能第2笔买入吗？
# ────────────────────────────────────────────────────────
# buy2[0] = -prices[0] 表示"在第1天同时完成第1笔交易和第2次买入"。
# 这等价于：第1天买入（-price），立刻卖出（0利润），再买入（-price）。
# 这虽然现实中不可能同一天既买又卖再买，但数学上是正确的：
#   buy2[0] = sell1[0] - prices[0] = 0 - prices[0] = -prices[0]
# 这种初始化方式保证后续递推能正确处理所有情况。
#
#
# 常见错误：
# ────────────────────────────────────────────────────────
# 1. 混淆"最多两笔"和"恰好两笔"：
#    本题是"最多两笔"，可以做0笔或1笔。
#    sell2 ≥ sell1 ≥ 0 保证了这一点。
#
# 2. 初始值设置错误：
#    buy1 = -prices[0]，不能设为 0 或负无穷
#    sell1 = 0，不能设为负无穷（因为可以不交易）
#    buy2 = -prices[0]，不能设为 0（否则第2次买入变成白嫖）
#
# 3. 更新顺序错误（使用滚动变量时）：
#    如果不利用上述等价关系，必须从 sell2→buy2→sell1→buy1 逆序更新，
#    或使用临时变量保存旧值。
#    代码中采用了从 sell2→buy2→sell1→buy1 的逆序更新方式。
#
# 4. 忘记处理边界情况：
#    prices 长度为 0 或 1 时，无法完成交易，应返回 0。
#
# 5. 混淆"第i次买入"和"第i天买入"：
#    buy1/sell1/buy2/sell2 表示的是"第几笔交易"，不是"第几天"。
#    第1笔交易可以在任意一天买入、任意一天卖出。
#
# 6. 拓展到 k 笔交易时的注意：
#    当 k ≥ n/2 时，等价于无限次交易（股票 II），
#    应该用贪心算法而非 DP，否则会超时或超空间。
# ══════════════════════════════════════════════════════════
