"""
单词拆分（Word Break）

题目描述：
给你一个字符串 s 和一个字符串列表 wordDict 作为字典。如果可以利用字典中出现的一个或多个单词拼接出 s 则返回 true。
注意：不要求字典中出现的单词全部都使用，并且字典中的单词可以重复使用。

示例 1：
输入: s = "leetcode", wordDict = ["leet", "code"]
输出: true
解释: 返回 true 因为 "leetcode" 可以由 "leet" 和 "code" 拼接成。

示例 2：
输入: s = "applepenapple", wordDict = ["apple", "pen"]
输出: true
解释: 返回 true 因为 "applepenapple" 可以由 "apple" "pen" "apple" 拼接成。
     注意，你可以重复使用字典中的单词。

示例 3：
输入: s = "catsandog", wordDict = ["cats", "dog", "sand", "and", "cat"]
输出: false

════════════════════════════════════════════════════════════════════════
核心联系：本题与完全背包问题的关系
════════════════════════════════════════════════════════════════════════

维度              完全背包问题（求极值）              单词拆分（本题）
────────────────────────────────────────────────────────────────────────
问题本质          求最小价值/最大价值                判断是否可达（布尔型）
递推公式          dp[i] = min/max(dp[i], dp[i-w]+v)  dp[i] = dp[i] or dp[i-len(word)]
初始条件          dp[0] = 0，其余为 inf/-inf         dp[0] = True，其余为 False
最终答案          dp[amount] 的值                   dp[n] 是否为 True
────────────────────────────────────────────────────────────────────────

为什么本题是布尔型 DP？
────────────────────────────────────────────────────────────────────────
本题只关心"能否拆分"，不关心"最少用几个单词"或"有多少种拆分方式"。
因此 DP 数组存储的是布尔值，递推使用 OR 运算。

解题思路总览：
────────────────────────────────────────────────────────────────────────
解法                          时间复杂度       空间复杂度       说明
────────────────────────────────────────────────────────────────────────
回溯算法                       O(n^n)          O(n)            超时，仅用于理解
记忆化搜索（Top-Down DP）       O(n²)           O(n)            ⭐⭐
动态规划（Bottom-Up DP）        O(n²)           O(n)            ⭐⭐⭐⭐  推荐
广度优先搜索（BFS）             O(n²)           O(n)            ⭐⭐⭐   适合找可达性
───────────────────────────────────────────────────────────────────────
（n = len(s)，假设字典中单词平均长度为常数）

核心思想（动态规划）：
────────────────────────────────────────────────────────────────────────
关键洞察：
1. 如果 s[0..i] 可以拆分，且 s[i+1..j] 在字典中，那么 s[0..j] 可以拆分
2. dp[i] 表示 s[0..i-1] 是否可以被拆分（前 i 个字符）

状态定义（一维 DP）：
    dp[i] = s 的前 i 个字符（s[0..i-1]）是否可以被字典中的单词拼接

状态转移：
    对于每个位置 i，遍历所有单词 word：
    如果 s[i-len(word):i] == word 且 dp[i-len(word)] == True，
    则 dp[i] = True

    含义：如果前 i-len(word) 个字符可以拆分，且末尾正好匹配一个单词，
          那么前 i 个字符也可以拆分

初始条件：
    dp[0] = True（空字符串可以被拆分）

最终答案：
    dp[n]（n = len(s)）
"""


# ══════════════════════════════════════════════════════════
# 解法一：回溯算法（Brute Force）
# ══════════════════════════════════════════════════════════


def wordBreak_backtrack(s: str, wordDict: list[str]) -> bool:
    """
    回溯算法（Brute Force）

    核心思路：
    ────────────────────────────────────────────────────────
    枚举每一种单词组合，检查是否能拼接成字符串 s。

    算法步骤：
    1. 使用回溯枚举每种单词的组合
    2. 维护当前拼接的字符串 current
    3. 当 current == s 时，返回 True
    4. 当 current 不是 s 的前缀时，剪枝

    时间复杂度：O(n^n) — 最坏情况每层递归 n 个分支
    空间复杂度：O(n)   — 递归调用栈深度

    问题分析：
    ────────────────────────────────────────────────────────
    对于 s = "aaaaaaaaaaaaa"，递归树非常庞大，效率极低。
    仅用于理解问题本质。
    """
    def backtrack(current: str) -> bool:
        if current == s:
            return True
        if not s.startswith(current):
            return False

        for word in wordDict:
            if backtrack(current + word):
                return True

        return False

    return backtrack("")


# ══════════════════════════════════════════════════════════
# 解法二：记忆化搜索（Top-Down DP）
# ══════════════════════════════════════════════════════════


def wordBreak_memo(s: str, wordDict: list[str]) -> bool:
    """
    记忆化搜索解法（Top-Down DP）⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    在回溯的基础上，添加缓存避免重复计算。

    算法步骤：
    1. 创建缓存 memo[i]，表示从位置 i 开始的子串是否可以拆分
    2. 使用递归 + 缓存求解
    3. 对于每个起始位置，尝试匹配字典中的单词

    时间复杂度：O(n²) — 每个位置最多计算一次
    空间复杂度：O(n)   — 缓存 + 递归调用栈

    状态定义：
    - memo[i] = s[i:] 是否可以被字典中的单词拼接

    状态转移：
    - memo[i] = any(memo[i + len(word)] for word in wordDict if s[i:i+len(word)] == word)

    初始条件：
    - i == len(s) 时返回 True（空字符串可以被拆分）
    """
    memo = {}

    def dfs(start: int) -> bool:
        if start == len(s):
            return True
        if start in memo:
            return memo[start]

        for word in wordDict:
            word_len = len(word)
            if start + word_len <= len(s) and s[start:start + word_len] == word:
                if dfs(start + word_len):
                    memo[start] = True
                    return True

        memo[start] = False
        return False

    return dfs(0)


# ══════════════════════════════════════════════════════════
# 解法三：动态规划（Bottom-Up DP）— 写法一：外层位置，内层单词
# ══════════════════════════════════════════════════════════


def wordBreak_dp_v1(s: str, wordDict: list[str]) -> bool:
    """
    动态规划解法（Bottom-Up DP）⭐⭐⭐⭐ 推荐 — 写法一

    核心思想：
    ────────────────────────────────────────────────────────
    标准的布尔型 DP 解法。
    通过一维数组记录前 i 个字符是否可以被拆分。

    算法步骤：
    1. 创建一维 DP 数组 dp[n+1]，初始化 dp[0] = True，其余为 False
    2. 外层循环遍历位置 i（从 1 到 n）
    3. 内层循环遍历位置 j（从 0 到 i-1）
    4. 如果 dp[j] == True 且 s[j:i] 在字典中，设 dp[i] = True

    时间复杂度：O(n²) — 双重循环
    空间复杂度：O(n)   — 一维 DP 数组

    图解示例：
    ────────────────────────────────────────────────────────
    s = "leetcode", wordDict = ["leet", "code"]

    dp = [T, F, F, F, F, F, F, F, F]

    i=1: j=0 → s[0:1]="l"不在字典，dp[1]=F
    i=2: j=0 → s[0:2]="le"不在字典
         j=1 → s[1:2]="e"不在字典，dp[2]=F
    i=3: j=0 → s[0:3]="lee"不在字典
         j=1 → s[1:3]="ee"不在字典
         j=2 → s[2:3]="e"不在字典，dp[3]=F
    i=4: j=0 → s[0:4]="leet"在字典，且dp[0]=T → dp[4]=T
    i=5: j=0..4 → 没有匹配，dp[5]=F
    ...（中间步骤省略）...
    i=8: j=4 → s[4:8]="code"在字典，且dp[4]=T → dp[8]=T

    结果：dp[8] = True ✓

    状态定义：
    - dp[i] = s 的前 i 个字符（s[0..i-1]）是否可以被拆分

    状态转移：
    - 如果存在 j < i，使得 dp[j] == True 且 s[j:i] 在字典中，则 dp[i] = True

    初始条件：
    - dp[0] = True（空字符串可以被拆分）

    最终答案：
    - dp[n]（n = len(s)）
    """
    n = len(s)
    dp = [False] * (n + 1)
    dp[0] = True
    word_set = set(wordDict)

    for i in range(1, n + 1):
        for j in range(i):
            if dp[j] and s[j:i] in word_set:
                dp[i] = True
                break

    return dp[n]


# ══════════════════════════════════════════════════════════
# 解法四：动态规划（Bottom-Up DP）— 写法二：从可达位置扩展
# ══════════════════════════════════════════════════════════


def wordBreak_dp_v2(s: str, wordDict: list[str]) -> bool:
    """
    动态规划解法（Bottom-Up DP）⭐⭐⭐⭐ 推荐 — 写法二

    核心思想：
    ────────────────────────────────────────────────────────
    另一种 DP 写法：从已知可达的位置出发，尝试用每个单词向后扩展。

    算法步骤：
    1. 创建一维 DP 数组 dp[n+1]，初始化 dp[0] = True，其余为 False
    2. 外层循环遍历位置 i（从 0 到 n-1）
    3. 如果 dp[i] == True，内层循环遍历每个单词 word
    4. 如果 s[i:i+len(word)] == word，设 dp[i+len(word)] = True

    时间复杂度：O(n×m) — n 为字符串长度，m 为字典大小
    空间复杂度：O(n)   — 一维 DP 数组

    状态定义：
    - dp[i] = s 的前 i 个字符（s[0..i-1]）是否可以被拆分

    状态转移：
    - 如果 dp[i] == True 且 s[i:i+len(word)] == word，则 dp[i+len(word)] = True

    初始条件：
    - dp[0] = True（空字符串可以被拆分）

    最终答案：
    - dp[n]（n = len(s)）

    与写法一的区别：
    ────────────────────────────────────────────────────────
    写法一：对每个位置 i，检查所有可能的 j，看 s[j:i] 是否在字典中
    写法二：对每个可达位置 i，尝试用每个单词向后扩展

    当字典中单词数量较少时，写法二更高效。
    """
    n = len(s)
    dp = [False] * (n + 1)
    dp[0] = True

    for i in range(n):
        if dp[i]:
            for word in wordDict:
                word_len = len(word)
                if i + word_len <= n and s[i:i + word_len] == word:
                    dp[i + word_len] = True

    return dp[n]


# ══════════════════════════════════════════════════════════
# 解法五：广度优先搜索（BFS）
# ══════════════════════════════════════════════════════════


def wordBreak_bfs(s: str, wordDict: list[str]) -> bool:
    """
    广度优先搜索解法（BFS）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    将问题转化为图的可达性问题：
    - 每个位置是一个节点（0 到 n）
    - 使用一个单词 word 相当于从节点 i 走到节点 i+len(word)
    - 求从节点 0 到节点 n 是否可达

    BFS 的特性保证了第一次到达目标时就是最短路径。

    算法步骤：
    1. 使用队列存储待处理的位置
    2. 使用 visited 数组避免重复访问
    3. 每次出队一个位置，尝试用每个单词向后扩展
    4. 当到达位置 n 时，返回 True

    时间复杂度：O(n×m) — 每个位置最多访问一次
    空间复杂度：O(n)   — 队列和 visited 数组

    图解示例：
    ────────────────────────────────────────────────────────
    s = "leetcode", wordDict = ["leet", "code"]

    队列：[0]
    出队 0：匹配 "leet" → 到达位置 4，入队 → queue = [4]
            匹配 "code" → s[0:4]="leetc"≠"code"，跳过

    出队 4：匹配 "leet" → s[4:8]="code"≠"leet"，跳过
            匹配 "code" → s[4:8]="code"="code"，到达位置 8 → 返回 True

    结果：True ✓

    适用场景：
    ────────────────────────────────────────────────────────
    当最短路径较短时，BFS 效率高于 DP。
    """
    n = len(s)
    visited = [False] * n
    queue = []
    queue.append(0)
    visited[0] = True

    while queue:
        current = queue.pop(0)

        for word in wordDict:
            word_len = len(word)
            next_pos = current + word_len

            if next_pos == n and s[current:next_pos] == word:
                return True
            if next_pos < n and not visited[next_pos] and s[current:next_pos] == word:
                visited[next_pos] = True
                queue.append(next_pos)

    return False


# ══════════════════════════════════════════════════════════
# 重要说明：算法正确性说明与易错点提醒
# ══════════════════════════════════════════════════════════
#
# 算法正确性说明：
# ────────────────────────────────────────────────────────
# 1. 动态规划解法的正确性：
#    - dp[i] 表示前 i 个字符是否可以拆分
#    - dp[0] = True 是递推的起点，表示空字符串可以被拆分
#    - 对于每个位置 i，如果存在 j < i 使得 dp[j] == True 且 s[j:i] 在字典中，
#      那么前 i 个字符也可以拆分，即 dp[i] = True
#    - 这是一个布尔型 DP，递推使用 OR 运算而非 min/max

# 2. BFS 解法的正确性：
#    - 将位置视为节点，单词视为边
#    - 从位置 0 出发，每次使用一个单词向前推进
#    - 如果能到达位置 n，说明整个字符串可以被拆分
#    - visited 数组避免了重复访问同一个位置

# 3. 记忆化搜索的正确性：
#    - memo[i] 表示从位置 i 开始的子串是否可以拆分
#    - 递归尝试匹配每个单词，如果匹配成功则继续递归
#    - 缓存避免了重复计算，将时间复杂度从 O(n^n) 降到 O(n²)

# 常见错误：
# ────────────────────────────────────────────────────────
# 1. 忘记 dp[0] = True 的初始化
#    - 错误：dp[0] = False
#    - 后果：所有 dp[i] 都会保持为 False，无法正确递推
#    - 原因：dp[0] 是递推的起点，表示"空字符串可以被拆分"
#    - 示例：s="a", wordDict=["a"]，dp[0]=True → dp[1]=dp[0] and s[0:1]="a"在字典 → dp[1]=True

# 2. 子串索引错误
#    - 错误：s[i:i+len(word)] 写成 s[i:i+len(word)+1]
#    - 后果：索引越界或匹配错误的子串
#    - 正确：Python 切片是左闭右开，s[i:i+len(word)] 正好取 len(word) 个字符

# 3. 没有使用集合存储字典
#    - 错误：直接使用列表 wordDict 进行成员检查
#    - 后果：列表的 in 操作是 O(m)，导致整体时间复杂度变为 O(n²×m)
#    - 正确：转换为集合 word_set = set(wordDict)，in 操作变为 O(1)

# 4. BFS 中忘记标记 visited
#    - 错误：不使用 visited 数组
#    - 后果：同一个位置会被多次加入队列，导致超时或内存溢出
#    - 正确：每次入队前标记 visited[next_pos] = True

# 5. 混淆 dp[i] 的含义
#    - 错误：认为 dp[i] 表示 s[0..i] 是否可以拆分
#    - 正确：dp[i] 表示 s[0..i-1] 是否可以拆分（前 i 个字符）
#    - 原因：这样 dp[0] = True 表示空字符串，dp[n] 表示整个字符串

# 6. 回溯算法中的剪枝条件错误
#    - 错误：只检查 current == s，不检查是否为前缀
#    - 后果：如果 current 不是 s 的前缀，继续递归会浪费时间
#    - 正确：使用 s.startswith(current) 剪枝

# 7. 动态规划写法一中没有 break
#    - 错误：找到一个匹配后继续检查其他 j
#    - 后果：虽然结果正确，但会浪费时间
#    - 正确：找到一个匹配后立即 break，因为 dp[i] 已经是 True

# 8. 忽略单词可以重复使用的条件
#    - 错误：认为每个单词只能使用一次
#    - 正确：字典中的单词可以重复使用，无需额外标记已使用的单词
# ══════════════════════════════════════════════════════════
