"""
买卖股票的最佳时机含冷冻期（Best Time to Buy and Sell Stock with Cooldown）

题目描述：
给定一个整数数组 prices，其中 prices[i] 表示第 i 天的股票价格。
设计一个算法计算出最大利润。在满足以下约束条件下，你可以尽可能地完成更多的交易：
    卖出股票后，你无法在第二天买入股票（即冷冻期为 1 天）。
注意：你不能同时参与多笔交易（你必须在再次购买前出售掉之前的股票）。

输入描述：
整数数组 prices。

输出描述：
输出一个整数，表示最大利润。

输入示例：
prices = [1, 2, 3, 0, 2]
输出示例：
3

════════════════════════════════════════════════════════════════════════
核心联系：股票系列问题——冷冻期如何改变状态机
════════════════════════════════════════════════════════════════════════

维度              股票 II（k=∞）     含手续费（fee）      含冷冻期（本题）      区别
────────────────────────────────────────────────────────────────────────────────────
交易次数          无限次              无限次               无限次              相同
额外约束          无                  每笔扣 fee           卖后1天不能买       核心区别
状态数            2                   2                    3                   冷冻期多一个状态
贪心可行性        可以                不可以               不可以              都需DP
────────────────────────────────────────────────────────────────────────────────────

冷冻期带来的本质变化：
────────────────────────────────────────────────────────────────────────
无冷冻期时，"不持有股票"只有一种状态，买入只依赖 sell。
有冷冻期时，"不持有股票"分裂为两种：
    1. 今天刚卖出 → 明天处于冷冻期，不能买入
    2. 冷冻期已过 / 从未交易 → 明天可以买入

因此需要 3 个状态而非 2 个。

三种状态的含义：
────────────────────────────────────────────────────────────────────────
    状态 0：持有股票（今天结束时手上有股票）
    状态 1：今天卖出（今天结束时刚卖，明天冷冻）
    状态 2：不持有且非冷冻（今天结束时不持有，明天可以买入）

状态转换关系：
    持有(0)  ← 保持持有 / 从"非冷冻(2)"买入（冷冻期过后才能买！）
    卖出(1)  ← 从"持有(0)"卖出
    非冷冻(2) ← 保持非冷冻 / 从"刚卖出(1)"冷却结束

类比理解：
    - 股票 II  像是"无限制的交易"，随时买随时卖
    - 含手续费 像是"每次交易有成本"，但时间上无限制
    - 含冷冻期 像是"卖完必须冷静一天"，时间上有约束
    - 三者都是无限次交易，但约束不同导致状态数不同

解题思路总览：
────────────────────────────────────────────────────────────────────────
解法                          时间复杂度       空间复杂度       说明
────────────────────────────────────────────────────────────────────────
回溯算法                       O(2^n)          O(n)            超时，仅用于理解
记忆化搜索（Top-Down DP）       O(n)            O(n)            ⭐⭐⭐
动态规划（二维状态数组）        O(n)            O(n)            ⭐⭐⭐
动态规划（滚动变量优化）        O(n)            O(1)            ⭐⭐⭐⭐  推荐
────────────────────────────────────────────────────────────────────────

核心思想（动态规划）：
────────────────────────────────────────────────────────────────────────
关键洞察（状态机模型）：
1. 冷冻期导致"不持有"分裂为两个子状态
2. 买入只能从"非冷冻"状态转入，不能从"刚卖出"转入
3. 三种状态形成如下状态机：

    ┌─────────────────────────────────────────────────┐
    │                                                 │
    │   持有(0) ───卖出───→ 刚卖出(1) ───冷却───→ 非冷冻(2)
    │     ↑                      │                  │  │
    │     └────── 买入 ──────────┘                  │  │
    │     ↑                                        │  │
    │     └────────── 保持 ────────────────────────┘  │
    │                  保持 ←──────────────────────────┘
    └─────────────────────────────────────────────────┘

状态定义（3 个变量）：
    hold[i]  = 第 i 天结束时持有股票的最大利润
    sell[i]  = 第 i 天结束时刚卖出（明天冷冻）的最大利润
    rest[i]  = 第 i 天结束时不持有且非冷冻的最大利润

状态转移：
    hold[i]  = max(hold[i-1],  rest[i-1] - prices[i])   保持持有 / 从非冷冻买入
    sell[i]  = hold[i-1] + prices[i]                     今天卖出（必定从持有转入）
    rest[i]  = max(rest[i-1], sell[i-1])                 保持非冷冻 / 冷冻期结束

初始条件：
    hold[0]  = -prices[0]    第1天买入
    sell[0]  = 0              第1天不可能卖出（设为0，不影响后续max）
    rest[0]  = 0              第1天不持有且非冷冻

最终答案：
    max(sell[n-1], rest[n-1])（最后一天不持有股票的最大利润）
"""


# ══════════════════════════════════════════════════════════
# 解法一：回溯算法（Brute Force）
# ══════════════════════════════════════════════════════════


def maxProfit_brute(prices: list[int]) -> int:
    """
    回溯算法（Brute Force）

    核心思路：
    ────────────────────────────────────────────────────────
    在每一天，根据当前状态做出决策：
    - 持有股票：可以继续持有 或 卖出
    - 不持有且非冷冻：可以买入 或 不操作
    - 冷冻期：只能等待（不操作）

    算法步骤：
    1. 递归遍历每一天，维护当前状态和累计利润
    2. 对每个状态枚举所有合法的决策
    3. 在最后一天取所有路径的最大利润

    时间复杂度：O(2^n) — 每天最多两个选择（买入/卖出）
    空间复杂度：O(n)   — 递归调用栈深度

    问题分析：
    ────────────────────────────────────────────────────────
    指数级复杂度，n=30 时约 10^9 次递归，严重超时。
    仅用于理解问题本质和验证小规模用例。
    """
    n = len(prices)
    if n < 2:
        return 0

    result = 0

    def backtrack(day: int, holding: bool, cooldown: bool, profit: int) -> None:
        nonlocal result
        if day == n:
            result = max(result, profit)
            return

        price = prices[day]

        if holding:
            # 选择1：继续持有
            backtrack(day + 1, True, False, profit)
            # 选择2：卖出（卖后进入冷冻期）
            backtrack(day + 1, False, True, profit + price)
        elif cooldown:
            # 冷冻期：只能等待，明天解除冷冻
            backtrack(day + 1, False, False, profit)
        else:
            # 非冷冻且不持有
            # 选择1：不操作
            backtrack(day + 1, False, False, profit)
            # 选择2：买入
            backtrack(day + 1, True, False, profit - price)

    backtrack(0, False, False, 0)
    return result


# ══════════════════════════════════════════════════════════
# 解法二：记忆化搜索（Top-Down DP）
# ══════════════════════════════════════════════════════════


def maxProfit_memo(prices: list[int]) -> int:
    """
    记忆化搜索解法（Top-Down DP）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    回溯中大量子问题重复计算（同一天、同一状态被多次到达），
    用 memo 缓存 (day, state) 的结果即可消除重复。

    状态编码：
    - state=0：持有股票
    - state=1：不持有且非冷冻（可以买入）
    - state=2：冷冻期（刚卖出，不能买入）

    算法步骤：
    1. 定义 dfs(day, state) 返回从 day 天起的最优利润
    2. 对每个 (day, state) 缓存结果
    3. 递归终止条件：day == n

    时间复杂度：O(n) — 每个状态只计算一次，共 3n 个状态
    空间复杂度：O(n) — 缓存 + 递归调用栈
    """
    n = len(prices)
    if n < 2:
        return 0

    memo = {}

    def dfs(day: int, state: int) -> int:
        """
        state: 0=持有, 1=非冷冻不持有, 2=冷冻期
        返回：从第 day 天开始，在给定状态下能获得的最大利润
        """
        if day == n:
            return 0
        if (day, state) in memo:
            return memo[(day, state)]

        price = prices[day]

        if state == 0:
            # 持有：继续持有 或 卖出
            # dfs(day+1, 0)：继续持有，今天无操作，利润来自明天起
            # price + dfs(day+1, 2)：今天卖出，立刻到手 price，
            #   然后从明天起进入冷冻期(state=2)，dfs(day+1,2)是明天的【未来总利润】
            # 注意：price 是今天的收入，dfs(day+1,2) 是未来的收入，两者相加才是总利润
            #   dfs(day+1,2) 不是"冷冻期当天的利润"（那天利润增量为0），
            #   而是"从冷冻期开始，往后所有天的利润"
            res = max(dfs(day + 1, 0), price + dfs(day + 1, 2))
        elif state == 1:
            # 非冷冻不持有：不操作 或 买入
            # dfs(day+1, 1)：今天不操作，明天仍非冷冻
            # -price + dfs(day+1, 0)：今天买入，花费 price，明天持有
            res = max(dfs(day + 1, 1), -price + dfs(day + 1, 0))
        else:
            # 冷冻期：只能等待，明天变非冷冻
            # 冷冻期当天不能做任何操作，利润增量为0，
            # 但冷冻期结束后可能还有交易机会，所以递归到 dfs(day+1, 1)
            res = dfs(day + 1, 1)

        memo[(day, state)] = res
        return res

    # 初始状态：第0天，非冷冻不持有（可以买入或不操作）
    return dfs(0, 1)


# ══════════════════════════════════════════════════════════
# 解法三：动态规划（二维状态数组）
# ══════════════════════════════════════════════════════════


def maxProfit_dp(prices: list[int]) -> int:
    """
    动态规划解法（二维状态数组）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    用三个一维数组分别记录每天的三个状态的最大利润。
    冷冻期的关键约束：买入只能从 rest（非冷冻）状态转入。

    算法步骤：
    1. 创建 hold[n], sell[n], rest[n] 三个数组
    2. 初始化 hold[0]=-prices[0], sell[0]=0, rest[0]=0
    3. 逐天更新三个状态
    4. 最终返回 max(sell[n-1], rest[n-1])

    时间复杂度：O(n) — 遍历一次价格数组
    空间复杂度：O(n) — 三个一维数组

    图解示例：
    ────────────────────────────────────────────────────────
    prices = [1, 2, 3, 0, 2]

    初始化：hold[0]=-1, sell[0]=0, rest[0]=0

    i=1, price=2:
        hold = max(-1, 0-2)  = -1     保持持有（-1比-2好）
        sell = -1+2          = 1      卖出！利润1
        rest = max(0, 0)     = 0      保持

    i=2, price=3:
        hold = max(-1, 0-3)  = -1     保持持有
        sell = -1+3          = 2      卖出！利润2
        rest = max(0, 1)     = 1      冷冻期结束

    i=3, price=0:
        hold = max(-1, 1-0)  = 1      从非冷冻买入！用利润1买
        sell = -1+0          = -1     卖出亏，不卖（但sell必须算）
        rest = max(1, 2)     = 2      冷冻期结束

    i=4, price=2:
        hold = max(1, 2-2)   = 1      保持持有
        sell = 1+2           = 3      卖出！利润3
        rest = max(2, -1)    = 2      保持

    结果：max(sell[4], rest[4]) = max(3, 2) = 3 ✓
    对应：第1天买1，第2天卖2（利润1）→ 第3天冷冻 → 第4天买0，第5天卖2（利润2）= 3
    """
    n = len(prices)
    if n < 2:
        return 0

    hold = [0] * n
    sell = [0] * n
    rest = [0] * n

    hold[0] = -prices[0]
    sell[0] = 0
    rest[0] = 0

    for i in range(1, n):
        # 持有：保持持有 / 从非冷冻买入（关键：只能从rest买入！）
        hold[i] = max(hold[i - 1], rest[i - 1] - prices[i])
        # 卖出：必定从持有转入（卖出利润）
        sell[i] = hold[i - 1] + prices[i]
        # 非冷冻：保持非冷冻 / 冷冻期结束
        rest[i] = max(rest[i - 1], sell[i - 1])

    return max(sell[-1], rest[-1])


# ══════════════════════════════════════════════════════════
# 解法四：动态规划（滚动变量优化）
# ══════════════════════════════════════════════════════════


def maxProfit(prices: list[int]) -> int:
    """
    动态规划解法（滚动变量优化）⭐⭐⭐⭐ 推荐

    核心思想：
    ────────────────────────────────────────────────────────
    hold[i], sell[i], rest[i] 都只依赖前一天的值，
    因此用三个变量代替三个数组，空间从 O(n) 优化到 O(1)。

    更新顺序：
    ────────────────────────────────────────────────────────
    hold 依赖 rest 的旧值 ✓
    sell 依赖 hold 的旧值 ✓（但 hold 刚被更新了！❌）
    rest 依赖 sell 的旧值 ✓（但 sell 刚被更新了！❌）

    所以必须用临时变量保存旧值。

    时间复杂度：O(n) — 遍历一次价格数组
    空间复杂度：O(1) — 只用三个变量 + 临时变量

    状态定义：
        hold  = 当前持有股票的最大利润
        sell  = 当前刚卖出的最大利润（明天冷冻）
        rest  = 当前不持有且非冷冻的最大利润

    初始条件：
        hold  = -prices[0]   第1天买入
        sell  = 0             第1天不可能卖出
        rest  = 0             第1天不持有且非冷冻

    最终答案：
        max(sell, rest)（最后一天不持有股票的最大利润）
    """
    if len(prices) < 2:
        return 0

    hold = -prices[0]
    sell = 0
    rest = 0

    for price in prices[1:]:
        # 保存旧值，避免更新顺序问题
        prev_hold = hold
        prev_sell = sell

        # 持有：保持持有 / 从非冷冻买入
        hold = max(hold, rest - price)
        # 卖出：从持有卖出
        sell = prev_hold + price
        # 非冷冻：保持非冷冻 / 冷冻期结束
        rest = max(rest, prev_sell)

    return max(sell, rest)


# ══════════════════════════════════════════════════════════
# 重要说明：算法的正确性证明和易错点提示
# ══════════════════════════════════════════════════════════
#
# 算法正确性证明：
# ────────────────────────────────────────────────────────
#
# 定理：上述 DP 算法返回的 max(sell, rest) 等于含冷冻期的最大利润。
#
# 证明：
# （1）状态完备性：
#     任何一天结束时，投资者必然处于以下 3 种状态之一：
#     - 持有股票                           → hold
#     - 今天刚卖出（明天处于冷冻期）         → sell
#     - 不持有且非冷冻（明天可以买入）        → rest
#     3 种状态互斥且覆盖所有可能。
#
#     为什么"不持有"必须分成两个子状态？
#     因为"今天卖出"和"冷冻期已过"对后续决策的影响不同：
#     - 今天卖出 → 明天不能买入（冷冻约束）
#     - 冷冻期已过 → 明天可以买入
#     如果只用一个 sell 状态，无法区分这两种情况。
#
# （2）状态转移的正确性：
#     - hold: 保持持有 或 从 rest 买入（关键：不能从 sell 买入！）
#       hold = max(hold, rest - price)
#       如果错误地从 sell 买入：hold = max(hold, sell - price)
#       则违反冷冻期约束，因为 sell 表示"昨天刚卖出"。
#
#     - sell: 必定从 hold 卖出
#       sell = hold + price
#       这是确定性的，没有选择。因为"卖出"这个动作只从持有状态转入。
#
#     - rest: 保持非冷冻 或 冷冻期结束
#       rest = max(rest, sell)
#       昨天是 sell（刚卖出）→ 今天冷冻期结束，变成 rest。
#       昨天是 rest（非冷冻）→ 今天继续保持。
#
# （3）全局最优性（数学归纳法）：
#     基础：第1天的状态由初始条件正确给出。
#     归纳：第 i 天的状态转移覆盖所有合法选择并取最大值，
#           且无后效性（第 i 天的状态仅依赖第 i-1 天），
#           因此第 i 天的状态正确。
#     综上，最后一天的 max(sell, rest) 是全局最优解。
#
# （4）最终答案是 max(sell, rest) 而非 sell：
#     最后一天不持有股票有两种可能：
#     - 今天卖出（sell）— 但可能今天卖不是最优
#     - 之前已经不持有（rest）— 可能更早卖更优
#     所以取 max(sell, rest)。
#     hold 不参与最终答案，因为最后一天持有不如卖出。
#
#
# 为什么 hold 的买入只能从 rest 而非 sell 转入？
# ────────────────────────────────────────────────────────
# 这是本题最核心的约束。
# sell 表示"今天刚卖出"，冷冻期规则要求"卖出后下一天不能买入"。
# 所以买入只能从前天或更早卖出的状态转入，即 rest（非冷冻）。
#
# 如果错误地写成 hold = max(hold, sell - price)，
# 相当于允许"今天卖出，明天立刻买入"，冷冻期形同虚设。
#
# 对比无冷冻期的情况（股票 II / 含手续费）：
#   hold = max(hold, sell - price)   ← 无冷冻期，可以从 sell 买入
# 本题：
#   hold = max(hold, rest - price)   ← 有冷冻期，只能从 rest 买入
#
#
# 为什么 sell[i] = hold[i-1] + prices[i] 没有 max？
# ────────────────────────────────────────────────────────
# sell 表示"今天卖出"，这是一个确定的动作，不存在"保持 sell"的选择。
# 如果今天不卖出，状态就不是 sell，而是 hold 或 rest。
# 所以 sell 没有第二个选项，直接等于 hold + price。
#
# 这与 hold 和 rest 不同：
#   hold 可以"保持持有"或"买入" → 有 max
#   rest 可以"保持非冷冻"或"冷冻期结束" → 有 max
#   sell 只能"卖出" → 无 max
#
#
# sell[0] = 0 会不会影响结果？
# ────────────────────────────────────────────────────────
# sell[0] 表示"第1天卖出"，逻辑上不可能（没有股票可卖）。
# 设为 0 是因为：它只会影响 rest[1] = max(rest[0], sell[0]) = max(0, 0) = 0，
# 不影响结果。如果设为负无穷，rest[1] = max(0, -∞) = 0，结果相同。
# 但设为 0 更简洁，且不会导致负无穷的运算问题。
#
#
# 常见错误：
# ────────────────────────────────────────────────────────
# 1. 买入从 sell 状态转入（忽略冷冻期）：
#    hold = max(hold, sell - price)   ← ❌ 允许卖后立刻买
#    正确：hold = max(hold, rest - price)
#
# 2. 只用两个状态（hold 和 sell）：
#    无法区分"今天刚卖出"和"冷冻期已过"，
#    导致无法正确约束买入时机。
#    必须用三个状态：hold, sell, rest。
#
# 3. sell 的转移写成 max(sell, hold + price)：
#    sell 不存在"保持卖出"的概念，不能取 max。
#    sell[i] = hold[i-1] + prices[i] 是确定性的。
#
# 4. 滚动变量更新顺序错误：
#    hold 依赖 rest 的旧值 ✓
#    sell 依赖 hold 的旧值 — 但 hold 刚被更新！❌
#    rest 依赖 sell 的旧值 — 但 sell 刚被更新！❌
#    必须用临时变量保存旧值。
#
