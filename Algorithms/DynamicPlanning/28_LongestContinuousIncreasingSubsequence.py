"""
最长连续递增子序列（Longest Continuous Increasing Subsequence, LCIS）

题目描述：
给定一个未经排序的整数数组，找到最长且连续递增的子序列，并返回该序列的长度。
连续递增的子序列可以由两个下标 l 和 r（l < r）确定，
如果对于每个 l <= i < r，都有 nums[i] < nums[i + 1]，
那么子序列 [nums[l], nums[l+1], ..., nums[r]] 就是连续递增子序列。

输入描述：
一个整数数组 nums。

输出描述：
输出一个整数，表示最长连续递增子序列的长度。

输入示例：
[1, 3, 5, 4, 7]
输出示例：
3

════════════════════════════════════════════════════════════════════════
核心对比：LIS（子序列）vs LCIS（连续子序列）
════════════════════════════════════════════════════════════════════════

维度              LIS（子序列）                  LCIS（连续子序列，本题）
────────────────────────────────────────────────────────────────────────
元素位置          可以不连续                      必须连续
DP状态定义        dp[i] = 以 nums[i] 结尾的       dp[i] = 以 nums[i] 结尾的
                  最长递增子序列长度              最长连续递增子序列长度
DP状态转移        枚举所有 j < i 且 nums[j]<nums[i]  只看 i-1
                  dp[i] = max(dp[j]+1)            dp[i] = dp[i-1]+1（若 nums[i]>nums[i-1]）
时间复杂度        O(n²) 或 O(n log n)             O(n)
────────────────────────────────────────────────────────────────────────

为什么连续子序列比普通子序列简单？
────────────────────────────────────────────────────────────────────────
普通子序列：可以跳过中间元素，必须枚举所有可能的前驱，O(n²)。
连续子序列：只能接在前一个元素后面，只看 i-1 即可，O(n)。

"连续"这个约束大幅缩小了搜索空间，使得状态转移从 O(n) 降为 O(1)。

类比理解：
    - LIS  像是"跳跃爬山"，可以跳过坑洼，找所有能到达的位置
    - LCIS 像是"连续爬坡"，只能一步步往上走，断了就从头来

解题思路总览：
────────────────────────────────────────────────────────────────────────
解法                          时间复杂度       空间复杂度       说明
────────────────────────────────────────────────────────────────────────
暴力枚举                      O(n²)           O(1)            仅用于理解
动态规划（一维数组）            O(n)            O(n)            ⭐⭐⭐
动态规划（滚动变量优化）        O(n)            O(1)            ⭐⭐⭐⭐  推荐
贪心（单遍扫描）               O(n)            O(1)            ⭐⭐⭐⭐  推荐
────────────────────────────────────────────────────────────────────────

核心思想（动态规划）：
────────────────────────────────────────────────────────────────────────
状态定义：
    dp[i] = 以 nums[i] 结尾的最长连续递增子序列长度

状态转移：
    如果 nums[i] > nums[i-1]：dp[i] = dp[i-1] + 1（接在前一个元素后面）
    否则：dp[i] = 1（连续递增中断，从 nums[i] 重新开始）

初始条件：
    dp[0] = 1（第一个元素自身构成长度为1的连续递增子序列）

最终答案：
    max(dp[0], dp[1], ..., dp[n-1])

核心思想（贪心）：
────────────────────────────────────────────────────────────────────────
用一个计数器 cur 记录当前连续递增段的长度：
- 如果 nums[i] > nums[i-1]：cur += 1（继续递增）
- 否则：cur = 1（中断，从1重新开始）
- 每步更新 result = max(result, cur)

与 DP 本质相同，只是省去了 dp 数组，用变量替代。
"""


# ══════════════════════════════════════════════════════════
# 解法一：暴力枚举
# ══════════════════════════════════════════════════════════


def findLengthOfLCIS_brute(nums: list[int]) -> int:
    """
    暴力枚举

    核心思路：
    ────────────────────────────────────────────────────────
    以每个位置 i 为起点，向后扫描连续递增段，记录最大长度。

    算法步骤：
    1. 枚举每个起点 i
    2. 从 i+1 开始，只要 nums[j] > nums[j-1] 就继续
    3. 中断时记录当前长度，取全局最大

    时间复杂度：O(n²) — 最坏情况整个数组递增，每个起点都扫描到末尾
    空间复杂度：O(1)   — 只用常数变量

    问题分析：
    ────────────────────────────────────────────────────────
    大量重复扫描：以 i 为起点和以 i+1 为起点的扫描高度重叠。
    实际上只需一次遍历即可，不需要枚举起点。
    仅用于理解"连续递增"的含义。
    """
    n = len(nums)
    if n == 0:
        return 0

    result = 1

    for i in range(n - 1):
        # 以 i 为起点，向后扫描连续递增段
        cur = 1
        for j in range(i + 1, n):
            if nums[j] > nums[j - 1]:
                # 仍然连续递增，长度+1
                cur += 1
            else:
                # 连续递增中断，停止扫描
                break
        # result 更新必须放在内层循环外面，
        # 否则如果一直递增到末尾（不触发 else），会漏掉更新
        result = max(result, cur)

    return result


# ══════════════════════════════════════════════════════════
# 解法二：动态规划（一维数组）
# ══════════════════════════════════════════════════════════


def findLengthOfLCIS_dp(nums: list[int]) -> int:
    """
    动态规划解法（一维数组）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    dp[i] = 以 nums[i] 结尾的最长连续递增子序列长度。
    连续子序列只需看前一个元素，不需要像 LIS 那样枚举所有前驱。

    算法步骤：
    1. 初始化 dp[i] = 1
    2. 如果 nums[i] > nums[i-1]，则 dp[i] = dp[i-1] + 1
    3. 否则 dp[i] = 1（连续递增中断）
    4. 返回 max(dp)

    时间复杂度：O(n) — 单次遍历
    空间复杂度：O(n) — dp 数组

    图解示例：
    ────────────────────────────────────────────────────────
    nums = [1, 3, 5, 4, 7]

    初始化：dp = [1, 1, 1, 1, 1]

    i=1: nums[1]=3 > nums[0]=1 → dp[1] = dp[0]+1 = 2
         dp = [1, 2, 1, 1, 1]

    i=2: nums[2]=5 > nums[1]=3 → dp[2] = dp[1]+1 = 3
         dp = [1, 2, 3, 1, 1]

    i=3: nums[3]=4 < nums[2]=5 → dp[3] = 1（中断！）
         dp = [1, 2, 3, 1, 1]

    i=4: nums[4]=7 > nums[3]=4 → dp[4] = dp[3]+1 = 2
         dp = [1, 2, 3, 1, 2]

    结果：max(dp) = 3 ✓
    对应连续递增子序列：[1, 3, 5]
    """
    n = len(nums)
    if n == 0:
        return 0

    # dp[i] = 以 nums[i] 结尾的最长连续递增子序列长度
    # 初始值全为1：每个元素自身构成长度为1的连续递增子序列
    dp = [1] * n

    for i in range(1, n):
        if nums[i] > nums[i - 1]:
            # 连续递增：接在前一个元素后面，长度+1
            dp[i] = dp[i - 1] + 1
        # else: dp[i] 保持为1（连续递增中断，从当前元素重新开始）

    # 最长连续递增子序列不一定以最后一个元素结尾，取全局最大值
    return max(dp)


# ══════════════════════════════════════════════════════════
# 解法三：动态规划（滚动变量优化）
# ══════════════════════════════════════════════════════════


def findLengthOfLCIS_rolling(nums: list[int]) -> int:
    """
    动态规划解法（滚动变量优化）⭐⭐⭐⭐ 推荐

    核心思想：
    ────────────────────────────────────────────────────────
    dp[i] 只依赖 dp[i-1]，不需要整个数组，用一个变量 cur 替代。
    cur = 当前连续递增段的长度。

    时间复杂度：O(n) — 单次遍历
    空间复杂度：O(1) — 只用两个变量（cur 和 result）

    图解示例：
    ────────────────────────────────────────────────────────
    nums = [1, 3, 5, 4, 7]

    i=0: cur=1, result=1
    i=1: 3>1 → cur=2, result=2
    i=2: 5>3 → cur=3, result=3
    i=3: 4<5 → cur=1, result=3（中断，cur重置为1）
    i=4: 7>4 → cur=2, result=3

    结果：result = 3 ✓
    """
    n = len(nums)
    if n == 0:
        return 0

    # cur：当前连续递增段的长度
    cur = 1
    # result：全局最大长度
    result = 1

    for i in range(1, n):
        if nums[i] > nums[i - 1]:
            # 连续递增，长度+1
            cur += 1
            # 更新全局最大值
            result = max(result, cur)
        else:
            # 连续递增中断，从当前元素重新开始
            cur = 1

    return result


# ══════════════════════════════════════════════════════════
# 解法四：贪心（单遍扫描）
# ══════════════════════════════════════════════════════════


def findLengthOfLCIS(nums: list[int]) -> int:
    """
    贪心解法（单遍扫描）⭐⭐⭐⭐ 推荐

    核心思想：
    ────────────────────────────────────────────────────────
    与解法三本质相同，但以"滑动窗口"的视角理解：
    - 维护一个连续递增的窗口 [left, i]
    - 如果 nums[i] > nums[i-1]，窗口右边界扩展
    - 否则，窗口从 i 重新开始（left = i）
    - 每步记录窗口长度的最大值

    贪心的含义：
    ────────────────────────────────────────────────────────
    一旦连续递增中断，之前的位置不可能再构成更长的连续递增段，
    因此"贪心地"从当前位置重新开始，绝不回头。

    时间复杂度：O(n) — 单次遍历
    空间复杂度：O(1) — 只用两个变量

    图解示例：
    ────────────────────────────────────────────────────────
    nums = [1, 3, 5, 4, 7]

    i=0: left=0, 窗口=[1], 长度1
    i=1: 3>1, 窗口=[1,3], 长度2, result=2
    i=2: 5>3, 窗口=[1,3,5], 长度3, result=3
    i=3: 4<5, left=3, 窗口=[4], 长度1
    i=4: 7>4, 窗口=[4,7], 长度2, result=3

    结果：result = 3 ✓
    """
    n = len(nums)
    if n == 0:
        return 0

    # left：当前连续递增窗口的左边界
    left = 0
    result = 1

    for i in range(1, n):
        if nums[i] > nums[i - 1]:
            # 仍然连续递增，更新最大窗口长度
            result = max(result, i - left + 1)
        else:
            # 连续递增中断，窗口从 i 重新开始
            left = i

    return result


# ══════════════════════════════════════════════════════════
# 重要说明：算法的正确性证明和易错点提示
# ══════════════════════════════════════════════════════════
#
# 算法正确性证明：
# ────────────────────────────────────────────────────────
#
# 定理：上述 DP 算法返回的 max(dp) 等于最长连续递增子序列的长度。
#
# 证明：
# （1）状态完备性：
#     任何连续递增子序列都有一个"最后一个元素"，
#     因此必然以某个 nums[i] 结尾，dp[i] 覆盖了所有情况。
#
# （2）状态转移的正确性：
#     以 nums[i] 结尾的连续递增子序列，只有两种情况：
#     - nums[i] > nums[i-1]：子序列必然包含 nums[i-1]，
#       且以 nums[i-1] 结尾的连续递增子序列 + nums[i] 就是唯一的最长选择
#       （连续性保证了唯一性，不需要像 LIS 那样枚举所有前驱）
#     - nums[i] ≤ nums[i-1]：无法接在任何元素后面，子序列只有 nums[i] 自身
#
# （3）全局最优性：
#     dp[i] 记录了以每个位置结尾的局部最优，
#     全局最优一定以某个位置结尾，所以 max(dp) 是全局最优。
#
# （4）贪心解法的正确性：
#     一旦连续递增中断（nums[i] ≤ nums[i-1]），
#     包含 nums[i-1] 的连续递增段不可能再扩展，
#     从 i 重新开始是最优选择（不可能有更长的段跨越中断点）。
#
#
# LIS vs LCIS 的状态转移对比：
# ────────────────────────────────────────────────────────
# LIS（子序列）：
#   dp[i] = max(dp[j] + 1)  对所有 j < i 且 nums[j] < nums[i]
#   需要枚举所有前驱，因为子序列可以跳过中间元素。
#
# LCIS（连续子序列，本题）：
#   dp[i] = dp[i-1] + 1  若 nums[i] > nums[i-1]
#   dp[i] = 1            否则
#   只看前一个元素，因为连续性保证了只能接在 i-1 后面。
#
# 关键差异：连续性 → 唯一前驱 → O(1) 转移 → O(n) 总复杂度。
#
#
# 常见错误：
# ────────────────────────────────────────────────────────
# 1. 混淆"连续递增子序列"和"递增子序列"：
#    连续子序列只能接在前一个元素后面，不能跳过中间元素。
#    [1,3,5,4,7] 的 LCIS 是 [1,3,5] 长度3，不是 [1,3,5,7] 长度4。
#
# 2. dp[i] = dp[i-1] + 1 缺少条件判断：
#    必须在 nums[i] > nums[i-1] 时才能接上。
#    如果忘记判断，会把非递增的元素也算进去。
#
# 3. 中断时忘记重置 cur = 1（滚动变量解法）：
#    连续递增中断后，cur 应重置为1（从当前元素重新开始），
#    而不是重置为0（每个元素自身构成长度1的子序列）。
#
# 4. 暴力解法中 result 更新位置错误：
#    如果把 result = max(result, cur) 放在 else 分支内，
#    当整个数组递增（不触发 else）时会漏掉更新。
#    应该放在内层循环外面。
#
# 5. 最终答案写成 dp[-1]：
#    最长连续递增子序列不一定以最后一个元素结尾！
#    必须取 max(dp)。
#
# 6. 忘记处理空数组：
#    nums 为空时，应返回 0。
#    max(dp) 对空数组会报错，需要特判。
#
# 7. 窗口解法中 left 更新错误：
#    中断时 left 应设为 i（新窗口从 i 开始），
#    不是 i-1（i-1 是上一个窗口的末尾，nums[i] ≤ nums[i-1]，不能包含）。
# ══════════════════════════════════════════════════════════
