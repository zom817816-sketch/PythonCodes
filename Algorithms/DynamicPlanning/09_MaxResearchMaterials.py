# 输入输出处理
# ══════════════════════════════════════════════════════════


def main():
    """主函数：处理输入并输出结果"""
    import sys
    input_data = sys.stdin.read().strip().split('\n')
    
    # 解析输入
    m, n = map(int, input_data[0].split())
    space = list(map(int, input_data[1].split()))
    values = list(map(int, input_data[2].split()))

    # 验证输入
    if len(space) != m or len(values) != m:
        print("输入错误：材料数量不一致")
        return

    # 计算并输出结果
    result = maxResearchMaterials(space, values, n)
    print(result)


if __name__ == "__main__":
    main()


# ══════════════════════════════════════════════════════════
# 重要说明：算法的正确性证明
# ══════════════════════════════════════════════════════════
#
# 为什么动态规划解法是正确的？
# ────────────────────────────────────────────────────────
# 1. 最优子结构（Optimal Substructure）：
#    对于前 i 个物品、容量 j 的问题：
#        - 如果不选第 i 个物品，最优解 = 前 i-1 个物品、容量 j 的最优解
#        - 如果选第 i 个物品，最优解 = values[i] + 前 i-1 个物品、容量 j-space[i] 的最优解
#    最优解可以通过子问题的最优解构造。
#
# 2. 重叠子问题（Overlapping Subproblems）：
#    计算 dp[i][j] 需要 dp[i-1][j] 和 dp[i-1][j-space[i]]
#    这些子问题会被重复计算，用缓存或 DP 数组可以消除重复。
#
# 3. 无后效性（No Aftereffect）：
#    计算 dp[i][j] 时，只需要知道 dp[i-1][*]，
#    不需要知道这些值是如何得到的。
#
# 为什么空间优化是正确的？
# ────────────────────────────────────────────────────────
# 关键在于从后往前遍历：
#    - 如果从前往后遍历，dp[j-space[i]] 可能已经被当前物品更新过
#    - 从后往前遍历，保证 dp[j-space[i]] 是上一轮（i-1）的值
#    - 这样就避免了同一个物品被多次选择
#
# 时间复杂度对比：
# ────────────────────────────────────────────────────────
# - 暴力递归：O(2^M) — 指数级，不可接受
# - 记忆化搜索：O(M×N) — 多项式级
# - 动态规划：O(M×N) — 多项式级
# - 空间优化：O(M×N) — 多项式级，空间最优
#
# 空间复杂度对比：
# ────────────────────────────────────────────────────────
# - 暴力递归：O(M) — 递归栈
# - 记忆化搜索：O(M×N) — 缓存 + 递归栈
# - 动态规划：O(M×N) — DP 数组
# - 空间优化：O(N) — 一维 DP 数组，最优
# ══════════════════════════════════════════════════════════
