"""
不同的二叉搜索树（Unique Binary Search Trees）

题目描述：
给你一个整数 n ，求恰由 n 个节点组成且节点值从 1 到 n 互不相同的 二叉搜索树 有多少种？
返回满足题意的二叉搜索树的种数。

注意：
    - 1 <= n <= 19
    - 节点值从 1 到 n 互不相同
    - 二叉搜索树的性质：左子树所有节点值 < 根节点值 < 右子树所有节点值

示例 1：
    输入: n = 3
    输出: 5
    解释:
           1         3     3      2      1
            \       /     /      / \      \
             3     2     1      1   3      2
            /     /       \                 \
           2     1         2                 3
    共有 5 种不同的二叉搜索树。

示例 2：
    输入: n = 1
    输出: 1
    解释: 只有一种二叉搜索树：单个节点。

示例 3：
    输入: n = 2
    输出: 2
    解释:
           1         2
            \       /
             2     1

示例 4：
    输入: n = 4
    输出: 14

解题思路总览：
───────────────────────────────────────────────────────────────────────
解法                          时间复杂度    空间复杂度    说明
───────────────────────────────────────────────────────────────────────
暴力递归                       O(4^n/n^0.5)  O(n)        超时，仅用于理解
记忆化搜索（Top-Down DP）       O(n^2)       O(n)        ⭐⭐
动态规划（Bottom-Up DP）         O(n^2)       O(n)        ⭐⭐⭐
动态规划（空间优化）             O(n^2)       O(1)        ⭐⭐⭐⭐
数学解法（卡塔兰数）            O(n)         O(1)        ⭐⭐⭐⭐⭐ 最优
───────────────────────────────────────────────────────────────────────

核心思想（动态规划）：
───────────────────────────────────────────────────────────────────────
关键洞察：
1. 对于 n 个节点的二叉搜索树，可以选择任意一个节点作为根节点
2. 假设选择第 k 个节点作为根（1 <= k <= n）：
   - 左子树有 k-1 个节点（值为 1 到 k-1）
   - 右子树有 n-k 个节点（值为 k+1 到 n）
3. 左子树的种类数 × 右子树的种类数 = 以 k 为根的二叉搜索树种类数
4. 因此，dp[n] = Σ(dp[k-1] × dp[n-k])，其中 k 从 1 到 n

状态定义：
    dp[i] = 由 i 个节点组成的不同二叉搜索树的种数

状态转移：
    dp[i] = Σ(dp[j-1] × dp[i-j])，其中 j 从 1 到 i

初始条件：
    dp[0] = 1（空树也是一种有效的二叉搜索树）
    dp[1] = 1（只有一个节点）

最终答案：
    dp[n]

数学视角（卡塔兰数）：
───────────────────────────────────────────────────────────────────────
卡塔兰数定义：
    C(0) = 1
    C(n+1) = Σ(C(i) × C(n-i))，其中 i 从 0 到 n

第 n 个卡塔兰数的计算公式：
    C(n) = (1/(n+1)) × C(2n, n)
         = (2n)! / ((n+1)! × n!)

本题的结果就是第 n 个卡塔兰数！

前几个卡塔兰数：
    C(0) = 1
    C(1) = 1
    C(2) = 2
    C(3) = 5
    C(4) = 14
    C(5) = 42
    C(6) = 132
    ...
"""


# ══════════════════════════════════════════════════════════
# 解法一：暴力递归（Brute Force）
# ══════════════════════════════════════════════════════════


def numTrees_brute(n: int) -> int:
    """
    暴力递归解法（Brute Force）

    核心思路：
    ────────────────────────────────────────────────────────
    使用递归枚举所有可能的根节点，计算左子树和右子树的组合数。

    算法步骤：
    1. Base Case：
        - n == 0 或 n == 1：返回 1（空树或单节点树）
    2. 对于每个可能的根节点 k（1 到 n）：
        a. 计算左子树种类数：numTrees_brute(k-1)
        b. 计算右子树种类数：numTrees_brute(n-k)
        c. 累加左×右到结果中

    时间复杂度：O(4^n/n^0.5) — 卡塔兰数的增长速度
    空间复杂度：O(n)   — 递归调用栈深度

    问题分析：
    ────────────────────────────────────────────────────────
    对于 n = 5，递归调用如下：
        numTrees(5) 需要 numTrees(0)×numTrees(4) + numTrees(1)×numTrees(3) + ...
        numTrees(4) 需要 numTrees(0)×numTrees(3) + numTrees(1)×numTrees(2) + ...
        大量重复计算，效率极低。

    注意：这个解法在 n > 10 时就会明显变慢，仅用于理解问题。
    """
    # Base Case：空树或单节点树
    if n == 0 or n == 1:
        return 1

    total = 0
    # 枚举每个可能的根节点
    for k in range(1, n + 1):
        # 左子树有 k-1 个节点，右子树有 n-k 个节点
        left = numTrees_brute(k - 1)
        right = numTrees_brute(n - k)
        total += left * right

    return total


# ══════════════════════════════════════════════════════════
# 解法二：记忆化搜索（Top-Down DP）
# ══════════════════════════════════════════════════════════


def numTrees_memo(n: int) -> int:
    """
    记忆化搜索解法（Top-Down DP）⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    在暴力递归的基础上，增加一个缓存（一维数组），
    记录已经计算过的子问题结果，避免重复计算。

    算法步骤：
    1. 创建缓存 memo，存储已计算的二叉搜索树种数
    2. 定义递归函数 dfs(i)：返回 i 个节点的二叉搜索树种数
    3. 对于每个 i，枚举根节点 k（1 到 i）：
        a. 左子树：dfs(k-1)
        b. 右子树：dfs(i-k)
        c. 累加左×右到结果中
    4. 结果存入缓存

    时间复杂度：O(n^2) — 每个子问题只计算一次，每次计算 O(n)
    空间复杂度：O(n) — 缓存 + 递归调用栈

    图解示例：n = 3
    ────────────────────────────────────────────────────────
    计算 dfs(3)：
        k = 1: dfs(0) × dfs(2) = 1 × dfs(2)
        k = 2: dfs(1) × dfs(1) = 1 × 1 = 1
        k = 3: dfs(2) × dfs(0) = dfs(2) × 1
        
        先计算 dfs(2)：
            k = 1: dfs(0) × dfs(1) = 1 × 1 = 1
            k = 2: dfs(1) × dfs(0) = 1 × 1 = 1
            dfs(2) = 2
        
        所以 dfs(3) = 2 + 1 + 2 = 5 ✅

    子问题重叠：
        dfs(3) 需要 dfs(2), dfs(1), dfs(0)
        dfs(4) 需要 dfs(3), dfs(2), dfs(1), dfs(0)
        dfs(2) 被重复计算，用缓存避免。

    状态定义：
    - memo[i] = i 个节点组成的二叉搜索树种数

    状态转移：
    - memo[i] = Σ(memo[k-1] × memo[i-k])，其中 k 从 1 到 i
    """
    # 创建缓存，-1 表示未计算
    memo = [-1] * (n + 1)

    def dfs(i: int) -> int:
        """返回 i 个节点组成的二叉搜索树种数"""
        # Base Case：空树或单节点树
        if i == 0 or i == 1:
            return 1

        # 命中缓存
        if memo[i] != -1:
            return memo[i]

        # 计算二叉搜索树种数
        total = 0
        for k in range(1, i + 1):
            left = dfs(k - 1)
            right = dfs(i - k)
            total += left * right

        memo[i] = total
        return total

    return dfs(n)


# ══════════════════════════════════════════════════════════
# 解法三：动态规划（Bottom-Up DP）
# ══════════════════════════════════════════════════════════


def numTrees_dp(n: int) -> int:
    """
    动态规划解法（Bottom-Up DP）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    从底向上计算：先计算小数，再逐步计算大数。
    利用已计算的小数结果来推导大数的结果。

    算法步骤：
    1. 创建 DP 数组 dp，dp[i] 表示 i 个节点的二叉搜索树种数
    2. 初始化 dp[0] = 1, dp[1] = 1
    3. 对于 i 从 2 到 n：
        a. 枚举根节点 k 从 1 到 i
        b. dp[i] += dp[k-1] × dp[i-k]
    4. 返回 dp[n]

    时间复杂度：O(n^2) — 双重循环
    空间复杂度：O(n) — DP 数组

    图解示例：n = 4
    ────────────────────────────────────────────────────────
    dp[0] = 1
    dp[1] = 1

    i = 2:
        k = 1: dp[0] × dp[1] = 1 × 1 = 1
        k = 2: dp[1] × dp[0] = 1 × 1 = 1
        dp[2] = 2

    i = 3:
        k = 1: dp[0] × dp[2] = 1 × 2 = 2
        k = 2: dp[1] × dp[1] = 1 × 1 = 1
        k = 3: dp[2] × dp[0] = 2 × 1 = 2
        dp[3] = 5

    i = 4:
        k = 1: dp[0] × dp[3] = 1 × 5 = 5
        k = 2: dp[1] × dp[2] = 1 × 2 = 2
        k = 3: dp[2] × dp[1] = 2 × 1 = 2
        k = 4: dp[3] × dp[0] = 5 × 1 = 5
        dp[4] = 14 ✅

    状态定义：
    - dp[i] = i 个节点组成的二叉搜索树种数

    状态转移：
    - dp[i] = Σ(dp[k-1] × dp[i-k])，其中 k 从 1 到 i

    初始条件：
    - dp[0] = 1（空树）
    - dp[1] = 1（单节点树）

    最终答案：
    - dp[n]
    """
    # 创建 DP 数组
    dp = [0] * (n + 1)
    dp[0] = 1  # 空树
    dp[1] = 1  # 单节点树

    # 从 2 开始计算
    for i in range(2, n + 1):
        # 枚举根节点 k
        for k in range(1, i + 1):
            # 左子树有 k-1 个节点，右子树有 i-k 个节点
            dp[i] += dp[k - 1] * dp[i - k]

    return dp[n]


# ══════════════════════════════════════════════════════════
# 解法四：数学解法（卡塔兰数）
# ══════════════════════════════════════════════════════════


def numTrees(n: int) -> int:
    """
    数学解法（卡塔兰数）⭐⭐⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    本题的结果就是第 n 个卡塔兰数！

    卡塔兰数的性质：
    1. C(0) = 1
    2. C(n+1) = Σ(C(i) × C(n-i))，其中 i 从 0 到 n
    3. C(n) = (1/(n+1)) × C(2n, n)

    计算方法：
    可以通过递推公式计算：
        C(n+1) = C(n) × 2 × (2n + 1) / (n + 2)

    算法步骤：
    1. 初始化 catalan = 1（C(0)）
    2. 对于 i 从 0 到 n-1：
        catalan = catalan × 2 × (2i + 1) / (i + 2)
    3. 返回 catalan

    时间复杂度：O(n) — 单次循环
    空间复杂度：O(1) — 只用了常数空间

    图解示例：n = 3
    ────────────────────────────────────────────────────────
    catalan = 1（C(0)）
    
    i = 0: catalan = 1 × 2 × 1 / 2 = 1（C(1)）
    i = 1: catalan = 1 × 2 × 3 / 3 = 2（C(2)）
    i = 2: catalan = 2 × 2 × 5 / 4 = 5（C(3)）
    
    结果：5 ✅

    图解示例：n = 4
    ────────────────────────────────────────────────────────
    i = 3: catalan = 5 × 2 × 7 / 5 = 14（C(4)）
    
    结果：14 ✅
    """
    # 初始化为 C(0) = 1
    catalan = 1

    for i in range(n):
        # 递推公式：C(n+1) = C(n) × 2 × (2n + 1) / (n + 2)
        catalan = catalan * 2 * (2 * i + 1) // (i + 2)

    return catalan


# ══════════════════════════════════════════════════════════
# 重要说明：算法的正确性证明
# ══════════════════════════════════════════════════════════
#
# 为什么动态规划解法是正确的？
# ────────────────────────────────────────────────────────
# 1. 最优子结构（Optimal Substructure）：
#    对于 n 个节点的二叉搜索树，选择 k 作为根后：
#        - 左子树有 k-1 个节点，种数为 dp[k-1]
#        - 右子树有 n-k 个节点，种数为 dp[n-k]
#    总的种数为 dp[k-1] × dp[n-k]
#    最优解可以通过子问题的最优解构造。
#
# 2. 重叠子问题（Overlapping Subproblems）：
#    计算 dp[4] 需要 dp[3], dp[2], dp[1], dp[0]
#    计算 dp[3] 需要 dp[2], dp[1], dp[0]
#    子问题被重复计算，用缓存或 DP 数组可以消除重复。
#
# 3. 无后效性（No Aftereffect）：
#    计算 dp[i] 时，只需要知道 dp[0] 到 dp[i-1]，
#    不需要知道这些值是如何得到的。
#
# 为什么本题结果是卡塔兰数？
# ────────────────────────────────────────────────────────
# 卡塔兰数的递推公式：
#    C(0) = 1
#    C(n+1) = Σ(C(i) × C(n-i))，其中 i 从 0 到 n
#
# 本题的递推公式：
#    dp[0] = 1
#    dp[n] = Σ(dp[k-1] × dp[n-k])，其中 k 从 1 到 n
#
# 令 i = k-1，则 n-k = (n-1)-i，所以：
#    dp[n] = Σ(dp[i] × dp[(n-1)-i])，其中 i 从 0 到 n-1
#
# 这正是卡塔兰数的递推公式！因此 dp[n] = C(n)。
#
# 卡塔兰数的应用场景：
# ────────────────────────────────────────────────────────
# 1. 不同的二叉搜索树个数（本题）
# 2. 有效的括号序列数目
# 3. 多边形三角划分方案数
# 4. 出栈序列数目
# 5. 二叉树的结构种类数
#
# 时间复杂度对比：
# ────────────────────────────────────────────────────────
# - 暴力递归：O(4^n/n^0.5) — 指数级，不可接受
# - 记忆化搜索：O(n^2) — 多项式级
# - 动态规划：O(n^2) — 多项式级
# - 数学解法：O(n) — 最优
#
# 空间复杂度对比：
# ────────────────────────────────────────────────────────
# - 暴力递归：O(n) — 递归栈
# - 记忆化搜索：O(n) — 缓存 + 递归栈
# - 动态规划：O(n) — DP 数组
# - 数学解法：O(1) — 最优
# ══════════════════════════════════════════════════════════
