"""
组合总和 IV（Combination Sum IV）

题目描述：
给你一个由 不同 整数组成的数组 nums ，和一个目标整数 target 。
请你从 nums 中找出并返回总和为 target 的元素组合的个数。
题目数据保证答案符合 32 位整数范围。

示例 1：
    输入：nums = [1,2,3], target = 4
    输出：7
    解释：
    所有可能的组合为：
    (1, 1, 1, 1)
    (1, 1, 2)
    (1, 2, 1)
    (1, 3)
    (2, 1, 1)
    (2, 2)
    (3, 1)
    请注意，顺序不同的序列被视作不同的组合。

示例 2：
    输入：nums = [9], target = 3
    输出：0

════════════════════════════════════════════════════════════════════════
核心对比：本题与零钱兑换 II（Coin Change II）的本质区别
════════════════════════════════════════════════════════════════════════

维度              零钱兑换 II（Coin Change II）    组合总和 IV（本题）
────────────────────────────────────────────────────────────────────────
问题本质          求组合数（Combinations）         求排列数（Permutations）
顺序是否重要      不重要（1+2 和 2+1 视为相同）     重要（1+2 和 2+1 视为不同）
回溯遍历范围      for i in range(index, n)         for i in range(n)
记忆化状态        memo[(remaining, index)]         memo[remaining]
DP 循环顺序       外层硬币，内层金额               外层金额，内层数字
递推公式          dp[i] += dp[i - coin]            dp[i] += dp[i - num]
────────────────────────────────────────────────────────────────────────

为什么本题是排列数？
────────────────────────────────────────────────────────────────────────
本题要求"顺序不同的序列被视作不同的组合"，这意味着：
    - (1, 2) 和 (2, 1) 是两种完全不同的方案
    - 对于 target = 4, nums = [1, 2, 3]，有 7 种方案
    - 而在 Coin Change II 中，同样的输入只会得到 4 种方案
      {1,1,1,1}, {1,1,2}, {2,2}, {1,3}

类比理解：
    - Coin Change II 像"凑零钱"：你手里有 1 元和 2 元，凑 3 元只有 {1,1,1} 和 {1,2} 两种方式
    - 本题像"爬楼梯"：每次可以跨 1 步或 2 步，爬 3 楼有 {1,1,1}, {1,2}, {2,1} 三种方式

解题思路总览：
────────────────────────────────────────────────────────────────────────
解法                          时间复杂度       空间复杂度       说明
────────────────────────────────────────────────────────────────────────
回溯算法                       O(n^target)     O(target)       超时，仅用于理解
记忆化搜索（Top-Down DP）       O(n×target)     O(target)       ⭐⭐
动态规划（Bottom-Up DP）        O(n×target)     O(target)       ⭐⭐⭐⭐  推荐
────────────────────────────────────────────────────────────────────────

核心思想（动态规划）：
────────────────────────────────────────────────────────────────────────
关键洞察（转化为爬楼梯问题）：
1. 本题本质上是一个"爬楼梯"问题的扩展
2. 每次可以跨 nums[0]、nums[1]、...、nums[n-1] 步
3. 问爬到第 target 层有多少种不同的走法
4. 这与 Coin Change II 的区别在于：每一步的选择是独立的，顺序影响结果

状态定义（一维 DP）：
    dp[i] = 凑成金额 i 的排列数

状态转移：
    对于每个金额 i，遍历所有数字 num：
    如果 num <= i：
        dp[i] += dp[i - num]

    含义：凑成 i 的排列数 = 所有"最后一步选 num"的方案数之和

初始条件：
    dp[0] = 1（凑成金额 0 有 1 种方案：不选任何数字）

最终答案：
    dp[target]
"""


# ══════════════════════════════════════════════════════════
# 解法一：回溯算法（Brute Force）
# ══════════════════════════════════════════════════════════


def combinationSum4_brute(nums: list[int], target: int) -> int:
    """
    回溯算法（Brute Force）

    核心思路：
    ────────────────────────────────────────────────────────
    枚举所有可能的排列，统计能凑成 target 的方案数。

    算法步骤：
    1. 使用回溯遍历所有可能的序列
    2. 维护当前序列的和 current_sum
    3. 当 current_sum == target 时，计数加一
    4. 当 current_sum > target 时，剪枝

    时间复杂度：O(n^target) — 最坏情况每层递归 n 个分支
    空间复杂度：O(target)   — 递归调用栈深度

    ⚠️ 与 Coin Change II 的关键区别：
    ────────────────────────────────────────────────────────
    本题是排列数，顺序不同算不同方案，因此回溯时：
    - 每次都可以从 0 开始选（for i in range(n)）
    - 不需要 index 参数限制遍历范围

    而在 Coin Change II 中（组合数）：
    - 必须从 index 开始选（for i in range(index, n)）
    - 防止重复计算排列
    """
    result = 0
    n = len(nums)

    def backtrack(current_sum: int) -> None:
        nonlocal result
        if current_sum > target:
            return
        if current_sum == target:
            result += 1
            return

        # ⚠️ 本题是排列数，每次都可以重新选所有数字
        for i in range(n):
            current_sum += nums[i]
            backtrack(current_sum)
            current_sum -= nums[i]

    backtrack(0)
    return result


# ══════════════════════════════════════════════════════════
# 解法二：记忆化搜索（Top-Down DP）
# ══════════════════════════════════════════════════════════


def combinationSum4_memo(nums: list[int], target: int) -> int:
    """
    记忆化搜索解法（Top-Down DP）⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    将问题转化为爬楼梯问题：
    - 每次可以跨 nums[0]、nums[1]、...、nums[n-1] 步
    - 问爬到第 target 层有多少种不同的走法

    算法步骤：
    1. 创建缓存 memo[remaining]
    2. 使用递归 + 缓存求解
    3. 对于每个剩余金额，遍历所有数字

    时间复杂度：O(n×target) — 每个状态只计算一次
    空间复杂度：O(target)   — 缓存 + 递归调用栈

    ⚠️ 与 Coin Change II 的关键区别：
    ────────────────────────────────────────────────────────
    本题的记忆化搜索只需要一个维度：
    - memo[remaining] = 凑成 remaining 的排列数
    - 每次递归都可以重新选所有数字

    Coin Change II 需要两个维度：
    - memo[(remaining, index)] = 从 index 开始凑成 remaining 的组合数
    - 需要 index 限制只能从当前硬币往后选

    为什么本题不需要 index？
    ────────────────────────────────────────────────────────
    因为本题是排列数，顺序不同算不同方案。
    例如 nums=[1,2], target=3：
    - (1,2) 和 (2,1) 是两种不同的方案
    - 在递归中，每一步都可以重新选择 1 或 2
    - 不需要限制"只能从某个位置往后选"

    状态定义：
    - memo[remaining] = 凑成 remaining 的排列数

    状态转移：
    - memo[remaining] = sum(memo[remaining - num] for num in nums if num <= remaining)

    初始条件：
    - remaining == 0 时返回 1（找到一种有效排列）
    """
    memo = {}

    def dfs(remaining: int) -> int:
        if remaining == 0:
            return 1
        if remaining in memo:
            return memo[remaining]

        total = 0
        # 本题是排列数，每次都可以选所有数字
        for num in nums:
            if num <= remaining:
                total += dfs(remaining - num)

        memo[remaining] = total
        return total

    return dfs(target)


