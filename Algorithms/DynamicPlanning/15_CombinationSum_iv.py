# 解法三：动态规划（Bottom-Up DP）
# ══════════════════════════════════════════════════════════


def combinationSum4(nums: list[int], target: int) -> int:
    """
    动态规划解法（Bottom-Up DP）⭐⭐⭐⭐ 推荐

    核心思想：
    ────────────────────────────────────────────────────────
    标准的爬楼梯问题 DP 解法。
    通过控制循环顺序来保证计算的是排列数。

    算法步骤：
    1. 创建一维 DP 数组 dp[target+1]，初始化 dp[0] = 1
    2. 外层循环遍历金额，内层循环遍历数字
    3. 对于每个金额 i，更新 dp[i] += dp[i - num]

    时间复杂度：O(n×target) — 双重循环
    空间复杂度：O(target)   — 一维 DP 数组

    ⚠️ 易错点：循环顺序！
    ────────────────────────────────────────────────────────
    外层必须遍历金额，内层遍历数字！

    错误写法（计算组合数）：
        for num in nums:                     # 外层遍历数字
            for i in range(num, target + 1):   # 内层遍历金额
                dp[i] += dp[i - num]

    正确写法（计算排列数）：
        for i in range(1, target + 1):        # 外层遍历金额
            for num in nums:                   # 内层遍历数字
                if num <= i:
                    dp[i] += dp[i - num]

    为什么顺序很重要？
    ────────────────────────────────────────────────────────
    - 外层遍历金额：对于每个金额 i，考虑所有可能的"最后一步"
      例如 i=3 时，最后一步可以是 1、2 或 3
      这样 dp[3] = dp[2] + dp[1] + dp[0]
      分别对应以 1 结尾、以 2 结尾、以 3 结尾的排列

    - 外层遍历数字：每种数字按顺序被考虑
      先处理 num=1，再处理 num=2
      这样 {1,2} 和 {2,1} 只会被计算一次（变成组合数）

    图示示例：
    ────────────────────────────────────────────────────────
    nums = [1, 2, 3], target = 4

    正确写法（外层遍历金额，计算排列数）：
    dp = [1, 0, 0, 0, 0]

    i=1: num=1 → dp[1] += dp[0] = 1
         dp = [1, 1, 0, 0, 0]

    i=2: num=1 → dp[2] += dp[1] = 1
         num=2 → dp[2] += dp[0] = 2
         dp = [1, 1, 2, 0, 0]
         （排列：(1,1), (2)）

    i=3: num=1 → dp[3] += dp[2] = 2
         num=2 → dp[3] += dp[1] = 3
         num=3 → dp[3] += dp[0] = 4
         dp = [1, 1, 2, 4, 0]
         （排列：(1,1,1), (1,2), (2,1), (3)）

    i=4: num=1 → dp[4] += dp[3] = 4
         num=2 → dp[4] += dp[2] = 6
         num=3 → dp[4] += dp[1] = 7
         dp = [1, 1, 2, 4, 7]
         （排列：(1,1,1,1), (1,1,2), (1,2,1), (1,3), (2,1,1), (2,2), (3,1)）

    结果：dp[4] = 7 ✓

    错误写法（外层遍历数字，计算组合数）：
    dp = [1, 0, 0, 0, 0]

    num=1: dp[1] += dp[0] = 1
           dp[2] += dp[1] = 1
           dp[3] += dp[2] = 1
           dp[4] += dp[3] = 1
           dp = [1, 1, 1, 1, 1]

    num=2: dp[2] += dp[0] = 2
           dp[3] += dp[1] = 3
           dp[4] += dp[2] = 3
           dp = [1, 1, 2, 3, 3]

    num=3: dp[3] += dp[0] = 4
           dp[4] += dp[1] = 4
           dp = [1, 1, 2, 4, 4]

    结果：dp[4] = 4 ✗（错误！这是组合数，不是排列数）

    状态定义：
    - dp[i] = 凑成金额 i 的排列数

    状态转移：
    - dp[i] += dp[i - num]（当 num <= i）

    初始条件：
    - dp[0] = 1（凑成金额 0 有 1 种方案：不选任何数字）

    最终答案：
    - dp[target]
    """
    dp = [0] * (target + 1)
    dp[0] = 1

    # ⚠️ 外层遍历金额，内层遍历数字（顺序不能反！）
    for i in range(1, target + 1):
        for num in nums:
            if num <= i:
                dp[i] += dp[i - num]

    return dp[target]


# ══════════════════════════════════════════════════════════
# 重要说明：算法的正确性证明
# ══════════════════════════════════════════════════════════
#
# 为什么这个问题是求排列数？
# ────────────────────────────────────────────────────────
# 1. 题目明确说明"顺序不同的序列被视作不同的组合"
# 2. 例如 nums=[1,2], target=3：
#    - (1,2) 和 (2,1) 是两种不同的方案
#    - 这与 Coin Change II 中视为同一种方案不同
#
# 为什么外层遍历金额、内层遍历数字能得到排列数？
# ────────────────────────────────────────────────────────
# 核心思想：dp[i] 表示凑成金额 i 的排列数
#
# 对于每个金额 i，我们考虑所有可能的"最后一步"：
#    - 最后一步选 1：前面需要凑成 i-1，方案数为 dp[i-1]
#    - 最后一步选 2：前面需要凑成 i-2，方案数为 dp[i-2]
#    - ...
#    - 最后一步选 num：前面需要凑成 i-num，方案数为 dp[i-num]
#
# 因为我们在内层遍历所有数字，所以每种"最后一步"都被独立考虑。
# 这意味着顺序不同的序列会被分别计数。
#
# 对比 Coin Change II（组合数）：
# ────────────────────────────────────────────────────────
# Coin Change II 外层遍历硬币，内层遍历金额：
#    - 先处理硬币 1，再处理硬币 2
#    - 硬币 2 只能在硬币 1 之后被考虑
#    - 这样 {1,2} 和 {2,1} 只会被计算一次
#
# 本题外层遍历金额，内层遍历数字：
#    - 对于每个金额 i，所有数字都可以作为"最后一步"
#    - 硬币 1 和硬币 2 的地位完全平等
#    - 这样 {1,2} 和 {2,1} 会被分别计算
#
# 回溯法的对比：
# ────────────────────────────────────────────────────────
# Coin Change II（组合数）：
#    for i in range(index, n):
#        backtrack(i, current_sum + coins[i])
#    - 只能从 index 往后选，防止重复排列
#
# 本题（排列数）：
#    for i in range(n):
#        backtrack(current_sum + nums[i])
#    - 每次都可以选所有数字，顺序不同算不同方案
#
# 记忆化搜索的对比：
# ────────────────────────────────────────────────────────
# Coin Change II（组合数）：
#    memo[(remaining, index)]
#    - 需要 index 参数限制只能从当前硬币往后选
#
# 本题（排列数）：
#    memo[remaining]
#    - 只需要 remaining 一个维度
#    - 每次都可以重新选所有数字
#
# 时间复杂度对比：
# ────────────────────────────────────────────────────────
# - 回溯算法：O(n^target) — 指数级，不可接受
# - 记忆化搜索：O(n×target) — 多项式级
# - 动态规划：O(n×target) — 多项式级，空间最优
#
# 常见错误：
# ────────────────────────────────────────────────────────
# 1. 循环顺序写反：外层遍历数字、内层遍历金额（计算组合数）
# 2. 记忆化搜索缺少 index 参数（本题不需要 index，但 Coin Change II 需要）
# 3. 混淆排列数和组合数：本题是排列数，Coin Change II 是组合数
# 4. 忘记初始条件 dp[0] = 1
# ══════════════════════════════════════════════════════════
