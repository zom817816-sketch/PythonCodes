"""
一和零（Ones and Zeros）

题目描述：
给你一个二进制字符串数组 strs 和两个整数 m 和 n 。
请你找出并返回 strs 的最大子集的长度，该子集中 最多 有 m 个 0 和 n 个 1 。
如果 x 的所有元素也是 y 的元素，集合 x 是集合 y 的 子集 。

示例 1：
    输入：strs = ["10", "0001", "111001", "1", "0"], m = 5, n = 3
    输出：4
    解释：最多有 5 个 0 和 3 个 1 的最大子集是 {"10","0001","1","0"} ，因此答案是 4 。
    其他满足题意但较小的子集包括 {"0001","1"} 和 {"10","1","0"} 。
    {"111001"} 不满足题意，因为它含 4 个 1 ，大于 n 的值 3 。

示例 2：
    输入：strs = ["10", "0", "1"], m = 1, n = 1
    输出：2
    解释：最大的子集是 {"0", "1"} ，所以答案是 2 。

解题思路总览：
────────────────────────────────────────────────────────────────────────
解法                          时间复杂度       空间复杂度       说明
────────────────────────────────────────────────────────────────────────
回溯算法                       O(2^k)         O(k)           超时，仅用于理解
记忆化搜索（Top-Down DP）       O(k×m×n)       O(k×m×n)       ⭐⭐
动态规划（Bottom-Up DP）         O(k×m×n)       O(k×m×n)       ⭐⭐⭐
动态规划（空间优化）             O(k×m×n)       O(m×n)         ⭐⭐⭐⭐  推荐
────────────────────────────────────────────────────────────────────────
（k = len(strs)）

核心思想（动态规划）：
────────────────────────────────────────────────────────────────────────
关键洞察（转化为 0-1 背包问题）：
1. 每个字符串可以看作一个物品，有两种"重量"：
   - 0 的个数（zeros）
   - 1 的个数（ones）
2. 背包有两个维度的容量限制：
   - m：最多能容纳的 0 的个数
   - n：最多能容纳的 1 的个数
3. 目标：在不超过容量限制的前提下，选取尽可能多的物品（字符串）
4. 这是一个二维费用的 0-1 背包问题

状态定义（二维优化）：
    dp[i][j] = 使用最多 i 个 0 和 j 个 1 时，能选取的最大字符串数量

状态转移：
    对于每个字符串 s（zeros 个 0，ones 个 1）：
    dp[i][j] = max(dp[i][j], dp[i-zeros][j-ones] + 1)  （当 i >= zeros 且 j >= ones）

初始条件：
    dp[0][0] = 0（没有容量时，无法选取任何字符串）

最终答案：
    dp[m][n]
"""


# ══════════════════════════════════════════════════════════
# 解法一：回溯算法（Brute Force）
# ══════════════════════════════════════════════════════════


def findMaxForm_brute(strs: list, m: int, n: int) -> int:
    """
    回溯算法（Brute Force）

    核心思路：
    ────────────────────────────────────────────────────────
    枚举所有可能的子集，检查每个子集是否满足 0 和 1 的数量限制，
    返回满足条件的最大子集长度。

    算法步骤：
    1. 使用回溯遍历所有可能的子集
    2. 对于每个字符串，尝试选或不选
    3. 维护当前子集中 0 和 1 的总数
    4. 到达数组末尾时，检查是否满足限制条件

    时间复杂度：O(2^k) — 指数级增长（k 为字符串数量）
    空间复杂度：O(k)   — 递归调用栈深度

    问题分析：
    ────────────────────────────────────────────────────────
    对于 k = 60，就有 2^60 ≈ 10^18 种可能，效率极低。
    仅用于理解问题本质。
    """
    k = len(strs)
    max_count = 0

    def backtrack(index: int, used_zeros: int, used_ones: int, count: int) -> None:
        nonlocal max_count
        if used_zeros > m or used_ones > n:
            return

        if index == k:
            max_count = max(max_count, count)
            return

        # 不选当前字符串
        backtrack(index + 1, used_zeros, used_ones, count)

        # 选当前字符串
        zeros = strs[index].count("0")
        ones = strs[index].count("1")
        backtrack(index + 1, used_zeros + zeros, used_ones + ones, count + 1)

    backtrack(0, 0, 0, 0)
    return max_count


# ══════════════════════════════════════════════════════════
# 解法二：记忆化搜索（Top-Down DP）
# ══════════════════════════════════════════════════════════


def findMaxForm_memo(strs: list, m: int, n: int) -> int:
    """
    记忆化搜索解法（Top-Down DP）⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    将问题转化为二维费用的 0-1 背包问题：
    - 每个字符串是一个物品
    - 背包有两个维度的容量：m 个 0，n 个 1
    - 目标：选取最多的物品

    算法步骤：
    1. 预处理每个字符串的 0 和 1 的个数
    2. 创建缓存 memo[index][zeros][ones]，表示从第 index 个字符串开始，
       在剩余 zeros 个 0 和 ones 个 1 的限制下，能选取的最大数量
    3. 使用递归 + 缓存求解

    时间复杂度：O(k×m×n) — 每个状态只计算一次
    空间复杂度：O(k×m×n) — 缓存 + 递归调用栈

    状态定义：
    - memo[i][z][o] = 从第 i 个字符串开始，剩余 z 个 0 和 o 个 1 时，
      能选取的最大字符串数量

    状态转移：
    - 不选字符串 i：dfs(i+1, z, o)
    - 选字符串 i（如果 z >= zeros[i] 且 o >= ones[i]）：
      dfs(i+1, z-zeros[i], o-ones[i]) + 1
    - memo[i][z][o] = 以上两种选择的最大值
    """
    k = len(strs)

    # 预处理每个字符串的 0 和 1 的个数
    counts = [(s.count("0"), s.count("1")) for s in strs]

    # 创建缓存
    memo = [[[-1] * (n + 1) for _ in range(m + 1)] for _ in range(k)]

    def dfs(index: int, remaining_zeros: int, remaining_ones: int) -> int:
        if index == k:
            return 0

        if memo[index][remaining_zeros][remaining_ones] != -1:
            return memo[index][remaining_zeros][remaining_ones]

        # 不选当前字符串
        result = dfs(index + 1, remaining_zeros, remaining_ones)

        # 选当前字符串
        zeros, ones = counts[index]
        if remaining_zeros >= zeros and remaining_ones >= ones:
            result = max(
                result,
                dfs(index + 1, remaining_zeros - zeros, remaining_ones - ones) + 1,
            )

        memo[index][remaining_zeros][remaining_ones] = result
        return memo[index][remaining_zeros][remaining_ones]

    return dfs(0, m, n)


# ══════════════════════════════════════════════════════════
# 解法三：动态规划（Bottom-Up DP）
# ══════════════════════════════════════════════════════════


def findMaxForm_dp(strs: list, m: int, n: int) -> int:
    """
    动态规划解法（Bottom-Up DP）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    标准的二维费用 0-1 背包实现。

    算法步骤：
    1. 预处理每个字符串的 0 和 1 的个数
    2. 创建三维 DP 数组 dp[k+1][m+1][n+1]
    3. dp[i][j][k] = 前 i 个字符串中，使用最多 j 个 0 和 k 个 1 时，
       能选取的最大数量
    4. 从前往后计算，返回 dp[k][m][n]

    时间复杂度：O(k×m×n) — 三重循环
    空间复杂度：O(k×m×n) — DP 数组

    图解示例：
    ────────────────────────────────────────────────────────
    strs = ["10", "0", "1"], m = 1, n = 1

    每个字符串的 (zeros, ones)：
    "10" -> (1, 1)
    "0"  -> (1, 0)
    "1"  -> (0, 1)

    dp 表格（简化，只看 dp[i][1][1]）：
    i=0: dp[0][*][*] = 0（没有字符串可选）
    i=1 ("10"): dp[1][1][1] = 1（选 "10"）
    i=2 ("0"):  dp[2][1][1] = 1（选 "10" 或选 "0"）
    i=3 ("1"):  dp[3][1][1] = 2（选 "0" 和 "1"）

    最终答案：2 ✓

    状态定义：
    - dp[i][j][k] = 前 i 个字符串中，使用最多 j 个 0 和 k 个 1 时，
      能选取的最大字符串数量

    状态转移：
    - 不选第 i 个字符串：dp[i-1][j][k]
    - 选第 i 个字符串（如果 j >= zeros[i-1] 且 k >= ones[i-1]）：
      dp[i-1][j-zeros[i-1]][k-ones[i-1]] + 1
    - dp[i][j][k] = 以上两种的最大值

    初始条件：
    - dp[0][*][*] = 0（没有字符串可选时，数量为 0）

    最终答案：
    - dp[k][m][n]
    """
    k = len(strs)

    # 预处理每个字符串的 0 和 1 的个数
    counts = [(s.count("0"), s.count("1")) for s in strs]

    # 创建三维 DP 数组
    dp = [[[0] * (n + 1) for _ in range(m + 1)] for _ in range(k + 1)]

    for i in range(1, k + 1):
        zeros, ones = counts[i - 1]
        for j in range(m + 1):
            for p in range(n + 1):
                # 不选第 i 个字符串
                dp[i][j][p] = dp[i - 1][j][p]

                # 选第 i 个字符串
                if j >= zeros and p >= ones:
                    dp[i][j][p] = max(dp[i][j][p], dp[i - 1][j - zeros][p - ones] + 1)

    return dp[k][m][n]


# ══════════════════════════════════════════════════════════
# 解法四：动态规划（空间优化）
# ══════════════════════════════════════════════════════════


def findMaxForm(strs: list, m: int, n: int) -> int:
    """
    动态规划解法（空间优化）⭐⭐⭐⭐ 推荐

    核心思想：
    ────────────────────────────────────────────────────────
    观察到 dp[i][j][k] 只依赖于 dp[i-1][...][...]，
    可以使用二维数组，从后往前遍历，避免覆盖需要的数据。

    算法步骤：
    1. 预处理每个字符串的 0 和 1 的个数
    2. 创建二维 DP 数组 dp[m+1][n+1]，初始化为 0
    3. 对每个字符串，从后往前更新 dp 数组
    4. 返回 dp[m][n]

    时间复杂度：O(k×m×n) — 三重循环
    空间复杂度：O(m×n)   — 二维 DP 数组

    图解示例：
    ────────────────────────────────────────────────────────
    strs = ["10", "0", "1"], m = 1, n = 1

    每个字符串的 (zeros, ones)：
    "10" -> (1, 1)
    "0"  -> (1, 0)
    "1"  -> (0, 1)

    dp 初始化（全部为 0）：
         j=0  j=1
    i=0  [0,   0]
    i=1  [0,   0]

    遍历 "10" (zeros=1, ones=1)（从后往前）：
    dp[1][1] = max(dp[1][1], dp[0][0] + 1) = 1
         j=0  j=1
    i=0  [0,   0]
    i=1  [0,   1]

    遍历 "0" (zeros=1, ones=0)（从后往前）：
    dp[1][1] = max(dp[1][1], dp[0][1] + 1) = 1
    dp[1][0] = max(dp[1][0], dp[0][0] + 1) = 1
         j=0  j=1
    i=0  [0,   0]
    i=1  [1,   1]

    遍历 "1" (zeros=0, ones=1)（从后往前）：
    dp[1][1] = max(dp[1][1], dp[1][0] + 1) = 2
         j=0  j=1
    i=0  [0,   0]
    i=1  [1,   2]

    最终 dp[1][1] = 2
    答案：2 ✓

    关键点：从后往前遍历！
    ────────────────────────────────────────────────────────
    为什么从后往前？
        - dp[j][k] 新值依赖于 dp[j-zeros][k-ones]（上一轮的值）
        - 从后往前遍历，保证 dp[j-zeros][k-ones] 是未被当前字符串更新过的值
        - 从前往后遍历会导致同一个字符串被重复选择（变成完全背包）

    状态定义：
    - dp[j][k] = 使用最多 j 个 0 和 k 个 1 时，能选取的最大字符串数量

    状态转移：
    - dp[j][k] = max(dp[j][k], dp[j-zeros][k-ones] + 1)

    初始条件：
    - dp[0][0] = 0（没有容量时，无法选取任何字符串）

    最终答案：
    - dp[m][n]
    """
    # 预处理每个字符串的 0 和 1 的个数
    counts = [(s.count("0"), s.count("1")) for s in strs]

    # 创建二维 DP 数组
    dp = [[0] * (n + 1) for _ in range(m + 1)]

    for zeros, ones in counts:
        # 从后往前遍历，避免重复选择同一个字符串
        for j in range(m, zeros - 1, -1):
            for k in range(n, ones - 1, -1):
                dp[j][k] = max(dp[j][k], dp[j - zeros][k - ones] + 1)

    return dp[m][n]


# ══════════════════════════════════════════════════════════
# 重要说明：算法的正确性证明
# ══════════════════════════════════════════════════════════
#
# 为什么这个问题可以转化为 0-1 背包问题？
# ────────────────────────────────────────────────────────
# 1. 每个字符串只能选一次（子集的性质）→ 0-1 背包
# 2. 每个字符串有两种"重量"（0 的个数和 1 的个数）→ 二维费用
# 3. 背包容量为 (m, n)，目标是装入尽可能多的物品 → 求最大数量
#
# 为什么空间优化后必须从后往前遍历？
# ────────────────────────────────────────────────────────
# 从后往前遍历一维 dp 数组：
#    - dp[j-zeros][k-ones] 是上一轮（未考虑当前字符串）的值
#    - 保证每个字符串最多被选择一次
# 从前向后遍历：
#    - dp[j-zeros][k-ones] 可能已被当前字符串更新过
#    - 导致同一个字符串被多次选择（变成完全背包）
#
# 为什么二维 DP 可以优化为一维？
# ────────────────────────────────────────────────────────
# 1. 状态转移 dp[i][j][k] 只依赖于 dp[i-1][...][...]
# 2. 每一层只用到上一层的数据
# 3. 因此可以用一维数组滚动更新，空间从 O(k×m×n) 降到 O(m×n)
#
# 时间复杂度对比：
# ────────────────────────────────────────────────────────
# - 回溯算法：O(2^k) — 指数级，不可接受
# - 记忆化搜索：O(k×m×n) — 多项式级
# - 动态规划：O(k×m×n) — 多项式级
# - 空间优化：O(k×m×n) — 多项式级，空间最优
#
# 常见错误：
# ────────────────────────────────────────────────────────
# 1. 从前向后遍历一维 dp（导致同一个字符串被重复选择）
# 2. 初始条件错误：dp[0][0] 应初始化为 0（没有容量时无法选取）
# 3. 混淆 0-1 背包和完全背包：本题每个字符串只能用一次
