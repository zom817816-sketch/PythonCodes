"""
最长递增子序列（Longest Increasing Subsequence, LIS）

题目描述：
给你一个整数数组 nums，找到其中最长严格递增子序列的长度。
子序列是由数组派生而来的序列，删除（或不删除）数组中的元素而不改变其余元素的顺序。

输入描述：
一个整数数组 nums。

输出描述：
输出一个整数，表示最长严格递增子序列的长度。

输入示例：
[10, 9, 2, 5, 3, 7, 101, 18]
输出示例：
4

════════════════════════════════════════════════════════════════════════
核心概念：子序列 vs 子数组（子串）
════════════════════════════════════════════════════════════════════════

维度              子数组（Subarray）            子序列（Subsequence）
────────────────────────────────────────────────────────────────────────
元素位置          必须连续                      可以不连续
示例              [0,3,1,6,2,2,7]              [0,3,1,6,2,2,7]
                  子数组 [3,1,6] ✓             子序列 [3,6,7] ✓
                  子序列 [3,1,7] ✓             子数组 [3,1,7] ✗（不连续）
DP状态            只看前一个元素                必须看前面所有更小元素
时间复杂度        O(n)                         O(n²) 或 O(n log n)
────────────────────────────────────────────────────────────────────────

为什么子序列问题更难？
────────────────────────────────────────────────────────────────────────
子数组只需要考虑"是否接在末尾"，决策只有1个。
子序列需要考虑"接在哪个更小元素后面"，决策有多个（所有 j < i 且 nums[j] < nums[i]）。

本题的关键洞察：
────────────────────────────────────────────────────────────────────────
dp[i] = 以 nums[i] 结尾的最长递增子序列长度

为什么必须"以 nums[i] 结尾"？
    因为要判断能否接在后面，必须知道子序列的最后一个元素是什么。
    如果只记录"前 i 个元素的最长长度"，无法知道末尾元素，就无法判断能否拼接。

类比理解：
    - 最长递增子数组 像是"连续爬山"，只需看前一步
    - 最长递增子序列 像是"跳跃爬山"，可以跳过中间的坑，找所有能到达的位置

解题思路总览：
────────────────────────────────────────────────────────────────────────
解法                          时间复杂度       空间复杂度       说明
────────────────────────────────────────────────────────────────────────
动态规划（O(n²)）              O(n²)           O(n)            ⭐⭐⭐  直观
贪心 + 二分查找                O(n log n)      O(n)            ⭐⭐⭐⭐  推荐
────────────────────────────────────────────────────────────────────────

核心思想（动态规划 O(n²)）：
────────────────────────────────────────────────────────────────────────
状态定义：
    dp[i] = 以 nums[i] 结尾的最长递增子序列长度

状态转移：
    dp[i] = max(dp[j] + 1)  对所有 j < i 且 nums[j] < nums[i]
    如果不存在这样的 j，则 dp[i] = 1（子序列只有 nums[i] 自身）

初始条件：
    dp[i] = 1（每个元素自身构成长度为1的子序列）

最终答案：
    max(dp[0], dp[1], ..., dp[n-1])

核心思想（贪心 + 二分查找 O(n log n)）：
────────────────────────────────────────────────────────────────────────
关键洞察：
    维护一个数组 tails，tails[k] = 长度为 k+1 的递增子序列的末尾最小值。

    为什么维护"末尾最小值"？
    ────────────────────────────────────────────────────────
    末尾越小，后续能接上去的元素越多，越有利于构造更长的子序列。
    所以对每个长度，只保留末尾最小的那个子序列。

    算法步骤：
    1. 遍历每个元素 x
    2. 如果 x > tails[-1]，直接追加（找到了更长的子序列）
    3. 否则，在 tails 中二分查找第一个 ≥ x 的位置，替换为 x
       （让某个长度的子序列末尾更小，为后续创造更多机会）

    为什么正确？
    ────────────────────────────────────────────────────────
    替换操作不会改变 tails 的长度（不增加答案），
    但让某个位置的末尾变小，使得后续更容易追加。
    最终 tails 的长度就是 LIS 的长度。

    注意：tails 本身不一定是合法的 LIS，只是长度正确。
"""


# ══════════════════════════════════════════════════════════
# 解法一：动态规划（O(n²)）
# ══════════════════════════════════════════════════════════


def lengthOfLIS_dp(nums: list[int]) -> int:
    """
    动态规划解法（O(n²)）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    对每个元素，枚举前面所有更小的元素，取最大的 dp[j]+1。

    算法步骤：
    1. 初始化 dp[i] = 1（每个元素自身构成长度1的子序列）
    2. 对每个 i，遍历所有 j < i：
       如果 nums[j] < nums[i]，则 dp[i] = max(dp[i], dp[j]+1)
    3. 返回 dp 中的最大值

    时间复杂度：O(n²) — 双重循环
    空间复杂度：O(n)   — dp 数组

    图解示例：
    ────────────────────────────────────────────────────────
    nums = [10, 9, 2, 5, 3, 7, 101, 18]

    初始化：dp = [1, 1, 1, 1, 1, 1, 1, 1]

    i=0, nums[0]=10: 无前驱，dp[0]=1

    i=1, nums[1]=9:  10>9，无更小前驱，dp[1]=1

    i=2, nums[2]=2:  10>2, 9>2，无更小前驱，dp[2]=1

    i=3, nums[3]=5:  2<5 → dp[3]=max(1, dp[2]+1)=2
                     dp = [1, 1, 1, 2, 1, 1, 1, 1]

    i=4, nums[4]=3:  2<3 → dp[4]=max(1, dp[2]+1)=2
                     dp = [1, 1, 1, 2, 2, 1, 1, 1]

    i=5, nums[5]=7:  5<7 → dp[5]=max(1, dp[3]+1)=3
                     3<7 → dp[5]=max(3, dp[4]+1)=3
                     2<7 → dp[5]=max(3, dp[2]+1)=3
                     dp = [1, 1, 1, 2, 2, 3, 1, 1]

    i=6, nums[6]=101: 7<101 → dp[6]=max(1, dp[5]+1)=4
                      5<101 → dp[6]=max(4, dp[3]+1)=4
                      3<101 → dp[6]=max(4, dp[4]+1)=4
                      2<101 → dp[6]=max(4, dp[2]+1)=4
                      dp = [1, 1, 1, 2, 2, 3, 4, 1]

    i=7, nums[7]=18:  7<18 → dp[7]=max(1, dp[5]+1)=4
                      5<18 → dp[7]=max(4, dp[3]+1)=4
                      3<18 → dp[7]=max(4, dp[4]+1)=4
                      2<18 → dp[7]=max(4, dp[2]+1)=4
                      dp = [1, 1, 1, 2, 2, 3, 4, 4]

    结果：max(dp) = 4 ✓
    对应子序列：[2, 3, 7, 101] 或 [2, 5, 7, 101] 或 [2, 3, 7, 18]
    """
    n = len(nums)
    if n == 0:
        return 0

    dp = [1] * n

    for i in range(1, n):
        for j in range(i):
            if nums[j] < nums[i]:
                dp[i] = max(dp[i], dp[j] + 1)

    return max(dp)


# ══════════════════════════════════════════════════════════
# 解法二：贪心 + 二分查找（O(n log n)）
# ══════════════════════════════════════════════════════════


def lengthOfLIS(nums: list[int]) -> int:
    """
    贪心 + 二分查找解法（O(n log n)）⭐⭐⭐⭐ 推荐

    核心思想：
    ────────────────────────────────────────────────────────
    维护 tails 数组，tails[k] = 长度为 k+1 的递增子序列的最小末尾。
    对每个元素 x：
    - x > tails[-1]：可以接在最长子序列后面，追加
    - 否则：二分找第一个 ≥ x 的位置，替换（让末尾更小）

    为什么替换不会影响答案？
    ────────────────────────────────────────────────────────
    替换只是让某个长度的子序列末尾变小，不改变 tails 长度。
    更小的末尾为后续创造更多拼接机会，只可能让答案不变或更优。

    时间复杂度：O(n log n) — 遍历 n 个元素，每次二分查找 O(log n)
    空间复杂度：O(n)       — tails 数组（最坏情况所有元素递增）

    图解示例：
    ────────────────────────────────────────────────────────
    nums = [10, 9, 2, 5, 3, 7, 101, 18]

    x=10: tails=[], 追加 → tails=[10]
    x=9:  9<10, 替换tails[0] → tails=[9]
    x=2:  2<9, 替换tails[0] → tails=[2]
    x=5:  5>2, 追加 → tails=[2, 5]
    x=3:  3<5, 替换tails[1] → tails=[2, 3]
    x=7:  7>3, 追加 → tails=[2, 3, 7]
    x=101: 101>7, 追加 → tails=[2, 3, 7, 101]
    x=18:  18<101, 替换tails[3] → tails=[2, 3, 7, 18]

    结果：len(tails) = 4 ✓

    注意：tails=[2,3,7,18] 恰好是一个合法的 LIS，但这是巧合！
    tails 数组本身不一定构成合法子序列，但长度一定正确。

    反例：nums = [3, 5, 1, 7]
    x=3: tails=[3]
    x=5: tails=[3, 5]
    x=1: 替换 → tails=[1, 5]    ← [1,5] 不是 nums 中的子序列（1在5后面）
    x=7: tails=[1, 5, 7]        ← 长度3正确，但 [1,5,7] 不是原序列子序列
    实际LIS: [3,5,7] 长度3 ✓
    """
    import bisect

    tails = []

    for x in nums:
        pos = bisect.bisect_left(tails, x)
        if pos == len(tails):
            tails.append(x)
        else:
            tails[pos] = x

    return len(tails)


# ══════════════════════════════════════════════════════════
# 重要说明：算法的正确性证明和易错点提示
# ══════════════════════════════════════════════════════════
#
# 一、O(n²) DP 的正确性证明：
# ────────────────────────────────────────────────────────
#
# 定理：dp[i] = 以 nums[i] 结尾的 LIS 长度，max(dp) 即为答案。
#
# 证明：
# （1）状态完备性：
#     任何递增子序列都有一个"最后一个元素"，
#     因此必然以某个 nums[i] 结尾，dp[i] 覆盖了所有情况。
#
# （2）状态转移的正确性：
#     以 nums[i] 结尾的 LIS，其倒数第二个元素必然是某个 nums[j]，
#     其中 j < i 且 nums[j] < nums[i]。
#     枚举所有这样的 j，取 dp[j]+1 的最大值，即为 dp[i]。
#     如果不存在这样的 j，子序列只有 nums[i] 自身，长度为1。
#
# （3）全局最优性：
#     dp[i] 记录了以每个位置结尾的局部最优，
#     全局最优一定以某个位置结尾，所以 max(dp) 是全局最优。
#
#
# 二、O(n log n) 贪心+二分 的正确性证明：
# ────────────────────────────────────────────────────────
#
# 定理：len(tails) = LIS 长度。
#
# 证明：
# （1）tails 始终递增：
#     由归纳法，初始 tails 为空（递增）。
#     追加操作：x > tails[-1]，追加后仍递增。
#     替换操作：替换 tails[pos] 为 x，其中 x ≤ 原 tails[pos]，
#              且 x > tails[pos-1]（由 bisect_left 保证），
#              替换后仍递增。
#
# （2）len(tails) ≤ LIS 长度：
#     tails 递增且长度为 len(tails)，
#     每次追加时 x 比所有 tails 元素大，构成更长的递增序列，
#     所以 len(tails) 不超过真实 LIS 长度。
#
# （3）len(tails) ≥ LIS 长度：
#     关键引理：对每个 k，tails[k] 始终是"长度为 k+1 的递增子序列末尾的最小值"。
#     由归纳法：
#     - 追加时，x 构成了长度为 len(tails) 的递增子序列，末尾为 x。
#     - 替换时，用更小的 x 替换 tails[pos]，末尾变小，
#              对应的递增子序列仍然存在（通过替换前的子序列构造）。
#     因此当 LIS 长度为 L 时，tails 的长度至少为 L。
#
#     由（2）和（3）：len(tails) = LIS 长度。■
#
#
# 三、为什么用 bisect_left 而非 bisect_right？
# ────────────────────────────────────────────────────────
# 本题要求"严格递增"（nums[j] < nums[i]，不能等于）。
#
# bisect_left 找第一个 ≥ x 的位置：
#   如果 tails 中已有等于 x 的元素，替换它（不增加长度）。
#   这保证了 tails 中不会有重复元素，严格递增。
#
# bisect_right 找第一个 > x 的位置：
#   如果 tails 中已有等于 x 的元素，会追加在后面，
#   导致 tails 出现 [.., x, x, ..]，违反严格递增。
#
# 如果题目改为"非严格递增"（允许相等），则应改用 bisect_right。
#
#
# 四、tails 数组为什么不一定是合法的 LIS？
# ────────────────────────────────────────────────────────
# 替换操作改变了 tails[pos] 的值，但这个新值在原数组中的位置
# 可能比 tails[pos-1] 对应的位置更靠前，违反子序列的顺序要求。
#
# 例如 nums = [3, 5, 1, 7]：
#   处理1时，tails 从 [3,5] 变为 [1,5]，
#   但 [1,5] 在原数组中 1 在 5 后面，不是子序列。
#
# 不过这只影响 tails 的内容，不影响长度。
# 长度始终等于 LIS 长度，这是证明（3）保证的。
#
#
# 常见错误：
# ────────────────────────────────────────────────────────
# 1. 状态定义错误——dp[i] 记录"前 i 个元素的 LIS 长度"：
#    这样无法知道子序列末尾是什么，无法判断能否接上 nums[i]。
#    正确：dp[i] = 以 nums[i] 结尾的 LIS 长度。
#
# 2. 只看相邻元素（nums[i] vs nums[i-1]）：
#    这只能找最长连续递增子数组，不是子序列。
#    子序列可以跳过中间元素，必须枚举所有 j < i。
#
# 3. 最终答案写成 dp[-1]：
#    LIS 不一定以最后一个元素结尾！
#    必须取 max(dp)，因为 LIS 可能以任意位置结尾。
#
# 4. 二分查找用 bisect_right 而非 bisect_left：
#    严格递增必须用 bisect_left，否则重复元素会导致 tails 非严格递增。
#
# 5. 认为 tails 就是 LIS 本身：
#    tails 的长度正确，但内容不一定构成合法子序列（见上文反例）。
#    如果需要输出 LIS 本身，需要额外记录前驱信息。
#
# 6. 忘记处理空数组：
#    nums 为空时，应返回 0。
#    O(n²) 解法中 max(dp) 对空数组会报错，需要特判。
# ══════════════════════════════════════════════════════════
