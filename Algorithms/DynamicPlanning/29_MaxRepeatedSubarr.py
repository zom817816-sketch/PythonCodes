"""
最长重复子数组（Maximum Length of Repeated Subarray）

题目描述：
给两个整数数组 nums1 和 nums2，返回两个数组中公共的、长度最长的子数组的长度。

输入描述：
两个整数数组 nums1 和 nums2。

输出描述：
输出一个整数，表示最长公共子数组的长度。

输入示例：
nums1 = [1, 2, 3, 2, 1], nums2 = [3, 2, 1, 4, 7]
输出示例：
3

════════════════════════════════════════════════════════════════════════
核心概念：子数组 vs 子序列
════════════════════════════════════════════════════════════════════════

维度              子数组（Subarray）                子序列（Subsequence）
────────────────────────────────────────────────────────────────────────
元素位置          必须连续                          可以不连续
示例              [1,2,3,2,1]                     [1,2,3,2,1]
                  子数组 [3,2,1] ✓                子序列 [1,3,1] ✓
                  子序列 [1,3,1] ✓                子数组 [1,3,1] ✗（不连续）
DP状态定义        dp[i][j] = 以nums1[i]和nums2[j]  dp[i][j] = nums1[0..i]和nums2[0..j]
                  结尾的最长公共子数组长度          的最长公共子序列长度
DP状态转移        只依赖 dp[i-1][j-1]               依赖 dp[i-1][j], dp[i][j-1], dp[i-1][j-1]
时间复杂度        O(m×n)                           O(m×n)
────────────────────────────────────────────────────────────────────────

为什么子数组比子序列简单？
────────────────────────────────────────────────────────────────────────
子数组要求连续，所以当 nums1[i] == nums2[j] 时，
只能从 dp[i-1][j-1] 转移（前一个位置必须也匹配）。
而子序列可以跳过中间元素，需要考虑三种情况。

类比理解：
    - 最长公共子数组 像是"连续对齐"，必须一步步匹配
    - 最长公共子序列 像是"跳跃对齐"，可以跳过不匹配的位置

解题思路总览：
────────────────────────────────────────────────────────────────────────
解法                          时间复杂度       空间复杂度       说明
────────────────────────────────────────────────────────────────────────
暴力枚举                      O(m×n×min(m,n))  O(1)            仅用于理解
动态规划（二维数组）            O(m×n)           O(m×n)          ⭐⭐⭐
动态规划（滚动数组优化）        O(m×n)           O(min(m,n))     ⭐⭐⭐⭐ 推荐
二分查找 + 哈希               O((m+n)logL)     O(m+n)          ⭐⭐⭐⭐ 进阶
────────────────────────────────────────────────────────────────────────

核心思想（动态规划）：
────────────────────────────────────────────────────────────────────────
状态定义：
    dp[i][j] = 以 nums1[i-1] 和 nums2[j-1] 结尾的最长公共子数组长度
    （注意：dp 数组比原数组多一行一列，方便处理边界）

状态转移：
    如果 nums1[i-1] == nums2[j-1]：
        dp[i][j] = dp[i-1][j-1] + 1（当前位置匹配，长度+1）
    否则：
        dp[i][j] = 0（不匹配，公共子数组中断）

初始条件：
    dp[0][j] = 0（nums1 为空数组）
    dp[i][0] = 0（nums2 为空数组）

最终答案：
    max(dp[0..m][0..n])（dp 数组中的最大值）
"""


# ══════════════════════════════════════════════════════════
# 解法一：暴力枚举
# ══════════════════════════════════════════════════════════


def findLength_brute(nums1: list[int], nums2: list[int]) -> int:
    """
    暴力枚举

    核心思路：
    ────────────────────────────────────────────────────────
    以每个位置对 (i,j) 为起点，向后扫描最长公共子数组。

    算法步骤：
    1. 枚举 nums1 的每个起点 i
    2. 枚举 nums2 的每个起点 j
    3. 如果 nums1[i] == nums2[j]，向后匹配，记录长度
    4. 更新全局最大长度

    时间复杂度：O(m×n×min(m,n)) — 最坏情况每个位置对都匹配到末尾
    空间复杂度：O(1) — 只用常数变量

    问题分析：
    ────────────────────────────────────────────────────────
    大量重复匹配：不同起点的匹配过程高度重叠。
    DP 可以避免重复计算，将时间复杂度降至 O(m×n)。
    """
    m, n = len(nums1), len(nums2)
    result = 0

    for i in range(m):
        for j in range(n):
            if nums1[i] == nums2[j]:
                # 以 (i,j) 为起点，向后匹配
                length = 0
                k = 0
                while i + k < m and j + k < n and nums1[i + k] == nums2[j + k]:
                    length += 1
                    k += 1
                result = max(result, length)

    return result


