"""
目标和（Target Sum）

题目描述：
给你一个非负整数数组 nums 和一个整数 target 。
向数组中的每个整数前添加 '+' 或 '-' ，然后串联起所有整数，可以构造一个 表达式 ：
例如，nums = [2, 1] ，可以在 2 之前添加 '+' ，在 1 之前添加 '-' ，然后串联起来得到表达式 "+2-1" 。
返回可以通过上述方法构造的、运算结果等于 target 的不同 表达式 的数目。

示例 1：
    输入：nums = [1,1,1,1,1], target = 3
    输出：5
    解释：一共有 5 种方法让最终目标和为 3 。
    -1 + 1 + 1 + 1 + 1 = 3
    +1 - 1 + 1 + 1 + 1 = 3
    +1 + 1 - 1 + 1 + 1 = 3
    +1 + 1 + 1 - 1 + 1 = 3
    +1 + 1 + 1 + 1 - 1 = 3

示例 2：
    输入：nums = [1], target = 1
    输出：1

解题思路总览：
────────────────────────────────────────────────────────────────────────
解法                          时间复杂度    空间复杂度    说明
────────────────────────────────────────────────────────────────────────
回溯算法                       O(2^n)       O(n)         超时，仅用于理解
记忆化搜索（Top-Down DP）       O(n×sum)      O(n×sum)      ⭐⭐
动态规划（Bottom-Up DP）         O(n×sum)      O(n×sum)      ⭐⭐⭐
动态规划（空间优化）             O(n×sum)      O(sum)        ⭐⭐⭐⭐  推荐
────────────────────────────────────────────────────────────────────────

核心思想（动态规划）：
────────────────────────────────────────────────────────────────────────
关键洞察（转化为 0-1 背包问题）：
1. 设添加 '+' 的数字之和为 P，添加 '-' 的数字之和为 N
2. 则有：P + N = total（所有数字之和）
3. 同时：P - N = target（目标和）
4. 两式相加得：2P = target + total，即 P = (target + total) / 2
5. 问题转化为：从 nums 中选取若干数字，使其和恰好等于 P
6. 这正是 0-1 背包问题（求方案数）：
   - 数字重量 = 物品重量
   - P = 背包容量
   - 目标：计算恰好装满背包的方案数

前置条件：
    1. (target + total) 必须为偶数，否则无解
    2. abs(target) <= total，否则无解

状态定义（一维优化）：
    dp[j] = 从 nums 中选取数字，使其和恰好为 j 的方案数

状态转移：
    dp[j] += dp[j - num]（对于每个数字 num）

初始条件：
    dp[0] = 1（和为 0 有一种方案：什么都不选）

最终答案：
    dp[target_sum]，其中 target_sum = (target + total) // 2
"""


# ══════════════════════════════════════════════════════════
# 解法一：回溯算法（Brute Force）
# ══════════════════════════════════════════════════════════


def findTargetSumWays_brute(nums: list, target: int) -> int:
    """
    回溯算法（Brute Force）

    核心思路：
    ────────────────────────────────────────────────────────
    枚举每个数字的符号（'+' 或 '-'），计算所有可能的表达式结果，
    统计结果等于 target 的方案数。

    算法步骤：
    1. 使用回溯遍历所有符号组合
    2. 对于每个数字，尝试 '+' 和 '-' 两种选择
    3. 到达数组末尾时，检查当前和是否等于 target
    4. 返回满足条件的方案数

    时间复杂度：O(2^n) — 指数级增长
    空间复杂度：O(n)   — 递归调用栈深度

    问题分析：
    ────────────────────────────────────────────────────────
    对于 n = 20，就有 2^20 ≈ 100 万种可能，效率极低。
    仅用于理解问题本质。
    """
    n = len(nums)
    count = 0

    def backtrack(index: int, current_sum: int) -> None:
        nonlocal count
        if index == n:
            if current_sum == target:
                count += 1
            return

        # 选择 '+'（加上当前数字）
        backtrack(index + 1, current_sum + nums[index])
        # 选择 '-'（减去当前数字）
        backtrack(index + 1, current_sum - nums[index])

    backtrack(0, 0)
    return count


# ══════════════════════════════════════════════════════════
# 解法二：记忆化搜索（Top-Down DP）
# ══════════════════════════════════════════════════════════


def findTargetSumWays_memo(nums: list, target: int) -> int:
    """
    记忆化搜索解法（Top-Down DP）⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    将问题转化为 0-1 背包（求方案数）：
    - 在容量 target_sum = (target + total) // 2 的限制下
    - 求恰好装满背包的方案数

    算法步骤：
    1. 计算总和 total，target_sum = (target + total) // 2
    2. 创建缓存 memo[i][j]，表示从第 i 个数字开始，凑成和 j 的方案数
    3. 使用递归 + 缓存求解

    时间复杂度：O(n×sum) — 每个状态只计算一次
    空间复杂度：O(n×sum) — 缓存 + 递归调用栈

    状态定义：
    - memo[i][j] = 从第 i 个数字开始，凑成和 j 的方案数

    状态转移：
    - 不选数字 i：dfs(i+1, j)
    - 选数字 i（如果 j >= nums[i]）：dfs(i+1, j-nums[i])
    - memo[i][j] = 以上两种选择的方案数之和
    """
    total = sum(nums)

    # 前置条件检查
    if (target + total) % 2 == 1:
        return 0
    if abs(target) > total:
        return 0

    target_sum = (target + total) // 2
    n = len(nums)

    memo = [[-1] * (target_sum + 1) for _ in range(n)]

    def dfs(index: int, remaining: int) -> int:
        if index == n:
            return 1 if remaining == 0 else 0

        if memo[index][remaining] != -1:
            return memo[index][remaining]

        # 不选当前数字
        result = dfs(index + 1, remaining)

        # 选当前数字
        if remaining >= nums[index]:
            result += dfs(index + 1, remaining - nums[index])

        memo[index][remaining] = result
        return memo[index][remaining]

    return dfs(0, target_sum)


# ══════════════════════════════════════════════════════════
# 解法三：动态规划（Bottom-Up DP）
# ══════════════════════════════════════════════════════════


def findTargetSumWays_dp(nums: list, target: int) -> int:
    """
    动态规划解法（Bottom-Up DP）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    标准的 0-1 背包二维 DP 实现（求方案数）。

    算法步骤：
    1. 计算总和 total，target_sum = (target + total) // 2
    2. 创建二维 DP 数组 dp[n+1][target_sum+1]
    3. dp[i][j] = 前 i 个数字中，凑成和 j 的方案数
    4. 从前往后计算，返回 dp[n][target_sum]

    时间复杂度：O(n×sum) — 双重循环
    空间复杂度：O(n×sum) — DP 数组

    图解示例：
    ────────────────────────────────────────────────────────
    nums = [1, 1, 1, 1, 1], target = 3
    total = 5, target_sum = (3 + 5) // 2 = 4

    dp 表格（行表示数字索引，列表示目标和）：

                0   1   2   3   4
    初始化      1   0   0   0   0
    i=1 (n=1)   1   1   0   0   0
    i=2 (n=1)   1   2   1   0   0
    i=3 (n=1)   1   3   3   1   0
    i=4 (n=1)   1   4   6   4   1
    i=5 (n=1)   1   5  10  10   5

    dp[5][4] = 5

    最终答案：5 ✓

    状态定义：
    - dp[i][j] = 前 i 个数字中，凑成和 j 的方案数

    状态转移：
    - 不选第 i 个数字：dp[i-1][j]
    - 选第 i 个数字（如果 j >= nums[i-1]）：dp[i-1][j-nums[i-1]]
    - dp[i][j] = 以上两种的方案数之和

    初始条件：
    - dp[0][0] = 1（前 0 个数字凑成和 0 有 1 种方案：什么都不选）
    - dp[0][j] = 0（j > 0，前 0 个数字无法凑成正数和）
    """
    total = sum(nums)

    # 前置条件检查
    if (target + total) % 2 == 1:
        return 0
    if abs(target) > total:
        return 0

    target_sum = (target + total) // 2
    n = len(nums)

    dp = [[0] * (target_sum + 1) for _ in range(n + 1)]

    # 初始化：前 0 个数字凑成和 0 有 1 种方案
    dp[0][0] = 1

    for i in range(1, n + 1):
        for j in range(target_sum + 1):
            # 不选第 i 个数字
            dp[i][j] = dp[i - 1][j]

            # 选第 i 个数字（如果可以装入）
            if j >= nums[i - 1]:
                dp[i][j] += dp[i - 1][j - nums[i - 1]]

    return dp[n][target_sum]


# ══════════════════════════════════════════════════════════
# 解法四：动态规划（空间优化）
# ══════════════════════════════════════════════════════════


def findTargetSumWays(nums: list, target: int) -> int:
    """
    动态规划解法（空间优化）⭐⭐⭐⭐ 推荐

    核心思想：
    ────────────────────────────────────────────────────────
    观察到 dp[i][j] 只依赖于 dp[i-1][...]，
    可以使用一维数组，从后往前遍历，避免覆盖需要的数据。

    算法步骤：
    1. 计算总和 total，target_sum = (target + total) // 2
    2. 创建一维 DP 数组 dp[target_sum+1]，初始化为 0
    3. 对每个数字，从后往前更新 dp 数组
    4. 返回 dp[target_sum]

    时间复杂度：O(n×sum) — 双重循环
    空间复杂度：O(sum) — 一维 DP 数组

    图解示例：
    ────────────────────────────────────────────────────────
    nums = [1, 1, 1, 1, 1], target = 3
    total = 5, target_sum = (3 + 5) // 2 = 4

    dp = [0, 0, 0, 0, 0]
    初始化：dp[0] = 1
    dp = [1, 0, 0, 0, 0]

    遍历 num = 1（从后往前）：
        dp[4] += dp[3] = 0
        dp[3] += dp[2] = 0
        dp[2] += dp[1] = 0
        dp[1] += dp[0] = 1
    dp = [1, 1, 0, 0, 0]

    遍历 num = 1（从后往前）：
        dp[4] += dp[3] = 0
        dp[3] += dp[2] = 0
        dp[2] += dp[1] = 1
        dp[1] += dp[0] = 2
    dp = [1, 2, 1, 0, 0]

    遍历 num = 1（从后往前）：
        dp[4] += dp[3] = 0
        dp[3] += dp[2] = 1
        dp[2] += dp[1] = 3
        dp[1] += dp[0] = 3
    dp = [1, 3, 3, 1, 0]

    遍历 num = 1（从后往前）：
        dp[4] += dp[3] = 1
        dp[3] += dp[2] = 4
        dp[2] += dp[1] = 6
        dp[1] += dp[0] = 4
    dp = [1, 4, 6, 4, 1]

    遍历 num = 1（从后往前）：
        dp[4] += dp[3] = 5
        dp[3] += dp[2] = 10
        dp[2] += dp[1] = 10
        dp[1] += dp[0] = 5
    dp = [1, 5, 10, 10, 5]

    最终 dp[4] = 5
    答案：5 ✓

    关键点：从后往前遍历！
    ────────────────────────────────────────────────────────
    为什么从后往前？
        - dp[j] 新值依赖于 dp[j - num]（上一轮的值）
        - 从后往前遍历，保证 dp[j - num] 是未被当前数字更新过的值
        - 从前往后遍历会导致同一个数字被重复选择（变成完全背包）

    状态定义：
    - dp[j] = 凑成和 j 的方案数

    状态转移：
    - dp[j] += dp[j - num]

    初始条件：
    - dp[0] = 1（凑成和 0 有 1 种方案：什么都不选）

    最终答案：
    - dp[target_sum]
    """
    total = sum(nums)

    # 前置条件检查
    if (target + total) % 2 == 1:
        return 0
    if abs(target) > total:
        return 0

    target_sum = (target + total) // 2

    dp = [0] * (target_sum + 1)

    # 初始化：凑成和 0 有 1 种方案
    dp[0] = 1

    for num in nums:
        # 从后往前遍历，避免重复选择同一个数字
        for j in range(
            target_sum, num - 1, -1
        ):  # 为什么是遍历到num:dp[j] = dp[j] + dp[j - num],所以需要防止数组越界；逻辑上，也无法将一个比容量大的数字放入背包
            dp[j] += dp[j - num]

    return dp[target_sum]


# ══════════════════════════════════════════════════════════
# 重要说明：算法的正确性证明
# ══════════════════════════════════════════════════════════
#
# 为什么这个问题可以转化为 0-1 背包问题？
# ────────────────────────────────────────────────────────
# 1. 给每个数字添加 '+' 或 '-'，本质上是把 nums 分成两组：
#    - 正数组 P（添加 '+' 的数字）
#    - 负数组 N（添加 '-' 的数字）
# 2. 设 P 的和为 pos，N 的和为 neg，则：
#    - pos + neg = total（所有数字之和）
#    - pos - neg = target（目标和）
# 3. 两式相加得：2 * pos = total + target
#    即 pos = (total + target) / 2
# 4. 问题转化为：从 nums 中选取若干数字，使其和恰好等于 pos
# 5. 这正是 0-1 背包问题（求方案数）
#
# 为什么需要前置条件检查？
# ────────────────────────────────────────────────────────
# 1. (target + total) % 2 == 1：
#    - pos = (target + total) / 2 必须是整数
#    - 如果 target + total 是奇数，则无解
# 2. abs(target) > total：
#    - 即使所有数字都取 '+' 或都取 '-'，最大和最小结果分别为 total 和 -total
#    - 如果 target 超出 [-total, total] 范围，则无解
#
# 为什么不能从前向后遍历？
# ────────────────────────────────────────────────────────
# 从前向后遍历一维 dp 数组：
#    - dp[j - num] 可能已经被当前数字更新过
#    - 这意味着当前数字被多次使用
#    - 变成完全背包问题，与本题要求（每个数字只能用一次）不符
#
# 时间复杂度对比：
# ────────────────────────────────────────────────────────
# - 回溯算法：O(2^n) — 指数级，不可接受
# - 记忆化搜索：O(n×sum) — 多项式级
# - 动态规划：O(n×sum) — 多项式级
# - 空间优化：O(n×sum) — 多项式级，空间最优
#
# 空间复杂度对比：
# ────────────────────────────────────────────────────────
# - 回溯算法：O(n) — 递归栈
# - 记忆化搜索：O(n×sum) — 缓存
# - 动态规划：O(n×sum) — DP 数组
# - 空间优化：O(sum) — 一维数组，最优
#
# 常见错误：
# ────────────────────────────────────────────────────────
# 1. 从前向后遍历一维 dp（导致重复选择）
# 2. 忘记前置条件检查（target + total 为奇数或 abs(target) > total）
# 3. 初始条件错误：dp[0] 应初始化为 1（和为 0 有一种方案：什么都不选）
# 4. 混淆方案数和最大值：本题是求方案数（+=），不是求最大值（max）
# ══════════════════════════════════════════════════════════
