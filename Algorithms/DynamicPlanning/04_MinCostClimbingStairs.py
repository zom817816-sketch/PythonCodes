"""
使用最小花费爬楼梯（Min Cost Climbing Stairs）

题目描述：
给你一个整数数组 cost ，其中 cost[i] 是从楼梯第 i 个台阶向上爬需要支付的费用。
一旦你支付此费用，即可选择向上爬一个或者两个台阶。
你可以选择从下标为 0 或下标为 1 的台阶开始爬楼梯。
请你计算并返回达到楼梯顶部的最低花费。

注意：
    - 楼梯顶部在数组末尾之后（即第 n 阶，其中 n = len(cost)）
    - 可以从第 0 阶或第 1 阶开始

示例 1：
    输入：cost = [10, 15, 20]
    输出：15
    解释：
        你将从下标为 1 的台阶开始。
        - 支付 15，向上爬两个台阶，到达楼梯顶部。
        总花费为 15。

示例 2：
    输入：cost = [1, 100, 1, 1, 1, 100, 1, 1, 100, 1]
    输出：6
    解释：
        你将从下标为 0 的台阶开始。
        - 支付 1，向上爬两个台阶，到达下标为 2 的台阶。
        - 支付 1，向上爬两个台阶，到达下标为 4 的台阶。
        - 支付 1，向上爬两个台阶，到达下标为 6 的台阶。
        - 支付 1，向上爬一个台阶，到达下标为 7 的台阶。
        - 支付 1，向上爬两个台阶，到达下标为 9 的台阶。
        - 支付 1，向上爬一个台阶，到达楼梯顶部。
        总花费为 6。

示例 3：
    输入：cost = [1, 2, 3, 4, 5]
    输出：6
    解释：
        最优路径：1 → 3 → 5（顶部）
        花费：1 + 3 = 4？不对，应该是：
        从 0 开始：1（支付）→ 爬 2 步到 2 → 3（支付）→ 爬 2 步到 4 → 5（支付）→ 爬 1 步到顶部
        总花费：1 + 3 + 5 = 9
        
        或者：从 1 开始：2（支付）→ 爬 2 步到 3 → 4（支付）→ 爬 2 步到顶部
        总花费：2 + 4 = 6 ✅

解题思路总览：
───────────────────────────────────────────────────────────────────────
解法                          时间复杂度    空间复杂度    说明
───────────────────────────────────────────────────────────────────────
暴力递归                       O(2^n)       O(n)        超时
记忆化搜索（Top-Down DP）       O(n)         O(n)        ⭐⭐
动态规划（Bottom-Up DP）         O(n)         O(n)        ⭐⭐⭐
动态规划（空间优化）             O(n)         O(1)        ⭐⭐⭐⭐ 最优
───────────────────────────────────────────────────────────────────────

核心思想（动态规划）：
───────────────────────────────────────────────────────────────────────
关键洞察：
1. 要到达楼梯顶部（第 n 阶），最后一步只有两种可能：
   - 从第 n-1 阶爬 1 阶上去
   - 从第 n-2 阶爬 2 阶上去
2. 因此，到达顶部的最小花费 = min(到达 n-1 阶的花费 + cost[n-1], 到达 n-2 阶的花费 + cost[n-2])

状态定义（定义一）：
    dp[i] = 到达第 i 阶的最小花费（尚未支付第 i 阶的费用）

状态转移：
    dp[i] = min(dp[i-1] + cost[i-1], dp[i-2] + cost[i-2])

初始条件：
    dp[0] = 0（到达第 0 阶不需要花费，因为还没支付）
    dp[1] = 0（到达第 1 阶不需要花费，因为还没支付）

最终答案：
    dp[n]（到达顶部的最小花费）

状态定义（定义二）：
    dp[i] = 到达第 i 阶并支付费用后的最小花费

状态转移：
    dp[i] = cost[i] + min(dp[i-1], dp[i-2])

初始条件：
    dp[0] = cost[0]（从第 0 阶开始并支付）
    dp[1] = cost[1]（从第 1 阶开始并支付）

最终答案：
    min(dp[n-1], dp[n-2])（从最后两阶中选择花费较小的）

两种定义都正确，定义一更直观，定义二代码更简洁。
"""

from typing import List


# ══════════════════════════════════════════════════════════
# 解法一：暴力递归（Brute Force）
# ══════════════════════════════════════════════════════════


def minCostClimbingStairs_brute(cost: List[int]) -> int:
    """
    暴力递归解法（Brute Force）

    核心思路：
    ────────────────────────────────────────────────────────
    直接根据递推关系递归求解：
        minCost(i) = min(minCost(i-1) + cost[i-1], minCost(i-2) + cost[i-2])
    不做任何缓存，存在大量重复计算。

    算法步骤：
    1. Base Case：i = 0 或 i = 1 时返回 0（到达这两阶不需要花费）
    2. 递归求解：min(minCost(i-1) + cost[i-1], minCost(i-2) + cost[i-2])

    时间复杂度：O(2^n) — 递归树呈指数级增长
    空间复杂度：O(n)   — 递归调用栈的最大深度

    问题分析（以 cost = [10, 15, 20] 为例）：
    ────────────────────────────────────────────────────────
                        minCost(3)
                       /        \
            minCost(2)+20   minCost(1)+15
            /       \
    minCost(1)+15  minCost(0)+10

    minCost(1) 和 minCost(0) 被重复计算多次，效率极低。
    这就是为什么需要引入"记忆化"或"动态规划"来消除重复计算。

    注意：这个解法在 n > 35 左右就会明显变慢，仅用于理解问题。
    """
    n = len(cost)

    # 边界情况：只有一个台阶时，必须支付费用才能到达顶部
    if n == 1:
        return cost[0]

    def dfs(i: int) -> int:
        """递归求解到达第 i 阶的最小花费"""
        # Base Case：到达第 0 阶或第 1 阶不需要花费
        if i <= 1:
            return 0

        # 递归求解：从 i-1 或 i-2 爬上来
        return min(dfs(i - 1) + cost[i - 1], dfs(i - 2) + cost[i - 2])

    return dfs(n)


# ══════════════════════════════════════════════════════════
# 解法二：记忆化搜索（Top-Down DP）
# ══════════════════════════════════════════════════════════


def minCostClimbingStairs_memo(cost: List[int]) -> int:
    """
    记忆化搜索解法（Top-Down DP）⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    在暴力递归的基础上，增加一个缓存（字典/数组），
    记录已经计算过的子问题结果，避免重复计算。

    算法步骤：
    1. 创建缓存 memo，存储已计算的 minCost(k) 值
    2. 递归时先查缓存：
        a. 如果缓存中有结果，直接返回（命中）
        b. 如果缓存中没有，计算后存入缓存，再返回
    3. Base Case：i = 0 或 i = 1 时返回 0

    时间复杂度：O(n) — 每个子问题只计算一次
    空间复杂度：O(n) — 缓存 + 递归调用栈

    状态定义：
    - memo[k] = 到达第 k 阶的最小花费

    状态转移：
    - memo[k] = min(memo[k-1] + cost[k-1], memo[k-2] + cost[k-2])

    图解示例：cost = [10, 15, 20]
    ────────────────────────────────────────────────────────
    递归树（带缓存）：
                        minCost(3)
                       /        \
            minCost(2)+20   minCost(1)+15 ✓
            /       \
    minCost(1)+15 ✓  minCost(0)+10 ✓

    ✓ 表示命中缓存，不再递归。

    最终计算顺序：
        minCost(0) → 0（Base Case）
        minCost(1) → 0（Base Case）
        minCost(2) → min(0+10, 0+15) = 10
        minCost(3) → min(10+20, 0+15) = 15

    结果：15 ✅
    """
    n = len(cost)

    # 边界情况：只有一个台阶时，必须支付费用才能到达顶部
    if n == 1:
        return cost[0]

    # 缓存：记录已计算的 minCost(k)
    memo = {}

    def dfs(i: int) -> int:
        """递归求解到达第 i 阶的最小花费"""
        # 命中缓存，直接返回
        if i in memo:
            return memo[i]

        # Base Case：到达第 0 阶或第 1 阶不需要花费
        if i <= 1:
            return 0

        # 计算并存入缓存
        memo[i] = min(dfs(i - 1) + cost[i - 1], dfs(i - 2) + cost[i - 2])
        return memo[i]

    return dfs(n)


# ══════════════════════════════════════════════════════════
# 解法三：动态规划（Bottom-Up DP）
# ══════════════════════════════════════════════════════════


def minCostClimbingStairs_dp(cost: List[int]) -> int:
    """
    动态规划解法（Bottom-Up DP）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    从底向上计算：先算到达第 0 阶、第 1 阶的花费，
    再算到达第 2 阶、第 3 阶……直到到达顶部的花费。
    不需要递归，用迭代方式填充 DP 表。

    算法步骤：
    1. 创建 DP 数组 dp，dp[i] 表示到达第 i 阶的最小花费
    2. 初始化 base case：dp[0] = 0, dp[1] = 0
    3. 从 i = 2 到 n，依次计算：
        dp[i] = min(dp[i-1] + cost[i-1], dp[i-2] + cost[i-2])
    4. 返回 dp[n]

    时间复杂度：O(n) — 一次遍历
    空间复杂度：O(n) — DP 数组

    图解示例：cost = [10, 15, 20]
    ────────────────────────────────────────────────────────
        i      0    1    2     3
        dp[i]  0    0   10    15
                       ↑     ↑
                      min(0+10, 0+15)=10
                             min(10+20, 0+15)=15

    状态定义：
    - dp[i] = 到达第 i 阶的最小花费（尚未支付第 i 阶的费用）

    状态转移：
    - dp[i] = min(dp[i-1] + cost[i-1], dp[i-2] + cost[i-2])

    初始条件：
    - dp[0] = 0（到达第 0 阶不需要花费）
    - dp[1] = 0（到达第 1 阶不需要花费）

    最终答案：
    - dp[n]（到达顶部的最小花费）
    """
    n = len(cost)

    # Base Case：只有 1 个或 2 个台阶时，直接返回较小的花费
    if n <= 2:
        return min(cost)

    # 创建 DP 数组
    dp = [0] * (n + 1)
    dp[0], dp[1] = 0, 0

    # 从底向上填充
    for i in range(2, n + 1):
        dp[i] = min(dp[i - 1] + cost[i - 1], dp[i - 2] + cost[i - 2])

    return dp[n]


# ══════════════════════════════════════════════════════════
# 解法四：动态规划（空间优化版，推荐解）
# ══════════════════════════════════════════════════════════


def minCostClimbingStairs(cost: List[int]) -> int:
    """
    空间优化的动态规划解法 ⭐⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    观察状态转移方程：dp[i] = min(dp[i-1] + cost[i-1], dp[i-2] + cost[i-2])
    到达第 i 阶的花费只依赖到达第 i-1 阶和第 i-2 阶的花费，
    不需要整个数组，用两个滚动变量即可，空间复杂度降为 O(1)。

    算法步骤：
    1. 初始化：prev = 0 (到达第 0 阶的花费), curr = 0 (到达第 1 阶的花费)
    2. 从 i = 2 到 n，每次：
        a. 计算 next = min(curr + cost[i-1], prev + cost[i-2])
        b. 滚动更新：prev = curr, curr = next
    3. 返回 curr

    时间复杂度：O(n) — 一次遍历
    空间复杂度：O(1) — 只用了两个变量

    图解示例：cost = [10, 15, 20]
    ────────────────────────────────────────────────────────
        i     prev   curr   next (=min(curr+cost[i-1], prev+cost[i-2]))
        ────────────────────────────────────────────────────────────────
        初始   0      0      —
        2      0      10     min(0+10, 0+15) = 10
        3      10     15     min(10+20, 0+15) = 15  ← 最终结果
        ────────────────────────────────────────────────
        返回 curr = 15

    图解示例：cost = [1, 100, 1, 1, 1, 100, 1, 1, 100, 1]
    ────────────────────────────────────────────────────────
        i     prev   curr   next
        ───────────────────────────
        初始   0      0
        2      0      1      min(0+1, 0+100) = 1
        3      1      2      min(1+1, 0+100) = 2
        4      2      2      min(2+1, 1+1) = 2
        5      2      3      min(2+1, 2+1) = 3
        6      3      103    min(3+100, 2+1) = 103
        7      103    4      min(103+1, 3+100) = 4
        8      4      5      min(4+1, 103+1) = 5
        9      5      105    min(5+100, 4+1) = 105
       10     105    6      min(105+1, 5+100) = 6  ← 最终结果
        ───────────────────────────
        返回 curr = 6

    为什么只用两个变量就够了？
    ────────────────────────────────────────────────────────
    因为计算到达第 i 阶的花费时，只需要知道到达第 i-1 阶和第 i-2 阶的花费，
    更早的历史值（到达第 i-3 阶、第 i-4 阶……的花费）已经没用了。
    这是 DP 空间优化的经典技巧——"滚动变量"。
    """
    n = len(cost)

    # Base Case：只有 1 个或 2 个台阶时，直接返回较小的花费
    if n <= 2:
        return min(cost) if n > 0 else 0

    # 用两个变量代替数组，滚动更新
    prev, curr = 0, 0  # prev = dp[0], curr = dp[1]

    for i in range(2, n + 1):
        # 计算到达第 i 阶的最小花费
        next_cost = min(curr + cost[i - 1], prev + cost[i - 2])
        # 滚动更新
        prev, curr = curr, next_cost

    return curr


# ══════════════════════════════════════════════════════════
# 解法五：动态规划（另一种状态定义）
# ══════════════════════════════════════════════════════════


def minCostClimbingStairs_dp_alt(cost: List[int]) -> int:
    """
    动态规划解法（另一种状态定义）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    状态定义与前面不同：
        dp[i] = 到达第 i 阶并支付费用后的最小花费

    这样定义的好处是代码更简洁，但理解起来需要转换一下思维。

    算法步骤：
    1. 创建 DP 数组 dp，dp[i] 表示到达第 i 阶并支付费用后的最小花费
    2. 初始化 base case：dp[0] = cost[0], dp[1] = cost[1]
    3. 从 i = 2 到 n-1，依次计算：
        dp[i] = cost[i] + min(dp[i-1], dp[i-2])
    4. 返回 min(dp[n-1], dp[n-2])（从最后两阶中选择花费较小的）

    时间复杂度：O(n) — 一次遍历
    空间复杂度：O(n) — DP 数组

    图解示例：cost = [10, 15, 20]
    ────────────────────────────────────────────────────────
        i      0    1    2
        dp[i]  10   15   30 (=20 + min(15, 10))

        最终答案：min(dp[1], dp[2]) = min(15, 30) = 15 ✅

    状态定义：
    - dp[i] = 到达第 i 阶并支付费用后的最小花费

    状态转移：
    - dp[i] = cost[i] + min(dp[i-1], dp[i-2])

    初始条件：
    - dp[0] = cost[0]（从第 0 阶开始并支付）
    - dp[1] = cost[1]（从第 1 阶开始并支付）

    最终答案：
    - min(dp[n-1], dp[n-2])（从最后两阶中选择花费较小的）
    """
    n = len(cost)

    # Base Case：只有 1 个或 2 个台阶时，直接返回较小的花费
    if n <= 2:
        return min(cost) if n > 0 else 0

    # 创建 DP 数组
    dp = [0] * n
    dp[0], dp[1] = cost[0], cost[1]

    # 从底向上填充
    for i in range(2, n):
        dp[i] = cost[i] + min(dp[i - 1], dp[i - 2])

    # 返回从最后两阶中选择花费较小的
    return min(dp[n - 1], dp[n - 2])


# ══════════════════════════════════════════════════════════
# 重要说明：动态规划的正确性证明
# ══════════════════════════════════════════════════════════
#
# 为什么 dp[i] = min(dp[i-1] + cost[i-1], dp[i-2] + cost[i-2]) 是正确的？
# ────────────────────────────────────────────────────────
# 1. 最优子结构（Optimal Substructure）：
#    到达第 i 阶的最后一步只有两种可能：
#        a) 从第 i-1 阶爬 1 阶上去，需要支付 cost[i-1]
#        b) 从第 i-2 阶爬 2 阶上去，需要支付 cost[i-2]
#    这两种情况互斥且完备，所以：
#        dp[i] = min(dp[i-1] + cost[i-1], dp[i-2] + cost[i-2])
#
# 2. 重叠子问题（Overlapping Subproblems）：
#    在暴力递归中，dp[i-2] 会被 dp[i] 和 dp[i-1] 同时需要，
#    导致重复计算。用缓存或 DP 数组可以消除重复计算。
#
# 3. 无后效性（No Aftereffect）：
#    计算 dp[i] 时，只需要知道 dp[i-1] 和 dp[i-2]，
#    不需要知道是如何到达 i-1 或 i-2 的。
#    这保证了状态转移的正确性。
#
# 为什么空间优化是正确的？
# ────────────────────────────────────────────────────────
# 因为 dp[i] 只依赖 dp[i-1] 和 dp[i-2]，
# 更早的历史值（dp[i-3]、dp[i-4]……）已经没用了。
# 用两个滚动变量保存最近两个值即可。
#
# 两种状态定义的对比：
# ────────────────────────────────────────────────────────
# 定义一（dp[i] = 到达第 i 阶的花费，未支付）：
#     - 初始条件：dp[0] = 0, dp[1] = 0
#     - 最终答案：dp[n]
#     - 更直观，符合问题描述

# 定义二（dp[i] = 到达第 i 阶并支付后的花费）：
#     - 初始条件：dp[0] = cost[0], dp[1] = cost[1]
#     - 最终答案：min(dp[n-1], dp[n-2])
#     - 代码更简洁，但需要转换思维
# ══════════════════════════════════════════════════════════