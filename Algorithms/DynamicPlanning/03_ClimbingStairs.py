"""
爬楼梯（Climbing Stairs）

题目描述：
假设你正在爬楼梯。需要 n 阶你才能到达楼顶。
每次你可以爬 1 或 2 个台阶。你有多少种不同的方法可以爬到楼顶呢？

注意：给定 n 是一个正整数。

示例 1：
    输入：n = 2
    输出：2
    解释：有两种方法可以爬到楼顶。
        1. 1 阶 + 1 阶
        2. 2 阶

示例 2：
    输入：n = 3
    输出：3
    解释：有三种方法可以爬到楼顶。
        1. 1 阶 + 1 阶 + 1 阶
        2. 1 阶 + 2 阶
        3. 2 阶 + 1 阶

示例 3：
    输入：n = 4
    输出：5
    解释：有五种方法可以爬到楼顶。
        1. 1 + 1 + 1 + 1
        2. 1 + 1 + 2
        3. 1 + 2 + 1
        4. 2 + 1 + 1
        5. 2 + 2

解题思路总览：
───────────────────────────────────────────────────────────────────────
解法                          时间复杂度    空间复杂度    说明
───────────────────────────────────────────────────────────────────────
暴力递归                       O(2^n)       O(n)        超时
记忆化搜索（Top-Down DP）       O(n)         O(n)        ⭐⭐
动态规划（Bottom-Up DP）         O(n)         O(n)        ⭐⭐⭐
动态规划（空间优化）             O(n)         O(1)        ⭐⭐⭐⭐ 最优
───────────────────────────────────────────────────────────────────────

核心思想（动态规划）：
───────────────────────────────────────────────────────────────────────
关键洞察：
1. 要到达第 n 阶，最后一步只有两种可能：
   - 从第 n-1 阶爬 1 阶
   - 从第 n-2 阶爬 2 阶
2. 因此，到达第 n 阶的方法数 = 到达第 n-1 阶的方法数 + 到达第 n-2 阶的方法数
3. 这就是斐波那契数列的递推关系！

状态定义：
    dp[i] = 到达第 i 阶的方法数

状态转移：
    dp[i] = dp[i-1] + dp[i-2]

初始条件：
    dp[0] = 1（地面，不算台阶，但有 1 种方法：不动）
    dp[1] = 1（只能爬 1 阶）

最终答案：
    dp[n]

与斐波那契数列的关系：
───────────────────────────────────────────────────────────────────────
    爬楼梯问题        斐波那契数列
    ────────────────────────────────────────
    dp[0] = 1        F(0) = 0
    dp[1] = 1        F(1) = 1
    dp[2] = 2        F(2) = 1
    dp[3] = 3        F(3) = 2
    dp[4] = 5        F(4) = 3
    dp[5] = 8        F(5) = 5
    ────────────────────────────────────────
    dp[n] = F(n+1)

    所以爬楼梯问题本质上就是斐波那契数列，只是索引偏移了 1。
"""


# ══════════════════════════════════════════════════════════
# 解法一：暴力递归（Brute Force）
# ══════════════════════════════════════════════════════════


def climbStairs_brute(n: int) -> int:
    """
    暴力递归解法（Brute Force）

    核心思路：
    ────────────────────────────────────────────────────────
    直接根据递推关系递归求解：
        climb(n) = climb(n-1) + climb(n-2)
    不做任何缓存，存在大量重复计算。

    算法步骤：
    1. Base Case：n = 1 时返回 1（只有一种方法：爬 1 阶）
    2. 递归求解：climb(n-1) + climb(n-2)

    时间复杂度：O(2^n) — 递归树呈指数级增长
    空间复杂度：O(n)   — 递归调用栈的最大深度

    问题分析（以 n=4 为例）：
    ────────────────────────────────────────────────────────
                        climb(4)
                       /        \
                  climb(3)     climb(2)       ← climb(2) 被计算了 2 次
                  /     \        /    \
              climb(2) climb(1) climb(1) climb(0)  ← climb(1) 被计算了 3 次
              /     \
          climb(1) climb(0)

    climb(2)、climb(1) 被重复计算多次，效率极低。
    这就是为什么需要引入"记忆化"或"动态规划"来消除重复计算。

    注意：这个解法在 n > 35 左右就会明显变慢，仅用于理解问题。
    """
    # Base Case
    if n <= 1:
        return 1

    # 递归求解
    return climbStairs_brute(n - 1) + climbStairs_brute(n - 2)


# ══════════════════════════════════════════════════════════
# 解法二：记忆化搜索（Top-Down DP）
# ══════════════════════════════════════════════════════════


def climbStairs_memo(n: int) -> int:
    """
    记忆化搜索解法（Top-Down DP）⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    在暴力递归的基础上，增加一个缓存（字典/数组），
    记录已经计算过的子问题结果，避免重复计算。

    算法步骤：
    1. 创建缓存 memo，存储已计算的 climb(k) 值
    2. 递归时先查缓存：
        a. 如果缓存中有结果，直接返回（命中）
        b. 如果缓存中没有，计算后存入缓存，再返回
    3. Base Case：n = 1 时返回 1

    时间复杂度：O(n) — 每个子问题只计算一次
    空间复杂度：O(n) — 缓存 + 递归调用栈

    为什么这是动态规划？
    ────────────────────────────────────────────────────────
    DP 的两个核心要素：
    - 最优子结构：climb(n) 由 climb(n-1) 和 climb(n-2) 的结果决定
    - 重叠子问题：递归过程中，climb(n-2) 会被 climb(n) 和 climb(n-1) 同时需要
      → 用缓存消除重复计算，这就是"记忆化搜索"

    状态定义：
    - memo[k] = 到达第 k 阶的方法数

    状态转移：
    - memo[k] = memo[k-1] + memo[k-2]

    图解示例：n = 4
    ────────────────────────────────────────────────────────
    递归树（带缓存）：
                        climb(4)
                       /        \
                  climb(3)     climb(2) ✓
                  /     \
              climb(2) ✓ climb(1) ✓

    ✓ 表示命中缓存，不再递归。

    最终计算顺序：
        climb(1) → 1（Base Case）
        climb(2) → climb(1) + climb(0) = 1 + 1 = 2
        climb(3) → climb(2) + climb(1) = 2 + 1 = 3
        climb(4) → climb(3) + climb(2) = 3 + 2 = 5

    结果：5 ✅
    """
    # 缓存：记录已计算的 climb(k)
    # 初始化：climb(1) = 1, climb(2) = 2
    memo = {1: 1, 2: 2}

    def dfs(k: int) -> int:
        """递归求解 climb(k)"""
        # 命中缓存，直接返回
        if k in memo:
            return memo[k]

        # 计算并存入缓存
        memo[k] = dfs(k - 1) + dfs(k - 2)
        return memo[k]

    return dfs(n)


# ══════════════════════════════════════════════════════════
# 解法三：动态规划（Bottom-Up DP）
# ══════════════════════════════════════════════════════════


def climbStairs_dp(n: int) -> int:
    """
    动态规划解法（Bottom-Up DP）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    从底向上计算：先算 climb(1)、climb(2)，再算 climb(3)、climb(4)……直到 climb(n)。
    不需要递归，用迭代方式填充 DP 表。

    算法步骤：
    1. 创建 DP 数组 dp，dp[i] 表示到达第 i 阶的方法数
    2. 初始化 base case：dp[0] = 1, dp[1] = 1
    3. 从 i = 2 到 n，依次计算：
        dp[i] = dp[i-1] + dp[i-2]
    4. 返回 dp[n]

    时间复杂度：O(n) — 一次遍历
    空间复杂度：O(n) — DP 数组

    图解示例：n = 5
    ────────────────────────────────────────────────────────
        i      0    1    2    3    4    5
        dp[i]  1    1    2    3    5    8
                       ↑    ↑    ↑    ↑
                      1+1  1+2  2+3  3+5

    状态定义：
    - dp[i] = 到达第 i 阶的方法数

    状态转移：
    - dp[i] = dp[i-1] + dp[i-2]

    初始条件：
    - dp[0] = 1（地面）
    - dp[1] = 1（只能爬 1 阶）
    """
    # Base Case
    if n <= 1:
        return 1

    # 创建 DP 数组
    dp = [0] * (n + 1)
    dp[0], dp[1] = 1, 1

    # 从底向上填充
    for i in range(2, n + 1):
        dp[i] = dp[i - 1] + dp[i - 2]

    return dp[n]


# ══════════════════════════════════════════════════════════
# 解法四：动态规划（空间优化版，推荐解）
# ══════════════════════════════════════════════════════════


def climbStairs(n: int) -> int:
    """
    空间优化的动态规划解法 ⭐⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    观察状态转移方程：dp[i] = dp[i-1] + dp[i-2]
    climb(i) 只依赖 climb(i-1) 和 climb(i-2)，不需要整个数组，
    用两个滚动变量即可，空间复杂度降为 O(1)。

    算法步骤：
    1. 初始化：prev = 1 (climb(0)), curr = 1 (climb(1))
    2. 从 i = 2 到 n，每次：
        a. 计算 next = prev + curr
        b. 滚动更新：prev = curr, curr = next
    3. 返回 curr

    时间复杂度：O(n) — 一次遍历
    空间复杂度：O(1) — 只用了两个变量

    图解示例：n = 5
    ────────────────────────────────────────────────────────
        i     prev   curr   next (=prev+curr)
        ────────────────────────────────────────
        初始   1      1      —
        2      1      2      1+1=2
        3      2      3      1+2=3
        4      3      5      2+3=5
        5      5      8      3+5=8  ← 最终结果
        ────────────────────────────────────────
        返回 curr = 8

    为什么只用两个变量就够了？
    ────────────────────────────────────────────────────────
    因为计算 climb(i) 时只需要 climb(i-1) 和 climb(i-2)，
    更早的历史值（climb(i-3)、climb(i-4)……）已经没用了。
    这是 DP 空间优化的经典技巧——"滚动变量"。

    与斐波那契数列的关系：
    ────────────────────────────────────────────────────────
    climb(n) = F(n+1)，其中 F 是斐波那契数列。
    例如：
        climb(4) = 5 = F(5)
        climb(5) = 8 = F(6)
    """
    # Base Case
    if n <= 1:
        return 1

    # 用两个变量代替数组，滚动更新
    prev, curr = 1, 1  # prev = climb(0), curr = climb(1)

    for _ in range(2, n + 1):
        prev, curr = curr, prev + curr  # 滚动：prev ← curr, curr ← next

    return curr


# ══════════════════════════════════════════════════════════
# 重要说明：动态规划的正确性证明
# ══════════════════════════════════════════════════════════
#
# 为什么 dp[i] = dp[i-1] + dp[i-2] 是正确的？
# ────────────────────────────────────────────────────────
# 1. 最优子结构（Optimal Substructure）：
#    到达第 i 阶的最后一步只有两种可能：
#        a) 从第 i-1 阶爬 1 阶
#        b) 从第 i-2 阶爬 2 阶
#    这两种情况互斥且完备，所以：
#        climb(i) = climb(i-1) + climb(i-2)
#
# 2. 重叠子问题（Overlapping Subproblems）：
#    在暴力递归中，climb(i-2) 会被 climb(i) 和 climb(i-1) 同时需要，
#    导致重复计算。用缓存或 DP 数组可以消除重复计算。
#
# 3. 无后效性（No Aftereffect）：
#    计算 climb(i) 时，只需要知道 climb(i-1) 和 climb(i-2)，
#    不需要知道是如何到达 i-1 或 i-2 的。
#    这保证了状态转移的正确性。
#
# 为什么空间优化是正确的？
# ────────────────────────────────────────────────────────
# 因为 dp[i] 只依赖 dp[i-1] 和 dp[i-2]，
# 更早的历史值（dp[i-3]、dp[i-4]……）已经没用了。
# 用两个滚动变量保存最近两个值即可。
# 推广：每次可爬 {1,3,5} 步 → 递推变为 dp[n] = dp[n-1] + dp[n-3] + dp[n-5]
# 另外，还有一个问题：爬楼梯问题：f(2) = f(1) + f(0) = 1 + f(0) → 
# 物理意义：f(n) 表示从地面（第0阶）爬到第 n 阶的不同方法数。那么 f(0) 就是“从地面到地面”的方法数——只有1种方式：什么也不做（不动）。这个定义符合组合计数的直觉。
