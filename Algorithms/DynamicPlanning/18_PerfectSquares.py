"""
完全平方数（Perfect Squares）

题目描述：
给你一个整数 n ，返回 和为 n 的完全平方数的最少数量 。
完全平方数 是一个整数，其值等于另一个整数的平方；换句话说，其值等于一个整数自乘的积。
例如，1、4、9 和 16 都是完全平方数，而 3 和 11 不是。

示例 1：
输入：n = 12
输出：3 
解释：12 = 4 + 4 + 4

示例 2：
输入：n = 13
输出：2
解释：13 = 4 + 9

════════════════════════════════════════════════════════════════════════
核心联系：本题与零钱兑换（Coin Change）的本质关系
════════════════════════════════════════════════════════════════════════

维度              零钱兑换（Coin Change）              完全平方数（本题）
────────────────────────────────────────────────────────────────────────
问题本质          完全背包求最小硬币数                 完全背包求最小平方数个数
可选"硬币"        coins = [c1, c2, ..., cn]           squares = [1, 4, 9, ..., m²]（m = √n）
目标值            amount                              n
递推公式          dp[i] = min(dp[i], dp[i-coin]+1)    dp[i] = min(dp[i], dp[i-s]+1)
初始条件          dp[0] = 0，其余为 inf               dp[0] = 0，其余为 n（最多用 n 个 1）
────────────────────────────────────────────────────────────────────────

为什么本题是完全背包问题？
────────────────────────────────────────────────────────────────────────
每种完全平方数可以无限使用（例如 4 可以用多次），目标是求最小值（最少个数）。
这与零钱兑换问题的本质完全相同，只是"硬币面额"变成了完全平方数。

解题思路总览：
────────────────────────────────────────────────────────────────────────
解法                          时间复杂度       空间复杂度       说明
────────────────────────────────────────────────────────────────────────
回溯算法                       O(m^n)          O(n)            超时，仅用于理解
记忆化搜索（Top-Down DP）       O(n×m)          O(n)            ⭐⭐
动态规划（Bottom-Up DP）        O(n×m)          O(n)            ⭐⭐⭐⭐  推荐
广度优先搜索（BFS）             O(n×m)          O(n)            ⭐⭐⭐   适合找最短路径
数学解法（Lagrange定理）        O(√n)           O(1)            ⭐⭐⭐⭐⭐ 最优
────────────────────────────────────────────────────────────────────────
（m = √n，即完全平方数的个数）

核心思想（动态规划）：
────────────────────────────────────────────────────────────────────────
关键洞察（转化为完全背包问题）：
1. 可选的"硬币" = [1², 2², 3², ..., m²]，其中 m = floor(√n)
2. 每种"硬币"可以无限使用 → 完全背包问题
3. 目标是求最小值（最少平方数个数）
4. 递推公式：dp[i] = min(dp[i], dp[i - s] + 1)

状态定义（一维 DP）：
    dp[i] = 和为 i 的完全平方数的最少数量

状态转移：
    对于每个数 i，尝试使用每种完全平方数 s：
    dp[i] = min(dp[i], dp[i - s] + 1)（当 s <= i）

    含义：和为 i 的最少平方数 = min(当前最小值, 和为 i-s 的最少平方数 + 1)

初始条件：
    dp[0] = 0（和为 0 需要 0 个完全平方数）
    dp[1..n] = n（初始化为 n，最多用 n 个 1）

最终答案：
    dp[n]
"""


# ══════════════════════════════════════════════════════════
# 解法一：回溯算法（Brute Force）
# ══════════════════════════════════════════════════════════


def numSquares_backtrack(n: int) -> int:
    """
    回溯算法（Brute Force）

    核心思路：
    ────────────────────────────────────────────────────────
    枚举每一种完全平方数的组合，记录最少的个数。

    算法步骤：
    1. 生成所有 ≤ n 的完全平方数
    2. 使用回溯枚举每种平方数的使用次数
    3. 维护剩余值 remaining 和当前计数 count
    4. 当 remaining == 0 时，更新最小计数
    5. 当 remaining < 0 时，剪枝

    时间复杂度：O(m^n) — 最坏情况每层递归 m 个分支
    空间复杂度：O(n)   — 递归调用栈深度

    问题分析：
    ────────────────────────────────────────────────────────
    对于 n = 12, squares = [1, 4, 9]，递归树非常庞大，效率极低。
    仅用于理解问题本质。
    """
    m = int(n ** 0.5)
    squares = [i ** 2 for i in range(1, m + 1)]
    min_count = float("inf")

    def backtrack(remaining: int, count: int) -> None:
        nonlocal min_count
        if remaining == 0:
            min_count = min(min_count, count)
            return
        if remaining < 0:
            return

        for s in squares:
            if remaining >= s:
                backtrack(remaining - s, count + 1)

    backtrack(n, 0)
    return min_count


# ══════════════════════════════════════════════════════════
# 解法二：记忆化搜索（Top-Down DP）
# ══════════════════════════════════════════════════════════


def numSquares_memo(n: int) -> int:
    """
    记忆化搜索解法（Top-Down DP）⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    在回溯的基础上，添加缓存避免重复计算。

    算法步骤：
    1. 生成所有 ≤ n 的完全平方数
    2. 创建缓存 memo[remaining]
    3. 使用递归 + 缓存求解
    4. 对于每个剩余值，枚举所有可能的平方数

    时间复杂度：O(n×m) — 每个状态只计算一次
    空间复杂度：O(n)   — 缓存 + 递归调用栈

    状态定义：
    - memo[remaining] = 和为 remaining 的完全平方数的最少数量

    状态转移：
    - memo[remaining] = min(memo[remaining - s] + 1 for s in squares if s <= remaining)

    初始条件：
    - remaining == 0 时返回 0（已经凑成目标值）
    """
    m = int(n ** 0.5)
    squares = [i ** 2 for i in range(1, m + 1)]
    memo = {}

    def dfs(remaining: int) -> int:
        if remaining == 0:
            return 0
        if remaining in memo:
            return memo[remaining]

        min_count = float("inf")
        for s in squares:
            if remaining >= s:
                res = dfs(remaining - s)
                if res != -1:
                    min_count = min(min_count, res + 1)

        memo[remaining] = min_count if min_count != float("inf") else -1
        return memo[remaining]

    return dfs(n)


# ══════════════════════════════════════════════════════════
# 解法三：动态规划（Bottom-Up DP）— 写法一：外层数值，内层平方数
# ══════════════════════════════════════════════════════════


def numSquares_dp_v1(n: int) -> int:
    """
    动态规划解法（Bottom-Up DP）⭐⭐⭐⭐ 推荐 — 写法一

    核心思想：
    ────────────────────────────────────────────────────────
    标准的完全背包问题解法。
    通过一维数组记录和为每个数所需的最少平方数个数。

    算法步骤：
    1. 生成所有 ≤ n 的完全平方数
    2. 创建一维 DP 数组 dp[n+1]，初始化 dp[0] = 0，其余为 n
    3. 外层循环遍历数值 i（从 1 到 n）
    4. 内层循环遍历平方数 s（从 1 到 m²）
    5. 对于每个数值 i，尝试用每种平方数来更新 dp[i]

    时间复杂度：O(n×m) — 双重循环
    空间复杂度：O(n)   — 一维 DP 数组

    图解示例：
    ────────────────────────────────────────────────────────
    n = 12, squares = [1, 4, 9]

    dp = [0, 12, 12, 12, 12, 12, 12, 12, 12, 12, 12, 12, 12]

    i=1: s=1 → dp[1] = min(12, dp[0]+1) = 1
         s=4 → 4 > 1，跳过
         s=9 → 跳过
         dp = [0, 1, 12, 12, 12, 12, 12, 12, 12, 12, 12, 12, 12]

    i=2: s=1 → dp[2] = min(12, dp[1]+1) = 2
         s=4 → 4 > 2，跳过
         s=9 → 跳过
         dp = [0, 1, 2, 12, 12, 12, 12, 12, 12, 12, 12, 12, 12]

    i=4: s=1 → dp[4] = min(12, dp[3]+1) = 4
         s=4 → dp[4] = min(4, dp[0]+1) = 1
         s=9 → 9 > 4，跳过
         dp = [0, 1, 2, 3, 1, 2, 3, 4, 2, 1, 2, 3, 3]

    ...（中间步骤省略）...

    i=12: s=1 → dp[12] = min(?, dp[11]+1) = ?
          s=4 → dp[12] = min(?, dp[8]+1) = 3
          s=9 → dp[12] = min(3, dp[3]+1) = 3
          dp = [0, 1, 2, 3, 1, 2, 3, 4, 2, 1, 2, 3, 3]

    结果：dp[12] = 3 ✓

    状态定义：
    - dp[i] = 和为 i 的完全平方数的最少数量

    状态转移：
    - dp[i] = min(dp[i], dp[i - s] + 1)（当 s <= i）

    初始条件：
    - dp[0] = 0（和为 0 需要 0 个完全平方数）
    - dp[1..n] = n（初始化为 n，最多用 n 个 1）

    最终答案：
    - dp[n]
    """
    m = int(n ** 0.5)
    squares = [i ** 2 for i in range(1, m + 1)]
    dp = [n] * (n + 1)
    dp[0] = 0

    for i in range(1, n + 1):
        for s in squares:
            if s <= i:
                dp[i] = min(dp[i], dp[i - s] + 1)
            else:
                break

    return dp[n]


# ══════════════════════════════════════════════════════════
# 解法四：动态规划（Bottom-Up DP）— 写法二：外层平方数，内层数值
# ══════════════════════════════════════════════════════════


def numSquares_dp_v2(n: int) -> int:
    """
    动态规划解法（Bottom-Up DP）⭐⭐⭐⭐ 推荐 — 写法二

    核心思想：
    ────────────────────────────────────────────────────────
    另一种完全背包问题的标准写法。
    先遍历平方数，再遍历数值，确保每种平方数可以被多次使用。

    算法步骤：
    1. 生成所有 ≤ n 的完全平方数
    2. 创建一维 DP 数组 dp[n+1]，初始化 dp[0] = 0，其余为 n
    3. 外层循环遍历平方数 s（从 1 到 m²）
    4. 内层循环遍历数值 i（从 s 到 n）
    5. 对于每种平方数，更新所有能用它来优化的数值

    时间复杂度：O(n×m) — 双重循环
    空间复杂度：O(n)   — 一维 DP 数组

    状态定义：
    - dp[i] = 和为 i 的完全平方数的最少数量

    状态转移：
    - dp[i] = min(dp[i], dp[i - s] + 1)

    初始条件：
    - dp[0] = 0（和为 0 需要 0 个完全平方数）
    - dp[1..n] = n（初始化为 n，最多用 n 个 1）

    最终答案：
    - dp[n]

    为什么内层循环从 s 开始？
    ────────────────────────────────────────────────────────
    当 i < s 时，无法使用该平方数，直接跳过。
    这样可以避免无效的比较和索引越界。
    """
    m = int(n ** 0.5)
    squares = [i ** 2 for i in range(1, m + 1)]
    dp = [n] * (n + 1)
    dp[0] = 0

    for s in squares:
        for i in range(s, n + 1):
            dp[i] = min(dp[i], dp[i - s] + 1)

    return dp[n]


# ══════════════════════════════════════════════════════════
# 解法五：广度优先搜索（BFS）
# ══════════════════════════════════════════════════════════


def numSquares_bfs(n: int) -> int:
    """
    广度优先搜索解法（BFS）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    将问题转化为最短路径问题：
    - 每个数值是一个节点
    - 使用一个完全平方数 s 相当于从节点 i 走到节点 i-s
    - 求从 n 到 0 的最短路径长度

    BFS 的特性保证了第一次到达目标时就是最短路径。

    算法步骤：
    1. 生成所有 ≤ n 的完全平方数
    2. 使用队列存储待处理的数值和当前步数
    3. 使用 visited 数组避免重复访问
    4. 每次出队一个数值，尝试减去每种平方数
    5. 当数值为 0 时，返回当前步数

    时间复杂度：O(n×m) — 每个节点最多访问一次
    空间复杂度：O(n)   — 队列和 visited 数组

    图解示例：
    ────────────────────────────────────────────────────────
    n = 12, squares = [1, 4, 9]

    队列：[(12, 0)]
    出队 12：减去 1→11, 减去 4→8, 减去 9→3，步数+1
    队列：[(11, 1), (8, 1), (3, 1)]

    出队 11：减去 1→10, 减去 4→7, 减去 9→2，步数+1
    队列：[(8, 1), (3, 1), (10, 2), (7, 2), (2, 2)]

    出队 8：减去 1→7(已访问), 减去 4→4, 减去 9→-1(无效)，步数+1
    队列：[(3, 1), (10, 2), (7, 2), (2, 2), (4, 2)]

    出队 4：减去 1→3(已访问), 减去 4→0，步数+1 → 返回 3

    结果：3 ✓

    适用场景：
    ────────────────────────────────────────────────────────
    当目标值较大但最短路径较短时，BFS 效率高于 DP。
    """
    if n == 0:
        return 0

    from collections import deque

    m = int(n ** 0.5)
    squares = [i ** 2 for i in range(1, m + 1)]

    visited = [False] * (n + 1)
    queue = deque()
    queue.append((n, 0))
    visited[n] = True

    while queue:
        current, steps = queue.popleft()

        for s in squares:
            next_val = current - s
            if next_val == 0:
                return steps + 1
            if next_val > 0 and not visited[next_val]:
                visited[next_val] = True
                queue.append((next_val, steps + 1))

    return -1


# ══════════════════════════════════════════════════════════
# 解法六：数学解法（Lagrange 四平方定理 + Legendre 三平方定理）
# ══════════════════════════════════════════════════════════


def numSquares_math(n: int) -> int:
    """
    数学解法（Lagrange 四平方定理）⭐⭐⭐⭐⭐ 最优

    核心思想：
    ────────────────────────────────────────────────────────
    利用数论定理直接判断最少需要多少个完全平方数。

    Lagrange 四平方定理（1770 年）：
    ────────────────────────────────────────────────────────
    任何一个自然数都可以表示为最多 4 个完全平方数之和。
    即：对于任意正整数 n，存在整数 a, b, c, d 使得：
        n = a² + b² + c² + d²

    Legendre 三平方定理（1798 年）：
    ────────────────────────────────────────────────────────
    当且仅当 n 不满足 n = 4^a(8b+7) 时，n 可以表示为最多 3 个完全平方数之和。

    算法步骤：
    1. 判断 n 是否为完全平方数 → 返回 1
    2. 判断 n 是否满足 n = 4^a(8b+7) → 返回 4
    3. 判断 n 是否可以表示为两个完全平方数之和 → 返回 2
    4. 否则返回 3

    时间复杂度：O(√n) — 仅需遍历到 √n
    空间复杂度：O(1)   — 常数空间

    定理证明思路：
    ────────────────────────────────────────────────────────
    ① 如果 n 本身是完全平方数，答案显然是 1
    ② 如果 n = 4^a(8b+7)，根据 Legendre 定理，n 不能表示为 3 个或更少的平方数之和，
       再根据 Lagrange 定理，n 可以表示为 4 个平方数之和，因此答案是 4
    ③ 如果 n 可以表示为两个平方数之和，答案是 2
    ④ 否则，根据 Lagrange 定理，答案只能是 3
    """
    def is_square(x: int) -> bool:
        s = int(x ** 0.5)
        return s * s == x

    if is_square(n):
        return 1

    temp = n
    while temp % 4 == 0:
        temp //= 4
    if temp % 8 == 7:
        return 4

    for i in range(1, int(n ** 0.5) + 1):
        if is_square(n - i * i):
            return 2

    return 3


# ══════════════════════════════════════════════════════════
# 重要说明：算法正确性说明与易错点提醒
# ══════════════════════════════════════════════════════════
#
# 算法正确性说明：
# ────────────────────────────────────────────────────────
# 1. 动态规划解法的正确性：
#    - 完全背包问题的标准解法，通过状态转移方程正确计算每个数值的最小平方数个数
#    - dp[0] = 0 是递推的起点，表示"和为 0 需要 0 个平方数"
#    - 每种平方数可以无限使用，通过内层循环正向遍历实现
#
# 2. BFS 解法的正确性：
#    - 将问题转化为无权图的最短路径问题
#    - BFS 的层次遍历特性保证了第一次到达目标时就是最短路径
#    - visited 数组避免了重复访问，保证了算法的正确性和效率
#
# 3. 数学解法的正确性（基于数论定理）：
#    - Lagrange 四平方定理（1770）：任何自然数最多由 4 个平方数之和表示
#    - Legendre 三平方定理（1798）：当且仅当 n ≠ 4^a(8b+7) 时，n 可由 3 个平方数表示
#    - 这两个定理是数学解法 O(√n) 的理论基础，证明了答案只能是 1、2、3、4 中的一个
#
#    判断逻辑：
#    ① 先判断是否为完全平方数（答案=1）
#    ② 再判断是否满足 n = 4^a(8b+7)（答案=4）
#    ③ 再判断是否可以表示为两个平方数之和（答案=2）
#    ④ 否则答案=3（根据 Lagrange 定理，最多需要 4 个，排除其他情况后只能是 3）
#
# 常见错误：
# ────────────────────────────────────────────────────────
# 1. 生成完全平方数时边界错误
#    - 错误：range(1, m) 漏掉了 m²
#    - 正确：range(1, m + 1)，确保包含 m²
#    - 原因：m = √n，m² 是 ≤ n 的最大完全平方数
#
# 2. 数学解法中判断 n = 4^a(8b+7) 时的错误
#    - 错误：直接判断 n % 8 == 7，没有先除去所有 4 的因子
#    - 正确：先循环除以 4，直到无法整除，再判断 temp % 8 == 7
#    - 原因：4^a 不影响模 8 的结果，必须先除去才能正确判断
#    - 反例：n = 28 = 4^1 × 7，28 % 8 = 4，但 7 % 8 = 7，正确答案是 4
#
# 3. 动态规划中 dp[0] = 0 的意义
#    - 错误：忘记初始化 dp[0] = 0，或初始化为其他值
#    - 后果：所有 dp[i] 都会保持为初始值（n），无法正确递推
#    - 原因：dp[0] 是递推的起点，dp[i] = dp[i-s] + 1 需要用到 dp[0]
#
# 4. 动态规划中初始化 dp 值的选择
#    - 错误：初始化为 float("inf")
#    - 后果：inf + 1 会溢出或产生错误结果
#    - 正确：初始化为 n（最多用 n 个 1），或使用 min() 自动处理 inf
#
# 5. 两种 DP 循环顺序的理解
#    - 在本题中，两种写法结果相同（求最小值，顺序不影响 min 结果）
#    - 但在求组合数时顺序很重要：
#      * 外层平方数，内层数值 → 求组合数（不考虑顺序）
#      * 外层数值，内层平方数 → 求排列数（考虑顺序）
#
# 6. BFS 中忘记标记 visited
#    - 错误：不使用 visited 数组
#    - 后果：同一个数值会被多次加入队列，导致超时或内存溢出
#    - 正确：每次入队前标记 visited[next_val] = True
#
# 7. 数学解法中判断两个平方数之和的边界
#    - 错误：循环范围只到 int(n**0.5) // 2
#    - 正确：循环范围到 int(n**0.5)，确保所有可能的 i 都被检查
#    - 原因：i 可以是从 1 到 √n 的任意整数
# ══════════════════════════════════════════════════════════
