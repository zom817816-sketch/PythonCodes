"""
最大子数组和（Maximum Subarray）

题目描述：
给你一个整数数组 nums，请你找出一个具有最大和的连续子数组（子数组最少包含一个元素），
返回其最大和。

子数组是数组中的一个连续部分。

输入描述：
一个整数数组 nums。

输出描述：
输出一个整数，表示连续子数组的最大和。

输入示例：
[-2, 1, -3, 4, -1, 2, 1, -5, 4]
输出示例：
6

════════════════════════════════════════════════════════════════════════
核心概念：子数组 vs 子序列 & 贪心 vs DP
════════════════════════════════════════════════════════════════════════

维度              子数组（Subarray）            子序列（Subsequence）
────────────────────────────────────────────────────────────────────────
元素位置          必须连续                      可以不连续
示例              [0,3,1,6,2,2,7]              [0,3,1,6,2,2,7]
                  子数组 [3,1,6] ✓             子序列 [3,6,7] ✓
                  子序列 [3,1,7] ✓             子数组 [3,1,7] ✗
DP状态            只看前一个元素                必须看前面所有更小元素
时间复杂度        O(n)                          O(n²) 或 O(n log n)
────────────────────────────────────────────────────────────────────────

为什么 Kadane 算法既是贪心也是 DP？
────────────────────────────────────────────────────────────────────────
    - 贪心视角：一旦当前和为负，就抛弃它，因为负数和只会拖累后续
    - DP 视角：dp[i] = 以 nums[i] 结尾的最大子数组和
               dp[i] = max(nums[i], dp[i-1] + nums[i])
    两者数学等价，但 Kadane 算法用滚动变量实现 O(1) 空间。

关键洞察：
────────────────────────────────────────────────────────────────────────
如果之前累积的和是负数，那么"从当前元素重新开始"一定比
"在负数基础上继续加"更好。因此核心决策是：
    继续累加 vs 重新开始

解题思路总览：
────────────────────────────────────────────────────────────────────────
解法                          时间复杂度       空间复杂度       说明
────────────────────────────────────────────────────────────────────────
暴力枚举                      O(n²)           O(1)            超时，仅用于理解
动态规划（dp数组）             O(n)            O(n)            ⭐⭐⭐
Kadane 算法（贪心+滚动变量）   O(n)            O(1)            ⭐⭐⭐⭐ 推荐
────────────────────────────────────────────────────────────────────────

核心思想（Kadane 算法）：
────────────────────────────────────────────────────────────────────────
关键洞察（贪心的本质）：
1. 遍历数组，维护当前子数组的和 current_sum
2. 如果 current_sum > max_sum，更新最大值
3. 如果 current_sum < 0，重置 current_sum = 0
   （负数累积只会拖累后面的正数）

为什么可以重置？
────────────────────────────────────────────────────────────────────────
假设 current_sum = -5，下一个元素是 3：
    - 继续累加：-5 + 3 = -2
    - 重新开始：0 + 3 = 3
重新开始总是更优，因为 -5 只会让结果更小。

DP 状态定义：
    dp[i] = 以 nums[i] 结尾的最大子数组和

状态转移：
    dp[i] = max(nums[i], dp[i-1] + nums[i])
    要么从当前位置重新开始，要么接在前面的子数组后面继续累加

初始条件：
    dp[0] = nums[0]

最终答案：
    max(dp[0], dp[1], ..., dp[n-1])
"""


# ══════════════════════════════════════════════════════════
# 解法一：暴力枚举
# ══════════════════════════════════════════════════════════


def maxSubArray_brute(nums: list[int]) -> int:
    """
    暴力枚举解法

    核心思想：
    ────────────────────────────────────────────────────────
    枚举所有可能的连续子数组，计算每个子数组的和，取最大值。

    算法步骤：
    1. 枚举子数组的起始位置 i
    2. 枚举子数组的结束位置 j（j ≥ i）
    3. 计算 nums[i..j] 的和，更新最大值

    时间复杂度：O(n²) — 枚举所有子数组
    空间复杂度：O(1) — 只用常数额外空间

    问题分析：
    ────────────────────────────────────────────────────────
    对于 n = 10⁵，O(n²) 需要约 10¹⁰ 次运算，完全不可行。
    但暴力枚举是理解问题的基础，所有优化方案都始于暴力。

    图解示例：
    ────────────────────────────────────────────────────────
    nums = [-2, 1, -3, 4, -1, 2, 1, -5, 4]

    i=0: j=0 sum=-2, j=1 sum=-1, j=2 sum=-4, j=3 sum=0, ...
    i=1: j=1 sum=1, j=2 sum=-2, j=3 sum=2, ...
    ...
    最终全局最大 = 6（子数组 [4, -1, 2, 1]）

    结果：6 ✓
    """
    n = len(nums)
    if n == 0:
        return 0

    max_sum = float('-inf')

    # 枚举起始位置
    for i in range(n):
        current_sum = 0
        # 枚举结束位置
        for j in range(i, n):
            # 累加比每次重新计算更快：current_sum = sum(nums[i..j])
            current_sum += nums[j]
            max_sum = max(max_sum, current_sum)

    return max_sum


# ══════════════════════════════════════════════════════════
# 解法二：动态规划（dp 数组）
# ══════════════════════════════════════════════════════════


def maxSubArray_dp(nums: list[int]) -> int:
    """
    动态规划解法（dp 数组）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    dp[i] = 以 nums[i] 结尾的最大子数组和。
    对于每个位置 i，只有两种选择：
        1. 接在前面的子数组后面：dp[i-1] + nums[i]
        2. 重新开始一个新子数组：nums[i]
    取两者中的最大值。

    算法步骤：
    1. 初始化 dp[0] = nums[0]
    2. 从 i=1 开始遍历每个元素
    3. dp[i] = max(nums[i], dp[i-1] + nums[i])
    4. 返回 max(dp)

    时间复杂度：O(n) — 遍历一次数组
    空间复杂度：O(n) — dp 数组

    图解示例：
    ────────────────────────────────────────────────────────
    nums = [-2, 1, -3, 4, -1, 2, 1, -5, 4]
    dp   = [-2, ?,  ?,  ?,  ?,  ?,  ?,  ?,  ?]

    i=0: dp[0] = -2（以 -2 结尾最大和为 -2）
    i=1: dp[1] = max(1, -2+1) = 1（从 1 重新开始比接在 -2 后面好）
         以 [1] 结尾的最大子数组和 = 1
    i=2: dp[2] = max(-3, 1+(-3)) = -2（接在 1 后面，和为 -2）
         以 [-3] 结尾的最大子数组和 = -2
    i=3: dp[3] = max(4, -2+4) = 4（从 4 重新开始更好）
         以 [4] 结尾的最大子数组和 = 4
    i=4: dp[4] = max(-1, 4+(-1)) = 3（接在 4 后面，和为 3）
         以 [4,-1] 结尾的最大子数组和 = 3
    i=5: dp[5] = max(2, 3+2) = 5（接在 [4,-1] 后面，和为 5）
         以 [4,-1,2] 结尾的最大子数组和 = 5
    i=6: dp[6] = max(1, 5+1) = 6（接在 [4,-1,2] 后面，和为 6）
         以 [4,-1,2,1] 结尾的最大子数组和 = 6 ✓
    i=7: dp[7] = max(-5, 6+(-5)) = 1（接在 [4,-1,2,1] 后面，和为 1）
         以 [4,-1,2,1,-5] 结尾的最大子数组和 = 1
    i=8: dp[8] = max(4, 1+4) = 5（接在后面，和为 5）
         以 [4,-1,2,1,-5,4] 结尾的最大子数组和 = 5

    结果：max(dp) = 6 ✓
    对应子数组：[4, -1, 2, 1]
    """
    n = len(nums)
    if n == 0:
        return 0

    # dp[i] = 以 nums[i] 结尾的最大子数组和
    # 初始化 dp[0] = nums[0]：第一个元素只能自己作为一个子数组
    dp = [0] * n
    dp[0] = nums[0]

    for i in range(1, n):
        # 核心决策：重新开始（nums[i]）或接在后面（dp[i-1] + nums[i]）
        # 为什么不是 max(dp[i-1], dp[i-1] + nums[i])？
        # 因为 dp[i-1] 是"以 nums[i-1] 结尾"的最大和，它可能不包含 nums[i-1] 之前的前缀
        # 而我们要找的是"以 nums[i] 结尾"的，必须包含 nums[i]
        dp[i] = max(nums[i], dp[i - 1] + nums[i])

    # 全局最大值不一定以最后一个元素结尾
    return max(dp)


# ══════════════════════════════════════════════════════════
# 解法三：Kadane 算法（贪心 + 滚动变量）
# ══════════════════════════════════════════════════════════


def maxSubArray(nums: list[int]) -> int:
    """
    Kadane 算法（贪心 + 滚动变量优化）⭐⭐⭐⭐ 推荐

    核心思想：
    ────────────────────────────────────────────────────────
    观察 dp 转移方程：dp[i] = max(nums[i], dp[i-1] + nums[i])
    发现 dp[i] 只依赖 dp[i-1]，因此可以用一个变量 current_sum 代替 dp 数组。

    贪心解释：
    ────────────────────────────────────────────────────────
    一旦 current_sum < 0，就抛弃当前累积，从下一个元素重新开始。
    因为负数的累积和只会让后面的正数变小，"重新开始"总是更优的选择。

    关键要点：
    ────────────────────────────────────────────────────────
    1. 用 max_sum 记录整个过程中的最大值
    2. 用 current_sum 记录当前子数组的和
    3. 如果 current_sum < 0，重置为 0（相当于从下一个元素重新开始）
    4. current_sum += num 放在前面还是后面？先加再判断重置！

    时间复杂度：O(n) — 遍历一次数组
    空间复杂度：O(1) — 只用两个变量

    图解示例：
    ────────────────────────────────────────────────────────
    nums = [-2, 1, -3, 4, -1, 2, 1, -5, 4]

    初始化：current_sum = 0, max_sum = -∞

    num=-2: current_sum = 0 + (-2) = -2, max_sum = max(-∞, -2) = -2
            current_sum < 0 → 重置为 0
    num=1:  current_sum = 0 + 1 = 1, max_sum = max(-2, 1) = 1
    num=-3: current_sum = 1 + (-3) = -2, max_sum = max(1, -2) = 1
            current_sum < 0 → 重置为 0
    num=4:  current_sum = 0 + 4 = 4, max_sum = max(1, 4) = 4
    num=-1: current_sum = 4 + (-1) = 3, max_sum = max(4, 3) = 4
    num=2:  current_sum = 3 + 2 = 5, max_sum = max(4, 5) = 5
    num=1:  current_sum = 5 + 1 = 6, max_sum = max(5, 6) = 6 ✓
    num=-5: current_sum = 6 + (-5) = 1, max_sum = max(6, 1) = 6
    num=4:  current_sum = 1 + 4 = 5, max_sum = max(6, 5) = 6

    结果：max_sum = 6 ✓

    另外一个视角：
    ────────────────────────────────────────────────────────
    等价于 DP 的滚动变量版本：
        current_sum = max(num, current_sum + num)
        max_sum = max(max_sum, current_sum)
    这个写法直接对应 DP 转移方程，无需显式判断 < 0 再重置。
    但"先累加再判断重置"的写法更符合贪心的直觉：丢弃负数累积。
    """
    # max_sum：全局最大子数组和
    # 初始化为负无穷，确保任何有效的子数组和都能更新它
    max_sum = float('-inf')
    # current_sum：当前子数组的累积和
    current_sum = 0

    for num in nums:
        # 先累加当前元素
        current_sum += num
        # 更新全局最大值
        max_sum = max(max_sum, current_sum)
        # 如果当前和为负，则抛弃它，从下一个元素重新开始
        # 因为负数累积在后续只会拖累更大的正数
        if current_sum < 0:
            current_sum = 0

    return max_sum


# ══════════════════════════════════════════════════════════
# 重要说明：算法的正确性证明和易错点提示
# ══════════════════════════════════════════════════════════
#
# 一、Kadane 算法的正确性证明：
# ────────────────────────────────────────────────────────
#
# 定理：Kadane 算法返回的是连续子数组的最大和。
#
# 证明：
# （1）子问题定义：
#     设 current_sum[i] = 以 nums[i] 结尾的最大子数组和。
#     设 max_sum[i] = nums[0..i] 中的全局最大子数组和。
#
# （2）最优子结构：
#     以 nums[i] 结尾的最大子数组，有两种可能：
#       - 只有一个元素 nums[i]
#       - 由以 nums[i-1] 结尾的最大子数组 + nums[i] 组成
#     因此：current_sum[i] = max(nums[i], current_sum[i-1] + nums[i])
#
#      Kadane 算法中 current_sum 变量正好实现了这个转移：
#          先累加（相当于 current_sum[i-1] + nums[i]）
#          如果结果 < 0 则归零（相当于 max(..., 0)）
#          这样 current_sum 始终保持以某个位置结尾的最大非负累积
#
# （3）全局最优：
#     max_sum = max(max_sum, current_sum) 保存了所有位置的最大值，
#     因此最终结果就是全局最大子数组和。
#
# （4）正确性边界情况验证：
#     全负数数组 [-3, -2, -1]：
#       每次累加后 current_sum 为负数，立即重置为 0
#       max_sum 记录过程：-3, -2, -1
#       返回 -1（最大负数）✓
#
#
# 二、为什么要先判断 current_sum < 0 再重置为 0？
# ────────────────────────────────────────────────────────
# 因为 current_sum 在重置之前已经用于更新 max_sum 了。
# 即使 current_sum 为负数，它也可能是当前的最大值（全负数数组）。
# 所以更新 max_sum 必须在重置 current_sum 之前。
#
# 错误写法：
#     if current_sum < 0:
#         current_sum = 0
#     current_sum += num
#     max_sum = max(max_sum, current_sum)
# 这样会导致：全负数数组返回 0，而非最大负数。❌
#
#
# 三、为什么不能初始化 max_sum = 0？
# ────────────────────────────────────────────────────────
# 如果所有元素都是负数，全局最大子数组和一定是其中一个负数。
# 如果 max_sum = 0，最终会错误地返回 0，但 0 不是一个有效子数组。
# 必须初始化为 float('-inf')，或者初始化为 nums[0]。
#
#
# 四、等价写法（DP 滚动变量版本）：
# ────────────────────────────────────────────────────────
# current_sum = max(num, current_sum + num)
# max_sum = max(max_sum, current_sum)
# 这个写法更直接对应 DP 转移方程，但理解和记忆不如"先加再判断重置"直观。
#
#
# 五、本题与股票系列问题的联系
# ────────────────────────────────────────────────────────
# 股票 I（121）求"最大单笔利润"本质上是：
# min_price 跟踪最低买入价，max_profit 跟踪最大差价。
# 这与 Kadane 算法有异曲同工之妙——都用了滚动变量的思想。
# 实际上，股票 I 可以转化为"求价格差数组的最大子数组和"。
#
# 证明：prices[i] - min_price = max(0, prices[i] - prices[i-1] + ...)
# 这正是 Kadane 算法在差分数组上的应用。
#
#
# 常见错误：
# ────────────────────────────────────────────────────────
# 1. 忘记用 max_sum 记录最大值：
#    只维护了 current_sum，最终返回 current_sum（累加和而非最大和）。
#    正确做法：每次更新 current_sum 后，立即更新 max_sum。
#
# 2. 全负数数组返回 0：
#    初始化 max_sum = 0 会导致全负数数组错误返回 0。
#    正确做法：初始化 max_sum = float('-inf') 或 nums[0]。
#
# 3. 重置 current_sum 与更新 max_sum 的顺序颠倒：
#    先重置再更新 max_sum，会导致负数被丢弃，永远无法记录。
#    正确做法：先更新 max_sum，再判断是否重置。
#
# 4. 混淆子数组和子序列：
#    子数组要求连续，不能像 LIS 那样跳着选。
#    所以 Kadane 算法只看相邻元素，不看所有前面的。
#
# 5. 误解 dp[i] 的含义：
#    dp[i] 是"以 nums[i] 结尾"的子数组最大和，不是"前 i 个元素"的最大和。
#    如果是后者，转移时无法判断能否接上 nums[i]。
#
# 6. 忘记处理空数组：
#    nums 为空时，按题目说明不会出现，但严谨起见应返回 0。
# ══════════════════════════════════════════════════════════
