"""
买卖股票的最佳时机含手续费（Best Time to Buy and Sell Stock with Transaction Fee）

题目描述：
给定一个整数数组 prices，其中 prices[i] 表示第 i 天的股票价格；
整数 fee 代表了交易股票的手续费用。
你可以无限次地完成交易，但是你每笔交易都需要付手续费。
如果你已经购买了一个股票，在卖出它之前你就不能再继续购买股票了。
返回获得利润的最大值。

注意：这里的一笔交易指买入持有并卖出股票的整个过程，
每笔交易你只需要支付一次手续费。

输入描述：
整数数组 prices 和整数 fee。

输出描述：
输出一个整数，表示最大利润。

输入示例：
prices = [1, 3, 2, 8, 4, 9], fee = 2
输出示例：
8

════════════════════════════════════════════════════════════════════════
核心联系：股票系列问题——从"无限次交易"到"无限次交易+手续费"
════════════════════════════════════════════════════════════════════════

维度              股票 II（k=∞）         本题（k=∞+fee）        区别
────────────────────────────────────────────────────────────────────────────
交易次数          无限次                 无限次                相同
手续费            无                    每笔交易收 fee        唯一区别
状态数            2（buy, sell）         2（buy, sell）        相同
贪心可行性        可以（累加正差价）      不可以（需考虑fee）   fee改变了决策
────────────────────────────────────────────────────────────────────────────

手续费带来的本质变化：
────────────────────────────────────────────────────────────────────────
股票 II 中，只要 prices[i] > prices[i-1] 就该交易（利润严格为正）。
但加了手续费后，只有 prices[i] - prices[i-1] > fee 时才值得交易，
否则手续费会吞噬利润。

因此本题不能简单贪心，必须用 DP 记录完整的持有/不持有状态。

手续费的扣费时机：
────────────────────────────────────────────────────────────────────────
题目说"每笔交易支付一次手续费"，一笔交易 = 买入 + 卖出。
手续费可以在买入时扣或卖出时扣，数学上等价：
    - 卖出时扣：sell = buy + price - fee   ← 本题采用（更直观）
    - 买入时扣：buy = sell - price - fee   ← 也可以，最终结果相同

为什么等价？因为一笔交易只扣一次 fee，无论扣在哪一端，
总利润 = 卖出价 - 买入价 - fee，结果完全一样。

类比理解：
    - 股票 II  像是"无门槛的低买高卖"，每一次上涨都赚
    - 本题     像是"有门槛的低买高卖"，涨幅超过 fee 才值得交易
    - 股票 III 像是"两次翻盘机会"，交易次数有限但无手续费
    - 本题     交易次数无限，但每次交易有成本

解题思路总览：
────────────────────────────────────────────────────────────────────────
解法                          时间复杂度       空间复杂度       说明
────────────────────────────────────────────────────────────────────────
贪心算法                       O(n)            O(1)            ⭐⭐⭐
动态规划（二维状态数组）        O(n)            O(n)            ⭐⭐⭐
动态规划（滚动变量优化）        O(n)            O(1)            ⭐⭐⭐⭐  推荐
────────────────────────────────────────────────────────────────────────

核心思想（动态规划）：
────────────────────────────────────────────────────────────────────────
关键洞察（与股票 II 的对比）：
1. 股票 II 只有两种状态：持有 / 不持有，贪心即可
2. 本题也是两种状态，但需要 DP（因为贪心无法处理 fee 的延迟效应）
3. 唯一的变化：卖出时要扣手续费 fee

状态定义（2 个变量）：
    buy[i]  = 第 i 天结束时，持有股票的最大利润
    sell[i] = 第 i 天结束时，不持有股票的最大利润

状态转移：
    buy[i]  = max(buy[i-1],  sell[i-1] - prices[i])          保持持有 / 今天买入
    sell[i] = max(sell[i-1], buy[i-1] + prices[i] - fee)      保持不持有 / 今天卖出（扣fee）

初始条件：
    buy[0]  = -prices[0]   第1天买入
    sell[0] = 0             第1天不持有（未交易）

最终答案：
    sell[n-1]（最后一天不持有股票的最大利润）
"""


# ══════════════════════════════════════════════════════════
# 解法一：贪心算法
# ══════════════════════════════════════════════════════════


def maxProfit_greedy(prices: list[int], fee: int) -> int:
    """
    贪心算法

    核心思路：
    ────────────────────────────────────────────────────────
    维护两个变量：
    - min_price：到目前为止的最低"买入成本"
    - profit：累计利润

    遍历每一天：
    1. 如果当天价格 < min_price，更新买入成本
    2. 如果当天价格 > min_price + fee，可以卖出获利：
       - 累加利润：price - min_price - fee
       - 但不能立刻确认，因为后续可能有更高的卖出价
       - 将 min_price 更新为 price - fee（相当于"撤回卖出，改在更高价卖"）
       - 这样如果下一天价格更高，利润会自动增加

    为什么 min_price = price - fee？
    ────────────────────────────────────────────────────────
    假设第 i 天"卖出"，利润为 prices[i] - min_price - fee。
    如果第 i+1 天价格更高，应该在第 i+1 天卖：
       利润 = prices[i+1] - min_price - fee
    这等价于把第 i 天的卖出"撤回"，改在第 i+1 天卖。

    为了处理这种情况，将 min_price 设为 prices[i] - fee：
    - 如果后续继续涨：利润 = prices[j] - (prices[i] - fee) - fee
                              = prices[j] - prices[i]
      但实际利润 = prices[j] - min_price_original - fee
      需要加上之前累加的 profit...

    更准确的理解：贪心方案实际是在"局部确认卖出"，
    但通过 min_price = price - fee 实现了"延迟确认"的效果。

    时间复杂度：O(n) — 遍历一次
    空间复杂度：O(1) — 只用两个变量

    图示理解：
    ────────────────────────────────────────────────────────
    prices = [1, 3, 2, 8, 4, 9], fee = 2

    i=0, price=1: min_price=1
    i=1, price=3: 3 > 1+2=3? 不，不卖（刚好等于fee，无利润）
    i=2, price=2: min_price=2（比1+fee=3低...实际不更新）
       等等，此处 min_price=1 仍保持
    i=3, price=8: 8 > 1+2=3? 是！profit += 8-1-2=5, min_price=8-2=6
    i=4, price=4: min_price=4（4 < 6）
    i=5, price=9: 9 > 4+2=6? 是！profit += 9-4-2=3, 总profit=8 ✓
    """
    if len(prices) < 2:
        return 0

    min_price = prices[0]
    profit = 0

    for price in prices[1:]:
        if price < min_price:
            # 发现更低的买入价
            min_price = price
        elif price > min_price + fee:
            # 卖出获利
            profit += price - min_price - fee
            # 延迟确认：如果后续有更高价，相当于改在更高价卖
            min_price = price - fee

    return profit


# ══════════════════════════════════════════════════════════
# 解法二：动态规划（二维状态数组）
# ══════════════════════════════════════════════════════════


def maxProfit_dp(prices: list[int], fee: int) -> int:
    """
    动态规划解法（二维状态数组）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    用两个一维数组记录每天的持有/不持有状态的最大利润。
    与股票 II 的 DP 几乎完全相同，唯一区别是卖出时扣 fee。

    算法步骤：
    1. 创建 buy[n] 和 sell[n] 两个数组
    2. 初始化 buy[0] = -prices[0], sell[0] = 0
    3. 逐天更新两个状态
    4. 最终返回 sell[n-1]

    时间复杂度：O(n) — 遍历一次价格数组
    空间复杂度：O(n) — 两个一维数组

    图解示例：
    ────────────────────────────────────────────────────────
    prices = [1, 3, 2, 8, 4, 9], fee = 2

    初始化：buy[0]=-1, sell[0]=0

    i=1, price=3:
        buy  = max(-1, 0-3)  = -1     保持持有
        sell = max(0, -1+3-2) = 0     卖出利润仅0，不如不卖

    i=2, price=2:
        buy  = max(-1, 0-2)  = -1     保持持有
        sell = max(0, -1+2-2) = 0     亏损，不卖

    i=3, price=8:
        buy  = max(-1, 0-8)  = -1     保持持有
        sell = max(0, -1+8-2) = 5     危出！利润5

    i=4, price=4:
        buy  = max(-1, 5-4)  = 1      用利润5再买入，成本4
        sell = max(5, -1+4-2) = 5     保持不持有

    i=5, price=9:
        buy  = max(1, 5-9)   = 1      保持持有
        sell = max(5, 1+9-2)  = 8     危出！利润8 ✓

    结果：sell[5] = 8
    对应：第1笔（第1天买1，第4天卖8，利润8-1-2=5）+ 第2笔（第5天买4，第6天卖9，利润9-4-2=3）= 8
    """
    n = len(prices)
    if n < 2:
        return 0

    buy = [0] * n
    sell = [0] * n

    buy[0] = -prices[0]
    sell[0] = 0

    for i in range(1, n):
        # 持有：保持之前的持有，或今天买入（用昨天不持有的利润）
        buy[i] = max(buy[i - 1], sell[i - 1] - prices[i])
        # 不持有：保持之前的不持有，或今天卖出（扣手续费）
        sell[i] = max(sell[i - 1], buy[i - 1] + prices[i] - fee)

    return sell[-1]


# ══════════════════════════════════════════════════════════
# 解法三：动态规划（滚动变量优化）
# ══════════════════════════════════════════════════════════


def maxProfit(prices: list[int], fee: int) -> int:
    """
    动态规划解法（滚动变量优化）⭐⭐⭐⭐ 推荐

    核心思想：
    ────────────────────────────────────────────────────────
    buy[i] 和 sell[i] 只依赖前一天的值，
    因此用两个变量代替两个数组，空间从 O(n) 优化到 O(1)。

    更新顺序：
    ────────────────────────────────────────────────────────
    buy 依赖 sell 的旧值，sell 依赖 buy 的旧值。
    如果先更新 buy，sell 会用到新 buy（不正确）；
    如果先更新 sell，buy 会用到新 sell（不正确）。
    因此必须用临时变量保存旧值。

    时间复杂度：O(n) — 遍历一次价格数组
    空间复杂度：O(1) — 只用两个变量 + 两个临时变量

    状态定义：
        buy  = 当前持有股票的最大利润
        sell = 当前不持有股票的最大利润

    初始条件：
        buy  = -prices[0]   第1天买入
        sell = 0             第1天不持有

    最终答案：
        sell（最后不持有股票的最大利润）
    """
    if len(prices) < 2:
        return 0

    buy = -prices[0]
    sell = 0

    for price in prices[1:]:
        # 用临时变量保存旧值，避免更新顺序问题
        new_buy = max(buy, sell - price)
        new_sell = max(sell, buy + price - fee)
        buy = new_buy
        sell = new_sell

    return sell


# ══════════════════════════════════════════════════════════
# 重要说明：算法的正确性证明和易错点提示
# ══════════════════════════════════════════════════════════
#
# 算法正确性证明：
# ────────────────────────────────────────────────────────
#
# 定理：上述 DP 算法返回的 sell 等于无限次交易（含手续费 fee）的最大利润。
#
# 证明：
# （1）状态完备性：
#     任何一天结束时，投资者只有两种状态：
#     - 持有股票 → buy
#     - 不持有股票 → sell
#     2 种状态覆盖了所有可能（本题交易次数无限，不需要区分第几笔）。
#
# （2）状态转移的正确性：
#     - buy: 保持持有（不操作）或 从不持有买入（sell - price）
#     - sell: 保持不持有（不操作）或 从持有卖出（buy + price - fee）
#     每种选择取最大值，保证了局部最优。
#
# （3）全局最优性（数学归纳法）：
#     基础：第1天 buy=-prices[0], sell=0 正确。
#     归纳：第 i 天的状态转移覆盖所有选择，取最大值，且无后效性，
#           因此第 i 天的状态正确。
#     综上，最后一天的 sell 是全局最优解。
#
# （4）最终答案是 sell 而非 max(buy, sell)：
#     sell ≥ buy（因为 sell 可以选择"今天买入再卖出"，利润为 price-fee ≥ buy），
#     但更本质的原因是：最后一天持有股票意味着没卖完，
#     不如之前就卖出（少亏一笔），所以 sell 一定是最优。
#     严谨地说：sell[n-1] ≥ buy[n-1] + prices[n-1] - fee ≥ buy[n-1]，
#     所以 sell[n-1] ≥ buy[n-1]，返回 sell 即可。
#
#
# 手续费扣在买入端 vs 卖出端：
# ────────────────────────────────────────────────────────
# 危出端扣费（本题采用）：
#   buy  = max(buy, sell - price)
#   sell = max(sell, buy + price - fee)
#
# 买入端扣费（等价写法）：
#   buy  = max(buy, sell - price - fee)
#   sell = max(sell, buy + price)
#
# 两种写法的结果完全相同，因为一笔交易的总利润都是：
#   卖出价 - 买入价 - fee
# 证明：设一笔交易在第 a 天买入、第 b 天卖出。
#   危出端扣费：sell 的贡献 = buy[a] + prices[b] - fee
#              buy[a] = sell[a-1] - prices[a]
#              总利润 = prices[b] - prices[a] - fee
#   买入端扣费：buy 的贡献 = sell[a-1] - prices[a] - fee
#              sell[b] = buy[a] + prices[b]
#              总利润 = prices[b] - prices[a] - fee
#   两者相同。
#
# 注意：不能两端都扣 fee！那样每笔交易会扣 2*fee。
#
#
# 为什么本题不能用股票 II 的简单贪心？
# ────────────────────────────────────────────────────────
# 股票 II（无手续费）：只要 prices[i] > prices[i-1] 就交易，因为任何正差价都增加利润。
# 本题（有手续费）：prices[i] - prices[i-1] > 0 不一定值得交易，
# 只有 prices[i] - prices[i-1] > fee 时才值得。
#
# 更深层的原因：手续费导致了"跨日效应"——
# 假设 prices = [1, 5, 3, 8], fee = 2：
#   方案A：第1天买1，第4天卖8 → 利润 = 8-1-2 = 5
#   方案B：第1天买1卖5，第3天买3卖8 → 利润 = (5-1-2)+(8-3-2) = 2+3 = 5
#   方案C：第1天买1卖5 → 利润 = 5-1-2 = 2（后续3→8差价5>fee=2，应该再交易）
#
# 简单贪心无法判断"是一次长交易"还是"多次短交易"更优，需要 DP。
#
#
# 常见错误：
# ────────────────────────────────────────────────────────
# 1. 滚动变量更新顺序错误：
#    buy 和 sell 互相依赖对方的旧值，不能直接顺序更新。
#    必须用临时变量（new_buy, new_sell）保存旧值。
#    错误写法：
#        buy = max(buy, sell - price)      ← sell 被旧值 ✓
#        sell = max(sell, buy + price - fee) ← buy 已被更新！❌
#
# 2. 手续费扣错位置：
#    - 只在卖出时扣一次 fee，或只在买入时扣一次 fee
#    - 不能两端都扣，也不能不扣
#    - sell = max(sell, buy + price - fee) ← 正确（卖出扣）
#    - sell = max(sell, buy + price)       ← 忘记扣 fee！❌
#    - sell = max(sell, buy + price - 2*fee) ← 扣了两次！❌
#
# 3. 最终答案写成 max(buy, sell)：
#    虽然结果碰巧正确（因为 sell ≥ buy），但语义上应该是 sell。
#    最后一天持有股票不如卖出，所以答案一定是 sell。
#    写 max(buy, sell) 不算错，但说明没理解"最后一定不持有"。
#
# 4. 初始值设置错误：
#    buy[0] = -prices[0]，不能设为 0（相当于免费买入）。
#    sell[0] = 0，不能设为负无穷（可以不交易，利润为0）。
#
# 5. 忘记处理边界情况：
#    prices 长度为 0 或 1 时，无法完成交易，应返回 0。
#
# 6. 贪心解法中忘记 min_price = price - fee：
#    贪心卖出后必须更新 min_price = price - fee，
#    否则后续如果有更高的卖出价，会重复扣 fee。
#    这个"回退"操作是贪心解法最难理解的部分。
# ══════════════════════════════════════════════════════════
