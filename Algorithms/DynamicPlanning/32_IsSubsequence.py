"""
判断子序列（Is Subsequence）

题目描述：
给定字符串 s 和 t，判断 s 是否为 t 的子序列。

字符串的一个子序列是原始字符串删除一些（也可以不删除）字符而不改变剩余字符相对位置形成的新字符串。
（例如，"ace"是"abcde"的一个子序列，而"aec"不是）。

进阶：
如果有大量输入的 S，称作 S1, S2, ... , Sk 其中 k >= 10亿，
你需要依次检查它们是否为 T 的子序列。在这种情况下，你会怎样改变代码？

输入描述：
两个字符串 s 和 t。

输出描述：
输出一个布尔值，表示 s 是否是 t 的子序列。

输入示例：
s = "abc", t = "ahbgdc"
输出示例：
true

输入示例：
s = "axc", t = "ahbgdc"
输出示例：
false

════════════════════════════════════════════════════════════════════════
核心概念：子序列（Subsequence）vs 子串（Substring）
════════════════════════════════════════════════════════════════════════

维度              子串（Substring）          子序列（Subsequence）
────────────────────────────────────────────────────────────────────────
元素位置          必须连续                    可以不连续
示例              t="ahbgdc"                 t="ahbgdc"
                  "ahb" ✓                    "abc" ✓（删除 h,g,d）
                  "abc" ✗（不连续）            "axc" ✗（x 不存在）
判断方式          字符串查找（KMP）           双指针 / DP
时间复杂度        O(n+m)                      O(n+m)
────────────────────────────────────────────────────────────────────────

为什么"子序列"比"子串"更简单？（从判断角度）
────────────────────────────────────────────────────────────────────────
子串要求完全匹配一段连续区域，需要 KMP 等算法。
子序列只要求相对顺序一致，可以跳过不匹配字符，用双指针即可 O(n) 解决。

关键洞察：
    匹配子序列时，不需要回溯，因为目标只是"能否在 t 中找到完整的 s"。
    双指针天然满足贪心性质：尽早匹配 s 中的字符，为后续留出更多空间。

解题思路总览：
────────────────────────────────────────────────────────────────────────
解法                          时间复杂度       空间复杂度       说明
────────────────────────────────────────────────────────────────────────
双指针（贪心匹配）              O(n)            O(1)            ⭐⭐⭐⭐  推荐
动态规划（2D DP）               O(m×n)          O(m×n)          ⭐⭐⭐
动态规划（滚动数组优化）         O(m×n)          O(min(m,n))     ⭐⭐⭐
预处理 + 二分查找（进阶）        O(n + k×logm)   O(n)            ⭐⭐⭐⭐  推荐（进阶）
────────────────────────────────────────────────────────────────────────

核心思想（双指针 / 贪心匹配）：
────────────────────────────────────────────────────────────────────────
不涉及 DP 状态定义，是一种贪心策略：
    遍历字符串 t，同时用指针 i 指向 s 的当前待匹配字符。
    当 t[j] == s[i] 时，i 向前移动一步，表示匹配成功。
    最终如果 i == len(s)，说明 s 中所有字符都在 t 中被按顺序匹配。

为什么贪心是正确的？
    由于 s 中的字符必须按顺序出现，尽早匹配当前字符一定不会使结果变差。
    因为早匹配 = 留下更长的 t 后缀供后续字符匹配，是最优的选择。

进阶问题（k >= 10亿）：
    当需要检查大量字符串是否为 T 的子序列时，不能每次都 O(n) 遍历 T。
    优化的思路是：先对 T 预处理，记录每个字符出现的位置（有序列表），
    然后用二分查找快速判断 s 中每个字符是否能在 t 中找到符合顺序的位置。
"""


# ══════════════════════════════════════════════════════════
# 解法一：双指针（贪心匹配）
# ══════════════════════════════════════════════════════════


def isSubsequence(s: str, t: str) -> bool:
    """
    双指针（贪心匹配）⭐⭐⭐⭐ 推荐

    核心思想：
    ────────────────────────────────────────────────────────
    同时遍历 s 和 t，在 t 中按顺序寻找 s 的每个字符。
    一旦匹配成功，s 的指针前移；无论是否匹配，t 的指针始终前移。
    利用了"子序列无需连续"的特性，贪心地尽早匹配。

    算法步骤：
    1. 用指针 i 指向 s 的当前字符，指针 j 指向 t 的当前字符
    2. 遍历 t，当 s[i] == t[j] 时，i 前移一位
    3. 结束后检查 i 是否等于 len(s)

    时间复杂度：O(n) — n = len(t)，只需遍历 t 一次
    空间复杂度：O(1) — 只使用两个指针变量

    图解示例：
    ────────────────────────────────────────────────────────
    s = "abc", t = "ahbgdc"

    初始：i=0 (指向 'a'), j=0 (指向 'a')
    ┌─────────────────────────────────────────────┐
    │ j=0: t[0]='a' == s[0]='a'  → 匹配, i→1     │
    │ j=1: t[1]='h' ≠ s[1]='b'  → 跳过           │
    │ j=2: t[2]='b' == s[1]='b'  → 匹配, i→2     │
    │ j=3: t[3]='g' ≠ s[2]='c'  → 跳过           │
    │ j=4: t[4]='d' ≠ s[2]='c'  → 跳过           │
    │ j=5: t[5]='c' == s[2]='c'  → 匹配, i→3     │
    └─────────────────────────────────────────────┘
    结果：i=3 == len(s)=3  →  True ✓

    s = "axc", t = "ahbgdc"

    初始：i=0 (指向 'a'), j=0 (指向 'a')
    ┌─────────────────────────────────────────────┐
    │ j=0: t[0]='a' == s[0]='a'  → 匹配, i→1     │
    │ j=1: t[1]='h' ≠ s[1]='x'  → 跳过           │
    │ j=2: t[2]='b' ≠ s[1]='x'  → 跳过           │
    │ j=3: t[3]='g' ≠ s[1]='x'  → 跳过           │
    │ j=4: t[4]='d' ≠ s[1]='x'  → 跳过           │
    │ j=5: t[5]='c' ≠ s[1]='x'  → 跳过           │
    │ 遍历结束, i=1 ≠ len(s)=3                    │
    └─────────────────────────────────────────────┘
    结果：False ✓
    """
    # 特判：空字符串是任何字符串的子序列
    if not s:
        return True

    i = 0  # s 的指针
    for ch in t:
        # 当前字符匹配成功，s 指针前移
        if ch == s[i]:
            i += 1
            # 提前结束：s 的所有字符都已匹配完
            if i == len(s):
                return True

    return i == len(s)


# ══════════════════════════════════════════════════════════
# 解法二：动态规划（2D DP，最长公共子序列思想）
# ══════════════════════════════════════════════════════════


def isSubsequence_dp(s: str, t: str) -> bool:
    """
    动态规划（2D DP）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    将问题转化为"求 s 和 t 的最长公共子序列（LCS）长度"。
    本质上是在 |s|×|t| 的网格上走一条路径：
    - 字符相等时 → 对角线方向（匹配一个字符）
    - 字符不等时 → 向右走（跳过 t 的当前字符）

    dp[i][j] 表示 s[:i] 与 t[:j] 的最长公共子序列长度。
    最终判断 dp[m][n] 是否等于 m。

    状态定义：
        dp[i][j] = s 的前 i 个字符与 t 的前 j 个字符的 LCS 长度

    状态转移：
        dp[i][j] = dp[i-1][j-1] + 1     如果 s[i-1] == t[j-1]
        dp[i][j] = dp[i][j-1]           如果 s[i-1] != t[j-1]
        （注意：这里只向右走，不向上走，因为我们是单向匹配 s 到 t）

    为什么不等时只取 dp[i][j-1] 而不是 max(dp[i-1][j], dp[i][j-1])？
        本题是"s 是否是 t 的子序列"，s 的字符必须全部在 t 中按序出现。
        当 s[i-1] != t[j-1] 时，只能跳过 t[j-1]，不能跳过 s[i-1]。
        （完整的 LCS 需要两边都可以跳过，但这里是单向子序列判定）
        因此转移只有 dp[i][j-1]——跳过 t 的当前字符。

    初始条件：
        dp[0][*] = 0（空串与任何字符串的 LCS 为 0）
        dp[*][0] = 0（任何字符串与空串的 LCS 为 0）

    最终答案：
        dp[m][n] == m（如果 LCS 长度等于 s 的长度，说明 s 被完整匹配）

    时间复杂度：O(m×n) — m = len(s), n = len(t)
    空间复杂度：O(m×n) — 存储整个 dp 表

    图解示例：
    ────────────────────────────────────────────────────────
    s = "abc", t = "ahbgdc"

    初始化 dp 表（行 = s 索引 0..3, 列 = t 索引 0..7）
         ''  a   h   b   g   d   c
    ''  [ 0,  0,  0,  0,  0,  0,  0]
    a   [ 0,  1,  1,  1,  1,  1,  1]
    b   [ 0,  0,  0,  2,  2,  2,  2]
    c   [ 0,  0,  0,  0,  0,  0,  3]
                ↑匹配a     ↑匹配b     ↑匹配c

    最终 dp[3][6] = 3 == len(s)  →  True ✓
    """
    m, n = len(s), len(t)

    # 特判：空字符串是任何字符串的子序列
    if not s:
        return True

    # 创建 (m+1) × (n+1) 的 dp 表，初始化为 0
    dp = [[0] * (n + 1) for _ in range(m + 1)]

    # 填充 dp 表
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            if s[i - 1] == t[j - 1]:
                # 字符匹配：LCS 长度在之前的基础上 +1
                dp[i][j] = dp[i - 1][j - 1] + 1
            else:
                # 字符不匹配：跳过 t 的当前字符，保持 s 的匹配进度
                dp[i][j] = dp[i][j - 1]

    # 如果 LCS 长度等于 s 的长度，说明 s 是 t 的子序列
    return dp[m][n] == m


# ══════════════════════════════════════════════════════════
# 解法三：动态规划（滚动数组优化空间）
# ══════════════════════════════════════════════════════════


def isSubsequence_dp_scroll(s: str, t: str) -> bool:
    """
    动态规划（滚动数组优化）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    观察 2D DP 的转移方程：
        dp[i][j] = dp[i-1][j-1] + 1  （匹配时）
        dp[i][j] = dp[i][j-1]        （不匹配时）

    可以发现，计算 dp[i][*] 时：
    - 从 dp[i-1][*] 需要用到 dp[i-1][j-1]（左上角的值）
    - 从 dp[i][*] 需要用到 dp[i][j-1]（左边的值）

    因此只需要保留两行（当前行和上一行），
    且由于不匹配时直接取左边值，可以使用一维数组 + 临时变量。

    让 n = len(t) 作为列的维度（通常 t 更长），
    只用长度 n+1 的一维数组，空间从 O(m×n) 降为 O(n)。

    算法步骤：
    1. 创建长度为 n+1 的 dp 数组，初始化全 0
    2. 外层循环遍历 s 的每个字符（行 i）
    3. 内层循环遍历 t 的每个字符（列 j）
    4. 用变量 prev 保存左上角的值 dp[j-1]（相当于 dp[i-1][j-1]）
    5. 匹配时 dp[j] = prev + 1；不匹配时 dp[j] 保持不变（就是 dp[i][j-1]）

    时间复杂度：O(m×n) — 与 2D DP 相同
    空间复杂度：O(n) — 只使用一维数组，n = len(t)

    图解示例：
    ────────────────────────────────────────────────────────
    s = "abc", t = "ahbgdc"

    初始 dp = [0, 0, 0, 0, 0, 0, 0]

    i=1 (s[0]='a'):
    j=1: t[0]='a' == 'a'  →  dp[1] = prev+1 = 0+1 = 1
    j=2: t[1]='h' ≠ 'a'  →  dp[2] = dp[1] = 1
    j=3: t[2]='b' ≠ 'a'  →  dp[3] = dp[2] = 1
    j=4: t[3]='g' ≠ 'a'  →  dp[4] = dp[3] = 1
    j=5: t[4]='d' ≠ 'a'  →  dp[5] = dp[4] = 1
    j=6: t[5]='c' ≠ 'a'  →  dp[6] = dp[5] = 1
    dp = [0, 1, 1, 1, 1, 1, 1]

    i=2 (s[1]='b'):
    j=1: t[0]='a' ≠ 'b'  →  dp[1] = dp[0] = 0
    j=2: t[1]='h' ≠ 'b'  →  dp[2] = dp[1] = 0
    j=3: t[2]='b' == 'b'  →  dp[3] = prev+1 = 1+1 = 2
    j=4: t[3]='g' ≠ 'b'  →  dp[4] = dp[3] = 2
    j=5: t[4]='d' ≠ 'b'  →  dp[5] = dp[4] = 2
    j=6: t[5]='c' ≠ 'b'  →  dp[6] = dp[5] = 2
    dp = [0, 0, 0, 2, 2, 2, 2]

    i=3 (s[2]='c'):
    j=1: t[0]='a' ≠ 'c'  →  dp[1] = dp[0] = 0
    j=2: t[1]='h' ≠ 'c'  →  dp[2] = dp[1] = 0
    j=3: t[2]='b' ≠ 'c'  →  dp[3] = dp[2] = 0
    j=4: t[3]='g' ≠ 'c'  →  dp[4] = dp[3] = 0
    j=5: t[4]='d' ≠ 'c'  →  dp[5] = dp[4] = 0
    j=6: t[5]='c' == 'c'  →  dp[6] = prev+1 = 2+1 = 3
    dp = [0, 0, 0, 0, 0, 0, 3]

    结果：dp[6] = 3 == len(s)  →  True ✓
    """
    m, n = len(s), len(t)

    # 特判：空字符串是任何字符串的子序列
    if not s:
        return True

    # 一维滚动数组，长度 = len(t) + 1
    dp = [0] * (n + 1)

    # 外层遍历 s 的每个字符
    for i in range(1, m + 1):
        prev = 0  # 保存左上角的值 dp[j-1]（即 dp[i-1][j-1]）
        for j in range(1, n + 1):
            temp = dp[j]  # 先保存当前位置的旧值，作为下一轮的左上角
            if s[i - 1] == t[j - 1]:
                dp[j] = prev + 1  # 匹配：左上角 + 1
            else:
                dp[j] = dp[j - 1]  # 不匹配：取左边的值（dp[i][j-1]）
            prev = temp  # 更新左上角值为下一轮准备

    return dp[n] == m


# ══════════════════════════════════════════════════════════
# 解法四：预处理 + 二分查找（进阶问题）
# ══════════════════════════════════════════════════════════


def isSubsequence_followup(s: str, t: str) -> bool:
    """
    预处理 + 二分查找 ⭐⭐⭐⭐ 推荐（进阶）

    核心思想：
    ────────────────────────────────────────────────────────
    进阶问题是：如果有 k >= 10亿 个字符串 S1, S2, ..., Sk 需要检查，
    它们是否都是 T 的子序列？此时每次 O(n) 遍历 T 会超时。

    优化策略：空间换时间
    1. 预处理 T，记录每个字符在 T 中出现的所有位置（有序列表）
       例如 t = "ahbgdc" → {'a':[0], 'h':[1], 'b':[2], 'g':[3], 'd':[4], 'c':[5]}
    2. 对于每个待检查的 s，使用二分查找快速定位每个字符的位置
       维护一个变量 pos 表示当前在 T 中的搜索位置
       对于 s[i]，在 hashmap[s[i]] 中二分查找第一个 >= pos 的位置
       如果找到，更新 pos = 找到的位置 + 1
       如果没找到，返回 False

    算法步骤：
    1. 建立字符到位置列表的映射：遍历 T，将每个字符的下标加入对应列表
    2. 对于每个 s：
       a. 初始化搜索起始位置 pos = 0
       b. 遍历 s 的每个字符 ch：
          - 如果 ch 不在映射中，返回 False
          - 在 pos_list[ch] 中二分查找第一个 >= pos 的位置
          - 如果找到，更新 pos = 该位置 + 1
          - 如果没找到，返回 False
       c. 全部匹配成功，返回 True

    时间复杂度：
        - 预处理：O(n) — 遍历 T 一次
        - 每个 s 的检查：O(|s| × log n) — 二分查找每个字符
        - 总时间（k 个 s）：O(n + k × log n)
    空间复杂度：O(n) — 存储所有字符的位置索引

    图解示例：
    ────────────────────────────────────────────────────────
    t = "ahbgdc" → 预处理映射：
    'a': [0]      'h': [1]      'b': [2]
    'g': [3]      'd': [4]      'c': [5]

    s = "abc"：
    ┌─────────────────────────────────────────────┐
    │ pos=0, ch='a': 在 [0] 中找 >=0 → 0          │
    │   更新 pos=0+1=1                             │
    │ pos=1, ch='b': 在 [2] 中找 >=1 → 2          │
    │   更新 pos=2+1=3                             │
    │ pos=3, ch='c': 在 [5] 中找 >=3 → 5          │
    │   更新 pos=5+1=6                             │
    │ 全部匹配 → True ✓                            │
    └─────────────────────────────────────────────┘

    s = "axc"：
    ┌─────────────────────────────────────────────┐
    │ pos=0, ch='a': 在 [0] 中找 >=0 → 0          │
    │   更新 pos=0+1=1                             │
    │ pos=1, ch='x': 'x' 不在映射中 → False ✗     │
    └─────────────────────────────────────────────┘
    """
    from bisect import bisect_left
    from collections import defaultdict

    # 预处理：记录 T 中每个字符出现的位置（有序）
    pos_map = defaultdict(list)
    for idx, ch in enumerate(t):
        pos_map[ch].append(idx)

    # 二分查找第一个 >= pos 的位置
    pos = 0  # 当前在 T 中的搜索起始位置
    for ch in s:
        lst = pos_map.get(ch)
        if lst is None:
            return False  # 字符不在 T 中

        # 在 lst 中二分查找第一个 >= pos 的位置
        index_in_lst = bisect_left(lst, pos)
        if index_in_lst == len(lst):
            return False  # 找不到符合顺序的位置

        # 更新 pos 为匹配位置的下一个位置
        pos = lst[index_in_lst] + 1

    return True


# ══════════════════════════════════════════════════════════
# 重要说明：算法的正确性证明和易错点提示
# ══════════════════════════════════════════════════════════
#
# 算法正确性证明：
# ────────────────────────────────────────────────────────
#
# 定理：双指针贪心算法能正确判断 s 是否为 t 的子序列。
#
# 证明（贪心选择性质和最优子结构）：
#
# （1）贪心选择性质
#     假设我们要在 t 中匹配 s[0]，有多个位置 t[i] = t[j] = s[0]（i < j）。
#     选择最小的 i（最早出现的位置）不会影响后续匹配：
#     因为 t[i+1:] 一定包含 t[j+1:] 的所有字符，选择更靠前的位置
#     留下了更长的后缀供后续字符匹配，所以贪心选择最早出现的位置是最优的。
#
# （2）最优子结构
#     如果在 t[pos:] 中匹配了 s[i] 在位置 k，那么子问题就变为
#     在 t[k+1:] 中匹配 s[i+1:]。这是一个规模更小的相同问题，
#     可以用相同策略递归解决。双指针就是迭代版本的实现。
#
# （3）归纳法
#     基础情况：s 为空串，显然是任何字符串的子序列。
#     归纳步骤：假设贪心匹配了前 i-1 个字符后，剩余问题等价于
#     在剩余 t 后缀中匹配 s[i:]。按贪心选择性质，选择最早可匹配位置最优。
#     由数学归纳法，算法对所有长度的 s 正确。
#
#
# DP 的等价性：LCS vs 子序列判定
# ────────────────────────────────────────────────────────
#
# 完整的 LCS 转移方程是：
#     dp[i][j] = max(dp[i-1][j], dp[i][j-1])  如果 s[i-1] != t[j-1]
#
# 但在判断"子序列"时，我们只关心 s 是否能完整出现在 t 中，
# 因此不需要跳过 s 中的字符（跳过了意味着 s 中的字符被忽略）。
# 所以不匹配时的转移简化为 dp[i][j] = dp[i][j-1]（只跳过 t）。
#
# 完整 LCS 与本题 DP 的关系：
# - LCS：两个字符串都可以跳过字符，求最长公共部分
# - 本题：s 必须完整匹配，只能跳过 t 的字符
# - 所以本题的 DP 是 LCS 的一个特例——"强制 s 全匹配"的 LCS
#
#
# 常见错误：
# ────────────────────────────────────────────────────────
# 1. DP 不匹配时写成 dp[i][j] = dp[i-1][j-2]
#    错误原因：j-2 在 j=1 时会取到负索引，Python 不会报错但会静默取最后一列，
#    导致结果完全错误。正确做法是 dp[i][j] = dp[i][j-1]。
#
# 2. DP 匹配时忘记 +1
#    错误原因：dp[i-1][j-1] 是匹配之前的 LCS 长度，匹配了一个新字符后
#    长度应该增加 1。如果写成 dp[i][j] = dp[i-1][j-1]，dp 表全为 0。
#
# 3. 直接用 find/index 判断
#    错误想法：遍历 s 的每个字符，用 t.find(ch, pos) 查找位置。
#    这种做法忽略了要按顺序匹配的要求。如果 s = "aba", t = "ba"，
#    先用 find 找 'a'，然后再找 'b' 会在 'a' 之前，但实际上 'a' 在 'b' 之后出现。
#    正确做法是每次更新搜索起始位置。
#
# 4. 进阶问题中直接套用双指针
#    错误原因：每个 s 都 O(n) 遍历 t，当 k >= 10亿 时不可行。
#    正确做法是先预处理 t 的字符位置索引，然后对每个 s 用二分查找 O(|s| log n) 判断。
#    ══════════════════════════════════════════════════════
#    进阶问题的本质：预处理 + 二分查找将单个判断从 O(n) 降为 O(|s| log n)，
#    当 k 极大时（10亿级），总复杂度从 O(kn) 降为 O(n + k log n)，
#    这是质变的提升。
#    ══════════════════════════════════════════════════════
#
# 5. 忘记处理 s 为空串的情况
#    空字符串是任何字符串的子序列，应该直接返回 True。
