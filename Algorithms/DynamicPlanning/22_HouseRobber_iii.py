"""
小偷又发现了一个新的可行窃的地区。这个地区只有一个入口，我们称之为 root 。

除了 root 之外，每栋房子有且只有一个"父"房子与之相连。一番侦察之后，聪明的小偷意识到"这个地方的所有房屋的排列类似于一棵二叉树"。 如果 两个直接相连的房子在同一天晚上被打劫 ，房屋将自动报警。

给定二叉树的 root 。返回 在不触动警报的情况下 ，小偷能够盗取的最高金额 。

 

示例 1:

输入: root = [3,2,3,null,3,null,1]
输出: 7 
解释: 小偷一晚能够盗取的最高金额 3 + 3 + 1 = 7

示例 2:

输入: root = [3,4,5,1,3,null,1]
输出: 9
解释: 小偷一晚能够盗取的最高金额 4 + 5 = 9

════════════════════════════════════════════════════════════════════════
核心联系：本题与打家劫舍系列的关系
════════════════════════════════════════════════════════════════════════

维度              打家劫舍 I（数组）              打家劫舍 III（本题，树）
────────────────────────────────────────────────────────────────────────
数据结构          线性数组                        二叉树
相邻约束          不能偷相邻下标的房屋            不能偷父子相连的房屋
DP 遍历方式       从左到右顺序遍历                后序遍历（左右根）
状态维度          dp[i] 表示前 i 个房屋的最优值    每个节点返回 [不偷, 偷] 两个状态
递推公式          dp[i] = max(dp[i-1], dp[i-2]+nums[i])  不偷: max(左)+max(右)
                                                         偷: 当前值+左[不偷]+右[不偷]
初始条件          dp[0]=nums[0], dp[1]=max(nums[0],nums[1])  空节点返回 [0, 0]
最终答案          max(dp[n-1], dp[n-2])           max(root的[不偷], root的[偷])
────────────────────────────────────────────────────────────────────────

为什么本题需要两个状态？
────────────────────────────────────────────────────────────────────────
树形结构中，一个节点的决策会影响其父节点和子节点：
- 如果偷了当前节点，则其子节点一定不能偷
- 如果不偷当前节点，则其子节点可偷可不偷
因此每个节点必须同时返回"偷"和"不偷"两种状态的最优值，
供父节点做决策时使用。这就是树形 DP 的核心思想。

解题思路总览：
────────────────────────────────────────────────────────────────────────
解法                          时间复杂度       空间复杂度       说明
────────────────────────────────────────────────────────────────────────
暴力递归                       O(2^n)          O(n)            超时，仅用于理解
记忆化搜索（Top-Down DP）       O(n)            O(n)            ⭐⭐
树形 DP（后序遍历）             O(n)            O(n)            ⭐⭐⭐⭐  推荐
────────────────────────────────────────────────────────────────────────
（n 为树中节点个数）

核心思想（树形 DP）：
────────────────────────────────────────────────────────────────────────
关键洞察：
1. 当前节点偷不偷，取决于子节点的状态
2. 如果偷当前节点，则左右子节点都不能偷
3. 如果不偷当前节点，则左右子节点各自取最优（偷或不偷取较大值）
4. 必须用后序遍历：先处理子树，才能决定当前节点

状态定义（每个节点返回长度为 2 的数组）：
    result[0] = 不偷当前节点时，以当前节点为根的子树能偷到的最高金额
    result[1] = 偷当前节点时，以当前节点为根的子树能偷到的最高金额

状态转移：
    不偷当前节点：result[0] = max(left[0], left[1]) + max(right[0], right[1])
                  左子树取最优 + 右子树取最优（子节点偷不偷都可以）
    偷当前节点：  result[1] = node.val + left[0] + right[0]
                  当前值 + 左子节点不偷 + 右子节点不偷

初始条件：
    空节点返回 [0, 0]（没有节点，偷或不偷都是 0）

最终答案：
    max(root_result[0], root_result[1])
"""

from typing import Optional


class TreeNode:
    def __init__(self, val: int = 0,
                 left: Optional['TreeNode'] = None,
                 right: Optional['TreeNode'] = None):
        self.val = val
        self.left = left
        self.right = right


# ══════════════════════════════════════════════════════════
# 解法一：暴力递归（Brute Force）
# ══════════════════════════════════════════════════════════


def rob_brute(root: Optional[TreeNode]) -> int:
    """
    暴力递归解法（Brute Force）

    核心思路：
    ────────────────────────────────────────────────────────
    对于每个节点，分两种情况递归：
    1. 偷当前节点：则不能偷子节点，只能偷孙子节点
    2. 不偷当前节点：则可以偷子节点

    时间复杂度：O(2^n) — 每个节点有两个分支，指数级增长
    空间复杂度：O(n)   — 递归调用栈深度（树高）

    问题分析：
    ────────────────────────────────────────────────────────
    递归树高度为 n，每层两个分支，导致大量重复计算。
    例如偷当前节点时，需要递归到孙子节点（4个），
    而不偷当前节点时，又要递归到子节点（2个），
    子节点的递归又会到达孙子节点，产生大量重叠子问题。
    """
    if root is None:                          # 空节点，无法偷窃
        return 0

    # 情况一：偷当前节点
    val_rob = root.val                        # 偷当前节点的金额
    if root.left:                             # 左子节点不能偷，只能偷左孙子
        val_rob += rob_brute(root.left.left) + rob_brute(root.left.right)
    if root.right:                            # 右子节点不能偷，只能偷右孙子
        val_rob += rob_brute(root.right.left) + rob_brute(root.right.right)

    # 情况二：不偷当前节点
    val_not_rob = rob_brute(root.left) + rob_brute(root.right)  # 偷左右子节点

    return max(val_rob, val_not_rob)          # 取两种情况的较大值


# ══════════════════════════════════════════════════════════
# 解法二：记忆化搜索（Top-Down DP）
# ══════════════════════════════════════════════════════════


def rob_memo(root: Optional[TreeNode]) -> int:
    """
    记忆化搜索解法（Top-Down DP）⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    在暴力递归的基础上，添加缓存避免重复计算。
    每个节点只计算一次，后续直接从缓存中读取。

    算法步骤：
    1. 创建字典 memo，缓存每个节点的计算结果
    2. 递归逻辑与暴力解法相同
    3. 计算前先检查缓存，计算后存入缓存

    时间复杂度：O(n) — 每个节点最多计算一次
    空间复杂度：O(n) — 缓存 + 递归调用栈

    图解示例：
    ────────────────────────────────────────────────────────
    示例 1: root = [3,2,3,null,3,null,1]

            3
           / \
          2   3
           \   \
            3   1

    计算过程（后序遍历）：
    - 节点 3（左下）: 偷=3, 不偷=0 → max=3
    - 节点 1（右下）: 偷=1, 不偷=0 → max=1
    - 节点 2（左子）: 偷=2+0+0=2, 不偷=3+0=3 → max=3
    - 节点 3（右子）: 偷=3+0+0=3, 不偷=0+1=1 → max=3
    - 节点 3（根）:   偷=3+0+0=3, 不偷=3+3=6 → max=6... 不对！

    等等，这里偷根节点3时，左右子节点2和3都不能偷，
    所以只能从孙子节点获取：偷=3+3+1=7
    不偷根节点3时，取左右子树各自最优：3+3=6
    最终 max(7, 6) = 7 ✓
    """
    memo = {}  # 缓存：存储每个节点的最优偷窃金额

    def dfs(node: Optional[TreeNode]) -> int:
        if node is None:                      # 空节点，无法偷窃
            return 0
        if node in memo:                      # 已计算过，直接返回缓存值
            return memo[node]

        # 情况一：偷当前节点
        val_rob = node.val                    # 偷当前节点的金额
        if node.left:                         # 左子节点不能偷，只能偷左孙子
            val_rob += dfs(node.left.left) + dfs(node.left.right)
        if node.right:                        # 右子节点不能偷，只能偷右孙子
            val_rob += dfs(node.right.left) + dfs(node.right.right)

        # 情况二：不偷当前节点
        val_not_rob = dfs(node.left) + dfs(node.right)  # 偷左右子节点

        memo[node] = max(val_rob, val_not_rob)  # 缓存结果
        return memo[node]

    return dfs(root)


# ══════════════════════════════════════════════════════════
# 解法三：树形 DP — 后序遍历（推荐）⭐⭐⭐⭐
# ══════════════════════════════════════════════════════════


def rob(root: Optional[TreeNode]) -> int:
    """
    树形 DP 解法（后序遍历）⭐⭐⭐⭐ 推荐

    核心思想：
    ────────────────────────────────────────────────────────
    每个节点返回一个长度为 2 的数组 [不偷, 偷]：
    - result[0]：不偷当前节点时的最优值
    - result[1]：偷当前节点时的最优值

    通过后序遍历，先得到子树结果，再计算当前节点，
    自然消除了重叠子问题，且无需额外缓存。

    算法步骤：
    1. 后序遍历：先递归左右子树
    2. 获取左右子树的 [不偷, 偷] 结果
    3. 计算当前节点的 [不偷, 偷]
    4. 根节点处取 max(不偷, 偷) 即为答案

    时间复杂度：O(n) — 每个节点遍历一次
    空间复杂度：O(n) — 递归调用栈（最坏为树高）

    图解示例：
    ────────────────────────────────────────────────────────
    示例 1: root = [3,2,3,null,3,null,1]

            3
           / \
          2   3
           \   \
            3   1

    后序遍历过程：
    ┌─────────────────────────────────────────────────┐
    │ 节点    左子树结果     右子树结果     当前结果     │
    │─────────────────────────────────────────────────│
    │ 3(左下) [0,0]         [0,0]         [0, 3]      │
    │ 1(右下) [0,0]         [0,0]         [0, 1]      │
    │ 2(左子) [0,0]         [0,3]         [3, 2]      │
    │ 3(右子) [0,0]         [0,1]         [1, 3]      │
    │ 3(根)   [3,2]         [1,3]         [6, 7]      │
    └─────────────────────────────────────────────────┘

    详细计算：
    - 节点 3（左下叶）: 不偷=0, 偷=3 → [0, 3]
    - 节点 1（右下叶）: 不偷=0, 偷=1 → [0, 1]
    - 节点 2（左子）:   不偷=max(0,0)+max(0,3)=3,  偷=2+0+0=2  → [3, 2]
    - 节点 3（右子）:   不偷=max(0,0)+max(0,1)=1,  偷=3+0+0=3  → [1, 3]
    - 节点 3（根）:     不偷=max(3,2)+max(1,3)=6,   偷=3+3+1=7  → [6, 7]

    结果：max(6, 7) = 7 ✓

    状态定义：
    - result[0] = 不偷当前节点时，以当前节点为根的子树能偷到的最高金额
    - result[1] = 偷当前节点时，以当前节点为根的子树能偷到的最高金额

    状态转移：
    - result[0] = max(left[0], left[1]) + max(right[0], right[1])
    - result[1] = node.val + left[0] + right[0]

    初始条件：
    - 空节点返回 [0, 0]

    最终答案：
    - max(root_result[0], root_result[1])

    与记忆化搜索的区别：
    ────────────────────────────────────────────────────────
    记忆化搜索：每个节点偷/不偷对应一个标量值，需要递归到孙子节点
    树形 DP：   每个节点返回 [不偷, 偷] 两个值，只需递归到子节点
    树形 DP 状态更紧凑，递归层级更浅，代码更简洁。
    """
    def dfs(node: Optional[TreeNode]) -> list[int]:
        if node is None:                      # 空节点：偷或不偷都是 0
            return [0, 0]

        left = dfs(node.left)                 # 递归左子树，获取左子树的 [不偷, 偷]
        right = dfs(node.right)               # 递归右子树，获取右子树的 [不偷, 偷]

        # 不偷当前节点：左右子树各自取最优（偷或不偷取较大值）
        val_not_rob = max(left[0], left[1]) + max(right[0], right[1])

        # 偷当前节点：当前值 + 左子节点不偷 + 右子节点不偷
        val_rob = node.val + left[0] + right[0]

        return [val_not_rob, val_rob]         # 返回当前节点的 [不偷, 偷]

    result = dfs(root)                        # 从根节点开始后序遍历
    return max(result[0], result[1])          # 返回根节点偷或不偷的较大值


# ══════════════════════════════════════════════════════════
# 重要说明：算法正确性说明与易错点提醒
# ══════════════════════════════════════════════════════════
#
# 算法正确性说明：
# ────────────────────────────────────────────────────────
# 1. 树形 DP 解法的正确性：
#    - 每个节点返回 [不偷, 偷] 两个状态，完整描述了以该节点为根的子树的最优解
#    - 不偷当前节点时，子节点不受约束，可以各自取最优（偷或不偷取 max）
#    - 偷当前节点时，子节点必须不偷，所以只能加 left[0] + right[0]
#    - 后序遍历保证了处理当前节点时，子节点的结果已经计算完毕
#    - 这种方式自然消除了重叠子问题，无需额外缓存

# 2. 暴力递归的正确性：
#    - 对于每个节点，枚举偷或不偷两种情况
#    - 偷当前节点时，跳过子节点，递归孙子节点
#    - 不偷当前节点时，递归子节点
#    - 取两种情况的较大值，保证结果最优

# 3. 记忆化搜索的正确性：
#    - 与暴力递归逻辑相同，但通过缓存避免了重复计算
#    - 缓存 key 为节点对象本身（TreeNode 引用），确保唯一性

# 常见错误：
# ────────────────────────────────────────────────────────
# 1. 偷当前节点时，错误地加上子节点的最优值
#    - 错误：val_rob = node.val + max(left) + max(right)
#    - 后果：子节点被偷，与当前节点直接相连，触发警报
#    - 正确：val_rob = node.val + left[0] + right[0]
#    - 原因：偷了当前节点，子节点必须不偷，只能加 left[0]（不偷）而非 max(left)

# 2. 不偷当前节点时，错误地加上子节点的"不偷"值
#    - 错误：val_not_rob = left[0] + right[0]
#    - 后果：漏掉了子节点可以被偷的情况，结果偏小
#    - 正确：val_not_rob = max(left[0], left[1]) + max(right[0], right[1])
#    - 原因：不偷当前节点，子节点偷不偷都可以，取各自较大值

# 3. 使用前序或中序遍历
#    - 错误：先处理当前节点，再处理子节点
#    - 后果：当前节点计算时，子节点结果尚未得出，无法正确递推
#    - 正确：使用后序遍历（左右根），确保子树结果先计算

# 4. 暴力递归中遗漏孙子节点
#    - 错误：偷当前节点时，只加 node.val，不递归孙子节点
#    - 后果：漏掉了孙子节点的金额，结果偏小
#    - 正确：偷当前节点时，递归所有孙子节点（4 个可能的分支）

# 5. 空节点返回值错误
#    - 错误：空节点返回 [1, 0] 或其他非零值
#    - 后果：影响父节点计算，导致结果错误
#    - 正确：空节点返回 [0, 0]，因为没有节点可偷

# 6. 记忆化搜索中使用节点值作为缓存 key
#    - 错误：memo[node.val]
#    - 后果：不同节点可能有相同的值，导致缓存命中错误
#    - 正确：memo[node]（使用节点对象引用作为 key）

# 7. 混淆 [不偷, 偷] 的下标
#    - 错误：认为 result[0] 是偷，result[1] 是不偷
#    - 后果：状态转移公式全部用反，结果错误
#    - 正确：result[0] = 不偷，result[1] = 偷（约定俗成）

# 8. 忽略树的退化情况
#    - 错误：未考虑树退化为链表的情况
#    - 后果：暴力递归在链表树上也指数级增长
#    - 正确：树形 DP 无论树形态如何，时间复杂度都是 O(n)
# ══════════════════════════════════════════════════════════
