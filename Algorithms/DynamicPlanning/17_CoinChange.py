"""
零钱兑换（Coin Change）

题目描述：
给你一个整数数组 coins ，表示不同面额的硬币；以及一个整数 amount ，表示总金额。
计算并返回可以凑成总金额所需的 最少的硬币个数 。如果没有任何一种硬币组合能组成总金额，返回 -1 。
你可以认为每种硬币的数量是无限的。

示例 1：
输入：coins = [1, 2, 5], amount = 11
输出：3 
解释：11 = 5 + 5 + 1

示例 2：
输入：coins = [2], amount = 3
输出：-1

示例 3：
输入：coins = [1], amount = 0
输出：0

════════════════════════════════════════════════════════════════════════
核心联系：本题与完全背包问题的本质关系
════════════════════════════════════════════════════════════════════════

维度              完全背包问题                        零钱兑换（本题）
────────────────────────────────────────────────────────────────────────
问题本质          求最大价值                         求最小硬币数
物品限制          每种物品可无限使用                 每种硬币可无限使用
递推公式          dp[i] = max(dp[i], dp[i-w]+v)     dp[i] = min(dp[i], dp[i-coin]+1)
初始条件          dp[0] = 0，其余为 -inf            dp[0] = 0，其余为 inf
────────────────────────────────────────────────────────────────────────

为什么 dp[0] = 0？
────────────────────────────────────────────────────────────────────────
dp[0] = 0 表示"凑成金额 0 需要 0 枚硬币"。
这个初始条件使得递推公式能够正确运行：
    dp[1] = dp[1-1] + 1 = dp[0] + 1 = 1（用 1 枚 1 元硬币）
    dp[2] = min(dp[2-1]+1, dp[2-2]+1) = min(2, 1) = 1（用 1 枚 2 元硬币）

如果没有 dp[0] = 0，所有 dp[i] 都会保持为 inf。

解题思路总览：
────────────────────────────────────────────────────────────────────────
解法                          时间复杂度       空间复杂度       说明
────────────────────────────────────────────────────────────────────────
回溯算法                       O(S^amount)     O(amount)       超时，仅用于理解
记忆化搜索（Top-Down DP）       O(n×amount)     O(n×amount)     ⭐⭐
动态规划（Bottom-Up DP）        O(n×amount)     O(amount)       ⭐⭐⭐⭐  推荐
广度优先搜索（BFS）             O(n×amount)     O(amount)       ⭐⭐⭐   适合找最短路径
────────────────────────────────────────────────────────────────────────
（n = len(coins)，S = 平均硬币面额）

核心思想（动态规划）：
────────────────────────────────────────────────────────────────────────
关键洞察（转化为完全背包问题）：
1. 每种硬币可以无限使用 → 完全背包问题
2. 目标是求最小值（最少硬币数）
3. 递推公式：dp[i] = min(dp[i], dp[i - coin] + 1)

状态定义（一维 DP）：
    dp[i] = 凑成金额 i 所需的最少硬币数

状态转移：
    对于每个金额 i，尝试使用每种硬币 coin：
    dp[i] = min(dp[i], dp[i - coin] + 1)（当 coin <= i）

    含义：凑成金额 i 的最少硬币数 = min(当前最小值, 凑成金额 i-coin 的最少硬币数 + 1)

初始条件：
    dp[0] = 0（凑成金额 0 需要 0 枚硬币）
    dp[1..amount] = inf（初始化为无穷大，表示暂时无法凑成）

最终答案：
    dp[amount] 如果 dp[amount] != inf，否则 -1
"""


# ══════════════════════════════════════════════════════════
# 解法一：回溯算法（Brute Force）
# ══════════════════════════════════════════════════════════


def coinChange_backtrack(coins: list[int], amount: int) -> int:
    """
    回溯算法（Brute Force）

    核心思路：
    ────────────────────────────────────────────────────────
    枚举每一种硬币组合，记录最少的硬币数。

    算法步骤：
    1. 使用回溯枚举每种硬币的使用次数
    2. 维护剩余金额 remaining 和当前硬币数 count
    3. 当 remaining == 0 时，更新最小硬币数
    4. 当 remaining < 0 时，剪枝

    时间复杂度：O(S^amount) — 最坏情况每层递归 S 个分支
    空间复杂度：O(amount)   — 递归调用栈深度（最多 amount 层）

    问题分析：
    ────────────────────────────────────────────────────────
    对于 amount = 11, coins = [1, 2, 5]，递归树非常庞大，效率极低。
    仅用于理解问题本质。
    """
    min_coins = float("inf")  # 初始化最小硬币数为无穷大

    def backtrack(remaining: int, count: int) -> None:
        nonlocal min_coins  # 使用外层函数的变量
        if remaining == 0:   # 找到一个有效组合
            min_coins = min(min_coins, count)  # 更新最小硬币数
            return
        if remaining < 0:    # 剩余金额为负，剪枝
            return

        for coin in coins:   # 枚举每种硬币
            if remaining >= coin:  # 当前硬币面额不超过剩余金额
                backtrack(remaining - coin, count + 1)  # 使用该硬币，递归

    backtrack(amount, 0)  # 从目标金额开始，初始硬币数为 0
    return min_coins if min_coins != float("inf") else -1  # 返回结果，无法凑成返回 -1


# ══════════════════════════════════════════════════════════
# 解法二：记忆化搜索（Top-Down DP）
# ══════════════════════════════════════════════════════════


def coinChange_memo(coins: list[int], amount: int) -> int:
    """
    记忆化搜索解法（Top-Down DP）⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    在回溯的基础上，添加缓存避免重复计算。

    算法步骤：
    1. 创建缓存 memo[remaining]
    2. 使用递归 + 缓存求解
    3. 对于每个剩余金额，枚举所有可能的硬币

    时间复杂度：O(n×amount) — 每个状态只计算一次
    空间复杂度：O(n×amount) — 缓存 + 递归调用栈

    状态定义：
    - memo[remaining] = 凑成金额 remaining 所需的最少硬币数

    状态转移：
    - memo[remaining] = min(memo[remaining - coin] + 1 for coin in coins if coin <= remaining)

    初始条件：
    - remaining == 0 时返回 0（已经凑成目标金额）
    """
    memo = {}  # 缓存：存储已计算的剩余金额的最小硬币数

    def dfs(remaining: int) -> int:
        if remaining == 0:          # 剩余金额为 0，已经凑成，返回 0
            return 0
        if remaining in memo:       # 如果已计算过，直接返回缓存值
            return memo[remaining]

        min_count = float("inf")    # 初始化最小硬币数为无穷大
        for coin in coins:          # 枚举每种硬币
            if remaining >= coin:   # 当前硬币面额不超过剩余金额
                res = dfs(remaining - coin)  # 递归计算剩余金额的最小硬币数
                if res != -1:       # 如果剩余金额可以凑成
                    min_count = min(min_count, res + 1)  # 更新最小硬币数

        memo[remaining] = min_count if min_count != float("inf") else -1  # 缓存结果
        return memo[remaining]  # 返回结果

    return dfs(amount)  # 从目标金额开始递归


# ══════════════════════════════════════════════════════════
# 解法三：动态规划（Bottom-Up DP）— 写法一：外层金额，内层硬币
# ══════════════════════════════════════════════════════════


def coinChange_dp_v1(coins: list[int], amount: int) -> int:
    """
    动态规划解法（Bottom-Up DP）⭐⭐⭐⭐ 推荐 — 写法一

    核心思想：
    ────────────────────────────────────────────────────────
    标准的完全背包问题解法。
    通过一维数组记录凑成每个金额所需的最少硬币数。

    算法步骤：
    1. 创建一维 DP 数组 dp[amount+1]，初始化 dp[0] = 0，其余为 inf
    2. 外层循环遍历金额 i（从 1 到 amount）
    3. 内层循环遍历硬币 coin（从 1 到 n）
    4. 对于每个金额 i，尝试用每种硬币来更新 dp[i]

    时间复杂度：O(n×amount) — 双重循环
    空间复杂度：O(amount)   — 一维 DP 数组

    图解示例：
    ────────────────────────────────────────────────────────
    coins = [1, 2, 5], amount = 11

    dp = [0, inf, inf, inf, inf, inf, inf, inf, inf, inf, inf, inf]

    i=1: coin=1 → dp[1] = min(inf, dp[0]+1) = 1
         coin=2 → 2 > 1，break
         coin=5 → 跳过
         dp = [0, 1, inf, inf, inf, inf, inf, inf, inf, inf, inf, inf]

    i=2: coin=1 → dp[2] = min(inf, dp[1]+1) = 2
         coin=2 → dp[2] = min(2, dp[0]+1) = 1
         coin=5 → 5 > 2，break
         dp = [0, 1, 1, inf, inf, inf, inf, inf, inf, inf, inf, inf]

    ...（中间步骤省略）...

    i=11: coin=1 → dp[11] = min(?, dp[10]+1) = ?
          coin=2 → dp[11] = min(?, dp[9]+1) = ?
          coin=5 → dp[11] = min(?, dp[6]+1) = 3
          dp = [0, 1, 1, 2, 2, 1, 2, 2, 3, 3, 2, 3]

    结果：dp[11] = 3 ✓

    状态定义：
    - dp[i] = 凑成金额 i 所需的最少硬币数

    状态转移：
    - dp[i] = min(dp[i], dp[i - coin] + 1)（当 coin <= i）

    初始条件：
    - dp[0] = 0（凑成金额 0 需要 0 枚硬币）
    - dp[1..amount] = inf（初始化为无穷大）

    最终答案：
    - dp[amount] 如果 dp[amount] != inf，否则 -1

    优化技巧：
    ────────────────────────────────────────────────────────
    coins.sort() + break：排序后，当 coin > i 时，后面的硬币都更大，无需继续尝试
    """
    dp = [float("inf")] * (amount + 1)  # 创建 DP 数组，初始化为无穷大
    dp[0] = 0                           # 初始化：凑成金额 0 需要 0 枚硬币
    coins.sort()                        # 排序硬币，便于剪枝优化

    for i in range(1, amount + 1):      # 遍历每个金额（从 1 到 amount）
        for coin in coins:              # 遍历每种硬币
            if coin <= i:               # 如果当前硬币面额不超过金额 i
                # 状态转移：凑成金额 i 的最少硬币数 = min(当前值, 凑成 i-coin 的最少硬币数 + 1)
                dp[i] = min(dp[i], dp[i - coin] + 1)
            else:
                break  # 硬币已排序，后面的硬币更大，无需继续尝试

    # 返回结果：如果 dp[amount] 仍是无穷大，说明无法凑成，返回 -1
    return dp[amount] if dp[amount] != float("inf") else -1


# ══════════════════════════════════════════════════════════
# 解法四：动态规划（Bottom-Up DP）— 写法二：外层硬币，内层金额
# ══════════════════════════════════════════════════════════


def coinChange_dp_v2(coins: list[int], amount: int) -> int:
    """
    动态规划解法（Bottom-Up DP）⭐⭐⭐⭐ 推荐 — 写法二

    核心思想：
    ────────────────────────────────────────────────────────
    另一种完全背包问题的标准写法。
    先遍历硬币，再遍历金额，确保每种硬币可以被多次使用。

    算法步骤：
    1. 创建一维 DP 数组 dp[amount+1]，初始化 dp[0] = 0，其余为 inf
    2. 外层循环遍历硬币 coin（从 0 到 n-1）
    3. 内层循环遍历金额 i（从 coin 到 amount）
    4. 对于每种硬币，更新所有能用它来优化的金额

    时间复杂度：O(n×amount) — 双重循环
    空间复杂度：O(amount)   — 一维 DP 数组

    状态定义：
    - dp[i] = 凑成金额 i 所需的最少硬币数

    状态转移：
    - dp[i] = min(dp[i], dp[i - coin] + 1)

    初始条件：
    - dp[0] = 0（凑成金额 0 需要 0 枚硬币）
    - dp[1..amount] = inf（初始化为无穷大）

    最终答案：
    - dp[amount] 如果 dp[amount] != inf，否则 -1

    为什么内层循环从 coin 开始？
    ────────────────────────────────────────────────────────
    当 i < coin 时，无法使用该硬币，直接跳过。
    这样可以避免无效的比较和索引越界。
    """
    dp = [float("inf")] * (amount + 1)  # 创建 DP 数组，初始化为无穷大
    dp[0] = 0                           # 初始化：凑成金额 0 需要 0 枚硬币

    for coin in coins:                  # 遍历每种硬币
        # 内层循环从 coin 开始：当 i < coin 时无法使用该硬币
        for i in range(coin, amount + 1):
            # 状态转移：使用当前硬币更新 dp[i]
            dp[i] = min(dp[i], dp[i - coin] + 1)

    # 返回结果：如果 dp[amount] 仍是无穷大，说明无法凑成，返回 -1
    return dp[amount] if dp[amount] != float("inf") else -1


# ══════════════════════════════════════════════════════════
# 解法五：广度优先搜索（BFS）
# ══════════════════════════════════════════════════════════


def coinChange_bfs(coins: list[int], amount: int) -> int:
    """
    广度优先搜索解法（BFS）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    将问题转化为最短路径问题：
    - 每个金额是一个节点
    - 使用一枚硬币 coin 相当于从节点 i 走到节点 i-coin
    - 求从 amount 到 0 的最短路径长度

    BFS 的特性保证了第一次到达目标时就是最短路径。

    算法步骤：
    1. 使用队列存储待处理的金额和当前步数
    2. 使用 visited 数组避免重复访问
    3. 每次出队一个金额，尝试减去每种硬币
    4. 当金额为 0 时，返回当前步数

    时间复杂度：O(n×amount) — 每个节点最多访问一次
    空间复杂度：O(amount)   — 队列和 visited 数组

    图解示例：
    ────────────────────────────────────────────────────────
    coins = [1, 2, 5], amount = 11

    队列：[(11, 0)]
    出队 11：减去 1→10, 减去 2→9, 减去 5→6，步数+1
    队列：[(10, 1), (9, 1), (6, 1)]

    出队 10：减去 1→9(已访问), 减去 2→8, 减去 5→5，步数+1
    队列：[(9, 1), (6, 1), (8, 2), (5, 2)]

    ...（中间步骤省略）...

    当金额减到 0 时，返回当前步数 3

    结果：3 ✓

    适用场景：
    ────────────────────────────────────────────────────────
    当目标金额较大但最短路径较短时，BFS 效率高于 DP。
    例如 amount = 10000，但最优解只需要 2 枚硬币（如 [5000, 1]）。
    """
    if amount == 0:                    # 特殊情况：金额为 0，直接返回 0
        return 0

    from collections import deque      # 导入队列

    visited = [False] * (amount + 1)  # 访问标记数组，避免重复访问
    queue = deque()                   # 创建队列，存储 (当前金额, 当前步数)
    queue.append((amount, 0))         # 初始状态：目标金额，步数为 0
    visited[amount] = True            # 标记初始金额已访问

    while queue:                      # BFS 循环
        current_amount, steps = queue.popleft()  # 出队：获取当前金额和步数

        for coin in coins:            # 尝试使用每种硬币
            next_amount = current_amount - coin  # 减去硬币后的金额
            if next_amount == 0:      # 到达目标（金额为 0），返回步数+1
                return steps + 1
            # 如果金额大于 0 且未被访问过
            if next_amount > 0 and not visited[next_amount]:
                visited[next_amount] = True       # 标记已访问
                queue.append((next_amount, steps + 1))  # 入队

    return -1  # 无法凑成目标金额


# ══════════════════════════════════════════════════════════
# 重要说明：两种 DP 写法的对比与易错点提醒
# ══════════════════════════════════════════════════════════
#
# 两种 DP 写法的对比：
# ────────────────────────────────────────────────────────
# 写法一（外层金额，内层硬币）：
#   for i in range(1, amount + 1):
#       for coin in coins:
#
#   语义：对于每个金额 i，尝试用所有硬币来凑它
#   特点：可以利用 coins.sort() + break 优化
#   适用：求最小硬币数（本题）、求排列数
#
# 写法二（外层硬币，内层金额）：
#   for coin in coins:
#       for i in range(coin, amount + 1):
#
#   语义：对于每种硬币，更新所有能用它来优化的金额
#   特点：标准完全背包写法，代码更简洁
#   适用：求最小硬币数（本题）、求组合数
#
# 为什么本题两种写法结果相同？
# ────────────────────────────────────────────────────────
# 本题求的是最小硬币数，不管先遍历金额还是先遍历硬币：
#   - 每个金额都会被所有硬币尝试更新
#   - 最终取的是 min()，顺序不影响最小值
#
# ⚠️ 但求组合数时顺序很重要！（关联 Coin Change II 和 Combination Sum IV）
# ────────────────────────────────────────────────────────
# 求组合数（不考虑顺序）：外层硬币，内层金额（如 Coin Change II）
# 求排列数（考虑顺序）：外层金额，内层硬币（如 Combination Sum IV）
#
# 常见错误：
# ────────────────────────────────────────────────────────
# 1. 忘记初始化 dp[0] = 0
#    - 后果：所有 dp[i] 都会保持为 inf
#    - 原因：dp[0] 是递推的起点
#
# 2. dp[i - coin] 为 inf 时直接加 1
#    - 错误：dp[i] = dp[i - coin] + 1（当 dp[i-coin] = inf 时，inf + 1 会溢出）
#    - 正确：dp[i] = min(dp[i], dp[i - coin] + 1)（min 会自动处理 inf）
#
# 3. 内层循环从 0 开始而非从 coin 开始（写法二）
#    - 错误：for i in range(0, amount + 1)
#    - 后果：当 i < coin 时，i - coin 为负数，导致索引越界或错误结果
#    - 正确：for i in range(coin, amount + 1)
#
# 4. 使用 break 时忘记排序 coins（写法一）
#    - 错误：直接 break，不排序
#    - 后果：如果 coins = [5, 1, 2]，当 coin=5 > i=3 时就 break，跳过了 1 和 2
#    - 正确：先 coins.sort()，再 break
#
# 5. 返回 dp[amount] 时忘记判断是否为 inf
#    - 错误：直接 return dp[amount]
#    - 后果：无法凑成金额时返回 inf，而非 -1
#    - 正确：return dp[amount] if dp[amount] != float("inf") else -1
#
# 6. BFS 中忘记标记 visited
#    - 错误：不使用 visited 数组
#    - 后果：同一个金额会被多次加入队列，导致超时或内存溢出
#    - 正确：每次入队前标记 visited[next_amount] = True
# ══════════════════════════════════════════════════════════
