"""
爬楼梯（扩展版）

题目描述：
假设你正在爬楼梯。需要 n 阶你才能到达楼顶。
每次你可以爬至多 m 个台阶（即每次可以跨 1, 2, ..., m 步）。
你有多少种不同的方法可以爬到楼顶呢？

注意：给定 n 是一个正整数。

输入描述：
包含两个正整数，分别表示 n（台阶数）和 m（最大步数）。

输出描述：
输出一个整数，表示爬到楼顶的方法数。

输入示例：
3 2
输出示例：
3

════════════════════════════════════════════════════════════════════════
核心联系：本题与组合总和 IV（Combination Sum IV）的本质关系
════════════════════════════════════════════════════════════════════════

维度              组合总和 IV（Combination Sum IV）    爬楼梯（本题）
────────────────────────────────────────────────────────────────────────
问题本质          求排列数（Permutations）             求排列数（Permutations）
可选数字          nums = [1, 2, 3, ...]（给定数组）     steps = [1, 2, ..., m]（连续整数）
目标值            target                              n（台阶数）
顺序是否重要      重要（1+2 和 2+1 视为不同）           重要（先跨1再跨2，与先跨2再跨1是不同的走法）
递推公式          dp[i] += dp[i - num]                dp[i] += dp[i - step]
────────────────────────────────────────────────────────────────────────

为什么本题是排列数？
────────────────────────────────────────────────────────────────────────
爬楼梯的每一步都是独立的决策，顺序不同代表完全不同的走法。

例如 n = 3, m = 2（3阶楼梯，每次最多跨2步）：
    - (1, 1, 1)：每次跨1步
    - (1, 2)：先跨1步，再跨2步
    - (2, 1)：先跨2步，再跨1步
    共 3 种不同的方法。

如果本题是组合数（不考虑顺序），那么 (1, 2) 和 (2, 1) 会被视为同一种，
答案就只有 2 种：{1,1,1} 和 {1,2}。但这不符合爬楼梯问题的实际意义。

类比理解：
    - 组合总和 IV 像"用不同面额的硬币凑金额"，顺序不同算不同方案
    - 爬楼梯像"每一步的决策都有先后之分"，(1,2) 和 (2,1) 是两条不同的路径

解题思路总览：
────────────────────────────────────────────────────────────────────────
解法                          时间复杂度       空间复杂度       说明
────────────────────────────────────────────────────────────────────────
回溯算法                       O(m^n)          O(n)            超时，仅用于理解
记忆化搜索（Top-Down DP）       O(n×m)          O(n)            ⭐⭐
动态规划（Bottom-Up DP）        O(n×m)          O(n)            ⭐⭐⭐⭐  推荐
────────────────────────────────────────────────────────────────────────

核心思想（动态规划）：
────────────────────────────────────────────────────────────────────────
关键洞察（转化为组合总和 IV）：
1. 本题本质上是组合总和 IV 的特例
2. nums = [1, 2, 3, ..., m]，target = n
3. 求用这些数字（可重复使用）凑成 n 的排列数
4. 递推公式：dp[i] = dp[i-1] + dp[i-2] + ... + dp[i-m]

当 m = 2 时的特例（经典爬楼梯）：
    dp[i] = dp[i-1] + dp[i-2]
    这就是斐波那契数列！

状态定义（一维 DP）：
    dp[i] = 爬到第 i 阶楼梯的方法数

状态转移：
    对于每个台阶 i，考虑所有可能的"最后一步"：
    dp[i] += dp[i - step]（当 step <= i，且 step ∈ [1, m]）

    含义：爬到第 i 阶的方法数 = 所有"最后一步跨 step 阶"的方法数之和

初始条件：
    dp[0] = 1（地面有一种方式到达：就是不动）

最终答案：
    dp[n]
"""


# ══════════════════════════════════════════════════════════
# 解法一：回溯算法（Brute Force）
# ══════════════════════════════════════════════════════════


def climbStairs_brute(m: int, n: int) -> int:
    """
    回溯算法（Brute Force）

    核心思路：
    ────────────────────────────────────────────────────────
    枚举每一步跨多少阶，统计能到达第 n 阶的所有路径数。

    算法步骤：
    1. 使用回溯枚举每一步的选择（跨 1, 2, ..., m 步）
    2. 维护剩余台阶数 remaining
    3. 当 remaining == 0 时，说明到达楼顶，计数加一
    4. 当 remaining < 0 时，剪枝

    时间复杂度：O(m^n) — 最坏情况每层递归 m 个分支
    空间复杂度：O(n)   — 递归调用栈深度（最多 n 层）

    问题分析：
    ────────────────────────────────────────────────────────
    对于 n = 20, m = 2，就有约 2^20 ≈ 100 万种可能，效率极低。
    仅用于理解问题本质。
    """
    result = 0

    def backtrack(remaining: int) -> None:
        nonlocal result
        if remaining == 0:
            result += 1
            return
        if remaining < 0:
            return

        # 枚举每一步跨多少阶
        for step in range(1, m + 1):
            if remaining >= step:
                backtrack(remaining - step)

    backtrack(n)
    return result


# ══════════════════════════════════════════════════════════
# 解法二：记忆化搜索（Top-Down DP）
# ══════════════════════════════════════════════════════════


def climbStairs_memo(m: int, n: int) -> int:
    """
    记忆化搜索解法（Top-Down DP）⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    将问题转化为组合总和 IV：
    - nums = [1, 2, ..., m]，target = n
    - 求用这些数字凑成 n 的排列数

    算法步骤：
    1. 创建缓存 memo[remaining]
    2. 使用递归 + 缓存求解
    3. 对于每个剩余台阶数，枚举所有可能的步数

    时间复杂度：O(n×m) — 每个状态只计算一次
    空间复杂度：O(n)   — 缓存 + 递归调用栈

    状态定义：
    - memo[remaining] = 还剩 remaining 阶时，爬到楼顶的方法数

    状态转移：
    - memo[remaining] = sum(memo[remaining - step] for step in [1..m] if step <= remaining)

    初始条件：
    - remaining == 0 时返回 1（已经到达楼顶）
    """
    memo = {}

    def dfs(remaining: int) -> int:
        if remaining == 0:
            return 1
        if remaining in memo:
            return memo[remaining]

        total = 0
        for step in range(1, m + 1):
            if remaining >= step:
                total += dfs(remaining - step)

        memo[remaining] = total
        return total

    return dfs(n)


# ══════════════════════════════════════════════════════════
# 解法三：动态规划（Bottom-Up DP）
# ══════════════════════════════════════════════════════════


def climbStairs(m: int, n: int) -> int:
    """
    动态规划解法（Bottom-Up DP）⭐⭐⭐⭐ 推荐

    核心思想：
    ────────────────────────────────────────────────────────
    标准的组合总和 IV 的 DP 解法。
    通过一维数组记录到达每阶楼梯的方法数。

    算法步骤：
    1. 创建一维 DP 数组 dp[n+1]，初始化 dp[0] = 1
    2. 外层循环遍历台阶 i（从 1 到 n）
    3. 内层循环遍历步数 step（从 1 到 m）
    4. 对于每阶 i，累加所有可能的"最后一步"的方法数

    时间复杂度：O(n×m) — 双重循环
    空间复杂度：O(n)   — 一维 DP 数组

    图解示例：
    ────────────────────────────────────────────────────────
    n = 3, m = 2（3阶楼梯，每次最多跨2步）

    dp = [1, 0, 0, 0]

    i=1: step=1 → dp[1] += dp[0] = 1
         step=2 → 2 > 1，跳过
         dp = [1, 1, 0, 0]

    i=2: step=1 → dp[2] += dp[1] = 1
         step=2 → dp[2] += dp[0] = 2
         dp = [1, 1, 2, 0]
         （方法：(1,1), (2)）

    i=3: step=1 → dp[3] += dp[2] = 2
         step=2 → dp[3] += dp[1] = 3
         dp = [1, 1, 2, 3]
         （方法：(1,1,1), (1,2), (2,1)）

    结果：dp[3] = 3 ✓

    经典特例：m = 2（每次只能跨 1 或 2 步）
    ────────────────────────────────────────────────────────
    n = 1: dp[1] = dp[0] = 1
    n = 2: dp[2] = dp[1] + dp[0] = 2
    n = 3: dp[3] = dp[2] + dp[1] = 3
    n = 4: dp[4] = dp[3] + dp[2] = 5
    ...
    这就是斐波那契数列！

    状态定义：
    - dp[i] = 爬到第 i 阶楼梯的方法数

    状态转移：
    - dp[i] += dp[i - step]（当 step ∈ [1, m] 且 step <= i）

    初始条件：
    - dp[0] = 1（地面有一种方式到达：就是不动）

    最终答案：
    - dp[n]
    """
    dp = [0] * (n + 1)
    dp[0] = 1

    for i in range(1, n + 1):
        for step in range(1, m + 1):
            if i >= step:
                dp[i] += dp[i - step]
            else:
                break

    return dp[n]


# ══════════════════════════════════════════════════════════
# 重要说明：算法的正确性证明
# ══════════════════════════════════════════════════════════
#
# 为什么 dp[0] = 1？
# ────────────────────────────────────────────────────────
# dp[0] = 1 是一个数学上的约定，表示"在地面有一种方式"（就是不爬）。
# 这个初始条件使得递推公式能够正确运行：
#   dp[1] = dp[0] = 1（跨1步到达第1阶）
#   dp[2] = dp[1] + dp[0] = 2（跨1+1步，或跨2步）
# 如果没有 dp[0] = 1，所有 dp[i] 都会是 0。
#
# 为什么本题是组合总和 IV 的特例？
# ────────────────────────────────────────────────────────
# 组合总和 IV：
#   给定 nums 和 target，求用 nums 中的数字（可重复）凑成 target 的排列数
#   dp[i] += dp[i - num] for num in nums
#
# 爬楼梯：
#   给定 m 和 n，求用 [1, 2, ..., m] 中的数字（可重复）凑成 n 的排列数
#   dp[i] += dp[i - step] for step in [1, 2, ..., m]
#
# 两者本质上是同一个问题，只是爬楼梯的"可选数字"是连续整数 [1, m]。
#
# 为什么循环顺序是外层台阶、内层步数？
# ────────────────────────────────────────────────────────
# 本题是排列数问题，顺序不同算不同方案。
# 外层遍历台阶 i，内层遍历步数 step：
#   - 对于每个台阶 i，考虑所有可能的"最后一步"
#   - step=1, step=2, ..., step=m 的地位完全平等
#   - (1,2) 和 (2,1) 会被分别计数
#
# 如果反过来（外层步数、内层台阶）：
#   - 先处理 step=1，再处理 step=2
#   - step=2 只能在 step=1 之后被考虑
#   - (1,2) 和 (2,1) 只会被计算一次（变成组合数）
#
# 常见错误：
# ────────────────────────────────────────────────────────
# 1. range(1, m) 漏掉跨 m 步的情况，应写为 range(1, m + 1)
# 2. 忘记初始条件 dp[0] = 1
# 3. 混淆组合数和排列数：本题是排列数
# 4. 当 m = 2 时，错误地写成 dp[i] = dp[i-1] + dp[i-3] 等
# ══════════════════════════════════════════════════════════
