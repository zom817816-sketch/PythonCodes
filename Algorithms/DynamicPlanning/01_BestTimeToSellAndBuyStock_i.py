"""
给定一个数组 prices ，它的第 i 个元素 prices[i] 表示一支给定股票第 i 天的价格。
你只能选择 某一天 买入这只股票，并选择在 未来的某一个不同的日子 卖出该股票。设计一个算法来计算你所能获取的最大利润。
返回你可以从这笔交易中获取的最大利润。如果你不能获取任何利润，返回 0 。

示例 1：

输入：[7,1,5,3,6,4]
输出：5
解释：在第 2 天（股票价格 = 1）的时候买入，在第 5 天（股票价格 = 6）的时候卖出，最大利润 = 6-1 = 5 。
     注意利润不能是 7-1 = 6, 因为卖出价格需要大于买入价格；同时，你不能在买入前卖出股票。
示例 2：

输入：prices = [7,6,4,3,1]
输出：0
解释：在这种情况下, 没有交易完成, 所以最大利润为 0。
"""

from typing import List


def maxProfit(prices: List[int]) -> int:
    """
    暴力解法（Brute Force）

    核心思路：
    ──────────────────────────────────────────────────
    枚举所有可能的（买入天, 卖出天）组合，计算每对组合的利润，
    取最大值。买入天必须在卖出天之前。

    时间复杂度：O(n²) — 双重循环
    空间复杂度：O(1) — 只用了常数变量

    注意：这个解法在大数据量下会超时（TLE），仅作为理解题意的参考。
    """
    profit = 0
    # 买入日范围：第 0 天 ～ 倒数第 2 天
    # （因为买入后至少需要 1 天来卖出，所以不需要遍历到最后一天）
    for buy_time in range(len(prices) - 1):
        # 卖出日范围：买入日的后一天 ～ 最后一天
        for sell_time in range(buy_time + 1, len(prices)):
            # 计算这次买卖的利润，并更新全局最大利润
            profit = max(profit, prices[sell_time] - prices[buy_time])
    return profit


def maxProfit_dp(prices: List[int]) -> int:
    """
    动态规划解法（一次遍历）⭐⭐⭐

    核心思想：
    ──────────────────────────────────────────────────
    把问题拆成两个子问题，在一次遍历中同时更新：

        1. 历史最低价格  cost
            → 找到第 i 天之前的最低价格，作为最佳买入成本

        2. 历史最大利润  profit
            → 如果今天卖出，利润 = prices[i] - cost
            → 将今天卖出的利润与之前记录的最大利润比较，取较大值

    算法步骤：
    ──────────────────────────────────────────────────
    1. 初始化 cost = +∞（还没遇到任何价格）
    2. 初始化 profit = 0（还没产生任何利润）
    3. 遍历每一天的价格 prices[i]：
        a. cost = min(cost, prices[i])
            → 更新前 i 天中的最低价格（最低买入成本）
        b. profit = max(profit, prices[i] - cost)
            → 如果用这个最低成本在今天卖出，利润是多少？
            → 和之前记录的最大利润比一比，取大的那个
    4. 返回 profit

    时间复杂度：O(n) — 一次遍历
    空间复杂度：O(1) — 只用了两个变量

    图解示例：prices = [7, 1, 5, 3, 6, 4]
    ──────────────────────────────────────────────────
        i   prices[i]   cost (= min)   prices[i]-cost   profit (= max)
    ─────────────────────────────────────────────────────────────
        0      7          7                 0               0
        1      1          1                 0               0
        2      5          1                 4               4
        3      3          1                 2               4
        4      6          1                 5               5  ← 最大利润
        5      4          1                 3               5
    ─────────────────────────────────────────────────────────────
        最终 profit = 5（第 1 天买 1，第 4 天卖 6，赚 5）

    为什么这是动态规划？
    ──────────────────────────────────────────────────
    DP 的两个核心要素：
    - 最优子结构：第 i 天的最大利润 = max(第 i-1 天的最大利润, 今天卖出的利润)
    - 无后效性：cost 只跟过去的价格有关，不影响未来的决策

    状态定义：
    - cost[i]   = 前 i 天的最低价格
    - profit[i] = 前 i 天的最大利润

    状态转移：
    - cost[i]   = min(cost[i-1], prices[i])
    - profit[i] = max(profit[i-1], prices[i] - cost[i])

    由于 profit[i] 和 cost[i] 只依赖 profit[i-1] 和 cost[i-1]，
    所以可以用两个滚动变量代替数组，空间复杂度降为 O(1)。
    """
    # 初始化：还没遍历任何价格，买入成本设为无穷大
    profit = 0
    cost = float("inf")

    # 遍历每一天的价格
    for i in range(len(prices)):
        # 步骤 1：更新前 i 天的最低价格（最低买入成本）
        # 如果今天的价格比之前记录的最低价格还低，就更新 cost
        cost = min(cost, prices[i])

        # 步骤 2：计算如果今天卖出能赚多少，并更新最大利润
        # prices[i] - cost = 今天卖出能获得的利润
        # max(profit, ...) = 跟历史最大利润比一比，取大的
        profit = max(profit, prices[i] - cost)

    return profit