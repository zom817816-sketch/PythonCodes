"""
买卖股票的最佳时机 IV（Best Time to Buy and Sell Stock IV）

题目描述：
给你一个整数数组 prices 和一个整数 k ，其中 prices[i] 是某支给定的股票在第 i 天的价格。
设计一个算法来计算你所能获取的最大利润。你最多可以完成 k 笔交易。
注意：你不能同时参与多笔交易（你必须在再次购买前出售掉之前的股票）。

输入描述：
整数 k（最大交易次数）和整数数组 prices（每天股票价格）。

输出描述：
输出一个整数，表示最大利润。

输入示例：
k = 2, prices = [3,2,6,5,0,3]
输出示例：
7

════════════════════════════════════════════════════════════════════════
核心联系：从股票 III（k=2）到股票 IV（k=任意）的推广
════════════════════════════════════════════════════════════════════════

维度              股票 I（k=1）   股票 III（k=2）    股票 IV（k=任意）     股票 II（k=∞）
────────────────────────────────────────────────────────────────────────────────────
状态表示          buy1, sell1     buy1, sell1,       buy[1..k],           贪心即可
                                  buy2, sell2         sell[1..k]
状态数            2               4                  2k                   不需要DP
时间复杂度        O(n)            O(n)               O(n×k)               O(n)
空间复杂度        O(1)            O(1)               O(k)                 O(1)
────────────────────────────────────────────────────────────────────────────────────

股票 III → 股票 IV 的推广：
────────────────────────────────────────────────────────────────────────
股票 III 中，我们有 4 个固定变量：buy1, sell1, buy2, sell2
股票 IV 中，k 不固定，所以用数组替代：buy[j], sell[j]（j = 1, 2, ..., k）

状态转移的对比：
    股票 III：                         股票 IV：
    buy1  = max(buy1, -price)          buy[1]  = max(buy[1], -price)
    sell1 = max(sell1, buy1+price)     sell[1] = max(sell[1], buy[1]+price)
    buy2  = max(buy2, sell1-price)     buy[j]  = max(buy[j], sell[j-1]-price)  （j≥2）
    sell2 = max(sell2, buy2+price)     sell[j] = max(sell[j], buy[j]+price)

本质上是同一个状态机模型，只是从"4个变量"推广为"2k个数组元素"。

关键优化：当 k ≥ n/2 时退化为无限次交易
────────────────────────────────────────────────────────────────────────
n 天内最多能完成 ⌊n/2⌋ 笔交易（每笔至少需要1天买入+1天卖出），
所以当 k ≥ n/2 时，交易次数的限制实际上不存在，
等价于股票 II（无限次交易），用贪心算法 O(n) 即可解决。

类比理解：
    - 股票 I   像是"只有一次机会"，精打细算
    - 股票 III 像是"两次翻盘机会"，需要在两次交易中最大化
    - 股票 IV  像是"k次翻盘机会"，是 III 的推广
    - 股票 II  像是"无限次低买高卖"，抓住每一次上涨

解题思路总览：
────────────────────────────────────────────────────────────────────────
解法                          时间复杂度       空间复杂度       说明
────────────────────────────────────────────────────────────────────────
贪心（k ≥ n/2 的退化情况）     O(n)            O(1)            必须先判断
动态规划（二维状态数组）        O(n×k)          O(n×k)          ⭐⭐⭐
动态规划（滚动数组优化）        O(n×k)          O(k)            ⭐⭐⭐⭐  推荐
────────────────────────────────────────────────────────────────────────

核心思想（动态规划）：
────────────────────────────────────────────────────────────────────────
关键洞察（状态机模型的推广）：
1. k 笔交易形成 2k 个状态：buy[1] → sell[1] → buy[2] → sell[2] → ... → buy[k] → sell[k]
2. 每个状态可以从"保持不变"或"从上一状态转换"而来
3. 最终答案为 sell[k]（完成最多 k 笔交易后的最大利润）

状态定义：
    buy[j]  = 当前处于"第 j 次买入"状态的最大利润（持有股票）
    sell[j] = 当前处于"第 j 次卖出"状态的最大利润（不持有股票）

状态转移：
    buy[j]  = max(buy[j],  sell[j-1] - price)   保持 / 今天第 j 次买入
    sell[j] = max(sell[j], buy[j]   + price)     保持 / 今天第 j 次卖出

初始条件：
    buy[j]  = -prices[0]  （对所有 j：第1天买入、卖出、再买入...）
    sell[j] = 0            （对所有 j：可以不交易，利润为0）

最终答案：
    sell[k]（最多 k 笔交易的最大利润）
"""


# ══════════════════════════════════════════════════════════
# 解法一：贪心（k ≥ n/2 退化为无限次交易）
# ══════════════════════════════════════════════════════════


def maxProfit_greedy(prices: list[int]) -> int:
    """
    贪心解法（当 k ≥ n/2 时使用）

    核心思路：
    ────────────────────────────────────────────────────────
    n 天内最多完成 ⌊n/2⌋ 笔交易（每笔至少需要买+卖两天），
    所以当 k ≥ n/2 时，交易次数限制形同虚设，
    等价于"无限次交易"，用贪心算法即可。

    贪心策略：只要明天涨，今天就买明天卖。
    等价于累加所有正差价：sum(max(0, prices[i] - prices[i-1]))

    时间复杂度：O(n) — 遍历一次
    空间复杂度：O(1)

    为什么这个优化很重要？
    ────────────────────────────────────────────────────────
    如果不做这个判断，当 k 很大时（比如 k = 10^9），
    DP 数组会过大，时间也会超限。
    而实际上 k ≥ n/2 就已经是无限次了，不需要更大的 k。
    """
    profit = 0
    for i in range(1, len(prices)):
        if prices[i] > prices[i - 1]:
            profit += prices[i] - prices[i - 1]
    return profit


# ══════════════════════════════════════════════════════════
# 解法二：动态规划（二维状态数组）
# ══════════════════════════════════════════════════════════


def maxProfit_dp(k: int, prices: list[int]) -> int:
    """
    动态规划解法（二维状态数组）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    将股票 III 的 4 个固定变量推广为 2k 个数组元素：
    buy[j][i]  = 第 i 天结束时，处于"第 j 次买入"状态的最大利润
    sell[j][i] = 第 i 天结束时，处于"第 j 次卖出"状态的最大利润

    算法步骤：
    1. 先判断 k ≥ n/2 的情况，退化为贪心
    2. 创建 buy[k+1] 和 sell[k+1] 两个数组（1-indexed）
    3. 初始化 buy[j] = -prices[0]，sell[j] = 0
    4. 逐天更新，内层逆序遍历交易次数 j

    时间复杂度：O(n×k) — 双重循环
    空间复杂度：O(n×k) — 二维数组

    图解示例：
    ────────────────────────────────────────────────────────
    k = 2, prices = [3, 2, 6, 5, 0, 3]

    初始化：buy = [_, -3, -3], sell = [_, 0, 0]
                      j=1  j=2       j=1  j=2

    i=0, price=3:
        buy[1]=max(-3, -3)=-3     sell[1]=max(0, -3+3)=0
        buy[2]=max(-3, 0-3)=-3    sell[2]=max(0, -3+3)=0

    i=1, price=2:
        buy[1]=max(-3, -2)=-2     sell[1]=max(0, -3+2)=0
        buy[2]=max(-3, 0-2)=-2    sell[2]=max(0, -3+2)=0

    i=2, price=6:
        buy[1]=max(-2, -6)=-2     sell[1]=max(0, -2+6)=4
        buy[2]=max(-2, 4-6)=-2    sell[2]=max(0, -2+6)=4

    i=3, price=5:
        buy[1]=max(-2, -5)=-2     sell[1]=max(4, -2+5)=4
        buy[2]=max(-2, 4-5)=-1    sell[2]=max(4, -2+5)=4

    i=4, price=0:
        buy[1]=max(-2, 0)=-0      sell[1]=max(4, -2+0)=4
        buy[2]=max(-1, 4-0)=4     sell[2]=max(4, -1+0)=4

    i=5, price=3:
        buy[1]=max(0, -3)=0       sell[1]=max(4, 0+3)=4
        buy[2]=max(4, 4-3)=4      sell[2]=max(4, 4+3)=7 ✓

    结果：sell[2] = 7
    对应：第1笔（第2天买2，第3天卖6，利润4）+ 第2笔（第5天买0，第6天卖3，利润3）= 7
    """
    n = len(prices)
    if n < 2 or k == 0:
        return 0

    if k >= n // 2:
        return maxProfit_greedy(prices)

    # buy[j] = 第 j 次买入后的最大利润，sell[j] = 第 j 次卖出后的最大利润
    buy = [-prices[0]] * (k + 1)
    sell = [0] * (k + 1)

    for i in range(1, n):
        for j in range(k, 0, -1):
            sell[j] = max(sell[j], buy[j] + prices[i])
            buy[j] = max(buy[j], sell[j - 1] - prices[i])

    return sell[k]


# ══════════════════════════════════════════════════════════
# 重要说明：算法的正确性证明和易错点提示
# ══════════════════════════════════════════════════════════
#
# 算法正确性证明：
# ────────────────────────────────────────────────────────
#
# 定理：上述 DP 算法返回的 sell[k] 等于最多完成 k 笔交易的最大利润。
#
# 证明：
# （1）状态完备性：
#     任何一天结束时，投资者必然处于以下 2k+1 种状态之一：
#     - sell[0]：未进行任何交易，持有0股，利润为0
#     - buy[j]：已完成 j-1 笔交易，持有1股（第 j 次买入后未卖出）
#     - sell[j]：已完成 j 笔交易，持有0股（第 j 次卖出后）
#     其中 j ∈ [1, k]。
#     2k+1 种状态覆盖了所有可能。
#
# （2）状态转移的正确性：
#     对于每个状态，当天只有两种选择：保持不变 或 从上一状态转换。
#     - buy[j]: 保持 或 从 sell[j-1] 买入（sell[j-1] - price）
#     - sell[j]: 保持 或 从 buy[j] 卖出（buy[j] + price）
#     每种选择都取最大值，保证了局部最优。
#
# （3）全局最优性（数学归纳法）：
#     基础：第1天的状态由初始条件正确给出。
#     归纳：假设第 i-1 天所有状态都正确，
#           第 i 天的状态转移覆盖了所有选择并取最大值，
#           且无后效性（第 i 天的状态仅依赖第 i-1 天），
#           因此第 i 天的状态也是正确的。
#     综上，第 n-1 天的 sell[k] 就是全局最优解。
#
# （4）"最多 k 笔"的保证：
#     sell[j] ≥ sell[j-1] ≥ ... ≥ sell[1] ≥ sell[0] = 0，
#     因为每笔交易都可以选择不做（保持上一个状态），
#     所以 sell[k] ≥ sell[1] ≥ 0，即使只做1笔或不做交易也是正确的。
#
# （5）k ≥ n/2 退化的正确性：
#     n 天最多完成 ⌊n/2⌋ 笔交易，每笔至少需要1天买+1天卖。
#     当 k ≥ n/2 时，k 的限制不起作用，等价于无限次交易。
#     贪心算法在无限次交易下是最优的（累加所有正差价），
#     因为每次上涨都可以通过"昨天买、今天卖"来捕获。
#
#
# 为什么逆序遍历 j 是正确的？
# ────────────────────────────────────────────────────────
# 滚动数组只有一个副本，更新 sell[j] 时需要用"昨天的 buy[j]"，
# 更新 buy[j] 时需要用"昨天的 sell[j-1]"。
#
# 逆序遍历 j = k, k-1, ..., 1：
#   - sell[j] 使用 buy[j]：buy[j] 还没被更新，是旧值 ✓
#   - buy[j] 使用 sell[j-1]：sell[j-1] 还没被更新，是旧值 ✓
#
# 如果正序遍历 j = 1, 2, ..., k：
#   - buy[1] 先被更新为新值
#   - sell[1] 使用了新的 buy[1]，相当于同一天买入又卖出 ❌
#   - buy[2] 使用了新的 sell[1]，相当于同一天完成第1笔又买入 ❌
#   这就变成了一天之内完成多笔交易，违反了题意。
#
#
# 为什么 sell[0] 恒为 0？
# ────────────────────────────────────────────────────────
# sell[0] 表示"完成了0笔交易后的最大利润"，即从未买入过股票。
# 没有买入就没有卖出，利润恒为0。
# 这个值在递推中作为 buy[1] 的前置状态：
#   buy[1] = max(buy[1], sell[0] - price) = max(buy[1], -price)
# 与股票 III 中 buy1 = max(buy1, -price) 完全一致。
#
#
# 为什么 buy[j] 都初始化为 -prices[0]？
# ────────────────────────────────────────────────────────
# 与股票 III 中 buy2[0] = -prices[0] 同理：
# buy[j][0] = sell[j-1][0] - prices[0] = 0 - prices[0] = -prices[0]
# 表示在第1天完成 j-1 笔"买入即卖出"的零利润交易后，再第 j 次买入。
# 虽然现实中不可能，但数学上保证了递推的连续性。
#
#
# 常见错误：
# ────────────────────────────────────────────────────────
# 1. 忘记处理 k ≥ n/2 的退化情况：
#    当 k 很大时，DP 的 O(n×k) 会超时或超空间。
#    必须先判断 k >= n // 2，退化为贪心 O(n)。
#
# 2. 内层循环正序遍历 j：
#    正序会导致使用"当天已更新"的值，
#    等价于同一天完成多笔交易，结果错误。
#    必须逆序遍历 j = k, k-1, ..., 1。
#
# 3. 初始值设置错误：
#    buy[j] 应初始化为 -prices[0]，不能设为 0 或负无穷。
#    设为 0 相当于"免费买入"，设为负无穷会导致 max 永远取另一个。
#    sell[j] 应初始化为 0，不能设为负无穷（可以不交易）。
#
# 4. 忘记处理边界情况：
#    - prices 为空：返回 0
#    - k = 0：返回 0
#    - prices 长度为 1：无法完成交易，返回 0
#
# 5. 混淆"第 j 次买入"和"第 j 天买入"：
#    buy[j] / sell[j] 的 j 表示第几笔交易，不是第几天。
#
# 6. 数组大小设置为 k 而非 k+1：
#    使用 1-indexed 更直观（buy[1] 表示第1次买入），
#    所以数组长度应为 k+1，sell[0] = 0 作为哨兵。
#    如果用 0-indexed 则需要注意 buy[0] 的特殊性。
#
# 7. 理解误区——"最多 k 笔"vs"恰好 k 笔"：
#    本题是"最多 k 笔"，可以做 0~k 笔。
#    sell[k] ≥ sell[k-1] ≥ ... ≥ sell[0] = 0 保证了这一点。
# ══════════════════════════════════════════════════════════
