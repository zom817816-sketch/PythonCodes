"""
不同路径（Unique Paths）

题目描述：
一个机器人位于一个 m x n 网格的左上角（起始点在下图中标记为 "Start"）。
机器人每次只能向下或者向右移动一步。机器人试图达到网格的右下角（在下图中标记为 "Finish"）。
问总共有多少条不同的路径？

注意：
    - m 和 n 的值均不超过 100

示例 1：
    输入：m = 3, n = 7
    输出：28

示例 2：
    输入：m = 3, n = 2
    输出：3
    解释：
        从左上角开始，总共有 3 条路径可以到达右下角。
        1. 向右 -> 向下 -> 向下
        2. 向下 -> 向下 -> 向右
        3. 向下 -> 向右 -> 向下

示例 3：
    输入：m = 7, n = 3
    输出：28
    解释：交换 m 和 n 的值，结果相同，因为路径具有对称性。

示例 4：
    输入：m = 3, n = 3
    输出：6

解题思路总览：
───────────────────────────────────────────────────────────────────────
解法                          时间复杂度    空间复杂度    说明
───────────────────────────────────────────────────────────────────────
暴力递归                       O(2^(m+n))   O(m+n)      超时
记忆化搜索（Top-Down DP）       O(m × n)     O(m × n)    ⭐⭐
动态规划（Bottom-Up DP）         O(m × n)     O(m × n)    ⭐⭐⭐
动态规划（空间优化）             O(m × n)     O(min(m,n)) ⭐⭐⭐⭐
组合数学                       O(min(m,n))  O(1)        ⭐⭐⭐⭐⭐ 最优
───────────────────────────────────────────────────────────────────────

核心思想（动态规划）：
───────────────────────────────────────────────────────────────────────
关键洞察：
1. 要到达网格中的某个位置 (i, j)，最后一步只有两种可能：
   - 从上方 (i-1, j) 向下移动一步
   - 从左方 (i, j-1) 向右移动一步
2. 因此，到达 (i, j) 的路径数 = 到达 (i-1, j) 的路径数 + 到达 (i, j-1) 的路径数

状态定义：
    dp[i][j] = 到达位置 (i, j) 的不同路径数

状态转移：
    dp[i][j] = dp[i-1][j] + dp[i][j-1]

初始条件：
    - 第一行（i=0）：只能从左边来，所以 dp[0][j] = 1
    - 第一列（j=0）：只能从上面来，所以 dp[i][0] = 1

最终答案：
    dp[m-1][n-1]（到达右下角的路径数）

组合数学视角：
───────────────────────────────────────────────────────────────────────
从左上角到右下角，机器人需要移动 (m-1) 次向下 + (n-1) 次向右，共 (m+n-2) 次移动。
问题转化为：在 (m+n-2) 次移动中选择 (m-1) 次向下（或 (n-1) 次向右）的组合数。

公式：C(m+n-2, m-1) = (m+n-2)! / ((m-1)! × (n-1)!)
"""


# ══════════════════════════════════════════════════════════
# 解法一：暴力递归（Brute Force）
# ══════════════════════════════════════════════════════════


def uniquePaths_brute(m: int, n: int) -> int:
    """
    暴力递归解法（Brute Force）

    核心思路：
    ────────────────────────────────────────────────────────
    直接根据递推关系递归求解：
        paths(i, j) = paths(i-1, j) + paths(i, j-1)
    不做任何缓存，存在大量重复计算。

    算法步骤：
    1. Base Case：i = 0 或 j = 0 时返回 1（只能沿边缘走）
    2. 递归求解：paths(i-1, j) + paths(i, j-1)

    时间复杂度：O(2^(m+n)) — 递归树呈指数级增长
    空间复杂度：O(m+n)   — 递归调用栈的最大深度

    问题分析（以 m=3, n=2 为例）：
    ────────────────────────────────────────────────────────
                        paths(2, 1)
                       /           \
              paths(1, 1)        paths(2, 0)=1
              /        \
        paths(0, 1)=1  paths(1, 0)=1

    paths(1, 0) 被重复计算多次，效率极低。
    这就是为什么需要引入"记忆化"或"动态规划"来消除重复计算。

    注意：这个解法在 m, n > 10 时就会明显变慢，仅用于理解问题。
    """
    def dfs(i: int, j: int) -> int:
        """递归求解到达位置 (i, j) 的路径数"""
        # Base Case：第一行或第一列，只有一条路径
        if i == 0 or j == 0:
            return 1

        # 递归求解：从上方或左方来
        return dfs(i - 1, j) + dfs(i, j - 1)

    return dfs(m - 1, n - 1)


# ══════════════════════════════════════════════════════════
# 解法二：记忆化搜索（Top-Down DP）
# ══════════════════════════════════════════════════════════


def uniquePaths_memo(m: int, n: int) -> int:
    """
    记忆化搜索解法（Top-Down DP）⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    在暴力递归的基础上，增加一个缓存（二维数组），
    记录已经计算过的子问题结果，避免重复计算。

    算法步骤：
    1. 创建缓存 memo，存储已计算的 paths(i, j) 值
    2. 递归时先查缓存：
        a. 如果缓存中有结果，直接返回（命中）
        b. 如果缓存中没有，计算后存入缓存，再返回
    3. Base Case：i = 0 或 j = 0 时返回 1

    时间复杂度：O(m × n) — 每个子问题只计算一次
    空间复杂度：O(m × n) — 缓存 + 递归调用栈

    图解示例：m = 3, n = 2
    ────────────────────────────────────────────────────────
    递归树（带缓存）：
                        paths(2, 1)
                       /           \
              paths(1, 1)        paths(2, 0)=1 ✓
              /        \
        paths(0,1)=1 ✓  paths(1,0)=1 ✓

    ✓ 表示命中缓存，不再递归。

    最终计算顺序：
        paths(0,0) → 1（Base Case）
        paths(0,1) → 1（Base Case）
        paths(1,0) → 1（Base Case）
        paths(1,1) → paths(0,1) + paths(1,0) = 2
        paths(2,1) → paths(1,1) + paths(2,0) = 3

    结果：3 ✅

    状态定义：
    - memo[i][j] = 到达位置 (i, j) 的路径数

    状态转移：
    - memo[i][j] = memo[i-1][j] + memo[i][j-1]
    """
    # 创建缓存（二维数组）
    memo = [[-1 for _ in range(n)] for _ in range(m)]

    def dfs(i: int, j: int) -> int:
        """递归求解到达位置 (i, j) 的路径数"""
        # Base Case：第一行或第一列，只有一条路径
        if i == 0 or j == 0:
            return 1

        # 命中缓存，直接返回
        if memo[i][j] != -1:
            return memo[i][j]

        # 计算并存入缓存
        memo[i][j] = dfs(i - 1, j) + dfs(i, j - 1)
        return memo[i][j]

    return dfs(m - 1, n - 1)


# ══════════════════════════════════════════════════════════
# 解法三：动态规划（Bottom-Up DP）
# ══════════════════════════════════════════════════════════


def uniquePaths_dp(m: int, n: int) -> int:
    """
    动态规划解法（Bottom-Up DP）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    从底向上计算：先初始化第一行和第一列，
    再按行或列依次填充 DP 表。
    不需要递归，用迭代方式填充。

    算法步骤：
    1. 创建 DP 数组 dp，dp[i][j] 表示到达位置 (i, j) 的路径数
    2. 初始化第一行和第一列为 1
    3. 按行填充：dp[i][j] = dp[i-1][j] + dp[i][j-1]
    4. 返回 dp[m-1][n-1]

    时间复杂度：O(m × n) — 一次遍历
    空间复杂度：O(m × n) — DP 数组

    图解示例：m = 3, n = 3
    ────────────────────────────────────────────────────────
            j=0   j=1   j=2
        ────────────────────
    i=0    1     1     1      ← 第一行：只能从左边来
    i=1    1     2     3      ← dp[1][1] = dp[0][1] + dp[1][0] = 2
    i=2    1     3     6      ← dp[2][2] = dp[1][2] + dp[2][1] = 3 + 3 = 6

    结果：6 ✅

    状态定义：
    - dp[i][j] = 到达位置 (i, j) 的路径数

    状态转移：
    - dp[i][j] = dp[i-1][j] + dp[i][j-1]

    初始条件：
    - dp[i][0] = 1（第一列，只能从上面来）
    - dp[0][j] = 1（第一行，只能从左边来）

    最终答案：
    - dp[m-1][n-1]（到达右下角的路径数）
    """
    # 创建 DP 数组
    dp = [[0 for _ in range(n)] for _ in range(m)]

    # 初始化第一列
    for i in range(m):
        dp[i][0] = 1

    # 初始化第一行
    for j in range(n):
        dp[0][j] = 1

    # 从第二行第二列开始填充
    for i in range(1, m):
        for j in range(1, n):
            dp[i][j] = dp[i - 1][j] + dp[i][j - 1]

    return dp[m - 1][n - 1]


# ══════════════════════════════════════════════════════════
# 解法四：动态规划（空间优化版）
# ══════════════════════════════════════════════════════════


def uniquePaths_optimized(m: int, n: int) -> int:
    """
    空间优化的动态规划解法 ⭐⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    观察状态转移方程：dp[i][j] = dp[i-1][j] + dp[i][j-1]
    计算第 i 行时，只需要第 i-1 行的数据和当前行已经计算的数据。
    可以用一维数组代替二维数组，空间复杂度降为 O(min(m, n))。

    算法步骤：
    1. 创建一维 DP 数组 dp，长度为 min(m, n)
    2. 初始化 dp[0] = 1（起点）
    3. 外层循环遍历行（或列），内层循环更新 dp[j] = dp[j] + dp[j-1]
    4. 返回 dp[-1]

    时间复杂度：O(m × n) — 一次遍历
    空间复杂度：O(min(m, n)) — 一维 DP 数组

    为什么可以用一维数组？
    ────────────────────────────────────────────────────────
    在计算 dp[i][j] 时：
        - dp[j]（更新前）= dp[i-1][j]（上一行的值）
        - dp[j-1]（更新后）= dp[i][j-1]（当前行左边的值）
    所以：dp[j] = dp[j] + dp[j-1]

    图解示例：m = 3, n = 3（用一维数组）
    ────────────────────────────────────────────────────────
    初始：dp = [1, 1, 1]（第一行）

    i=1（第二行）：
        j=1: dp[1] = dp[1] + dp[0] = 1 + 1 = 2 → dp = [1, 2, 1]
        j=2: dp[2] = dp[2] + dp[1] = 1 + 2 = 3 → dp = [1, 2, 3]

    i=2（第三行）：
        j=1: dp[1] = dp[1] + dp[0] = 2 + 1 = 3 → dp = [1, 3, 3]
        j=2: dp[2] = dp[2] + dp[1] = 3 + 3 = 6 → dp = [1, 3, 6]

    结果：6 ✅

    为了进一步优化空间，可以选择较短的维度作为数组长度。
    """
    # 优化：选择较短的维度作为数组长度
    if m < n:
        return uniquePaths_optimized(n, m)

    # 创建一维 DP 数组
    dp = [1] * n

    # 外层循环遍历行（从第二行开始）
    for i in range(1, m):
        # 内层循环遍历列（从第二列开始）
        for j in range(1, n):
            # dp[j] = 上一行的值 + 当前行左边的值
            dp[j] = dp[j] + dp[j - 1]

    return dp[-1]


# ══════════════════════════════════════════════════════════
# 解法五：组合数学（推荐解）
# ══════════════════════════════════════════════════════════


def uniquePaths(m: int, n: int) -> int:
    """
    组合数学解法 ⭐⭐⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    从左上角到右下角，机器人需要移动 (m-1) 次向下 + (n-1) 次向右，
    共 (m+n-2) 次移动。问题转化为：在 (m+n-2) 次移动中选择 (m-1) 次向下
    （或 (n-1) 次向右）的组合数。

    公式：C(m+n-2, m-1) = (m+n-2)! / ((m-1)! × (n-1)!)

    为了避免计算大数阶乘，我们可以逐步计算：
        C(n, k) = C(n, k-1) × (n-k+1) / k

    算法步骤：
    1. 计算 k = min(m-1, n-1)，减少计算次数
    2. 初始化 result = 1
    3. 循环 k 次，逐步计算组合数
    4. 返回 result

    时间复杂度：O(min(m, n)) — 循环 min(m-1, n-1) 次
    空间复杂度：O(1) — 只用了几个变量

    图解示例：m = 3, n = 3
    ────────────────────────────────────────────────────────
    需要移动：(3-1) + (3-1) = 4 次
    需要选择：min(2, 2) = 2 次向下（或向右）
    C(4, 2) = 4! / (2! × 2!) = 6

    结果：6 ✅

    图解示例：m = 3, n = 7
    ────────────────────────────────────────────────────────
    需要移动：(3-1) + (7-1) = 8 次
    需要选择：min(2, 6) = 2 次向下
    C(8, 2) = 8 × 7 / (2 × 1) = 28

    结果：28 ✅

    为什么这个公式正确？
    ────────────────────────────────────────────────────────
    从 (0,0) 到 (m-1, n-1)，必须向下走 (m-1) 步，向右走 (n-1) 步。
    总共有 (m+n-2) 步，我们需要从中选择 (m-1) 步向下走（剩下的自然向右走）。
    这就是组合数 C(m+n-2, m-1) 的含义。
    """
    # 计算 k = min(m-1, n-1)，减少计算次数
    k = min(m - 1, n - 1)
    total_steps = m + n - 2

    # 计算组合数 C(total_steps, k)
    result = 1
    for i in range(k):
        # C(n, k) = C(n, k-1) × (n-k+1) / k
        result = result * (total_steps - i) // (i + 1)

    return result


# ══════════════════════════════════════════════════════════
# 重要说明：算法的正确性证明
# ══════════════════════════════════════════════════════════
#
# 为什么动态规划解法是正确的？
# ────────────────────────────────────────────────────────
# 1. 最优子结构（Optimal Substructure）：
#    到达位置 (i, j) 的最后一步只有两种可能：
#        a) 从上方 (i-1, j) 向下移动一步
#        b) 从左方 (i, j-1) 向右移动一步
#    这两种情况互斥且完备，所以：
#        dp[i][j] = dp[i-1][j] + dp[i][j-1]
#
# 2. 重叠子问题（Overlapping Subproblems）：
#    在暴力递归中，很多子问题会被重复计算，
#    比如到达 (1,1) 的路径数会被 (2,1) 和 (1,2) 都用到。
#    用缓存或 DP 数组可以消除重复计算。
#
# 3. 无后效性（No Aftereffect）：
#    计算 dp[i][j] 时，只需要知道 dp[i-1][j] 和 dp[i][j-1]，
#    不需要知道是如何到达这些位置的。
#    这保证了状态转移的正确性。
#
# 为什么组合数学解法是正确的？
# ────────────────────────────────────────────────────────
# 从 (0,0) 到 (m-1, n-1)，必须恰好走 (m-1) 步向下和 (n-1) 步向右。
# 问题等价于：在 (m+n-2) 个位置中选择 (m-1) 个位置放向下箭头，
# 其余位置放向右箭头。这正是组合数的定义。
#
# 为什么空间优化是正确的？
# ────────────────────────────────────────────────────────
# 因为计算第 i 行时，只需要第 i-1 行的数据。
# 用一维数组时：
#    - dp[j]（更新前）= 上一行第 j 列的值
#    - dp[j-1]（更新后）= 当前行第 j-1 列的值
# 所以可以用 dp[j] = dp[j] + dp[j-1] 来更新。
# ══════════════════════════════════════════════════════════