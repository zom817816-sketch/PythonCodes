# 解法三：贪心算法（差分思想）
# ══════════════════════════════════════════════════════════

def canCompleteCircuit_diff(gas: List[int], cost: List[int]) -> int:
    """
    贪心算法（差分思想）⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    与解法一本质相同，但换一种表述方式：
    1. 计算每个站点的净油量 diff[i] = gas[i] - cost[i]
    2. 如果 sum(diff) < 0，无解
    3. 否则，找一个起点使得从该点开始的任意前缀和都非负

    差分思想的优势：
    ────────────────────────────────────────────────────────
    - 把"加油-耗油"抽象为"净油量"，更清晰
    - 直观理解：净油量为正表示该站点"产出"油，为负表示"消耗"油
    - 我们需要找一个起点，使得从该点开始的累计净油量始终非负

    图解示例：gas = [1,2,3,4,5], cost = [3,4,5,1,2]
    ────────────────────────────────────────────────────────
    diff = gas - cost = [-2, -2, -2, 3, 3]

    尝试起点 3：
        站点3: diff=3, 累计=3 >= 0 ✅
        站点4: diff=3, 累计=6 >= 0 ✅
        站点0: diff=-2, 累计=4 >= 0 ✅
        站点1: diff=-2, 累计=2 >= 0 ✅
        站点2: diff=-2, 累计=0 >= 0 ✅
        完成一圈 ✅

    时间复杂度：O(n)
    空间复杂度：O(1)
    """
    n = len(gas)

    # 检查总油量
    if sum(gas) < sum(cost):
        return -1

    tank = 0
    start = 0

    for i in range(n):
        tank += gas[i] - cost[i]

        if tank < 0:
            start = i + 1
            tank = 0

    return start
