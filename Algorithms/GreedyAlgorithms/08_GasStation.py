"""
加油站（Gas Station）

题目描述：
在一条环路上有 n 个加油站，其中第 i 个加油站有汽油 gas[i] 升。
你有一辆油箱容量无限的汽车，从第 i 个加油站开往第 i+1 个加油站需要消耗汽油 cost[i] 升。
你从其中的一个加油站出发，开始时油箱为空。
给定两个整数数组 gas 和 cost，如果你可以按顺序绕环路行驶一周，则返回出发时加油站的编号，否则返回 -1。
如果存在解，则保证它是唯一的。

示例 1：
    输入：gas = [1,2,3,4,5], cost = [3,4,5,1,2]
    输出：3
    解释：
        从 3 号加油站出发，获得 4 升汽油，油箱 = 4
        开往 4 号加油站，消耗 1 升，获得 5 升，油箱 = 8
        开往 0 号加油站，消耗 2 升，获得 1 升，油箱 = 7
        开往 1 号加油站，消耗 3 升，获得 2 升，油箱 = 6
        开往 2 号加油站，消耗 4 升，获得 3 升，油箱 = 5
        开往 3 号加油站，消耗 5 升，油箱 = 0，刚好回到起点

示例 2：
    输入：gas = [2,3,4], cost = [3,4,3]
    输出：-1
    解释：无论从哪个加油站出发，都无法绕环路行驶一周

解题思路总览：
───────────────────────────────────────────────────────────────────────
解法                      时间复杂度    空间复杂度    说明
───────────────────────────────────────────────────────────────────────
贪心算法（一次遍历）       O(n)          O(1)        ⭐ 最优解法
暴力枚举                  O(n²)         O(1)        直观但超时
贪心算法（差分数组）       O(n)          O(1)        另一种贪心思路
───────────────────────────────────────────────────────────────────────

核心思想：
───────────────────────────────────────────────────────────────────────
1. 如果总汽油量 < 总消耗量，肯定无法完成一圈，返回 -1
2. 如果总汽油量 >= 总消耗量，一定有解
3. 使用贪心策略：从起点开始，如果油箱变成负数，说明起点不对，
   尝试从下一个加油站开始

为什么贪心是对的？
───────────────────────────────────────────────────────────────────────
假设从站点 A 出发，到站点 B 时油箱变负了。
这意味着从 A 到 B 的任意中间站点出发，都无法到达 B。
因为如果从中间站点 C 出发能到达 B，那么从 A 到 C 的油量是正的，
从 C 到 B 的油量也是正的，加起来应该也是正的，矛盾。
所以我们可以直接跳过 A 到 B 之间的所有站点，从 B+1 重新开始。
"""

from typing import List


# ══════════════════════════════════════════════════════════
# 解法一：贪心算法（一次遍历，推荐解）
# ══════════════════════════════════════════════════════════

def canCompleteCircuit_greedy(gas: List[int], cost: List[int]) -> int:
    """
    贪心算法（一次遍历）⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    1. 如果总油量 < 总消耗，不可能完成，返回 -1
    2. 从站点 0 开始，维护当前油箱油量 current_tank
    3. 如果 current_tank < 0，说明从之前的起点无法到达当前站点
       → 重置起点为下一个站点，当前油量归零
    4. 遍历结束后，最后记录的起点就是答案

    关键洞察：
    ────────────────────────────────────────────────────────
    - 当油箱变负时，说明从起点到当前站点的净油量为负
    - 由于是环形，中间任何站点出发都到不了当前站点
    - 所以直接跳过，从下一个站点重新开始
    - 只要总油量够，最后记录的起点一定有效

    图解示例：gas = [1,2,3,4,5], cost = [3,4,5,1,2]
    ────────────────────────────────────────────────────────
    每个站点的净油量（gas[i] - cost[i]）：
        站点:  0    1    2    3    4
        净量: -2   -2   -2    3    3
        总和:  0（>= 0，有解）

    遍历过程：
    ────────────────────────────────────────────────────────
        i   gas[i]  cost[i]  current_tank   start   操作
    ────────────────────────────────────────────────────────
        0     1       3        -2           0     tank<0, 重置起点=1, tank=0
        1     2       4        -2           1     tank<0, 重置起点=2, tank=0
        2     3       5        -2           2     tank<0, 重置起点=3, tank=0
        3     4       1         3           3     tank>=0, 继续
        4     5       2         6           3     tank>=0, 继续
    ────────────────────────────────────────────────────────
        最终 start = 3，返回 3 ✅

    验证：从站点 3 出发
        站点3: 加4油，耗1油，剩3
        站点4: 加5油，耗2油，剩6
        站点0: 加1油，耗3油，剩4
        站点1: 加2油，耗4油，剩2
        站点2: 加3油，耗5油，剩0
        站点3: 回到起点 ✅

    图解示例：gas = [2,3,4], cost = [3,4,3]
    ────────────────────────────────────────────────────────
    每个站点的净油量（gas[i] - cost[i]）：
        站点:  0    1    2
        净量: -1   -1    1
        总和: -1（< 0，无解）

    遍历过程：
    ────────────────────────────────────────────────────────
        i   gas[i]  cost[i]  current_tank   start   操作
    ────────────────────────────────────────────────────────
        0     2       3        -1           0     tank<0, 重置起点=1, tank=0
        1     3       4        -1           1     tank<0, 重置起点=2, tank=0
        2     4       3         1           2     tank>=0, 继续
    ────────────────────────────────────────────────────────
        total_tank = -1 < 0，返回 -1 ✅

    时间复杂度：O(n) — 只遍历一次
    空间复杂度：O(1) — 只用了几个变量
    """
    n = len(gas)

    # 第一步：检查总油量是否足够
    if sum(gas) < sum(cost):
        return -1

    # 第二步：贪心遍历
    current_tank = 0  # 当前油箱油量
    start = 0  # 起始加油站

    for i in range(n):
        # 在当前站点加油，然后开往下一站
        current_tank += gas[i] - cost[i]

        # 如果油箱变负，说明从 start 到 i 都无法完成
        if current_tank < 0:
            # 重置起点为下一个站点
            start = i + 1
            # 油箱归零（从新起点开始）
            current_tank = 0

    return start


# ══════════════════════════════════════════════════════════
# 重要说明：为什么只需要遍历到站点 n-1？
# ══════════════════════════════════════════════════════════
#
# 问题：代码只遍历了站点 0 到站点 4，为什么就能确定起点？
# 不是需要验证从站点 4 返回站点 3 吗？
#
# 解答：
# ────────────────────────────────────────────────────────
# 关键在于两个条件的组合：
#
# 条件1（循环前）：sum(gas) >= sum(cost)
#   含义：总油量 >= 总消耗量，保证整个环路的净油量 >= 0
#
# 条件2（循环中）：从候选起点到终点的任何中间段油箱非负
#   含义：贪心遍历保证了从 start 到站点 n-1 的路径上油量够
#
# 为什么不需要再检查从站点 4 返回站点 3？
# ────────────────────────────────────────────────────────
# 用数学证明：
#
#   整个环路净油量 = sum(gas) - sum(cost) = 15 - 15 = 0
#
#   从站点 3（起点）到站点 4（终点）的净油量 = current_tank = 6
#
#   从站点 4（终点）返回站点 3（起点）的净油量
#   = 整个环路净油量 - 从站点 3 到站点 4 的净油量
#   = 0 - 6
#   = -6
#
#   所以从站点 4 回到站点 3 需要消耗 6 升油，
#   而我们在站点 4 结束时油箱正好有 6 升油，刚好够用！
#
# ────────────────────────────────────────────────────────
# 证明梗概（数学归纳/前缀和法）：
# 设 delta[i] = gas[i] - cost[i]。
# 定义一个数组 prefix[k] = delta[0] + delta[1] + ... + delta[k]（0≤k<n）。
# 我们找 prefix 的最小值点（或最后一个最小值点）的下一个位置作为起点。
# 在这个起点之后的所有前缀（绕回到起点之前的那一段）都不会变成负数，因为环上的总和 ≥ 0，所以后半段能补偿前半段的亏空。
# 算法中 current 就是当前试探起点的前缀和，一旦它<0就重置起点，最终 start 就是那个能保证全程非负的起点。
# 因此不必再走一圈，因为数学上已经保证了从 start 出发全程油量不会低于0。
# ────────────────────────────────────────────────────────
#   不需要再循环回来的原因：
#   1. 总油量检查保证了"整圈油量够"
#   2. 贪心遍历保证了"从起点到终点的路径上油量够"
#   3. 两者结合，自然保证了"从终点返回起点的油量也够"
# ══════════════════════════════════════════════════════════


# ══════════════════════════════════════════════════════════
# 解法二：暴力枚举（超时，仅用于理解）
# ══════════════════════════════════════════════════════════

def canCompleteCircuit_brute_force(gas: List[int], cost: List[int]) -> int:
    """
    暴力枚举 ⭐

    核心思路：
    ────────────────────────────────────────────────────────
    枚举每个站点作为起点，尝试能否绕一圈。
    如果某个起点能完成一圈，返回该起点。
    如果所有起点都失败，返回 -1。

    图解示例：gas = [1,2,3,4,5], cost = [3,4,5,1,2]
    ────────────────────────────────────────────────────────
    从站点 0 开始尝试：
        加油1，耗油3 → 剩-2 < 0，失败

    从站点 1 开始尝试：
        加油2，耗油4 → 剩-2 < 0，失败

    从站点 2 开始尝试：
        加油3，耗油5 → 剩-2 < 0，失败

    从站点 3 开始尝试：
        加油4，耗油1 → 剩3
        加油5，耗油2 → 剩6
        加油1，耗油3 → 剩4
        加油2，耗油4 → 剩2
        加油3，耗油5 → 剩0，完成一圈 ✅

    返回 3

    时间复杂度：O(n²) — 最坏情况下需要尝试每个起点
    空间复杂度：O(1)
    """
    n = len(gas)

    # 枚举每个站点作为起点
    for start in range(n):
        tank = 0
        # 尝试绕一圈
        for step in range(n):
            idx = (start + step) % n  # 环形索引
            tank += gas[idx] - cost[idx]
            if tank < 0:
                # 这个起点不行，跳出内层循环
                break
        else:
            # 完成了一整圈，返回起点
            return start

    # 没有任何起点能完成一圈
    return -1


