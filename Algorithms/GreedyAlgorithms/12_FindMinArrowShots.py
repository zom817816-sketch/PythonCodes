"""
用最少数量的箭引爆气球（Minimum Number of Arrows to Burst Balloons）

题目描述：
有一些球形气球贴在一堵用 XY 平面表示的墙面上。墙面上的气球记录在整数数组 points，
其中 points[i] = [xstart, xend] 表示水平直径在 xstart 和 xend 之间的气球。
你不知道气球的确切 y 坐标。

一支弓箭可以沿着 x 轴从不同点 完全垂直 地射出。在坐标 x 处射出一支箭，
若有一个气球的直径的开始和结束坐标为 xstart, xend，且满足 xstart ≤ x ≤ xend，
则该气球会被 引爆。可以射出的弓箭的数量 没有限制。
弓箭一旦被射出之后，可以无限地前进。

给你一个数组 points，返回引爆所有气球所必须射出的 最小 弓箭数。

示例 1：
    输入：points = [[10,16],[2,8],[1,6],[7,12]]
    输出：2
    解释：气球可以用 2 支箭来爆破：
        - 在 x = 6 处射出箭，击破气球 [2,8] 和 [1,6]
        - 在 x = 11 处发射箭，击破气球 [10,16] 和 [7,12]

示例 2：
    输入：points = [[1,2],[3,4],[5,6],[7,8]]
    输出：4
    解释：每个气球需要射出一支箭，总共需要 4 支箭

示例 3：
    输入：points = [[1,2],[2,3],[3,4],[4,5]]
    输出：2
    解释：气球可以用 2 支箭来爆破：
        - 在 x = 2 处发射箭，击破气球 [1,2] 和 [2,3]
        - 在 x = 4 处射出箭，击破气球 [3,4] 和 [4,5]

解题思路总览：
───────────────────────────────────────────────────────────────────────
解法                          时间复杂度    空间复杂度    说明
───────────────────────────────────────────────────────────────────────
贪心（先排序后贪心）             O(n log n)    O(1)        ⭐ 最优解法
───────────────────────────────────────────────────────────────────────

核心思想（贪心 + 排序）：
───────────────────────────────────────────────────────────────────────
关键洞察：
1. 将气球按结束坐标 xend 排序，然后贪心地选择射箭位置
2. 每支箭尽可能射中更多气球，即穿透重叠区间
3. 当遇到不重叠的气球时，必须射出新的一支箭

为什么贪心是正确的？
───────────────────────────────────────────────────────────────────────
1. 排序后第一个气球的 xend 是最左边的结束点
   - 在 xend 处射箭可以覆盖所有与它重叠的气球
   - 如果不在 xend 处射箭而选择更靠左，可能漏掉右边重叠的气球
   - 如果选择更靠右，可能左边的气球已经结束

2. 每次射箭后，移除被射中的气球
   - 剩余的第一个气球又是新的"最左边结束点"
   - 重复上述过程

3. 按 xend 排序（而不是 xstart）的原因：
   - 我们需要找到重叠区间的"最右可行射箭位置"
   - 按 xend 排序后，每次都能射中最右边还没被射的气球
"""

from typing import List

# ══════════════════════════════════════════════════════════
# 解法一：贪心（推荐解）— 按结束坐标排序
# ══════════════════════════════════════════════════════════


def findMinArrowShots(points: List[List[int]]) -> int:
    """
    贪心 — 按结束坐标排序 ⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    1. 将所有气球按结束坐标 xend 升序排序
    2. 从第一个气球的 xend 处射箭（此时它一定被射中）
    3. 向后遍历，跳过所有 xstart ≤ 当前箭位置 的气球（它们都被射中了）
    4. 当遇到 xstart > 当前箭位置 的气球时，说明需要新箭
    5. 在新气球的 xend 处射箭，重复步骤 3-4

    图解示例：points = [[10,16],[2,8],[1,6],[7,12]]
    ────────────────────────────────────────────────────────
    排序后（按 xend 升序）：
        [[1,6],[2,8],[7,12],[10,16]]
                 ↑ 按 xend 排序

    射箭过程：
        ────────────────────────────────────────────────────
        第 1 支箭：射在 x = 6
            [1,6]  → 1 ≤ 6 ≤ 6 ✓ 射中
            [2,8]  → 2 ≤ 6 ≤ 8 ✓ 射中
            [7,12] → 7 > 6 ✗ 没射中，需要新箭

        第 2 支箭：射在 x = 12
            [7,12]  → 7 ≤ 12 ≤ 12 ✓ 射中
            [10,16] → 10 ≤ 12 ≤ 16 ✓ 射中
        ────────────────────────────────────────────────────
        最终：需要 2 支箭 ✅

    图解示例：points = [[1,2],[2,3],[3,4],[4,5]]
    ────────────────────────────────────────────────────────
    排序后（按 xend 升序）：
        [[1,2],[2,3],[3,4],[4,5]]

    射箭过程：
        ────────────────────────────────────────────────────
        第 1 支箭：射在 x = 2
            [1,2] → 1 ≤ 2 ≤ 2 ✓ 射中
            [2,3] → 2 ≤ 2 ≤ 3 ✓ 射中
            [3,4] → 3 > 2 ✗ 没射中，需要新箭

        第 2 支箭：射在 x = 4
            [3,4] → 3 ≤ 4 ≤ 4 ✓ 射中
            [4,5] → 4 ≤ 4 ≤ 5 ✓ 射中
        ────────────────────────────────────────────────────
        最终：需要 2 支箭 ✅

    图解示例：points = [[1,2],[3,4],[5,6],[7,8]]
    ────────────────────────────────────────────────────────
    排序后（按 xend 升序）：
        [[1,2],[3,4],[5,6],[7,8]]

    射箭过程：
        ────────────────────────────────────────────────────
        第 1 支箭：射在 x = 2 → [1,2] ✓，[3,4]: 3 > 2 ✗
        第 2 支箭：射在 x = 4 → [3,4] ✓，[5,6]: 5 > 4 ✗
        第 3 支箭：射在 x = 6 → [5,6] ✓，[7,8]: 7 > 6 ✗
        第 4 支箭：射在 x = 8 → [7,8] ✓
        ────────────────────────────────────────────────────
        最终：需要 4 支箭 ✅（完全不重叠）

    时间复杂度：O(n log n) — 排序 O(n log n) + 遍历 O(n)
    空间复杂度：O(1) — 仅使用常数空间
    """
    if not points:
        return 0

    # 按结束坐标 xend 升序排序
    points.sort(key=lambda x: x[1])

    arrows = 1  # 至少需要一支箭
    arrow_pos = points[0][1]  # 第一支箭射在第一个气球的 xend 处

    for i in range(1, len(points)):
        if points[i][0] > arrow_pos:
            # 当前气球与上一支箭射击范围不重叠，需要新箭
            arrows += 1
            arrow_pos = points[i][1]  # 新箭射在当前气球的 xend 处

    return arrows


# ══════════════════════════════════════════════════════════
# 解法二：贪心 — 按开始坐标排序（不改变原数组）
# ══════════════════════════════════════════════════════════

# 重要说明：为什么也可以按 xstart 排序？
# ────────────────────────────────────────────────────────
# 按 xstart 排序的核心思想是维护一个"当前箭的射击区间右边界"，
# 每遇到一个气球，更新这个右边界为当前重叠区间的最小 xend。
#
# 两种排序方式的区别：
# ────────────────────────────────────────────────────────
# 按 xend 排序：     每次射箭位置固定为当前气球的 xend
# 按 xstart 排序：    需要维护一个动态的右边界（取最小 xend）
#
# 按 xstart 排序的优点：可以同时提供"不改变原数组"的版本
# （因为不需要原地修改数组元素，只需维护一个变量）


def findMinArrowShots_sort_by_start(points: List[List[int]]) -> int:
    """
    贪心 — 按开始坐标排序 ⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    1. 将所有气球按开始坐标 xstart 升序排序
    2. 维护一个"当前箭的射击区间右边界" current_border
    3. 如果当前气球的 xstart ≤ current_border，说明重叠
       - 更新 current_border = min(current_border, 当前气球的 xend)
       - 这是因为重叠区间的右边界取最小值，才能射中所有重叠气球
    4. 如果当前气球的 xstart > current_border，说明不重叠
       - 需要新箭，更新 current_border = 当前气球的 xend

    图解对比：为什么 xstart > current_border 时更新为 xend？
    ────────────────────────────────────────────────────────
    假设按 xstart 排序后：[[1,6],[2,8],[7,12],[10,16]]
        ────────────────────────────────────────────────
        第 1 支箭：
            current_border = 6（第一个气球 [1,6] 的 xend）
            [2,8]：2 ≤ 6，重叠 → border = min(6,8) = 6
            [7,12]：7 > 6，不重叠 → 需要新箭
            current_border = 12

        第 2 支箭：
            [10,16]：10 ≤ 12，重叠 → border = min(12,16) = 12
        ────────────────────────────────────────────────
        最终：需要 2 支箭 ✅

    时间复杂度：O(n log n) — 排序 O(n log n) + 遍历 O(n)
    空间复杂度：O(1) — 仅使用常数空间

    注意：此版本会改变原数组（排序时修改了 points）
    """
    if not points:
        return 0

    # 按开始坐标 xstart 升序排序
    points.sort(key=lambda x: x[0])

    arrows = 1
    current_border = points[0][1]  # 当前箭的射击区间右边界

    for i in range(1, len(points)):
        if points[i][0] > current_border:
            # 不重叠，需要新箭
            arrows += 1
            current_border = points[i][1]  # 新箭的右边界
        else:
            # 重叠，缩紧右边界（取最小的 xend）
            current_border = min(current_border, points[i][1])

    return arrows


# ══════════════════════════════════════════════════════════
# 重要说明：为什么贪心算法是正确的？
# ══════════════════════════════════════════════════════════
#
# 问题：为什么先按 xend 排序，然后在 xend 处射箭是最优的？
#
# 解答：
# ────────────────────────────────────────────────────────
# 使用反证法：
#
# 假设有一个最优解中，第一支箭没有射在第一个气球的 xend 处，
# 而是射在了 x' < xend 的位置。
#
# 情况 1：x' 仍然在第一个气球的范围内（xstart ≤ x' ≤ xend）
#   - 由于 x' < xend，x' 可能会放过一些 xstart ≤ xend 但 xstart > x' 的气球
#   - 比如 [1,6] 和 [5,7]，xend=6，如果射在 x'=4
#     则 [5,7] 无法被射中（5 > 4）
#   - 但如果射在 x=6，[5,7] 就可以被射中（5 ≤ 6 ≤ 7）
#   - 所以射在 x' 不一定比射在 xend 更好
#
# 情况 2：x' < xstart（超出第一个气球范围）
#   - 这样第一支箭无法射中第一个气球，浪费了一支箭
#   - 显然不是最优解
#
# 结论：射在第一个气球的 xend 处不会比任何其他位置差。
# 贪心选择不会影响后续的选择（最优子结构）。
# ══════════════════════════════════════════════════════════
#
#
# 问题：为什么按 xstart 排序时，需要维护最小 xend 作为右边界？
#
# 解答：
# ────────────────────────────────────────────────────────
# 按 xstart 排序后，气球按开始坐标从左到右排列。
# 但结束坐标是无序的，可能出现这种情况：
#
#   [[1,10], [2,5], [6,7]]
#
# 在 x=5 处射箭可以射中 [1,10] 和 [2,5]
# 但 [6,7] 需要另一支箭
#
# 如果在处理 [1,10] 时把 border 设为 10，处理 [2,5] 时
# 发现重叠，就需要把 border 缩小到 min(10,5)=5
# 这样在处理 [6,7] 时，6 > 5，才知道需要新箭
#
# 如果不缩小 border，border=10，6 < 10，会错误地认为
# [6,7] 也被射中了，但实际上在 x=5 到 x=10 之间，
# 在 x=6 处并没有箭射出（我们只在 x=10 处射了箭）
# 而 [6,7] 的 xend=7 < 10，所以 [6,7] 在 x=10 时已经结束了！
#
# 所以说：重叠区间的射箭位置必须在所有重叠气球的 xend 的最小值处。
# ══════════════════════════════════════════════════════════
