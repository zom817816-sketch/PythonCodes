"""
根据身高重建队列（Queue Reconstruction by Height）

题目描述：
假设有打乱顺序的一群人站成一个队列，数组 people 表示队列中一些人的属性（不一定按顺序）。
每个 people[i] = [hi, ki] 表示第 i 个人的身高为 hi，前面正好有 ki 个身高大于或等于 hi 的人。
请你重新构造并返回输入数组 people 所表示的队列。
返回的队列应该格式化为数组 queue，其中 queue[j] = [hj, kj] 是队列中第 j 个人的属性（queue[0] 是排在队列前面的人）。

示例 1：
    输入：people = [[7,0],[4,4],[7,1],[5,0],[6,1],[5,2]]
    输出：[[5,0],[7,0],[5,2],[6,1],[4,4],[7,1]]
    解释：
        编号为 0 的人身高为 5，没有身高更高或者相同的人排在他前面。
        编号为 1 的人身高为 7，没有身高更高或者相同的人排在他前面。
        编号为 2 的人身高为 5，有 2 个身高更高或者相同的人排在他前面，即编号为 0 和 1 的人。
        编号为 3 的人身高为 6，有 1 个身高更高或者相同的人排在他前面，即编号为 1 的人。
        编号为 4 的人身高为 4，有 4 个身高更高或者相同的人排在他前面，即编号为 0、1、2、3 的人。
        编号为 5 的人身高为 7，有 1 个身高更高或者相同的人排在他前面，即编号为 1 的人。
        因此 [[5,0],[7,0],[5,2],[6,1],[4,4],[7,1]] 是重新构造后的队列。

示例 2：
    输入：people = [[6,0],[5,0],[4,0],[3,2],[2,2],[1,4]]
    输出：[[4,0],[5,0],[2,2],[3,2],[1,4],[6,0]]

解题思路总览：
───────────────────────────────────────────────────────────────────────
解法                      时间复杂度    空间复杂度    说明
───────────────────────────────────────────────────────────────────────
贪心 + 插入               O(n²)         O(n)        ⭐ 最优解法
贪心 + 链表               O(n²)         O(n)        思路相同，用链表优化插入
暴力回溯                  O(n!)         O(n)        超时，仅用于理解
───────────────────────────────────────────────────────────────────────

核心思想（贪心 + 插入）：
───────────────────────────────────────────────────────────────────────
关键洞察：
1. 先处理高个子，再处理矮个子
2. 高个子不会被矮个子影响（矮个子不影响高个子的 ki 计数）
3. 矮个子插入时，前面的人一定都 >= 自己的身高

排序策略：
    - 第一关键字：身高 h 降序（高个子优先）
    - 第二关键字：k 值升序（k 小的排前面）

插入策略：
    - 每个人插入到结果队列的第 k 个位置
    - 因为前面已经排好的人都 >= 自己的身高
    - 所以插入到第 k 位，前面正好有 k 个 >= 自己的人

为什么贪心是正确的？
───────────────────────────────────────────────────────────────────────
1. 身高相同的人之间：
   - 排序时 k 小的在前，插入时 k 小的先占位
   - 后插入的同身高的人，前面已经有 k 小的同身高的人
   - 不会影响彼此的计数

2. 高个子先处理：
   - 高个子之间互相调整位置（按 k 值）
   - 矮个子插入时，不会改变高个子的相对位置
   - 因为矮个子 < 高个子，不影响高个子的 ki 计数

3. 矮个子后处理：
   - 插入到第 k 位，前面已经排好的人都 >= 自己
   - 正好满足 ki 的要求

图解示例：people = [[7,0],[4,4],[7,1],[5,0],[6,1],[5,2]]
───────────────────────────────────────────────────────────────────────
第一步：排序（h 降序，k 升序）
    原始：[[7,0],[4,4],[7,1],[5,0],[6,1],[5,2]]
    排序后：[[7,0],[7,1],[6,1],[5,0],[5,2],[4,4]]

第二步：逐个插入
    ────────────────────────────────────────────────────────
    插入 [7,0]：k=0，插入位置 0
        结果：[[7,0]]

    插入 [7,1]：k=1，插入位置 1
        结果：[[7,0],[7,1]]

    插入 [6,1]：k=1，插入位置 1
        结果：[[7,0],[6,1],[7,1]]
              前面 1 个人 [7,0] 身高 7 >= 6 ✅

    插入 [5,0]：k=0，插入位置 0
        结果：[[5,0],[7,0],[6,1],[7,1]]
              前面 0 个人 ✅

    插入 [5,2]：k=2，插入位置 2
        结果：[[5,0],[7,0],[5,2],[6,1],[7,1]]
              前面 2 个人 [5,0],[7,0] 身高 5,7 >= 5 ✅

    插入 [4,4]：k=4，插入位置 4
        结果：[[5,0],[7,0],[5,2],[6,1],[4,4],[7,1]]
              前面 4 个人 [5,0],[7,0],[5,2],[6,1] 身高 5,7,5,6 >= 4 ✅
    ────────────────────────────────────────────────────────
    最终结果：[[5,0],[7,0],[5,2],[6,1],[4,4],[7,1]] ✅

图解示例：people = [[6,0],[5,0],[4,0],[3,2],[2,2],[1,4]]
───────────────────────────────────────────────────────────────────────
第一步：排序（h 降序，k 升序）
    排序后：[[6,0],[5,0],[4,0],[3,2],[2,2],[1,4]]

第二步：逐个插入
    ────────────────────────────────────────────────────────
    插入 [6,0]：k=0 → 位置 0
        结果：[[6,0]]

    插入 [5,0]：k=0 → 位置 0
        结果：[[5,0],[6,0]]

    插入 [4,0]：k=0 → 位置 0
        结果：[[4,0],[5,0],[6,0]]

    插入 [3,2]：k=2 → 位置 2
        结果：[[4,0],[5,0],[3,2],[6,0]]

    插入 [2,2]：k=2 → 位置 2
        结果：[[4,0],[5,0],[2,2],[3,2],[6,0]]

    插入 [1,4]：k=4 → 位置 4
        结果：[[4,0],[5,0],[2,2],[3,2],[1,4],[6,0]]
    ────────────────────────────────────────────────────────
    最终结果：[[4,0],[5,0],[2,2],[3,2],[1,4],[6,0]] ✅
"""

from typing import List

# ══════════════════════════════════════════════════════════
# 解法一：贪心 + 插入（推荐解）
# ══════════════════════════════════════════════════════════


def reconstructQueue_greedy(people: List[List[int]]) -> List[List[int]]:
    """
    贪心 + 插入 ⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    1. 排序：身高降序，k 值升序
    2. 逐个插入：每个人插入到结果队列的第 k 个位置

    为什么这样排序？
    ────────────────────────────────────────────────────────
    - 身高降序：高个子先处理，因为他们不受矮个子影响
    - k 值升序：同身高的人，k 小的先插入，避免影响后续同身高的人

    为什么插入到第 k 位？
    ────────────────────────────────────────────────────────
    - 当前队列中已有的人都 >= 自己（因为按身高降序处理）
    - 插入到第 k 位，前面正好有 k 个人，且都 >= 自己
    - 完美满足 ki 的要求

    图解示例：people = [[7,0],[4,4],[7,1],[5,0],[6,1],[5,2]]
    ────────────────────────────────────────────────────────
    排序后：[[7,0],[7,1],[6,1],[5,0],[5,2],[4,4]]

    插入过程：
        [7,0] → 插入位置 0 → [[7,0]]
        [7,1] → 插入位置 1 → [[7,0],[7,1]]
        [6,1] → 插入位置 1 → [[7,0],[6,1],[7,1]]
        [5,0] → 插入位置 0 → [[5,0],[7,0],[6,1],[7,1]]
        [5,2] → 插入位置 2 → [[5,0],[7,0],[5,2],[6,1],[7,1]]
        [4,4] → 插入位置 4 → [[5,0],[7,0],[5,2],[6,1],[4,4],[7,1]]

    最终：[[5,0],[7,0],[5,2],[6,1],[4,4],[7,1]] ✅

    时间复杂度：O(n²) — 排序 O(n log n) + 插入 O(n²)
    空间复杂度：O(n) — 结果数组
    """
    # 第一步：排序
    # - 身高 h 降序（高个子优先）
    # - k 值升序（同身高时 k 小的优先）
    people.sort(key=lambda x: (-x[0], x[1]))

    # 第二步：逐个插入到第 k 位
    result = []
    for person in people:
        result.insert(person[1], person)

    return result


# ══════════════════════════════════════════════════════════
# 重要说明：为什么排序时同身高要按 k 升序？
# ══════════════════════════════════════════════════════════
#
# 问题：同身高的两个人，比如 [5,0] 和 [5,2]，为什么 [5,0] 要先处理？
#
# 解答：
# ────────────────────────────────────────────────────────
# 假设先处理 [5,2]，后处理 [5,0]：
#
#   插入 [5,2]：k=2，插入位置 2
#       结果：[?, ?, [5,2], ...]
#       前面需要有 2 个 >= 5 的人
#
#   插入 [5,0]：k=0，插入位置 0
#       结果：[[5,0], ?, ?, [5,2], ...]
#       [5,2] 被挤到了位置 3
#
#   现在 [5,2] 前面有 3 个人，但只需要 2 个 >= 5 的人
#   多出来的人可能 < 5，导致 [5,2] 的 ki 计数错误！
#
# ────────────────────────────────────────────────────────
# 如果先处理 [5,0]，后处理 [5,2]：
#
#   插入 [5,0]：k=0，插入位置 0
#       结果：[[5,0], ...]
#
#   插入 [5,2]：k=2，插入位置 2
#       结果：[[5,0], ?, [5,2], ...]
#       前面有 2 个人，其中 [5,0] 身高 5 >= 5 ✅
#
#   [5,2] 的 ki 计数正确！
# ══════════════════════════════════════════════════════════


# ══════════════════════════════════════════════════════════
# 重要说明：为什么插入到第 k 位是正确的？
# ══════════════════════════════════════════════════════════
#
# 问题：为什么插入到第 k 位，前面就正好有 k 个 >= 自己的人？
#
# 解答：
# ────────────────────────────────────────────────────────
# 1. 当前队列中已有的人都是按身高降序排列的
# 2. 当前处理的人身高为 h，前面所有人的身高都 >= h
# 3. 插入到第 k 位，前面正好有 k 个人
# 4. 这 k 个人的身高都 >= h（因为按身高降序处理）
# 5. 所以 ki 的要求被满足
#
# ────────────────────────────────────────────────────────
# 数学归纳法证明：
#
# 基础情况：队列为空时，第一个人插入到位置 0，前面 0 个人 ✅
#
# 归纳假设：前 i 个人都正确放置
#
# 归纳步骤：第 i+1 个人插入到位置 k
#   - 前面有 k 个人，都是之前放好的
#   - 因为按身高降序处理，这 k 个人的身高都 >= 当前人
#   - 所以 ki 的要求被满足
#
# 结论：所有人都正确放置 ✅
# ══════════════════════════════════════════════════════════


# ══════════════════════════════════════════════════════════
# 解法二：贪心 + 链表（优化插入效率）
# ══════════════════════════════════════════════════════════


def reconstructQueue_linkedlist(people: List[List[int]]) -> List[List[int]]:
    """
    贪心 + 链表 ⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    与解法一完全相同的贪心策略，但使用链表来优化插入操作。
    链表的插入操作是 O(1)，但找到插入位置仍需 O(k)。

    注意：Python 中没有内置链表，这里用 list 模拟。
    实际上 Python 的 list.insert() 底层也是数组插入，
    所以时间复杂度与解法一相同。此解法主要展示思路。

    时间复杂度：O(n²)
    空间复杂度：O(n)
    """
    # 排序：身高降序，k 值升序
    people.sort(key=lambda x: (-x[0], x[1]))

    # 使用链表（Python 中用 list 模拟）
    result = []
    for person in people:
        # 插入到第 k 位
        result.insert(person[1], person)

    return result


# ══════════════════════════════════════════════════════════
# 解法三：暴力回溯（超时，仅用于理解）
# ══════════════════════════════════════════════════════════


def reconstructQueue_brute_force(people: List[List[int]]) -> List[List[int]]:
    """
    暴力回溯 ⭐

    核心思路：
    ────────────────────────────────────────────────────────
    尝试所有可能的排列，检查每个排列是否满足所有人的 ki 要求。
    找到第一个满足条件的排列就返回。

    时间复杂度：O(n! × n) — n! 种排列，每种检查 O(n)
    空间复杂度：O(n)
    """
    n = len(people)

    def is_valid(queue):
        """检查队列是否满足所有人的 ki 要求"""
        for i in range(n):
            h, k = queue[i]
            # 统计前面身高 >= h 的人数
            count = 0
            for j in range(i):
                if queue[j][0] >= h:
                    count += 1
            if count != k:
                return False
        return True

    def backtrack(curr, used):
        """回溯尝试所有排列"""
        if len(curr) == n:
            if is_valid(curr):
                return curr[:]
            return None

        for i in range(n):
            if not used[i]:
                used[i] = True
                curr.append(people[i])
                result = backtrack(curr, used)
                if result is not None:
                    return result
                curr.pop()
                used[i] = False

        return None

    used = [False] * n
    return backtrack([], used) or []
