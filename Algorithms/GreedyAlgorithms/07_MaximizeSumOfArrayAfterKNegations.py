"""
最大化数组取反后的和（Maximize Sum Of Array After K Negations）

题目描述：
给你一个整数数组 nums 和一个整数 k，按以下方法修改该数组：
选择某个下标 i 并将 nums[i] 替换为 -nums[i]。
重复这个过程恰好 k 次。可以多次选择同一个下标 i。
以这种方式修改数组后，返回数组 可能的最大和。

示例 1：
    输入：nums = [4, 2, 3], k = 1
    输出：5
    解释：选择下标 1，nums 变为 [4, -2, 3]。

示例 2：
    输入：nums = [3, -1, 0, 2], k = 3
    输出：6
    解释：选择下标 (1, 2, 2)，nums 变为 [3, 1, 0, 2]。

示例 3：
    输入：nums = [2, -3, -1, 5, -4], k = 2
    输出：13
    解释：选择下标 (1, 4)，nums 变为 [2, 3, -1, 5, 4]。

解题思路总览：
───────────────────────────────────────────────────────────────────────
解法                      时间复杂度    空间复杂度    说明
───────────────────────────────────────────────────────────────────────
贪心 + 排序                O(n log n)    O(1)        ⭐ 最优解法
贪心 + 最小堆              O(n + k log n) O(n)       适合 k 较小的情况
贪心 + 排序（直接翻转）     O(n log n)    O(1)        更直观的实现
───────────────────────────────────────────────────────────────────────

贪心策略分析：
    要使数组和最大，我们需要：
    1. 优先取反负数（负数变正数，和增加最多）
    2. 优先取反绝对值最大的负数（如 -10 变成 10，比 -1 变成 1 增加更多）
    3. 如果 k 用完后还有剩余，且为奇数，取反绝对值最小的数（影响最小）

    关键洞察：
    ──────────────────────────────────────────────────────────────────────
    - 如果 k >= 负数个数：取反所有负数，剩余的 k 用于取反最小绝对值的数
    - 如果 k < 负数个数：取反 k 个最负的数（从左到右取反）
    - 如果有 0 存在：k 为奇数时可以取反 0（不影响和）
    - 如果没有 0 且 k 为奇数：取反当前最小绝对值的数
"""

from typing import List
import heapq


# ══════════════════════════════════════════════════════════
# 解法一：贪心 + 排序（标准解法）
# ══════════════════════════════════════════════════════════

def largestSumAfterKNegations_sort(nums: List[int], k: int) -> int:
    """
    贪心 + 排序 ⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    1. 先排序数组（从小到大）
    2. 从最左边（最负的数）开始，优先取反负数
    3. 如果 k 用完且为奇数，取反数组中最小绝对值的数

    算法步骤：
    1. 排序数组
    2. 遍历数组，优先取反负数（k > 0 且 num < 0），同时记录最小值下标
    3. 如果 k 为奇数，取反记录的最小值
    4. 返回所有数的和

    图解示例：nums = [2, -3, -1, 5, -4], k = 2
    ────────────────────────────────────────────────────────
    排序后：[-4, -3, -1, 2, 5]

    第一步：取反负数
        i=0: nums[0]=-4 < 0, k=2 > 0 → 取反为 4, k=1
        i=1: nums[1]=-3 < 0, k=1 > 0 → 取反为 3, k=0
        i=2: nums[2]=-1 < 0, k=0 = 0 → 不取反

    此时数组：[4, 3, -1, 2, 5]，k=0（偶数）
    不需要额外操作

    最终和：4 + 3 + (-1) + 2 + 5 = 13 ✅

    图解示例：nums = [3, -1, 0, 2], k = 3
    ────────────────────────────────────────────────────────
    排序后：[-1, 0, 2, 3]

    第一步：取反负数
        i=0: nums[0]=-1 < 0, k=3 > 0 → 取反为 1, k=2
        i=1: nums[1]=0 < 0? 不是 → 跳过
        i=2: nums[2]=2 < 0? 不是 → 跳过
        i=3: nums[3]=3 < 0? 不是 → 跳过

    此时数组：[1, 0, 2, 3]，k=2（偶数）
    不需要额外操作

    最终和：1 + 0 + 2 + 3 = 6 ✅

    图解示例：nums = [-8, -5, -5, -3, -2, -1], k = 4
    ────────────────────────────────────────────────────────
    排序后：[-8, -5, -5, -3, -2, -1]

    第一步：取反负数
        i=0: -8 < 0, k=4 → 取反为 8, k=3
        i=1: -5 < 0, k=3 → 取反为 5, k=2
        i=2: -5 < 0, k=2 → 取反为 5, k=1
        i=3: -3 < 0, k=1 → 取反为 3, k=0
        i=4: -2 < 0, k=0 → 不取反
        i=5: -1 < 0, k=0 → 不取反

    此时数组：[8, 5, 5, 3, -2, -1]，k=0（偶数）
    不需要额外操作

    最终和：8 + 5 + 5 + 3 + (-2) + (-1) = 18 ✅

    注意：
    ────────────────────────────────────────────────────────
    - 排序后从左到右取反，确保优先处理最负的数
    - 当 k 为奇数时，取反绝对值最小的数对总和影响最小
    - 如果存在 0，k 为奇数时可以取反 0（不影响和）

    时间复杂度：O(n log n) — 排序
    空间复杂度：O(1) — 原地排序，常数空间
    """
    n = len(nums)
    nums.sort()  # 从小到大排序

    # 在遍历时同时记录最小值的下标
    min_idx = 0

    # 从左到右遍历，优先取反负数
    for i in range(n):
        if nums[i] < 0 and k > 0:
            nums[i] = -nums[i]
            k -= 1
        # 同时记录处理后最小值的下标
        if nums[i] < nums[min_idx]:
            min_idx = i

    # 如果 k 还有剩余且为奇数，取反最小值
    if k % 2 == 1:
        nums[min_idx] = -nums[min_idx]

    return sum(nums)


# ══════════════════════════════════════════════════════════
# 解法二：贪心 + 最小堆
# ══════════════════════════════════════════════════════════

def largestSumAfterKNegations_heap(nums: List[int], k: int) -> int:
    """
    贪心 + 最小堆 ⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    使用最小堆维护数组，每次取出最小值取反后放回堆中，
    重复 k 次。

    算法步骤：
    1. 将数组元素全部加入最小堆
    2. 循环 k 次：
        a. 取出堆顶（最小值）
        b. 将其取反后放回堆中
    3. 堆中所有元素的和即为答案

    为什么用最小堆？
    ────────────────────────────────────────────────────────
    - 最小堆可以 O(log n) 获取当前最小值
    - 每次取反最小值，确保总和最大
    - 比排序更高效地处理 k 次操作

    图解示例：nums = [4, 2, 3], k = 1
    ────────────────────────────────────────────────────────
    初始最小堆：[2, 4, 3]（堆顶是 2）

    操作 1：
        取出堆顶 2，取反为 -2，放回堆中
        堆变为：[-2, 3, 4]（堆顶是 -2）

    k 次操作完成
    堆中元素：[-2, 3, 4]
    和 = -2 + 3 + 4 = 5 ✅

    时间复杂度：O(n + k log n) — 建堆 O(n)，k 次取反操作每次 O(log n)
    空间复杂度：O(n) — 堆存储所有元素

    适用场景：
    ────────────────────────────────────────────────────────
    - 当 k 远小于 n 时，比排序更高效
    - 当需要动态维护最小值时
    """
    # 将所有元素加入最小堆
    heap = nums[:]
    heapq.heapify(heap)

    # 循环 k 次，每次取出最小值取反后放回
    for _ in range(k):
        min_val = heapq.heappop(heap)
        heapq.heappush(heap, -min_val)

    return sum(heap)


# ══════════════════════════════════════════════════════════
# 解法三：贪心 + 排序（直接翻转，更直观）
# ══════════════════════════════════════════════════════════

def largestSumAfterKNegations_direct(nums: List[int], k: int) -> int:
    """
    贪心 + 排序（直接翻转）⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    与解法一类似，但实现更直观：
    1. 排序后，优先取反最负的数
    2. 如果 k 用完且为奇数，直接找到最小绝对值的数取反

    与解法一的区别：
    ────────────────────────────────────────────────────────
    解法一在遍历时就检查 k 是否为奇数，需要额外记录最小值
    解法三在 k 用完后统一处理，逻辑更清晰
    两者都采用在遍历时同时记录最小值下标的优化方式

    时间复杂度：O(n log n)
    空间复杂度：O(1)
    """
    nums.sort()
    min_idx = 0  # 记录处理后最小值的下标

    # 优先取反负数（从最负的开始）
    for i in range(len(nums)):
        if k <= 0:
            break
        if nums[i] < 0:
            nums[i] = -nums[i]
            k -= 1
        # 同时记录最小值的下标
        if nums[i] < nums[min_idx]:
            min_idx = i

    # 如果 k 还有剩余且为奇数，取反最小值
    if k % 2 == 1:
        nums[min_idx] = -nums[min_idx]

    return sum(nums)
