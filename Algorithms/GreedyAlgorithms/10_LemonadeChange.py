"""
柠檬水找零（Lemonade Change）

题目描述：
在柠檬水摊上，每一杯柠檬水的售价为 5 美元。顾客排队购买你的产品，
（按账单 bills 支付的顺序）一次购买一杯。
每位顾客只买一杯柠檬水，然后向你付 5 美元、10 美元或 20 美元。
你必须给每个顾客正确找零，也就是说净交易是每位顾客向你支付 5 美元。
注意，一开始你手头没有任何零钱。
给你一个整数数组 bills，其中 bills[i] 是第 i 位顾客付的账。
如果你能给每位顾客正确找零，返回 true，否则返回 false。

示例 1：
    输入：bills = [5,5,5,10,20]
    输出：true
    解释：
        前 3 位顾客那里，我们按顺序收取 3 张 5 美元的钞票。
        第 4 位顾客那里，我们收取一张 10 美元的钞票，并返还 5 美元。
        第 5 位顾客那里，我们找还一张 10 美元的钞票和一张 5 美元的钞票。
        由于所有客户都得到了正确的找零，所以我们输出 true。

示例 2：
    输入：bills = [5,5,10,10,20]
    输出：false
    解释：
        前 2 位顾客那里，我们按顺序收取 2 张 5 美元的钞票。
        对于接下来的 2 位顾客，我们收取一张 10 美元的钞票，然后返还 5 美元。
        对于最后一位顾客，我们无法退回 15 美元，因为我们现在只有两张 10 美元的钞票。
        由于不是每位顾客都得到了正确的找零，所以答案是 false。

解题思路总览：
───────────────────────────────────────────────────────────────────────
解法                      时间复杂度    空间复杂度    说明
───────────────────────────────────────────────────────────────────────
贪心算法                   O(n)          O(1)        ⭐ 最优解法
模拟（详细记录）            O(n)          O(n)        便于调试和理解
───────────────────────────────────────────────────────────────────────

核心思想：
───────────────────────────────────────────────────────────────────────
1. 只需要跟踪 5 美元和 10 美元的数量（20 美元不能用于找零）
2. 贪心策略：找零时优先使用大面额钞票
   - 收到 10 美元：找回 1 张 5 美元
   - 收到 20 美元：优先找回 1 张 10 + 1 张 5，否则找回 3 张 5
3. 为什么优先用 10 美元找零？
   - 10 美元只能用来给 20 美元找零（找回 15 = 10 + 5）
   - 5 美元既能给 10 找零，也能给 20 找零
   - 所以 10 美元更"珍贵"，应该优先使用

为什么贪心是正确的？
───────────────────────────────────────────────────────────────────────
收到 20 美元需要找回 15 美元，有两种方案：
    方案 A：1 张 10 + 1 张 5（优先选择）
    方案 B：3 张 5

关键分析：
    - 10 美元只能用于给 20 找零（10+5=15）
    - 5 美元可以用于给 10 找零（5=5）或给 20 找零（5+5+5=15）
    - 如果先用掉 3 张 5 元，后续收到 10 元时可能没有 5 元找零
    - 保留 5 元、消耗 10 元，是最安全的策略

图解示例：bills = [5,5,5,10,20]
───────────────────────────────────────────────────────────────────────
    顾客    支付    手头零钱          找零操作              结果
                    (5元, 10元)
───────────────────────────────────────────────────────────────────────
    0      5元     (0, 0)           收入 5 元 → (1, 0)     ✅
    1      5元     (1, 0)           收入 5 元 → (2, 0)     ✅
    2      5元     (2, 0)           收入 5 元 → (3, 0)     ✅
    3      10元    (3, 0)           找 1 张 5 → (2, 1)     ✅
    4      20元    (2, 1)           找 1张10+1张5 → (1,0)  ✅
───────────────────────────────────────────────────────────────────────
    最终：true ✅

图解示例：bills = [5,5,10,10,20]
───────────────────────────────────────────────────────────────────────
    顾客    支付    手头零钱          找零操作              结果
                    (5元, 10元)
───────────────────────────────────────────────────────────────────────
    0      5元     (0, 0)           收入 5 元 → (1, 0)     ✅
    1      5元     (1, 0)           收入 5 元 → (2, 0)     ✅
    2      10元    (2, 0)           找 1 张 5 → (1, 1)     ✅
    3      10元    (1, 1)           找 1 张 5 → (0, 2)     ✅
    4      20元    (0, 2)           需要 15 元：             ❌
                                    方案A: 1张10+1张5 → 5元不够!
                                    方案B: 3张5 → 5元不够!
                                    无法找零
───────────────────────────────────────────────────────────────────────
    最终：false ❌
"""

from typing import List

# ══════════════════════════════════════════════════════════
# 解法一：贪心算法（推荐解）
# ══════════════════════════════════════════════════════════


def lemonadeChange_greedy(bills: List[int]) -> bool:
    """
    贪心算法 ⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    1. 维护 5 元和 10 元的数量（20 元不参与找零）
    2. 遍历每位顾客：
       - 付 5 元：直接收下
       - 付 10 元：必须找回 1 张 5 元
       - 付 20 元：优先找 1张10+1张5，否则找 3张5
    3. 任何时候找不开就返回 false

    贪心策略：
    ────────────────────────────────────────────────────────
    收到 20 元时，有两种找零方案：
        方案 A：1 张 10 + 1 张 5（优先）
        方案 B：3 张 5

    为什么方案 A 优先？
    ────────────────────────────────────────────────────────
    - 10 元只能用于给 20 找零（10+5=15），使用场景唯一
    - 5 元可以用于给 10 找零（5=5）或给 20 找零（5+5+5=15）
    - 保留更通用的 5 元，消耗专用的 10 元，是最优策略

    图解示例：bills = [5,5,5,10,20]
    ────────────────────────────────────────────────────────
        顾客    支付    five   ten    找零操作           结果
    ────────────────────────────────────────────────────────
        0      5       1      0      收入 5            ✅
        1      5       2      0      收入 5            ✅
        2      5       3      0      收入 5            ✅
        3      10      2      1      找 1张5           ✅
        4      20      1      0      找 1张10+1张5     ✅
    ────────────────────────────────────────────────────────
        返回 true

    图解示例：bills = [5,5,10,10,20]
    ────────────────────────────────────────────────────────
        顾客    支付    five   ten    找零操作           结果
    ────────────────────────────────────────────────────────
        0      5       1      0      收入 5            ✅
        1      5       2      0      收入 5            ✅
        2      10      1      1      找 1张5           ✅
        3      10      0      2      找 1张5           ✅
        4      20      0      2      无法找零          ❌
    ────────────────────────────────────────────────────────
        返回 false

    时间复杂度：O(n) — 遍历一次 bills 数组
    空间复杂度：O(1) — 只用两个计数变量
    """
    five = 0  # 5 美元钞票数量
    ten = 0  # 10 美元钞票数量

    for bill in bills:
        if bill == 5:
            # 收入 5 元，无需找零
            five += 1

        elif bill == 10:
            # 收入 10 元，需要找回 1 张 5 元
            if five >= 1:
                five -= 1
                ten += 1
            else:
                return False

        else:  # bill == 20
            # 收入 20 元，需要找回 15 元
            # 贪心策略：优先使用 10 元
            if five >= 1 and ten >= 1:
                # 方案 A：1 张 10 + 1 张 5（优先）
                five -= 1
                ten -= 1
            elif five >= 3:
                # 方案 B：3 张 5
                five -= 3
            else:
                return False

    return True


# ══════════════════════════════════════════════════════════
# 重要说明：为什么不需要跟踪 20 元的数量？
# ══════════════════════════════════════════════════════════
#
# 问题：收到 20 元后为什么不记录？
#
# 解答：
# ────────────────────────────────────────────────────────
# 20 元面额太大，无法用于任何找零：
#   - 给 10 元找零需要 5 元 → 20 元没用
#   - 给 20 元找零需要 15 元 → 20 元没用
# 所以 20 元收了就是收了，不影响后续找零决策。
# 只需要跟踪 5 元和 10 元的数量即可。
# ══════════════════════════════════════════════════════════


# ══════════════════════════════════════════════════════════
# 重要说明：为什么方案 A 一定优于方案 B？
# ══════════════════════════════════════════════════════════
#
# 用反证法证明贪心策略的正确性：
# ────────────────────────────────────────────────────────
# 假设当前有 five=F, ten=T，需要给 20 元找零 15 元。
#
# 情况 1：F >= 1 且 T >= 1（两种方案都可行）
#   方案 A 用掉：1 张 5，1 张 10 → 剩余 F-1 张 5，T-1 张 10
#   方案 B 用掉：3 张 5           → 剩余 F-3 张 5，T 张 10
#
#   方案 A 后剩余的 5 元比方案 B 多 2 张。
#   方案 A 后剩余的 10 元比方案 B 少 1 张。
#
#   10 元唯一用途：给 20 元找零（10+5=15）
#   5 元用途：给 10 元找零（5）或给 20 元找零（5+5+5=15）
#
#   如果后续来了一个付 10 元的顾客，需要 1 张 5 元：
#     - 方案 A 后：F-1 张 5 元可用
#     - 方案 B 后：F-3 张 5 元可用
#     - 方案 A 更优（多 2 张 5 元）
#
#   如果后续来了一个付 20 元的顾客，需要 15 元：
#     - 方案 A 后：可以用 (F-1 张5, T-1 张10) 找零
#     - 方案 B 后：可以用 (F-3 张5, T 张10) 找零
#     - 两种情况都能用 10+5 找零（如果 F-1>=1 且 T-1>=1）
#     - 或者用 5+5+5 找零
#     - 方案 A 保留更多 5 元，选择空间更大
#
# 综上，方案 A 不会比方案 B 差，且在某些情况下严格更优。
# ══════════════════════════════════════════════════════════
