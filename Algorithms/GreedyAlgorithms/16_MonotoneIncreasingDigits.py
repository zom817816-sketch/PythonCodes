"""
单调递增的数字（Monotone Increasing Digits）

题目描述：
当且仅当每个相邻位数上的数字 x 和 y 满足 x <= y 时，
我们称这个整数是单调递增的。

给定一个整数 n，返回小于或等于 n 的最大数字，且数字呈单调递增。

示例 1：
    输入：n = 10
    输出：9
    解释：10 不是单调递增的（1 > 0），小于等于 10 的最大单调递增数字是 9。

示例 2：
    输入：n = 1234
    输出：1234
    解释：1234 本身就是单调递增的，直接返回。

示例 3：
    输入：n = 332
    输出：299
    解释：
        332 不是单调递增的（第二个 3 > 2）。
        331、330... 一直到 299 才是单调递增的。
        所以答案是 299。

示例 4：
    输入：n = 100
    输出：99
    解释：100 不是单调递增的，小于等于 100 的最大单调递增数字是 99。

解题思路总览：
───────────────────────────────────────────────────────────────────────
解法                          时间复杂度    空间复杂度    说明
───────────────────────────────────────────────────────────────────────
暴力枚举                         O(n × d)     O(1)        超时
贪心（从右向左扫描）              O(d)         O(d)        ⭐ 最优解法
贪心（转换为列表原地修改）         O(d)         O(1)        空间优化版
───────────────────────────────────────────────────────────────────────
注：d 为数字 n 的位数

核心思想（贪心）：
───────────────────────────────────────────────────────────────────────
关键洞察：
1. 从左到右遍历数字，找到第一个"下降"的位置（左边数字 > 右边数字）
2. 将左边数字减 1，右边所有数字变成 9
3. 但这样可能导致新的"下降"（比如 332 → 329，但 3 > 2 仍然下降）
4. 解决方案：从右向左扫描，找到最左边的需要减 1 的位置

为什么从右向左扫描？
───────────────────────────────────────────────────────────────────────
- 从右向左可以确保：当我们修改某个位置时，右边的所有位置都已经处理好了
- 如果从左向右，修改一个位置后，可能导致新的下降，需要回溯
- 从右向左只需要一次遍历，不需要回溯

贪心策略：
───────────────────────────────────────────────────────────────────────
1. 将数字转为字符串（或列表），方便逐位处理
2. 从右向左遍历，找到第一个满足 s[i-1] > s[i] 的位置 i
3. 将 s[i-1] 减 1，记录位置 i（从这里开始后面的都变成 9）
4. 继续向左遍历，如果还有下降，更新减 1 的位置和 flag
5. 最后将 flag 位置及之后的所有数字变成 9

为什么这是最优的？
───────────────────────────────────────────────────────────────────────
- 我们要找"小于等于 n 的最大"单调递增数字
- 这意味着我们要尽可能保持高位不变，只修改低位
- 从右向左找到第一个下降点，将前一位减 1，后面全变 9
- 这样得到的是满足条件的最大数字（因为高位尽可能大）
"""

from typing import List

# ══════════════════════════════════════════════════════════
# 解法一：暴力枚举（纯暴力，超时）
# ══════════════════════════════════════════════════════════


def monotoneIncreasingDigits_brute(n: int) -> int:
    """
    暴力枚举（纯暴力）

    核心思路：
    ────────────────────────────────────────────────────────
    从 n 开始递减，逐个检查每个数字是否单调递增，
    第一个满足条件的数字就是答案。

    检查单调递增的方法：
    - 从低位到高位遍历数字
    - 维护当前遇到的最大数字（从低位看）
    - 如果当前数字 > max_digit，说明出现下降，不是单调递增

    时间复杂度：O(n × d) — 最坏情况下遍历 n 个数字，每个数字检查 d 位
    空间复杂度：O(1) — 仅使用常数空间

    ⚠️ 注意：这个解法在 LeetCode 上会超时（TLE），
       仅作为理解题意的参考。
       实际做题请用贪心解法。
    """

    def is_monotone_increasing(num: int) -> bool:
        """检查数字是否单调递增（从低位到高位检查）"""
        max_digit = 10  # 初始化为 10，确保第一个数字一定 <= max_digit
        while num:
            digit = num % 10  # 取最低位
            if digit > max_digit:
                # 当前数字 > 之前遇到的数字（从低位看），说明下降
                return False
            max_digit = digit
            num //= 10  # 去掉最低位
        return True

    # 从 n 开始递减检查
    for num in range(n, -1, -1):
        if is_monotone_increasing(num):
            return num

    return 0  # 不会执行到这里，因为 0 一定是单调递增的


# ══════════════════════════════════════════════════════════
# 解法二：贪心（从右向左扫描，推荐解）
# ══════════════════════════════════════════════════════════


def monotoneIncreasingDigits(n: int) -> List[int]:
    """
    贪心 — 从右向左扫描 ⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    1. 将数字转为字符串，方便逐位处理
    2. 从右向左遍历，找到所有"下降"的位置
    3. 对于每个下降位置 i（即 s[i-1] > s[i]）：
       - 将 s[i-1] 减 1
       - 记录位置 i，表示从这里开始后面的数字都要变成 9
    4. 最后将记录位置及之后的所有数字变成 9

    为什么从右向左？
    ────────────────────────────────────────────────────────
    实际上，从左向右的问题在于：
    - 修改一个位置后，可能导致前面又出现下降
    - 比如 332，如果先处理 3>2，变成 329，但 3>2 仍然下降
    - 需要继续处理，变成 299

    从右向左的优势：
    - 先处理最右边的下降，然后向左推进
    - 每次修改后，右边的部分已经处理好了（都是 9）
    - 只需要记录最左边的需要减 1 的位置

    图解示例：n = 332
    ────────────────────────────────────────────────────────
    转为字符串："332"

    从右向左遍历（i 从 2 到 1）：
        ────────────────────────────────────────────────────
        i=2：比较 s[1]=3 和 s[2]=2
             3 > 2，下降！
             flag = 2（从位置 2 开始后面变 9）
             s[1] 减 1：s = "322"

        i=1：比较 s[0]=3 和 s[1]=2
             3 > 2，下降！
             flag = 1（更新为更左边的位置）
             s[0] 减 1：s = "222"
        ────────────────────────────────────────────────────

    将 flag 位置及之后变 9：
        s = "2" + "99" = "299"

    结果：299 ✅

    图解示例：n = 100
    ────────────────────────────────────────────────────────
    转为字符串："100"

    从右向左遍历：
        ────────────────────────────────────────────────────
        i=2：s[1]=0 <= s[2]=0，不下降
        i=1：s[0]=1 > s[1]=0，下降！
             flag = 1
             s[0] 减 1：s = "000"
        ────────────────────────────────────────────────────

    将 flag 位置及之后变 9：
        s = "0" + "99" = "099" = 99

    结果：99 ✅

    图解示例：n = 1234
    ────────────────────────────────────────────────────────
    转为字符串："1234"

    从右向左遍历：
        ────────────────────────────────────────────────────
        i=3：s[2]=3 <= s[3]=4，不下降
        i=2：s[1]=2 <= s[2]=3，不下降
        i=1：s[0]=1 <= s[1]=2，不下降
        ────────────────────────────────────────────────────
    没有下降，本身就是单调递增的

    结果：1234 ✅

    时间复杂度：O(d) — d 为数字的位数，只需遍历两次（一次找下降，一次变 9）
    空间复杂度：O(d) — 需要存储字符串
    """
    # 将数字转为字符串（列表形式方便修改）
    num_str = list(str(n))
    # flag 记录从哪个位置开始后面都变成 9
    # 初始化为 len(num_str)，表示如果没有任何下降，就不需要变 9
    flag = len(num_str)

    # 从右向左遍历（从倒数第二位开始，比较它和右边一位）
    for i in range(len(num_str) - 1, 0, -1):
        if num_str[i - 1] > num_str[i]:
            # 发现下降：左边数字 > 右边数字
            # 更新 flag，表示从这个位置开始后面要变 9
            flag = i
            # 将左边数字减 1
            num_str[i - 1] = str(int(num_str[i - 1]) - 1)

    # 将 flag 位置及之后的所有数字变成 9
    for i in range(flag, len(num_str)):
        num_str[i] = "9"

    # 转回整数返回
    return int("".join(num_str))


# ══════════════════════════════════════════════════════════
# 解法三：贪心 — 转换为列表原地修改（空间优化版）
# ══════════════════════════════════════════════════════════


def monotoneIncreasingDigits_optimized(n: int) -> int:
    """
    贪心 — 空间优化版 ⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    与解法二相同，但使用更简洁的代码实现：
    1. 直接用字符串操作，避免显式转换为列表
    2. 使用字符串切片和乘法来构造结果

    图解示例：n = 332
    ────────────────────────────────────────────────────────
    num_str = "332"
    flag = 3

    i=2：s[1]='3' > s[2]='2'，下降！
         flag = 2
         s = "3" + str(3-1) + "2" = "322"

    i=1：s[0]='3' > s[1]='2'，下降！
         flag = 1
         s = "" + str(3-1) + "22" = "222"

    最后：s[:1] + "9" * (3-1) = "2" + "99" = "299"

    结果：299 ✅

    注意：这种写法虽然简洁，但每次字符串拼接都会创建新字符串，
    实际时间复杂度是 O(d²)。对于 d ≤ 10 的数字位数来说可以接受，
    但对于更长的字符串，解法二（列表版本）更高效。

    时间复杂度：O(d²) — 每次字符串拼接 O(d)，最多 d 次
    空间复杂度：O(d) — 创建多个中间字符串
    """
    num_str = str(n)
    flag = len(num_str)

    for i in range(len(num_str) - 1, 0, -1):
        if num_str[i - 1] > num_str[i]:
            flag = i
            # 构造新字符串：前 i-1 位 + (第 i-1 位减 1) + 第 i 位及之后
            num_str = num_str[: i - 1] + str(int(num_str[i - 1]) - 1) + num_str[i:]

    # 构造结果：前 flag 位 + 后面全是 9
    num_str = num_str[:flag] + "9" * (len(num_str) - flag)

    return int(num_str)


# ══════════════════════════════════════════════════════════
# 解法四：贪心 — 数学方法（不用字符串转换）
# ══════════════════════════════════════════════════════════


def monotoneIncreasingDigits_math(n: int) -> int:
    """
    贪心 — 纯数学方法（不用字符串转换）⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    不使用字符串转换，直接用数学运算处理数字的每一位。

    算法步骤：
    1. 从低位到高位遍历数字的每一位
    2. 维护当前位和之前位的关系
    3. 如果发现下降，记录需要调整的位置
    4. 最后根据记录的位置构造结果

    具体实现：
    - 用数组存储每一位数字
    - 从右向左找到第一个下降点
    - 将该位减 1，后面所有位变 9
    - 将数组转回数字

    图解示例：n = 332
    ────────────────────────────────────────────────────────
    提取每一位：digits = [3, 3, 2]（高位到低位）

    从右向左找下降：
        ────────────────────────────────────────────────────
        i=2：digits[1]=3 > digits[2]=2，下降！
             flag = 2
             digits[1] = 2 → digits = [3, 2, 2]

        i=1：digits[0]=3 > digits[1]=2，下降！
             flag = 1
             digits[0] = 2 → digits = [2, 2, 2]
        ────────────────────────────────────────────────────

    将 flag 及之后变 9：
        digits = [2, 9, 9]

    转回数字：2 * 100 + 9 * 10 + 9 = 299

    结果：299 ✅

    时间复杂度：O(d) — 提取数字 O(d) + 处理 O(d) + 构造结果 O(d)
    空间复杂度：O(d) — 存储数字的每一位
    """
    if n < 10:
        # 个位数一定是单调递增的
        return n

    # 提取每一位数字（从低位到高位）
    digits = []
    temp = n
    while temp:
        digits.append(temp % 10)
        temp //= 10

    # digits 现在是从低位到高位，需要反转
    # 但为了方便，我们直接从低位索引处理
    # 实际上我们需要从高位到低位找下降

    # 反转，使得 digits[0] 是最高位
    digits = digits[::-1]

    flag = len(digits)

    # 从右向左找下降（从倒数第二位开始）
    for i in range(len(digits) - 1, 0, -1):
        if digits[i - 1] > digits[i]:
            flag = i
            digits[i - 1] -= 1

    # 将 flag 及之后变 9
    for i in range(flag, len(digits)):
        digits[i] = 9

    # 将数组转回数字
    result = 0
    for digit in digits:
        result = result * 10 + digit

    return result


# ══════════════════════════════════════════════════════════
# 重要说明：贪心算法的正确性证明
# ══════════════════════════════════════════════════════════
#
# 为什么从右向左扫描，遇到下降就减 1 是正确的？
# ────────────────────────────────────────────────────────
# 1. 必要性：
#    如果数字在某位置 i 出现下降（digits[i-1] > digits[i]），
#    那么任何单调递增数字要么：
#        a) 在位置 i-1 的数字 < digits[i-1]，或者
#        b) 在位置 i 的数字 > digits[i]
#    但 b) 会导致数字 > n，不满足"小于等于 n"的条件。
#    所以必须选择 a)，即把 digits[i-1] 减 1。
#
# 2. 充分性：
#    将 digits[i-1] 减 1，并将 i 及之后的所有位变成 9，
#    得到的数字一定是单调递增的（因为 9 是最大数字）。
#
# 3. 最优性（最大性）：
#    从右向左扫描，只在必要时才减 1，
#    这样高位尽可能保持不变，得到的数字尽可能大。
#    如果从左向右扫描，可能会过度减 1，导致结果偏小。
#
# 4. 为什么后面全变 9？
#    减 1 后，后面所有位变成 9 可以得到最大的数字。
#    例如 332 → 减 1 后变成 2xx，299 是最大的可能。
#
# 这个贪心策略满足：
#    - 贪心选择性质：每次遇到下降就减 1 不会导致更差的结果
#    - 最优子结构：处理完右边后，左边的问题与原问题同构
# ══════════════════════════════════════════════════════════
