"""
二叉树摄像头（Binary Tree Cameras）

题目描述：
给定一个二叉树，我们在树的节点上安装摄像头。
节点上的每个摄影头都可以监视其父对象、自身及其直接子对象。
计算监控树的所有节点所需的最小摄像头数量。

示例 1：
    输入：root = [0,0,null,0,null,null,null,0]
    输出：2
    解释：
        需要 2 个摄像头才能监控所有节点。
        一个摄像头放在根节点的左子节点，
        另一个摄像头放在根节点的右子节点的左子节点。

示例 2：
    输入：root = [0,0,null,0,null,0]
    输出：2
    解释：
        需要 2 个摄像头才能监控所有节点。

示例 3：
    输入：root = [0]
    输出：0
    解释：没有子节点的节点不需要摄像头。

示例 4：
    输入：root = [0,0,null,0,0]
    输出：1
    解释：一个摄像头放在根节点的左子节点即可。

解题思路总览：
───────────────────────────────────────────────────────────────────────
解法                          时间复杂度    空间复杂度    说明
───────────────────────────────────────────────────────────────────────
DFS + 状态贪心                 O(n)         O(h)        ⭐ 最优解法
DFS + 三状态标记               O(n)         O(h)        优雅实现
BFS + 贪心                    O(n)         O(w)        层次遍历版
───────────────────────────────────────────────────────────────────────
注：n 为节点数，h 为树高，w 为树宽

核心思想（贪心）：
───────────────────────────────────────────────────────────────────────
关键洞察：
1. 摄像头可以覆盖：父节点、自身、子节点
2. 贪心策略：尽量把摄像头放在叶子节点的父节点
3. 因为这样可以同时覆盖父节点和叶子节点

状态定义：
───────────────────────────────────────────────────────────────────────
- 状态 0：节点未被覆盖（需要父节点覆盖）
- 状态 1：节点有摄像头（可以覆盖父节点和子节点）
- 状态 2：节点被覆盖（被子节点的摄像头覆盖）

为什么用后序遍历？
───────────────────────────────────────────────────────────────────────
- 先处理子节点，再处理父节点
- 这样我们可以根据子节点的状态来决定父节点的操作
- 如果子节点未被覆盖，父节点必须安装摄像头
- 如果子节点有摄像头，父节点可以被覆盖

贪心策略：
───────────────────────────────────────────────────────────────────────
1. 从叶子节点开始向上处理（后序遍历）
2. 如果当前节点的子节点未被覆盖，在当前节点安装摄像头
3. 如果当前节点的子节点有摄像头，当前节点被覆盖
4. 根节点特殊处理：如果根节点未被覆盖，需要额外安装一个摄像头

为什么这是最优的？
───────────────────────────────────────────────────────────────────────
- 我们尽量在靠近叶子的位置安装摄像头
- 这样每个摄像头可以覆盖尽可能多的节点
- 避免在高层安装摄像头，浪费覆盖范围
"""

from typing import Optional


# 定义二叉树节点
class TreeNode:
    def __init__(self, val=0, left=None, right=None):
        self.val = val
        self.left = left
        self.right = right


# ══════════════════════════════════════════════════════════
# 解法一：DFS + 状态贪心（推荐解）
# ══════════════════════════════════════════════════════════


def minCameraCover(root: Optional[TreeNode]) -> int:
    """
    DFS + 状态贪心 ⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    使用后序遍历，每个节点返回其状态：
    - 0：未被覆盖（需要父节点覆盖）
    - 1：有摄像头（可以覆盖父节点和子节点）
    - 2：被覆盖（被子节点的摄像头覆盖）

    状态转移规则：
    1. 如果子节点状态为 0（未覆盖），当前节点必须安装摄像头（状态变为 1）
    2. 如果子节点状态为 1（有摄像头），当前节点被覆盖（状态变为 2）
    3. 如果子节点状态为 2（已覆盖），当前节点未被覆盖（状态变为 0）

    图解示例：root = [0,0,null,0,0]
    ────────────────────────────────────────────────────
            0 (root)
           /
          0
         / \
        0   0

    后序遍历：
        ────────────────────────────────────────────────────
        节点 3（左叶子）：无子节点，状态 = 0（未覆盖）
        节点 4（右叶子）：无子节点，状态 = 0（未覆盖）
        节点 2（中间节点）：
            左子 = 0（未覆盖）→ 安装摄像头，状态 = 1
            右子 = 0（未覆盖）→ 安装摄像头，状态 = 1
            但节点 2 只需要一个摄像头即可覆盖所有子节点
            所以节点 2 安装摄像头，状态 = 1
        节点 1（根节点）：
            左子 = 1（有摄像头）→ 被覆盖，状态 = 2
            右子 = null → 忽略
            根节点状态 = 2（被覆盖）
        ────────────────────────────────────────────────────

    结果：1 个摄像头 ✅

    图解示例：root = [0,0,null,0,null,null,null,0]
    ────────────────────────────────────────────────────
            0 (root)
           /
          0
         /
        0
       /
      0

    后序遍历：
        ────────────────────────────────────────────────────
        节点 4（最底叶子）：状态 = 0
        节点 3：左子 = 0 → 安装摄像头，状态 = 1
        节点 2：左子 = 1 → 被覆盖，状态 = 2
        节点 1（根节点）：
            左子 = 2（已覆盖）→ 未被覆盖，状态 = 0
            根节点未被覆盖，需要额外安装摄像头
        ────────────────────────────────────────────────────

    结果：2 个摄像头 ✅

    时间复杂度：O(n) — 每个节点访问一次
    空间复杂度：O(h) — 递归栈空间，h 为树高
    """
    # 摄像头数量
    cameras = 0

    def dfs(node: Optional[TreeNode]) -> int:
        """
        返回节点状态：
        0 - 未被覆盖
        1 - 有摄像头
        2 - 被覆盖
        """
        nonlocal cameras

        if not node:
            # 空节点视为已覆盖（不需要摄像头）
            return 2

        # 后序遍历：先处理子节点
        left = dfs(node.left)
        right = dfs(node.right)

        # 状态转移规则
        if left == 0 or right == 0:
            # 子节点未被覆盖，当前节点必须安装摄像头
            cameras += 1
            return 1
        elif left == 1 or right == 1:
            # 子节点有摄像头，当前节点被覆盖
            return 2
        else:
            # 子节点都已覆盖，当前节点未被覆盖
            return 0

    # 后序遍历处理
    status = dfs(root)

    # 如果根节点未被覆盖，需要额外安装一个摄像头
    if status == 0:
        cameras += 1

    return cameras


# ══════════════════════════════════════════════════════════
# 解法二：DFS + 三状态标记（优雅实现）
# ══════════════════════════════════════════════════════════


def minCameraCover_states(root: Optional[TreeNode]) -> int:
    """
    DFS + 三状态标记 ⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    与解法一相同，但使用更简洁的实现：
    1. 使用全局变量记录摄像头数量
    2. 直接修改节点的 val 来记录状态
    3. 避免额外的数据结构

    状态值：
    - 0：未被覆盖
    - 1：有摄像头
    - 2：被覆盖

    图解示例：root = [0,0,null,0,0]
    ────────────────────────────────────────────────────
            0 (root)
           /
          0
         / \
        0   0

    执行过程：
        ────────────────────────────────────────────────────
        后序遍历节点 3：val = 0（未覆盖）
        后序遍历节点 4：val = 0（未覆盖）
        后序遍历节点 2：
            左子 val = 0 → 安装摄像头
            node.val = 1（有摄像头）
            cameras = 1
        后序遍历节点 1（根节点）：
            左子 val = 1 → 被覆盖
            node.val = 2（被覆盖）
        ────────────────────────────────────────────────────

        最终状态：[2,1,null,1,1]
        根节点被覆盖，无需额外摄像头

    结果：1 个摄像头 ✅

    图解示例：root = [0,0,null,0,null,null,null,0]
    ────────────────────────────────────────────────────
            0 (root)
           /
          0
         /
        0
       /
      0

    执行过程：
        ────────────────────────────────────────────────────
        后序遍历节点 4：val = 0（未覆盖）
        后序遍历节点 3：
            左子 val = 0 → 安装摄像头
            node.val = 1
            cameras = 1
        后序遍历节点 2：
            左子 val = 1 → 被覆盖
            node.val = 2
        后序遍历节点 1（根节点）：
            左子 val = 2 → 未被覆盖
            node.val = 0
        ────────────────────────────────────────────────────

        根节点未被覆盖，需要额外安装摄像头
        cameras = 1 + 1 = 2

    结果：2 个摄像头 ✅

    时间复杂度：O(n) — 每个节点访问一次
    空间复杂度：O(h) — 递归栈空间
    """
    cameras = 0

    def dfs(node: Optional[TreeNode]) -> int:
        """
        返回节点状态，直接修改节点的 val
        0 - 未被覆盖
        1 - 有摄像头
        2 - 被覆盖
        """
        nonlocal cameras

        if not node:
            return 2

        # 后序遍历
        left = dfs(node.left)
        right = dfs(node.right)

        # 状态转移
        if left == 0 or right == 0:
            # 子节点未覆盖，安装摄像头
            cameras += 1
            return 1
        elif left == 1 or right == 1:
            # 子节点有摄像头，被覆盖
            return 2
        else:
            # 子节点都已覆盖，未被覆盖
            return 0

    # 处理根节点
    if dfs(root) == 0:
        cameras += 1

    return cameras


# ══════════════════════════════════════════════════════════
# 解法三：BFS + 贪心（层次遍历版）
# ══════════════════════════════════════════════════════════


def minCameraCover_bfs(root: Optional[TreeNode]) -> int:
    """
    BFS + 贪心（层次遍历版）⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    使用层次遍历（BFS）从下往上处理：
    1. 先处理叶子节点
    2. 叶子节点的父节点安装摄像头
    3. 父节点的父节点被覆盖
    4. 继续向上，直到处理完所有节点

    算法步骤：
    1. 使用 BFS 遍历树，记录每个节点的父节点
    2. 从叶子节点开始向上处理
    3. 如果节点未被覆盖，在父节点安装摄像头
    4. 父节点的父节点被覆盖
    5. 继续向上，直到处理完所有节点

    图解示例：root = [0,0,null,0,0]
    ────────────────────────────────────────────────────
            0 (root)
           /
          0
         / \
        0   0

    层次遍历：
        ────────────────────────────────────────────────────
        第 0 层：[0]
        第 1 层：[0]
        第 2 层：[0, 0]

    从下往上处理：
        ────────────────────────────────────────────────────
        节点 3（左叶子）：未覆盖 → 在父节点（节点 2）安装摄像头
        节点 4（右叶子）：未覆盖 → 在父节点（节点 2）安装摄像头
        节点 2：有摄像头 → 父节点（节点 1）被覆盖
        节点 1（根节点）：被覆盖
        ────────────────────────────────────────────────────

    结果：1 个摄像头 ✅

    时间复杂度：O(n) — 每个节点访问一次
    空间复杂度：O(w) — BFS 队列空间，w 为树宽
    """
    from collections import deque

    if not root:
        return 0

    # 记录每个节点的父节点
    parent = {}
    # 记录是否被覆盖
    covered = set()
    # 记录是否有摄像头
    camera = set()

    # BFS 遍历，记录父节点
    queue = deque([root])
    nodes = []
    while queue:
        node = queue.popleft()
        nodes.append(node)
        if node.left:
            parent[node.left] = node
            queue.append(node.left)
        if node.right:
            parent[node.right] = node
            queue.append(node.right)

    cameras = 0

    # 从叶子节点开始向上处理
    for node in reversed(nodes):
        # 如果节点未被覆盖，且不是根节点
        if node not in covered and node not in camera:
            # 如果有父节点，在父节点安装摄像头
            if node in parent:
                p = parent[node]
                camera.add(p)
                cameras += 1
                # 父节点覆盖其父节点、自身、子节点
                covered.add(p)
                if p in parent:
                    covered.add(parent[p])
                if p.left:
                    covered.add(p.left)
                if p.right:
                    covered.add(p.right)
            else:
                # 根节点未被覆盖，需要安装摄像头
                camera.add(node)
                cameras += 1
                covered.add(node)
                if node.left:
                    covered.add(node.left)
                if node.right:
                    covered.add(node.right)

    return cameras


# ══════════════════════════════════════════════════════════
# 算法正确性证明
# ══════════════════════════════════════════════════════════
#
# 为什么在叶子节点的父节点安装摄像头是最优的？
# ────────────────────────────────────────────────────────
# 1. 覆盖范围最大化：
#    - 摄像头覆盖父节点、自身、子节点
#    - 放在叶子节点的父节点可以同时覆盖：
#        a) 父节点（向上覆盖）
#        b) 自身（中间节点）
#        c) 叶子节点（向下覆盖）
#
# 2. 避免浪费：
#    - 如果在叶子节点安装摄像头，只能覆盖叶子节点和父节点
#    - 如果在更高层安装摄像头，可能无法覆盖叶子节点
#    - 在中间层安装可以平衡覆盖范围
#
# 3. 贪心选择：
#    - 从叶子节点开始向上处理
#    - 如果子节点未覆盖，在当前节点安装摄像头
#    - 这样可以确保每个摄像头覆盖尽可能多的节点
#
# 时间复杂度分析：
# ────────────────────────────────────────────────────────
# - DFS 解法：O(n)，每个节点访问一次
# - BFS 解法：O(n)，每个节点访问一次
# - 空间复杂度：O(h) 或 O(w)，取决于实现方式
#
# 与暴力解法的对比：
# ────────────────────────────────────────────────────────
# - 暴力解法：尝试所有可能的摄像头位置组合
#   时间复杂度：O(2^n)，不可行
#
# - 贪心解法：使用状态转移规则，线性时间
#   时间复杂度：O(n)，高效可行
