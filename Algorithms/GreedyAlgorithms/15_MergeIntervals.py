# 解法三：贪心 — 原地修改数组
# ══════════════════════════════════════════════════════════


def merge_inplace(intervals: List[List[int]]) -> List[List[int]]:
    """
    贪心 — 原地修改数组 ⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    1. 将所有区间按开始坐标 start 升序排序
    2. 使用指针 index 记录当前合并区间的位置
    3. 遍历剩余区间：
        - 如果当前区间与 intervals[index] 重叠
          → 扩展 intervals[index][1] = max(intervals[index][1], 当前区间的 end)
        - 否则不重叠
          → index++，将当前区间放到 intervals[index] 的位置
    4. 返回 intervals[:index+1]

    为什么原地修改更高效？
    ────────────────────────────────────────────────────────
    - 不需要额外的 result 数组，节省空间
    - 直接在原数组上操作，减少内存分配
    - 适用于不需要保留原数组的场景

    图解示例：intervals = [[1,3],[2,6],[8,10],[15,18]]
    ────────────────────────────────────────────────────────
    排序后（按 start 升序）：
        [[1,3], [2,6], [8,10], [15,18]]

    原地合并过程：
        ────────────────────────────────────────────────────
        初始化：index = 0，intervals[0] = [1,3]

        i=1：[2,6] 与 [1,3] 重叠
            → intervals[0][1] = max(3,6) = 6
            → intervals = [[1,6], [2,6], [8,10], [15,18]]

        i=2：[8,10] 与 [1,6] 不重叠
            → index++ = 1
            → intervals[1] = [8,10]
            → intervals = [[1,6], [8,10], [8,10], [15,18]]

        i=3：[15,18] 与 [8,10] 不重叠
            → index++ = 2
            → intervals[2] = [15,18]
            → intervals = [[1,6], [8,10], [15,18], [15,18]]
        ────────────────────────────────────────────────────
        结果：intervals[:3] = [[1,6], [8,10], [15,18]] ✅

    图解示例：intervals = [[1,4],[4,5]]
    ────────────────────────────────────────────────────────
    排序后（按 start 升序）：
        [[1,4], [4,5]]

    原地合并过程：
        ────────────────────────────────────────────────────
        初始化：index = 0，intervals[0] = [1,4]

        i=1：[4,5] 与 [1,4] 重叠
            → intervals[0][1] = max(4,5) = 5
            → intervals = [[1,5], [4,5]]
        ────────────────────────────────────────────────────
        结果：intervals[:1] = [[1,5]] ✅

    时间复杂度：O(n log n) — 排序 O(n log n) + 遍历 O(n)
    空间复杂度：O(1) — 仅使用常数空间（原地修改）
    """
    if not intervals:
        return []

    # 按开始坐标 start 升序排序
    intervals.sort(key=lambda x: x[0])

    # index 记录当前合并区间的位置
    index = 0

    for i in range(1, len(intervals)):
        # 如果当前区间与 intervals[index] 重叠
        if intervals[i][0] <= intervals[index][1]:
            # 扩展 intervals[index] 的结束坐标
            intervals[index][1] = max(intervals[index][1], intervals[i][1])
        else:
            # 不重叠，将当前区间放到下一个位置
            index += 1
            intervals[index] = intervals[i]

    # 返回合并后的区间（原数组的前 index+1 个元素）
    return intervals[: index + 1]


# ══════════════════════════════════════════════════════════
# 重要说明：贪心算法的正确性证明
# ══════════════════════════════════════════════════════════
#
# 为什么按开始坐标排序后，合并重叠区间是正确的？
# ────────────────────────────────────────────────────────
# 1. 贪心选择性质（Greedy Choice Property）：
#    对于按开始坐标排序后的区间，合并重叠区间不会错过任何可能的合并。
#
#    证明（反证法）：
#    假设有一个最优解中，某个重叠区间没有被合并。
#    设两个重叠区间为 [a, b] 和 [c, d]，其中 a ≤ c ≤ b。
#    如果不合并它们，结果会包含两个区间 [a, b] 和 [c, d]。
#    但合并后的区间 [a, max(b, d)] 覆盖了相同的范围，且区间数更少。
#    所以不合并重叠区间不是最优解。
#
# 2. 最优子结构（Optimal Substructure）：
#    合并了当前重叠区间后，原问题可以缩小为
#    "合并剩余区间"。
#    这个子问题与原问题同构，可以继续使用贪心策略。
#
# 3. 归纳成立：
#    每一步都合并当前可合并的区间，
#    最终得到的区间集合就是全局最优解。
#
# 为什么按结束坐标排序也正确？
# ────────────────────────────────────────────────────────
# 按结束坐标排序后，我们仍然可以正确判断重叠：
# - 如果当前区间的 start ≤ 前一个合并区间的 end，说明重叠
# - 扩展当前合并区间的 end = max(当前合并区间的 end, 当前区间的 end)
#
# 两种排序方式的区别：
# ────────────────────────────────────────────────────────
# 按开始坐标排序：更直观，从左到右处理区间
# 按结束坐标排序：在某些情况下可能更高效（如果区间按结束坐标已经部分有序）
# 两种方法的时间复杂度相同，都是 O(n log n)
# ══════════════════════════════════════════════════════════
