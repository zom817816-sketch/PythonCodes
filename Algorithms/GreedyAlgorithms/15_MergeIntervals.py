"""
合并区间（Merge Intervals）

题目描述：
以数组 intervals 表示若干个区间的集合，其中单个区间为 intervals[i] = [starti, endi]。
请你合并所有重叠的区间，并返回一个不重叠的区间数组，该数组需恰好覆盖输入中的所有区间。

注意：
    区间 [1,4] 和 [4,5] 可被视为重叠区间。
    输入和输出中的区间顺序可以任意。

示例 1：
    输入：intervals = [[1,3],[2,6],[8,10],[15,18]]
    输出：[[1,6],[8,10],[15,18]]
    解释：区间 [1,3] 和 [2,6] 重叠, 将它们合并为 [1,6]。

示例 2：
    输入：intervals = [[1,4],[4,5]]
    输出：[[1,5]]
    解释：区间 [1,4] 和 [4,5] 可被视为重叠区间。

示例 3：
    输入：intervals = [[4,7],[1,4]]
    输出：[[1,7]]
    解释：区间 [1,4] 和 [4,7] 可被视为重叠区间。

解题思路总览：
───────────────────────────────────────────────────────────────────────
解法                          时间复杂度    空间复杂度    说明
───────────────────────────────────────────────────────────────────────
贪心（按开始坐标排序）          O(n log n)    O(1)        ⭐ 最优解法
贪心（按结束坐标排序）          O(n log n)    O(1)        另一种思路
贪心（原地修改数组）            O(n log n)    O(1)        节省内存
───────────────────────────────────────────────────────────────────────

核心思想（贪心 + 排序）：
───────────────────────────────────────────────────────────────────────
关键洞察：
1. 将区间按开始坐标排序，然后从左到右遍历
2. 对于当前区间，检查它是否与前一个合并区间重叠
3. 如果重叠，扩展当前合并区间的结束坐标
4. 如果不重叠，将前一个合并区间加入结果，开始新的合并

为什么按开始坐标排序？
───────────────────────────────────────────────────────────────────────
- 按开始坐标排序后，我们可以从左到右处理区间
- 对于每个区间，我们只需要与前一个合并区间比较
- 如果当前区间的开始坐标 ≤ 前一个合并区间的结束坐标，说明重叠
- 这种顺序保证了我们不会遗漏任何可能的合并

与 13_NonOverlappingIntervals 的对比：
───────────────────────────────────────────────────────────────────────
    13_NonOverlappingIntervals：区间重叠 → 必须移除一个（消除重叠）
    15_MergeIntervals：         区间重叠 → 合并为一个（利用重叠）
    相同点：都涉及区间重叠的判断，都使用贪心策略
    不同点：
        - 无重叠区间问题：目标是保留最多的不重叠区间
        - 本问题：目标是合并所有重叠区间，减少区间数量
"""

from typing import List


# ══════════════════════════════════════════════════════════
# 解法一：贪心（推荐解）— 按开始坐标排序
# ══════════════════════════════════════════════════════════


def merge(intervals: List[List[int]]) -> List[List[int]]:
    """
    贪心 — 按开始坐标排序 ⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    1. 将所有区间按开始坐标 start 升序排序
    2. 初始化当前合并区间为第一个区间
    3. 遍历剩余区间：
        - 如果当前区间的 start ≤ 当前合并区间的 end，说明重叠
          → 扩展当前合并区间的 end = max(当前合并区间的 end, 当前区间的 end)
        - 否则说明不重叠
          → 将当前合并区间加入结果，开始新的合并区间
    4. 将最后一个合并区间加入结果

    为什么贪心是正确的？
    ────────────────────────────────────────────────────────
    - 按开始坐标排序后，区间按开始位置从左到右排列
    - 对于每个区间，我们只需要与前一个合并区间比较
    - 如果重叠，合并它们不会错过任何可能的合并
    - 如果不重叠，后面的区间也不会与前一个合并区间重叠（因为开始坐标更大）

    图解示例：intervals = [[1,3],[2,6],[8,10],[15,18]]
    ────────────────────────────────────────────────────────
    排序后（按 start 升序）：
        [[1,3], [2,6], [8,10], [15,18]]

    合并过程：
        ────────────────────────────────────────────────────
        初始化：当前合并区间 = [1,3]
        [2,6]：start=2 ≤ end=3 → 重叠，扩展 end = max(3,6) = 6
        [8,10]：start=8 > end=6 → 不重叠，加入结果 [1,6]，新合并区间 = [8,10]
        [15,18]：start=15 > end=10 → 不重叠，加入结果 [8,10]，新合并区间 = [15,18]
        最后：加入结果 [15,18]
        ────────────────────────────────────────────────────
        结果：[[1,6], [8,10], [15,18]] ✅

    图解示例：intervals = [[1,4],[4,5]]
    ────────────────────────────────────────────────────────
    排序后（按 start 升序）：
        [[1,4], [4,5]]

    合并过程：
        ────────────────────────────────────────────────────
        初始化：当前合并区间 = [1,4]
        [4,5]：start=4 ≤ end=4 → 重叠，扩展 end = max(4,5) = 5
        最后：加入结果 [1,5]
        ────────────────────────────────────────────────────
        结果：[[1,5]] ✅

    图解示例：intervals = [[4,7],[1,4]]
    ────────────────────────────────────────────────────────
    排序后（按 start 升序）：
        [[1,4], [4,7]]

    合并过程：
        ────────────────────────────────────────────────────
        初始化：当前合并区间 = [1,4]
        [4,7]：start=4 ≤ end=4 → 重叠，扩展 end = max(4,7) = 7
        最后：加入结果 [1,7]
        ────────────────────────────────────────────────────
        结果：[[1,7]] ✅

    时间复杂度：O(n log n) — 排序 O(n log n) + 遍历 O(n)
    空间复杂度：O(1) — 仅使用常数空间（不考虑结果存储）
    """
    if not intervals:
        return []

    # 按开始坐标 start 升序排序
    intervals.sort(key=lambda x: x[0])

    result = []
    # 当前合并区间的 [start, end]
    cur_start, cur_end = intervals[0]

    for i in range(1, len(intervals)):
        start, end = intervals[i]
        if start <= cur_end:
            # 重叠，扩展当前合并区间的右边界
            cur_end = max(cur_end, end)
        else:
            # 不重叠，将当前合并区间加入结果，开始新的合并区间
            result.append([cur_start, cur_end])
            cur_start, cur_end = start, end

    # 最后一个合并区间
    result.append([cur_start, cur_end])

    return result


# ══════════════════════════════════════════════════════════
# 解法二：贪心 — 按结束坐标排序
# ══════════════════════════════════════════════════════════


def merge_sort_by_end(intervals: List[List[int]]) -> List[List[int]]:
    """
    贪心 — 按结束坐标排序 ⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    1. 将所有区间按结束坐标 end 升序排序
    2. 初始化当前合并区间为第一个区间
    3. 遍历剩余区间：
        - 如果当前区间的 start ≤ 当前合并区间的 end，说明重叠
          → 扩展当前合并区间的 end = max(当前合并区间的 end, 当前区间的 end)
        - 否则说明不重叠
          → 将当前合并区间加入结果，开始新的合并区间
    4. 将最后一个合并区间加入结果

    与按开始坐标排序的区别：
    ────────────────────────────────────────────────────────
    - 按开始坐标排序：更直观，从左到右处理区间
    - 按结束坐标排序：在某些情况下可能更高效（如果区间按结束坐标已经部分有序）
    - 两种方法的时间复杂度相同，都是 O(n log n)

    图解示例：intervals = [[1,3],[2,6],[8,10],[15,18]]
    ────────────────────────────────────────────────────────
    排序后（按 end 升序）：
        [[1,3], [2,6], [8,10], [15,18]]

    合并过程：
        ────────────────────────────────────────────────────
        初始化：当前合并区间 = [1,3]
        [2,6]：start=2 ≤ end=3 → 重叠，扩展 end = max(3,6) = 6
        [8,10]：start=8 > end=6 → 不重叠，加入结果 [1,6]，新合并区间 = [8,10]
        [15,18]：start=15 > end=10 → 不重叠，加入结果 [8,10]，新合并区间 = [15,18]
        最后：加入结果 [15,18]
        ────────────────────────────────────────────────────
        结果：[[1,6], [8,10], [15,18]] ✅

    图解示例：intervals = [[1,4],[4,5]]
    ────────────────────────────────────────────────────────
    排序后（按 end 升序）：
        [[1,4], [4,5]]

    合并过程：
        ────────────────────────────────────────────────────
        初始化：当前合并区间 = [1,4]
        [4,5]：start=4 ≤ end=4 → 重叠，扩展 end = max(4,5) = 5
        最后：加入结果 [1,5]
        ────────────────────────────────────────────────────
        结果：[[1,5]] ✅

    时间复杂度：O(n log n) — 排序 O(n log n) + 遍历 O(n)
    空间复杂度：O(1) — 仅使用常数空间（不考虑结果存储）
    """
    if not intervals:
        return []

    # 按结束坐标 end 升序排序
    intervals.sort(key=lambda x: x[1])

    result = []
    # 当前合并区间的 [start, end]
    cur_start, cur_end = intervals[0]

    for i in range(1, len(intervals)):
        start, end = intervals[i]
        if start <= cur_end:
            # 重叠，扩展当前合并区间的右边界
            cur_end = max(cur_end, end)
        else:
            # 不重叠，将当前合并区间加入结果，开始新的合并区间
            result.append([cur_start, cur_end])
            cur_start, cur_end = start, end

    # 最后一个合并区间
    result.append([cur_start, cur_end])

    return result


