"""
跳跃游戏 II（Jump Game II）

题目描述：
给定一个长度为 n 的 0 索引整数数组 nums。初始位置在下标 0。
每个元素 nums[i] 表示从索引 i 向后跳转的最大长度。换句话说，如果你在索引 i 处，
你可以跳转到任意 i + j 处（0 <= j <= nums[i] 且 i + j < n）。
返回到达 n - 1 的**最小跳跃次数**。测试用例保证可以到达 n - 1。

示例 1：
    输入：nums = [2, 3, 1, 1, 4]
    输出：2
    解释：跳到最后一个位置的最小跳跃数是 2。
         从下标 0 跳到下标 1（跳 1 步），
         然后从下标 1 跳 3 步到达最后一个下标。

示例 2：
    输入：nums = [2, 3, 0, 1, 4]
    输出：2

解题思路总览：
──────────────────────────────────────────────
解法              时间复杂度    空间复杂度    说明
──────────────────────────────────────────────
回溯（递归）         O(2^n)       O(n)       纯暴力，超时
记忆化 DFS           O(n²)        O(n)       自顶向下 DP
贪心（BFS 思想）      O(n)         O(1)       ⭐ 最优解法
动态规划（自底向上）   O(n²)        O(n)       标准 DP
──────────────────────────────────────────────

与跳跃游戏 I 的区别：
    跳跃游戏 I 只问"能否到达终点"（布尔值），
    跳跃游戏 II 问"最少需要跳几次"（最小步数），且保证有解。
"""

from typing import List


# ══════════════════════════════════════════════════════════
# 解法一：回溯法（Backtracking）
# ══════════════════════════════════════════════════════════

def jump_backtrack(nums: List[int]) -> int:
    """
    回溯法（纯暴力递归）

    核心思路：
    ────────────────────────────────────────────────────────
    从起点（下标 0）开始，在每个位置上枚举所有可能的跳跃步数
    （从 1 到 nums[start]），递归地尝试每条路径。
    记录所有能到达终点的路径所消耗的步数，取最小值。

    算法步骤：
    1. 从下标 0 开始递归，初始步数为 0
    2. 如果当前下标 == n - 1（终点），更新最小步数
    3. 否则枚举当前可跳的所有步数（1 ～ nums[start]）
    4. 对每个步数递归到下一个位置，步数 + 1

    时间复杂度：O(2^n) — 最坏情况下每个位置都尝试所有步数
    空间复杂度：O(n)   — 递归栈的深度

    ⚠️ 注意：这个解法在 LeetCode 上会超时（TLE），
       仅作为理解题意和回溯思想的参考。
       实际做题请用贪心或 DP 解法。
    """
    n = len(nums)
    min_steps = float('inf')

    # 边界情况：数组长度为 1，起点就是终点，不需要跳跃
    if n == 1:
        return 0

    def backtrack(start: int, steps: int) -> None:
        nonlocal min_steps

        # 剪枝：当前步数已经 >= 已知最小步数，不用继续探索
        if steps >= min_steps:
            return

        # 到达终点，更新最小步数
        if start == n - 1:
            min_steps = min(min_steps, steps)
            return

        # 当前位置能跳的最大步数
        max_steps = nums[start]

        # 枚举所有可能的步数，从大到小能更快找到较小的步数
        for step in range(max_steps, 0, -1):
            next_pos = start + step
            # 如果超出数组范围，跳过
            if next_pos >= n:
                continue
            backtrack(next_pos, steps + 1)

    backtrack(0, 0)
    return min_steps


# ══════════════════════════════════════════════════════════
# 解法二：记忆化 DFS（自顶向下动态规划）
# ══════════════════════════════════════════════════════════

def jump_dfs_memo(nums: List[int]) -> int:
    """
    记忆化 DFS（自顶向下动态规划）⭐⭐

    为什么需要记忆化？
    ────────────────────────────────────────────────────────
    回溯法会重复探索相同位置的不同路径。
    例如 nums = [2, 3, 1, ...]，从下标 0 跳 2 步到下标 2，
    和从 0→1→2 都会到达下标 2，后续的搜索完全重复。
    用一个备忘录记录"从该位置到终点的最少步数"，避免重复计算。

    状态定义：
    ────────────────────────────────────────────────────────
    memo[i] = 从下标 i 出发到达终点的最少步数
        - memo[i] = k    → 需要 k 步才能到终点
        - memo[i] = None → 还没计算过（初始值）

    算法步骤：
    1. 从下标 0 开始 DFS
    2. 如果当前下标 == n - 1（终点），返回 0（已经在了）
    3. 如果当前下标已经计算过（memo[start] != None），直接返回结果
    4. 枚举所有可能的步数，递归搜索
    5. 取所有可行路径中的最小值 + 1（当前这一步）
    6. 将结果存入 memo[start] 并返回

    时间复杂度：O(n²) — 每个位置最多计算一次，每次最多枚举 n 种步数
    空间复杂度：O(n)   — 备忘录数组 + 递归栈
    """
    n = len(nums)

    # 边界情况：数组长度为 1，起点就是终点，不需要跳跃
    if n == 1:
        return 0

    # 备忘录：None = 未计算，k = 从该位置到终点的最少步数
    memo: List[int | None] = [None] * n
    memo[n - 1] = 0  # 终点到终点需要 0 步

    def dfs(start: int) -> int:
        # 如果已经计算过，直接返回备忘录中的结果
        if memo[start] is not None:
            return memo[start]

        # 当前位置能跳的最大步数
        max_steps = nums[start]
        min_steps_from_here = float('inf')

        # 枚举所有可能的步数
        for step in range(1, max_steps + 1):
            next_pos = start + step
            # 如果下一步位置在数组范围内
            if next_pos < n:
                steps_from_next = dfs(next_pos)
                # 如果从下一步能到终点，更新最小步数
                if steps_from_next != float('inf'):
                    min_steps_from_here = min(
                        min_steps_from_here, 1 + steps_from_next
                    )

        memo[start] = min_steps_from_here
        return memo[start]

    return dfs(0)


# ══════════════════════════════════════════════════════════
# 解法三：贪心算法（BFS 思想，推荐解）
# ══════════════════════════════════════════════════════════

def jump_greedy(nums: List[int]) -> int:
    """
    贪心算法（BFS 思想）⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    把跳跃问题想象成 **BFS（广度优先搜索）**：
    第 0 跳能到达的范围是第 1 层，第 1 跳能到达的范围是第 2 层……
    我们只需要计算：最少需要多少"层"才能覆盖到终点。

    贪心策略：
    - 维护三个变量：
        - jumps：当前已跳跃的次数
        - current_end：当前这"一跳"能到达的最远边界
        - farthest：遍历过程中能到达的最远位置
    - 遍历数组的每一个位置 i：
        1. 更新 farthest = max(farthest, i + nums[i])
        2. 如果 i == current_end（到达当前跳的边界）：
            - jumps += 1（必须再多跳一步）
            - current_end = farthest（更新下一跳的边界）
            - 如果 current_end >= n - 1，提前结束

    为什么这是 BFS？
    ────────────────────────────────────────────────────────
    - 每一"跳"相当于 BFS 中的一层
    - current_end 标记当前层的右边界
    - 当 i 到达边界时，意味着当前层的所有节点都探索完毕，
      必须进入下一层（多跳一步）

    为什么这是贪心？
    ────────────────────────────────────────────────────────
    - 局部最优：每次在当前可跳范围内，选择能跳得最远的位置
    - 全局最优：最终得到从起点到终点的最少跳跃次数
    - 不需要记录具体跳法，只需要知道每跳能覆盖到的最远边界

    图解示例：nums = [2, 3, 1, 1, 4]
    ─────────────────────────────────────────────────────────────
        i   nums[i]   farthest   current_end   jumps   触发跳步？
    ─────────────────────────────────────────────────────────────
        0     2         2           2           1       ✅ i == 初始边界
        1     3         4           2           1       ❌ 未到边界
        2     1         4           4           2       ✅ 到达边界 → 返回 2
    ─────────────────────────────────────────────────────────────
        ⚡ 第 1 跳的边界是 i=0（起始），到达后跳到边界 i=2；
        ⚡ 第 2 跳的边界是 i=2，到达后边界更新为 4 ≥ n-1，结束。
        最少需要 2 跳就能到达终点 ✅

    图解示例：nums = [2, 3, 0, 1, 4]
    ─────────────────────────────────────────────────────────────
        i   nums[i]   farthest   current_end   jumps   触发跳步？
    ─────────────────────────────────────────────────────────────
        0     2         2           2           1       ✅ i == 初始边界
        1     3         4           2           1       ❌ 未到边界
        2     0         4           4           2       ✅ 到达边界 → 返回 2
    ─────────────────────────────────────────────────────────────
        ⚡ 第 1 跳的边界是 i=0（起始），到达后跳到边界 i=2；
        ⚡ 第 2 跳的边界是 i=2，到达后边界更新为 4 ≥ n-1，结束。
        最少需要 2 跳就能到达终点 ✅

    注意事项：
    ────────────────────────────────────────────────────────
    ⚡ 不需要遍历最后一个位置（i < n - 1）：
       因为到达最后一个位置时游戏已经结束，
       不需要再计算下一跳的边界。
    ⚡ O(1) 额外空间，一次遍历就能解决问题。

    时间复杂度：O(n) — 一次遍历
    空间复杂度：O(1) — 只用了几个变量
    """
    n = len(nums)

    # 边界情况：数组长度为 1，起点就是终点，不需要跳跃
    if n == 1:
        return 0

    # 跳跃次数
    jumps = 0
    # 当前这一跳能到达的最远边界
    current_end = 0
    # 遍历过程中能到达的最远位置
    farthest = 0

    # 不需要遍历最后一个位置
    # 当 i 到达 current_end 时，说明需要再跳一次
    for i in range(n - 1):
        # 更新在当前位置能到达的最远位置
        farthest = max(farthest, i + nums[i])

        # 如果已经遍历到了当前跳的边界位置
        if i == current_end:
            # 必须再多跳一步
            jumps += 1
            # 更新下一跳的边界为当前能到达的最远位置
            current_end = farthest

            # 如果已经能覆盖终点，提前结束
            if current_end >= n - 1:
                break

    return jumps


# ══════════════════════════════════════════════════════════
# 解法四：动态规划（自底向上）
# ══════════════════════════════════════════════════════════

def jump_dp(nums: List[int]) -> int:
    """
    动态规划（自底向上）⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    从前往后遍历，记录到达每个位置的最少步数。

    状态定义：
    ────────────────────────────────────────────────────────
    dp[i] = 从起点（下标 0）到达下标 i 的最少跳跃次数

    状态转移：
    ────────────────────────────────────────────────────────
    对于每个位置 i，从 i 可以跳到 i+1 ～ i+nums[i] 的任意位置。
    对于每个目标位置 j（i < j ≤ i+nums[i] 且 j < n）：
        dp[j] = min(dp[j], dp[i] + 1)

    初始化：
    ────────────────────────────────────────────────────────
    dp[0] = 0（起点不需要跳跃）
    dp[i] = INF（其他位置初始化为无穷大）

    最终答案：
    ────────────────────────────────────────────────────────
    dp[n - 1]（到达终点的最少跳跃次数）

    图解示例：nums = [2, 3, 1, 1, 4]
    ────────────────────────────────────────────────────────
        初始：dp = [0, ∞, ∞, ∞, ∞]

        i=0, nums[0]=2 → 可以更新 dp[1], dp[2]
            dp[1] = min(∞, 0+1) = 1
            dp[2] = min(∞, 0+1) = 1
            dp = [0, 1, 1, ∞, ∞]

        i=1, nums[1]=3 → 可以更新 dp[2], dp[3], dp[4]
            dp[2] = min(1, 1+1) = 1
            dp[3] = min(∞, 1+1) = 2
            dp[4] = min(∞, 1+1) = 2
            dp = [0, 1, 1, 2, 2]

        i=2, nums[2]=1 → 可以更新 dp[3]
            dp[3] = min(2, 1+1) = 2
            dp = [0, 1, 1, 2, 2]

        i=3, nums[3]=1 → 可以更新 dp[4]
            dp[4] = min(2, 2+1) = 2
            dp = [0, 1, 1, 2, 2]
    ────────────────────────────────────────────────────────
        最终 dp[4] = 2 ✅

    时间复杂度：O(n²) — 每个位置 i 需要更新最多 nums[i] 个后续位置
    空间复杂度：O(n)   — dp 数组
    """
    n = len(nums)

    # 边界情况：数组长度为 1，起点就是终点，不需要跳跃
    if n == 1:
        return 0

    # dp[i] = 从起点到下标 i 的最少跳跃次数
    # 初始化为无穷大
    INF = float('inf')
    dp = [INF] * n
    dp[0] = 0  # 起点不需要跳跃

    # 从前往后遍历每个位置
    for i in range(n):
        # 如果当前位置不可达（理论上不会发生，因为题目保证有解），跳过
        if dp[i] == INF:
            continue

        # 从 i 出发能跳到的最远位置
        furthest = min(i + nums[i], n - 1)

        # 更新所有能跳到的位置的最少步数
        for j in range(i + 1, furthest + 1):
            dp[j] = min(dp[j], dp[i] + 1)

    return dp[n - 1]