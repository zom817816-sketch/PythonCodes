"""
分发糖果（Candy）

题目描述：
n 个孩子站成一排。给你一个整数数组 ratings 表示每个孩子的评分。
你需要按照以下要求，给这些孩子分发糖果：
    1. 每个孩子至少分配到 1 个糖果。
    2. 相邻两个孩子中，评分更高的那个会获得更多的糖果。
请你给每个孩子分发糖果，计算并返回需要准备的最少糖果数目。

示例 1：
    输入：ratings = [1,0,2]
    输出：5
    解释：你可以分别给第一个、第二个、第三个孩子分发 2、1、2 颗糖果。

示例 2：
    输入：ratings = [1,2,2]
    输出：4
    解释：你可以分别给第一个、第二个、第三个孩子分发 1、2、1 颗糖果。
         第三个孩子只得到 1 颗糖果，这满足题面中的两个条件。

示例 3：
    输入：ratings = [1,3,2,2,1]
    输出：7
    解释：分别给孩子们分发 1、2、1、2、1 颗糖果。

解题思路总览：
───────────────────────────────────────────────────────────────────────
解法                      时间复杂度    空间复杂度    说明
───────────────────────────────────────────────────────────────────────
贪心算法（两次遍历）       O(n)          O(n)        ⭐ 最优解法
贪心算法（常数空间）       O(n)          O(1)        ⭐⭐ 进阶优化
暴力枚举                  O(n²)         O(n)        直观但超时
───────────────────────────────────────────────────────────────────────

核心思想：
───────────────────────────────────────────────────────────────────────
1. 每个孩子至少 1 个糖果
2. 评分高的孩子要比相邻评分低的孩子获得更多糖果
3. 关键洞察：分别考虑左右两个方向的约束，取最大值

为什么需要两次遍历？
───────────────────────────────────────────────────────────────────────
单次从左到右遍历只能保证：如果 ratings[i] > ratings[i-1]，
则 candy[i] > candy[i-1]。但无法保证 ratings[i] > ratings[i+1] 时
candy[i] > candy[i+1]。

解决方案：
- 第一次从左到右：保证右边孩子比左边孩子评分高时，右边获得更多糖果
- 第二次从右到左：保证左边孩子比右边孩子评分高时，左边获得更多糖果
- 最终每个孩子的糖果数 = max(左遍历结果, 右遍历结果)

图解示例：ratings = [1, 0, 2]
───────────────────────────────────────────────────────────────────────
第一次遍历（从左到右）：
    i=0: ratings[0]=1，无左邻居，left[0] = 1
    i=1: ratings[1]=0 < ratings[0]=1，left[1] = 1
    i=2: ratings[2]=2 > ratings[1]=0，left[2] = left[1] + 1 = 2
    left = [1, 1, 2]

第二次遍历（从右到左）：
    i=2: ratings[2]=2，无右邻居，right=1，candy[2] = max(2, 1) = 2
    i=1: ratings[1]=0 < ratings[2]=2，right=1，candy[1] = max(1, 1) = 1
    i=0: ratings[0]=1 > ratings[1]=0，right=2，candy[0] = max(1, 2) = 2
    candy = [2, 1, 2]

最终结果：2 + 1 + 2 = 5 ✅

图解示例：ratings = [1, 3, 2, 2, 1]
───────────────────────────────────────────────────────────────────────
第一次遍历（从左到右）：
    i=0: left[0] = 1
    i=1: ratings[1]=3 > ratings[0]=1，left[1] = 2
    i=2: ratings[2]=2 < ratings[1]=3，left[2] = 1
    i=3: ratings[3]=2 = ratings[2]=2，left[3] = 1
    i=4: ratings[4]=1 < ratings[3]=2，left[4] = 1
    left = [1, 2, 1, 1, 1]

第二次遍历（从右到左）：
    i=4: right=1，candy[4] = max(1, 1) = 1
    i=3: ratings[3]=2 > ratings[4]=1，right=2，candy[3] = max(1, 2) = 2
    i=2: ratings[2]=2 = ratings[3]=2，right=1，candy[2] = max(1, 1) = 1
    i=1: ratings[1]=3 > ratings[2]=2，right=2，candy[1] = max(2, 2) = 2
    i=0: ratings[0]=1 < ratings[1]=3，right=1，candy[0] = max(1, 1) = 1
    candy = [1, 2, 1, 2, 1]

最终结果：1 + 2 + 1 + 2 + 1 = 7 ✅
"""

from typing import List

# ══════════════════════════════════════════════════════════
# 解法一：贪心算法（两次遍历，推荐解）
# ══════════════════════════════════════════════════════════


def candy_greedy(ratings: List[int]) -> int:
    """
    贪心算法（两次遍历）⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    1. 第一次从左到右遍历：
       - 如果 ratings[i] > ratings[i-1]，则 left[i] = left[i-1] + 1
       - 否则 left[i] = 1（至少 1 个）
    2. 第二次从右到左遍历：
       - 维护右侧约束的糖果数 right
       - 如果 ratings[i] > ratings[i+1]，则 right += 1
       - 否则 right = 1
       - candy[i] = max(left[i], right)
    3. 累加所有孩子的糖果数

    关键洞察：
    ────────────────────────────────────────────────────────
    - 每个孩子受两个方向的约束：左边和右边
    - 最终糖果数必须同时满足两个方向的约束
    - 所以取两个方向的最大值

    图解示例：ratings = [1, 0, 2]
    ────────────────────────────────────────────────────────
    第一次遍历（从左到右，只考虑左约束）：
        left = [1, 1, 2]

    第二次遍历（从右到左，考虑右约束并取最大值）：
        i=2: right=1, candy[2] = max(2, 1) = 2
        i=1: right=1, candy[1] = max(1, 1) = 1
        i=0: right=2, candy[0] = max(1, 2) = 2

    最终：2 + 1 + 2 = 5 ✅

    时间复杂度：O(n) — 两次遍历
    空间复杂度：O(n) — 需要 left 数组
    """
    n = len(ratings)

    # 第一次遍历：从左到右，处理左约束
    left = [0] * n
    for i in range(n):
        if i > 0 and ratings[i] > ratings[i - 1]:
            # 右边孩子评分更高，糖果数 +1
            left[i] = left[i - 1] + 1
        else:
            # 至少 1 个糖果
            left[i] = 1

    # 第二次遍历：从右到左，处理右约束并累加结果
    right = 0  # 右侧约束的糖果数
    ret = 0  # 总糖果数

    for i in range(n - 1, -1, -1):
        if i < n - 1 and ratings[i] > ratings[i + 1]:
            # 左边孩子评分更高，右侧约束糖果数 +1
            right += 1
        else:
            # 至少 1 个糖果
            right = 1

        # 取两个方向的最大值，满足两个方向的约束
        ret += max(left[i], right)

    return ret


# ══════════════════════════════════════════════════════════
# 重要说明：为什么两次遍历就能保证正确？
# ══════════════════════════════════════════════════════════
#
# 问题：为什么分别从左右遍历，取最大值就是最优解？
#
# 解答：
# ────────────────────────────────────────────────────────
# 1. 从左到右遍历保证了：
#    如果 ratings[i] > ratings[i-1]，则 left[i] > left[i-1]
#    即：满足了所有"右孩子比左孩子评分高"的约束
#
# 2. 从右到左遍历保证了：
#    如果 ratings[i] > ratings[i+1]，则 right > right_for_i+1
#    即：满足了所有"左孩子比右孩子评分高"的约束
#
# 3. 取最大值保证了：
#    candy[i] = max(left[i], right)
#    同时满足两个方向的约束
#
# 4. 最小性保证：
#    left[i] 是满足左约束的最小值
#    right 是满足右约束的最小值
#    取最大值后，仍然是满足两个约束的最小值
#
# ────────────────────────────────────────────────────────
# 证明：
# 假设存在更优解 candy'[i] < candy[i] = max(left[i], right)
# 如果 candy'[i] < left[i]，则违反左约束（左边有更高的评分但糖果更少）
# 如果 candy'[i] < right，则违反右约束（右边有更高的评分但糖果更少）
# 所以 candy[i] 已经是最小可能值
# ══════════════════════════════════════════════════════════


# ══════════════════════════════════════════════════════════
# 解法二：贪心算法（爬坡法，真正 O(1) 空间）
# ══════════════════════════════════════════════════════════


def candy_greedy_slope(ratings: List[int]) -> int:
    """
    贪心算法（爬坡法）⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    把评分序列看成山脉，有上升段、下降段和平坦段。
    关键观察：
    - 上升段：每个孩子比前一个多 1 个糖果
    - 下降段：每个孩子比前一个多 1 个糖果（从右往左看）
    - 峰值点：需要取上升段和下降段的最大值

    算法步骤：
    ────────────────────────────────────────────────────────
    1. 从左到右遍历，识别上升段和下降段
    2. 上升段长度 = up，下降段长度 = down
    3. 峰值点的糖果数 = max(up, down) + 1
    4. 每遇到一个新孩子，按当前坡度累加糖果数

    峰值调整逻辑：
    ────────────────────────────────────────────────────────
    在上升段，峰值已经被分配了 (1 + up) 个糖果。
    在下降段，峰值需要 (1 + down) 个糖果才能满足约束。
    - 如果 peak >= down：峰值已有足够糖果，不需要额外增加
    - 如果 peak < down：峰值不够，需要额外 +1

    因此下降段的累加公式为：1 + down + (-1 if peak >= down else 0)
    当 peak >= down 时减 1，表示峰值已经够了，少算一个。
    当 peak < down 时不减，表示需要给峰值多算一个。

    图解示例：ratings = [1, 2, 3, 2, 1]
    ────────────────────────────────────────────────────────
    评分序列：1 → 2 → 3 → 2 → 1
              上升段   峰值  下降段

    遍历过程（ret 初始 = 1）：
    ────────────────────────────────────────────────────────
        i   prev  curr  坡度   up  down  peak  累加   ret
    ────────────────────────────────────────────────────────
        1    1     2    上升    1    0     1    +2     3
        2    2     3    上升    2    0     2    +3     6
        3    3     2    下降    0    1     2    +1     7  (peak>=down, -1)
        4    2     1    下降    0    2     2    +2     9  (peak>=down, -1)
    ────────────────────────────────────────────────────────
        结果：9 ✅
        糖果分配：[1, 2, 3, 2, 1]

    图解示例：ratings = [1, 3, 2, 2, 1]
    ────────────────────────────────────────────────────────
    评分序列：1 → 3 → 2 → 2 → 1
              上升  下降  平坦  下降

    遍历过程（ret 初始 = 1）：
    ────────────────────────────────────────────────────────
        i   prev  curr  坡度   up  down  peak  累加   ret
    ────────────────────────────────────────────────────────
        1    1     3    上升    1    0     1    +2     3
        2    3     2    下降    0    1     1    +1     4  (peak>=down, -1)
        3    2     2    平坦    0    0     0    +1     5  (重置)
        4    2     1    下降    0    1     0    +2     7  (peak<down, 不减)
    ────────────────────────────────────────────────────────
        结果：7 ✅
        糖果分配：[1, 2, 1, 2, 1]

    时间复杂度：O(n)
    空间复杂度：O(1) — 只用了几个变量
    """
    n = len(ratings)

    if n <= 1:
        return n

    ret = 1  # 第一个孩子至少 1 个糖果
    up = 0  # 当前上升段长度
    down = 0  # 当前下降段长度
    peak = 0  # 峰值点的上升段长度

    for i in range(1, n):
        if ratings[i] > ratings[i - 1]:
            # 上升段：每上升一步，糖果数 +1
            up += 1
            down = 0
            peak = up  # 记录峰值的上升长度
            ret += 1 + up
        elif ratings[i] == ratings[i - 1]:
            # 平坦段：重置所有状态
            up = 0
            down = 0
            peak = 0
            ret += 1  # 至少 1 个糖果
        else:
            # 下降段：每下降一步，糖果数 +1
            up = 0
            down += 1
            # 峰值调整：
            # 1：新孩子至少 1 颗。
            # down：因为下降段长度增加 1，之前下降段里的每个孩子糖果数都必须 +1（否则无法满足递减的相邻大小关系）。
            # 当 peak >= down 时，山峰原先的高度足够“罩住”这段下坡，山峰不需要额外糖果，但由于前面的累加方式中，山峰的糖果其实已经部分计入了下降段，为了修正重复，这里需要 -1。
            # 当 peak < down 时，山坡太长，山峰原来的糖果不够，必须把山峰再加高 1（相当于给山峰补发一颗糖），因此不减 1，这多出的 1 刚好完成山峰的补发。
            ret += 1 + down + (-1 if peak >= down else 0)

    return ret


# ══════════════════════════════════════════════════════════
# 解法三：暴力枚举（超时，仅用于理解）
# ══════════════════════════════════════════════════════════


def candy_brute_force(ratings: List[int]) -> int:
    """
    暴力枚举 ⭐

    核心思路：
    ────────────────────────────────────────────────────────
    1. 初始每个孩子 1 个糖果
    2. 不断遍历，如果发现违反约束的情况，就增加糖果
    3. 直到所有约束都满足

    图解示例：ratings = [1, 0, 2]
    ────────────────────────────────────────────────────────
    初始：candy = [1, 1, 1]

    第一轮检查：
        i=1: ratings[1]=0 < ratings[0]=1，但 candy[1]=1 >= candy[0]=1
             需要调整：candy[0] = 2
        candy = [2, 1, 1]

    第二轮检查：
        i=2: ratings[2]=2 > ratings[1]=0，但 candy[2]=1 <= candy[1]=1
             需要调整：candy[2] = 2
        candy = [2, 1, 2]

    第三轮检查：无违反，结束

    总计：2 + 1 + 2 = 5 ✅

    时间复杂度：O(n²) — 最坏情况需要多轮遍历
    空间复杂度：O(n) — 需要糖果数组
    """
    n = len(ratings)

    # 初始每个孩子 1 个糖果
    candy = [1] * n

    # 不断遍历直到所有约束满足
    changed = True
    while changed:
        changed = False
        for i in range(n):
            # 检查左邻居
            if i > 0 and ratings[i] > ratings[i - 1] and candy[i] <= candy[i - 1]:
                candy[i] = candy[i - 1] + 1
                changed = True

            # 检查右邻居
            if i < n - 1 and ratings[i] > ratings[i + 1] and candy[i] <= candy[i + 1]:
                candy[i] = candy[i + 1] + 1
                changed = True

    return sum(candy)
