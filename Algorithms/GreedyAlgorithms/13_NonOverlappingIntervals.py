"""
无重叠区间（Non-overlapping Intervals）

题目描述：
给定一个区间的集合 intervals ，其中 intervals[i] = [starti, endi]。
返回需要移除区间的最小数量，使剩余区间互不重叠。

注意：
    只在一点上接触的区间是 不重叠 的。
    例如 [1, 2] 和 [2, 3] 是不重叠的。

示例 1：
    输入：intervals = [[1,2],[2,3],[3,4],[1,3]]
    输出：1
    解释：移除 [1,3] 后，剩下的区间没有重叠。

示例 2：
    输入：intervals = [[1,2],[1,2],[1,2]]
    输出：2
    解释：你需要移除两个 [1,2] 来使剩下的区间没有重叠。

示例 3：
    输入：intervals = [[1,2],[2,3]]
    输出：0
    解释：你不需要移除任何区间，因为它们已经是无重叠的了。

解题思路总览：
───────────────────────────────────────────────────────────────────────
解法                            时间复杂度    空间复杂度    说明
───────────────────────────────────────────────────────────────────────
贪心（按结束坐标排序）             O(n log n)    O(1)        ⭐ 最优解法
贪心（按开始坐标排序）             O(n log n)    O(1)        另一种思路
───────────────────────────────────────────────────────────────────────

核心思想（贪心 + 区间调度）：
───────────────────────────────────────────────────────────────────────
本问题等价于"最大不相交区间数量"（Interval Scheduling Maximization）：
    最少移除数量 = 总区间数 - 最多能保留的不重叠区间数

关键洞察：
1. 要保留最多的不重叠区间，应该优先选择"结束最早"的区间
2. 结束时间越早，留给后续区间的空间就越大
3. 这与"用最少数量的箭引爆气球"本质上是同一类问题，
   只不过那个问题是统计"最少箭数"，这个是统计"最少移除数"

与 12_FindMinArrowShots 的对比：
───────────────────────────────────────────────────────────────────────
    12_FindMinArrowShots：     区间重叠 → 可以用同一支箭（合并重叠）
    13_NonOverlappingIntervals：区间重叠 → 必须移除一个（消除重叠）
    相同点：都涉及区间重叠的判断，都使用贪心策略
    不同点：
        - 箭的问题：重叠区间可以共享箭，不重叠才需要新箭
        - 本问题：重叠区间必须删一个，目标是保留最多的不重叠区间
"""

from typing import List


# ══════════════════════════════════════════════════════════
# 解法一：贪心（推荐解）— 按结束坐标排序
# ══════════════════════════════════════════════════════════


def eraseOverlapIntervals(intervals: List[List[int]]) -> int:
    """
    贪心 — 按结束坐标排序 ⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    问题等价于"最多能保留多少个不重叠的区间"。
    采用经典的"活动选择"（Activity Selection）贪心策略：
    优先选择结束时间最早的区间，这样能为后续区间留出最大空间。

    算法步骤：
    1. 将所有区间按结束坐标 end 升序排序
    2. 初始化 end = 第一个区间的结束坐标，count = 1（保留第一个）
    3. 遍历剩余区间：
        - 如果当前区间的 start >= end，说明不重叠
          → 保留它，更新 end 为当前区间的结束坐标，count++
        - 否则说明重叠 → 需要移除当前区间（移除数 ++，不更新 end）
    4. 返回总区间数 - 保留数（即移除数）

    为什么按 end 排序是最优的？
    ────────────────────────────────────────────────────────
    - 结束时间越早，留给后面的空间越大
    - 优先选择结束早的区间，不会比选择结束晚的区间更差
    - 这是一个经典的贪心选择性质（贪心选择安全性）

    图解示例：intervals = [[1,2],[2,3],[3,4],[1,3]]
    ────────────────────────────────────────────────────────
    排序后（按 end 升序）：
        [[1,2], [2,3], [1,3], [3,4]]
          ↑ end=2 最早

    选择过程：
        ────────────────────────────────────────────────────
        保留 [1,2]，end = 2
        [2,3]：start=2 ≥ end=2 → 不重叠，保留，end = 3
        [1,3]：start=1 < end=3 → 重叠，移除 ❌
        [3,4]：start=3 ≥ end=3 → 不重叠，保留，end = 4
        ────────────────────────────────────────────────────
        保留 3 个，总区间 4 个 → 移除 1 个 ✅

    图解示例：intervals = [[1,2],[1,2],[1,2]]
    ────────────────────────────────────────────────────────
    排序后（按 end 升序）：
        [[1,2], [1,2], [1,2]]

    选择过程：
        ────────────────────────────────────────────────────
        保留 [1,2]，end = 2
        [1,2]：start=1 < end=2 → 重叠，移除 ❌
        [1,2]：start=1 < end=2 → 重叠，移除 ❌
        ────────────────────────────────────────────────────
        保留 1 个，总区间 3 个 → 移除 2 个 ✅

    图解示例：intervals = [[1,2],[2,3]]
    ────────────────────────────────────────────────────
    排序后（按 end 升序）：
        [[1,2], [2,3]]

    选择过程：
        ────────────────────────────────────────────────────
        保留 [1,2]，end = 2
        [2,3]：start=2 ≥ end=2 → 不重叠，保留，end = 3
        ────────────────────────────────────────────────────
        保留 2 个，总区间 2 个 → 移除 0 个 ✅

    时间复杂度：O(n log n) — 排序 O(n log n) + 遍历 O(n)
    空间复杂度：O(1) — 仅使用常数空间
    """
    if not intervals:
        return 0

    # 按结束坐标 end 升序排序
    intervals.sort(key=lambda x: x[1])

    # 保留的区间数量（第一个区间默认保留）
    keep = 1
    # 当前最后一个保留区间的结束坐标
    end = intervals[0][1]

    for i in range(1, len(intervals)):
        # 如果当前区间与上一个保留区间不重叠
        if intervals[i][0] >= end:
            keep += 1
            end = intervals[i][1]
        # 否则重叠，当前区间被移除（什么也不做）

    # 需要移除的区间数 = 总区间数 - 保留的区间数
    return len(intervals) - keep


# ══════════════════════════════════════════════════════════
# 解法二：贪心 — 按开始坐标排序
# ══════════════════════════════════════════════════════════


def eraseOverlapIntervals_sort_by_start(intervals: List[List[int]]) -> int:
    """
    贪心 — 按开始坐标排序 ⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    按开始坐标排序后，从左到右遍历相邻区间。
    当发现重叠时，需要移除其中一个。为了给后面留更多空间，
    应该移除"结束坐标更大"的那个（保留结束坐标更小的）。

    算法步骤：
    1. 将所有区间按开始坐标 start 升序排序
    2. 遍历相邻区间：
        - 如果当前区间与前一个区间不重叠，继续遍历
        - 如果重叠，移除数 +1，并保留结束坐标更小的那个
          （将当前区间的 end 更新为 min(两个区间的 end)）
    3. 返回移除数

    为什么重叠时要保留结束坐标更小的？
    ────────────────────────────────────────────────────────
    两个重叠区间 [a, b] 和 [c, d]（a ≤ c ≤ b）：
        - 保留 [a, b]：后续区间 start ≥ a，但 b 可能很大，容易与后面的重叠
        - 保留 [c, d]：后续区间 start ≥ c，且 d 可能与 b 一样大或更小
        - 但最关键的判断是：谁的 end 更小，谁就更"友好"
        - 所以保留 min(b, d) 的那个区间

    图解示例：intervals = [[1,2],[2,3],[3,4],[1,3]]
    ────────────────────────────────────────────────────────
    排序后（按 start 升序）：
        [[1,2], [1,3], [2,3], [3,4]]
                ↑ 同 start 时顺序无所谓

    遍历过程：
        ────────────────────────────────────────────────────
        [1,2] ← 第一个，保留，end=2

        [1,3]：start=1 < end=2 → 重叠，移除数=1
               保留 end 更小的 → end = min(2,3) = 2

        [2,3]：start=2 ≥ end=2 → 不重叠，end=3

        [3,4]：start=3 ≥ end=3 → 不重叠，end=4
        ────────────────────────────────────────────────────
        移除 1 个 ✅

    图解示例：intervals = [[1,2],[1,2],[1,2]]
    ────────────────────────────────────────────────────────
    排序后（按 start 升序）：
        [[1,2], [1,2], [1,2]]

    遍历过程：
        ────────────────────────────────────────────────────
        [1,2] ← 第一个，保留，end=2

        [1,2]：start=1 < end=2 → 重叠，移除数=1
               end = min(2,2) = 2

        [1,2]：start=1 < end=2 → 重叠，移除数=2
               end = min(2,2) = 2
        ────────────────────────────────────────────────────
        移除 2 个 ✅

    与按 end 排序的区别：
    ────────────────────────────────────────────────────────
    按 end 排序：直接统计"最多能保留多少个不重叠区间"
    按 start 排序：发现重叠时通过 min(end) 来"模拟移除更大 end 的区间"
    两种方法最终结果相同，但按 end 排序更直观、更高效（少一次 min 操作）

    时间复杂度：O(n log n) — 排序 O(n log n) + 遍历 O(n)
    空间复杂度：O(1) — 仅使用常数空间
    """
    if not intervals:
        return 0

    # 按开始坐标 start 升序排序
    intervals.sort(key=lambda x: x[0])

    remove_count = 0
    # 当前保留区间的结束坐标（初始化为第一个区间的 end）
    end = intervals[0][1]

    for i in range(1, len(intervals)):
        if intervals[i][0] >= end:
            # 不重叠，更新 end 为当前区间的结束坐标
            end = intervals[i][1]
        else:
            # 重叠，需要移除一个
            remove_count += 1
            # 保留结束坐标更小的区间（给后面留更多空间）
            end = min(end, intervals[i][1])

    return remove_count


# ══════════════════════════════════════════════════════════
# 重要说明：贪心算法的正确性证明
# ══════════════════════════════════════════════════════════
#
# 为什么按 end 排序 + 优先选择结束最早的区间是正确的？
# ────────────────────────────────────────────────────────
# 这是经典的"活动选择问题"（Activity Selection Problem），
# 可以用数学归纳法/反证法证明贪心选择的安全性：
#
# 1. 贪心选择性质（Greedy Choice Property）：
#    存在一个最优解包含结束时间最早的区间。
#
#    证明（反证法）：
#    假设最优解 S 不包含结束最早的区间 a（end 最小）。
#    设 S 中结束最早的区间为 b，则 end(b) ≥ end(a)。
#    将 S 中的 b 替换为 a，得到 S'：
#        - a 的结束时间不晚于 b，所以 a 不会与 S 中除 b 外的其他区间重叠
#        - S' 与 S 包含相同数量的区间
#    所以 S' 也是最优解，且包含 a。
#    因此存在一个最优解包含结束最早的区间。
#
# 2. 最优子结构（Optimal Substructure）：
#    选择了结束最早的区间 a 后，原问题可以缩小为
#    "在 start ≥ end(a) 的区间中，求最大不重叠子集"。
#    这个子问题与原问题同构，可以继续使用贪心策略。
#
# 3. 归纳成立：
#    每一步都选择当前可选的、结束最早的区间，
#    最终得到的区间数量就是全局最优解。
#
# 这个证明与 12_FindMinArrowShots 的证明本质上是相同的，
# 只不过箭的问题中，重叠区间可以被同一支箭覆盖；
# 而本问题中，重叠区间必须被移除一个。
# ══════════════════════════════════════════════════════════