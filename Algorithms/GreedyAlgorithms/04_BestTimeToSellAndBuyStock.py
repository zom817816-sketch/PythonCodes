from typing import List


# 方法1：贪心算法
def maxProfit_greedy(prices: List[int]) -> int:
    """
    贪心算法：所有正差值之和

    核心思想：
    只要 prices[i+1] > prices[i]，就赚取这段差值
    这等价于把所有上升区间的利润都加起来

    为什么正确？
    - 任何上升区间 [l, r] 的利润 = prices[r] - prices[l]
    - 这等于 (prices[l+1]-prices[l]) + (prices[l+2]-prices[l+1]) + ... + (prices[r]-prices[r-1])
    - 所以累加所有正差值就等于累加所有上升区间的利润

    算法步骤：
    1. 遍历所有相邻天
    2. 如果 prices[i+1] > prices[i]，加上差值
    3. 返回总和
    """
    profit = 0
    for i in range(len(prices) - 1):
        diff = prices[i + 1] - prices[i]
        if diff > 0:
            profit += diff
    return profit


# 方法2：动态规划（状态机）
def maxProfit_dp(prices: List[int]) -> int:
    """
    动态规划：状态机解法

    状态定义：
    - cash[i] = 第i天结束时不持有股票的最大利润
    - hold[i] = 第i天结束时持有股票的最大利润

    状态转移：
    - cash[i] = max(cash[i-1], hold[i-1] + prices[i])
      → 保持不持有，或卖出持有的股票
    - hold[i] = max(hold[i-1], cash[i-1] - prices[i])
      → 保持持有，或买入股票

    初始状态：
    - cash[0] = 0（开始时不持有，没交易）
    - hold[0] = -prices[0]（买入股票）

    最终结果：
    - cash[n-1]（不持有股票时利润更高）
    """
    n = len(prices)
    if n <= 1:
        return 0

    cash = 0      # 不持有股票
    hold = -prices[0]  # 持有股票（花prices[0]买入）

    for i in range(1, n):
        # 可能的两种状态
        new_cash = max(cash, hold + prices[i])  # 不持有：之前就不持，或卖掉
        new_hold = max(hold, cash - prices[i])  # 持有：之前就持，或买入

        cash = new_cash
        hold = new_hold

    return cash  # 最终不持有股票利润更高


# 方法3：峰谷法
def maxProfit_peak_valley(prices: List[int]) -> int:
    """
    峰谷法：找到所有上升区间

    核心思想：
    1. 找到连续的上升段（从谷到峰）
    2. 每个上升段的利润 = 峰 - 谷
    3. 所有上升段利润之和就是最大利润

    算法步骤：
    1. 找到所有谷点（比前后都低）
    2. 找到所有峰点（比前后都高）
    3. 相邻的谷和峰构成一个上升段

    图解：
          峰
         / \\
    ___/     \\___
       谷       峰
    """
    n = len(prices)
    if n <= 1:
        return 0

    profit = 0
    i = 0

    while i < n - 1:
        # 找谷点（下降段起点）
        while i < n - 1 and prices[i] >= prices[i + 1]:
            i += 1
        valley = prices[i]

        # 找峰点（上升段终点）
        while i < n - 1 and prices[i] <= prices[i + 1]:
            i += 1
        peak = prices[i]

        # 累加这段利润
        profit += peak - valley

    return profit


# 方法4：双指针法

def maxProfit_two_pointer(prices: List[int]) -> int:
    """
    双指针法：左右指针遍历

    核心思想：
    - left指针指向买入时机
    - right指针向后扫描找最高卖出点
    - 当找到更高点时，计算利润并更新left

    这个方法更直观地体现了"低买高卖"
    """
    n = len(prices)
    if n <= 1:
        return 0

    profit = 0
    left, right = 0, 1

    while right < n:
        # 如果右指针价格比左指针低，更新左指针（找新的买入点）
        if prices[right] < prices[left]:
            left = right
            right += 1
        else:
            # 计算当前利润（如果现在卖出）
            current_profit = prices[right] - prices[left]
            # 如果右指针到达末尾，或者下一个价格会下降，计算利润
            if right == n - 1 or prices[right] > prices[right + 1]:
                profit += current_profit
                left = right + 1
                right = left + 1
            else:
                right += 1

    return profit