"""
划分字母区间（Partition Labels）

题目描述：
给你一个字符串 s 。我们要把这个字符串划分为尽可能多的片段，
同一字母最多出现在一个片段中。

例如，字符串 "ababcc" 能够被分为 ["abab", "cc"]，
但类似 ["aba", "bcc"] 或 ["ab", "ab", "cc"] 的划分是非法的。

注意：
    划分结果需要满足：将所有划分结果按顺序连接，得到的字符串仍然是 s。

返回一个表示每个字符串片段的长度的列表。

示例 1：
    输入：s = "ababcbacadefegdehijhklij"
    输出：[9, 7, 8]
    解释：
        划分结果为 "ababcbaca"、"defegde"、"hijhklij"。
        每个字母最多出现在一个片段中。
        像 "ababcbacadefegde", "hijhklij" 这样的划分是错误的，
        因为划分的片段数较少。

示例 2：
    输入：s = "eccbbbbdec"
    输出：[10]
    解释：
        所有字母都出现在同一个区间内，无法进一步划分。

示例 3：
    输入：s = "ababcbacadefegdehijhklij"
    输出：[9, 7, 8]

解题思路总览：
───────────────────────────────────────────────────────────────────────
解法                            时间复杂度    空间复杂度    说明
───────────────────────────────────────────────────────────────────────
贪心（双指针 + 哈希表）            O(n)         O(1)        ⭐ 最优解法
贪心（区间合并思想）               O(n)         O(1)        另一种理解方式
───────────────────────────────────────────────────────────────────────

核心思想（贪心）：
───────────────────────────────────────────────────────────────────────
关键洞察：
1. 每个字母在字符串中有一个"首次出现位置"和一个"最后出现位置"
2. 如果一个片段包含了字母 'a'，那么这个片段必须延伸到 'a' 的最后出现位置
3. 片段的结束位置 = 当前片段中所有字母的最后出现位置的最大值
4. 当遍历位置 i 恰好等于这个最大值时，就可以在此处切分

为什么贪心是正确的？
───────────────────────────────────────────────────────────────────────
- 我们要"尽可能多地划分片段"
- 这意味着每个片段应该尽可能短
- 片段最短的长度 = 当前片段中所有字母最后出现位置的最远值
- 如果提前切分，某个字母会出现在两个片段中，违反规则
- 如果延后切分，虽然合法，但片段数更少，不是最优解
"""

from typing import List


# ══════════════════════════════════════════════════════════
# 解法一：贪心（双指针 + 哈希表，推荐解）
# ══════════════════════════════════════════════════════════


def partitionLabels(s: str) -> List[int]:
    """
    贪心 — 双指针 + 哈希表 ⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    1. 预处理：遍历字符串，记录每个字母的最后出现位置
    2. 双指针遍历：维护当前片段的 start 和 end
       - end = max(end, 当前字母的最后出现位置)
       - 当 i == end 时，说明当前片段已经包含了所有需要包含的字母
       - 此时切分，记录片段长度
    3. 继续处理下一个片段

    为什么双指针有效？
    ────────────────────────────────────────────────────────
    - start：当前片段的起始位置
    - end：当前片段中所有字母最后出现位置的最远值
    - 遍历到 i 时，如果 i < end，说明当前片段还没"收拢"
    - 当 i == end 时，当前片段恰好"收拢"，可以切分

    图解示例：s = "ababcbacadefegdehijhklij"
    ────────────────────────────────────────────────────────
    预处理：记录每个字母的最后出现位置
    ────────────────────────────────────────────────────────
        a: 8,  b: 5,  c: 7,  d: 14, e: 15, f: 11,
        g: 13, h: 19, i: 22, j: 23, k: 20, l: 21

    双指针遍历过程：
    ────────────────────────────────────────────────────────
        i   ch   last_pos[ch]   end    end==i?   操作
    ────────────────────────────────────────────────────────
        0   a         8          8       No      更新 end=8
        1   b         5          8       No      end 不变
        2   a         8          8       No      end 不变
        3   b         5          8       No      end 不变
        4   c         7          8       No      end 不变
        5   b         5          8       No      end 不变
        6   a         8          8       No      end 不变
        7   c         7          8       No      end 不变
        8   a         8          8      Yes!     切分！长度=8-0+1=9
                                                    start=9
        9   d        14         14       No      更新 end=14
       10   e        15         15       No      更新 end=15
       11   f        11         15       No      end 不变
       12   e        15         15       No      end 不变
       13   g        13         15       No      end 不变
       14   d        14         15       No      end 不变
       15   e        15         15      Yes!     切分！长度=15-9+1=7
                                                    start=16
       16   h        19         19       No      更新 end=19
       17   i        22         22       No      更新 end=22
       18   j        23         23       No      更新 end=23
       19   h        19         23       No      end 不变
       20   k        20         23       No      end 不变
       21   l        21         23       No      end 不变
       22   i        22         23       No      end 不变
       23   j        23         23      Yes!     切分！长度=23-16+1=8
    ────────────────────────────────────────────────────────
        结果：[9, 7, 8] ✅

    图解示例：s = "eccbbbbdec"
    ────────────────────────────────────────────────────────
    预处理：
        e: 8,  c: 9,  b: 6,  d: 7

    双指针遍历过程：
    ────────────────────────────────────────────────────────
        i   ch   last_pos[ch]   end    end==i?   操作
    ────────────────────────────────────────────────────────
        0   e         8          8       No      更新 end=8
        1   c         9          9       No      更新 end=9
        2   c         9          9       No      end 不变
        3   b         6          9       No      end 不变
        4   b         6          9       No      end 不变
        5   b         6          9       No      end 不变
        6   b         6          9       No      end 不变
        7   d         7          9       No      end 不变
        8   e         8          9       No      end 不变
        9   c         9          9      Yes!     切分！长度=9-0+1=10
    ────────────────────────────────────────────────────────
        结果：[10] ✅

    时间复杂度：O(n) — 两次遍历字符串（预处理 + 双指针）
    空间复杂度：O(1) — 哈希表最多存储 26 个字母（常数空间）
    """
    # 预处理：记录每个字母的最后出现位置
    last_pos = {}
    for i, ch in enumerate(s):
        last_pos[ch] = i

    result = []
    start = 0  # 当前片段的起始位置
    end = 0    # 当前片段的结束位置（所有字母最后出现位置的最远值）

    for i, ch in enumerate(s):
        # 更新当前片段的结束位置
        # 当前字母 ch 的最后出现位置可能扩展当前片段的边界
        end = max(end, last_pos[ch])

        # 当遍历位置 i 到达当前片段的边界时，可以切分
        if end == i:
            result.append(end - start + 1)
            start = i + 1  # 下一个片段从 i+1 开始

    return result


# ══════════════════════════════════════════════════════════
# 解法二：贪心 — 区间合并思想
# ══════════════════════════════════════════════════════════


def partitionLabels_intervals(s: str) -> List[int]:
    """
    贪心 — 区间合并思想 ⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    把每个字母看作一个"区间"[first_pos, last_pos]，
    问题转化为：将这些区间合并成尽可能多的不相交区间组，
    每组对应一个字符串片段。

    为什么可以这样做？
    ────────────────────────────────────────────────────────
    - 字母 'a' 首次出现在位置 2，最后出现在位置 8
      → 'a' 的区间是 [2, 8]
    - 任何包含 'a' 的片段，必须覆盖 [2, 8] 这个区间
    - 如果两个字母的区间有重叠，它们必须在同一个片段中
    - 如果两个字母的区间不重叠，它们可以在不同片段中

    算法步骤：
    1. 预处理：记录每个字母的 [first_pos, last_pos]
    2. 按 first_pos 排序所有区间
    3. 合并重叠区间（类似 13_NonOverlappingIntervals 的逆过程）
    4. 每个合并后的区间长度就是一个片段的长度

    图解示例：s = "ababcbacadefegdehijhklij"
    ────────────────────────────────────────────────────────
    预处理：每个字母的区间 [first, last]
    ────────────────────────────────────────────────────────
        a:[0,8],  b:[1,5],  c:[4,7],  d:[9,14], e:[10,15],
        f:[11,11], g:[13,13], h:[16,19], i:[17,22], j:[18,23],
        k:[20,20], l:[21,21]

    按 first_pos 排序后合并重叠区间：
    ────────────────────────────────────────────────────────
        当前区间 [0,8]（a）
            b:[1,5] 与 [0,8] 重叠 → 合并为 [0,8]
            c:[4,7] 与 [0,8] 重叠 → 合并为 [0,8]
            → 片段长度 = 8-0+1 = 9

        当前区间 [9,14]（d）
            e:[10,15] 与 [9,14] 重叠 → 合并为 [9,15]
            f:[11,11] 与 [9,15] 重叠 → 合并为 [9,15]
            g:[13,13] 与 [9,15] 重叠 → 合并为 [9,15]
            → 片段长度 = 15-9+1 = 7

        当前区间 [16,19]（h）
            i:[17,22] 与 [16,19] 重叠 → 合并为 [16,22]
            j:[18,23] 与 [16,22] 重叠 → 合并为 [16,23]
            k:[20,20] 与 [16,23] 重叠 → 合并为 [16,23]
            l:[21,21] 与 [16,23] 重叠 → 合并为 [16,23]
            → 片段长度 = 23-16+1 = 8
    ────────────────────────────────────────────────────────
        结果：[9, 7, 8] ✅

    与解法一的关系：
    ────────────────────────────────────────────────────────
    解法一的双指针本质上就是在做"隐式"的区间合并：
        - end = max(end, last_pos[ch]) 就是在扩展当前合并区间的右边界
        - 当 i == end 时，当前合并区间结束，可以切分
    解法二是显式地构造区间再合并，理解上更直观，但代码稍复杂。

    时间复杂度：O(n) — 预处理 O(n) + 合并 O(k)，k ≤ 26
    空间复杂度：O(1) — 最多 26 个字母的区间
    """
    if not s:
        return []

    # 预处理：记录每个字母的 [first_pos, last_pos]
    intervals = {}  # ch -> [first, last]
    for i, ch in enumerate(s):
        if ch not in intervals:
            intervals[ch] = [i, i]
        else:
            intervals[ch][1] = i

    # 按 first_pos 排序所有区间
    sorted_intervals = sorted(intervals.values(), key=lambda x: x[0])

    result = []
    # 当前合并区间的 [start, end]
    cur_start, cur_end = sorted_intervals[0]

    for i in range(1, len(sorted_intervals)):
        start, end = sorted_intervals[i]
        if start > cur_end:
            # 不重叠，切分当前区间
            result.append(cur_end - cur_start + 1)
            cur_start, cur_end = start, end
        else:
            # 重叠，扩展当前合并区间的右边界
            cur_end = max(cur_end, end)

    # 最后一个区间
    result.append(cur_end - cur_start + 1)

    return result


# ══════════════════════════════════════════════════════════
# 重要说明：贪心算法的正确性证明
# ══════════════════════════════════════════════════════════
#
# 为什么"当 i == end 时切分"是正确的？
# ────────────────────────────────────────────────────────
# 1. 必要性：
#    对于当前片段 [start, i]，end 是片段中所有字母最后出现位置的最大值。
#    如果 i < end，说明某个字母的最后出现位置还在后面，
#    此时切分会把这个字母分到两个片段中，违反规则。
#    所以必须等到 i == end 才能切分。
#
# 2. 充分性：
#    当 i == end 时，当前片段 [start, i] 中所有字母的最后出现位置
#    都不超过 i，所以这些字母不会出现在后面的片段中。
#    此时切分是合法的。
#
# 3. 最优性（片段数最多）：
#    我们在"最早可以切分的位置"就切分，
#    这样每个片段都尽可能短，总片段数就尽可能多。
#    如果延后切分，虽然合法，但会减少片段数。
#
# 这个贪心策略满足：
#    - 贪心选择性质：最早切分不会导致更差的结果
#    - 最优子结构：切分后，剩余子问题与原问题同构
# ══════════════════════════════════════════════════════════