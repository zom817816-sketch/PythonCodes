# 解法四：动态规划（自底向上）
# ══════════════════════════════════════════════════════════

def canJump_dp(nums: List[int]) -> bool:
    """
    动态规划（自底向上）⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    从后往前遍历，记录每个位置是否能到达终点。

    状态定义：
    ────────────────────────────────────────────────────────
    dp[i] = 从下标 i 出发能否到达终点
        - dp[i] = True  → 能到达终点
        - dp[i] = False → 不能到达终点

    状态转移：
    ────────────────────────────────────────────────────────
    dp[i] = OR(dp[i+1], dp[i+2], ..., dp[i+nums[i]])
    只要从 i 能跳到的任意位置 j（i < j ≤ i+nums[i]）
    满足 dp[j] = True，那么 dp[i] = True。

    初始化：
    ────────────────────────────────────────────────────────
    dp[n-1] = True（最后一个位置当然能到达终点）

    最终答案：
    ────────────────────────────────────────────────────────
    dp[0]（从起点能否到达终点）

    图解示例：nums = [2, 3, 1, 1, 4]
    ────────────────────────────────────────────────────────
        i   nums[i]   能跳到的位置      dp[i]
    ────────────────────────────────────────────────────────
        4     4         -              True（终点）
        3     1         4              True（dp[4] 可达）
        2     1         3              True（dp[3] 可达）
        1     3         2,3,4          True（dp[4] 可达）
        0     2         1,2            True（dp[1] 可达）
    ────────────────────────────────────────────────────────
        最终 dp[0] = True ✅

    时间复杂度：O(n²) — 每个位置 i 需要检查最多 nums[i] 个后续位置
    空间复杂度：O(n)   — dp 数组
    """
    n = len(nums)

    # 边界情况：数组长度为 1，起点就是终点
    if n == 1:
        return True

    # dp[i] = 从下标 i 出发能否到达终点
    dp = [False] * n
    dp[n - 1] = True  # 终点本身当然能到达终点

    # 从后往前遍历（因为 dp[i] 依赖后面的 dp 值）
    for i in range(n - 2, -1, -1):
        # 从 i 出发能跳到的最远位置
        # 不能超过 n-1，避免越界
        furthest = min(i + nums[i], n - 1)

        # 检查从 i 能跳到的所有位置中，
        # 是否存在一个位置 j，使得 dp[j] = True
        for j in range(i + 1, furthest + 1):
            if dp[j]:
                dp[i] = True
                break  # 找到一个就够，不用继续找了

    return dp[0]