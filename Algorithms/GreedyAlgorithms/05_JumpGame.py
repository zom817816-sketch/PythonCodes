# 解法三：贪心算法（推荐解）
# ══════════════════════════════════════════════════════════

def canJump_greedy(nums: List[int]) -> bool:
    """
    贪心算法⭐⭐⭐

    核心思想：
    ────────────────────────────────────────────────────────
    跳几步无所谓，关键在于可跳的 **覆盖范围**。
    问题转化为：跳跃覆盖范围究竟能不能覆盖到终点？

    贪心策略：
    - 维护一个变量 max_reach，表示当前能到达的最远下标
    - 遍历数组的每一个位置 i：
        1. 如果 i > max_reach → 当前位置不可达，返回 False
        2. 更新 max_reach = max(max_reach, i + nums[i])
        3. 如果 max_reach >= n-1 → 已经能覆盖终点，返回 True
    - 遍历结束后返回 True

    为什么这是贪心？
    ────────────────────────────────────────────────────────
    - 局部最优：每次取最大跳跃步数，尽可能扩大覆盖范围
    - 全局最优：最终得到整体最大覆盖范围，判断能否到终点
    - 不需要记录具体跳法，只需要知道最远能到哪里

    这个算法的巧妙之处：
    ────────────────────────────────────────────────────────
    ⚡ 只用 O(1) 额外空间，一次遍历就能解决问题。
    ⚡ 不需要回溯，因为"能到达 i"就意味着
       能到达 0 ~ i 之间的任意位置（因为步数可以灵活选择）。

    图解示例：nums = [2, 3, 1, 1, 4]
    ────────────────────────────────────────────────────────
        i   nums[i]   i+nums[i]   max_reach   能到终点？
    ────────────────────────────────────────────────────────
        0     2          2           2          No
        1     3          4           4          Yes! → 返回 True
    ────────────────────────────────────────────────────────
        第1天就能跳到终点，所以答案是 True。

    图解示例：nums = [3, 2, 1, 0, 4]
    ────────────────────────────────────────────────────────
        i   nums[i]   i+nums[i]   max_reach   能到终点？
    ────────────────────────────────────────────────────────
        0     3          3           3          No
        1     2          3           3          No
        2     1          3           3          No
        3     0          3           3          No
        4     -          -           -          i=4 > max_reach=3
                                               → 返回 False
    ────────────────────────────────────────────────────────
        在下标3卡住了，无法再前进，所以答案是 False。

    时间复杂度：O(n) — 一次遍历
    空间复杂度：O(1) — 只用一个变量
    """
    # 当前能到达的最远下标
    max_reach = 0

    for i in range(len(nums)):
        # 如果当前位置不可达（i 超过了 max_reach），返回 False
        # 这意味着我们在到达 i 之前就已经"跳不动"了
        if i > max_reach:
            return False

        # 更新最远可达位置
        # i + nums[i] = 从当前位置起跳能到达的最远位置
        max_reach = max(max_reach, i + nums[i])

        # 如果最远位置已覆盖终点，提前返回
        if max_reach >= len(nums) - 1:
            return True

    return True


