"""
跳跃游戏（Jump Game）

题目描述：
给你一个非负整数数组 nums ，你最初位于数组的 第一个下标 。
数组中的每个元素代表你在该位置可以跳跃的最大长度。
判断你是否能够到达最后一个下标，如果可以，返回 true ；否则，返回 false 。

示例 1：
    输入：nums = [2, 3, 1, 1, 4]
    输出：true
    解释：可以先跳 1 步，从下标 0 到达下标 1,
          然后再从下标 1 跳 3 步到达最后一个下标。

示例 2：
    输入：nums = [3, 2, 1, 0, 4]
    输出：false
    解释：无论怎样，总会到达下标为 3 的位置。
          但该下标的最大跳跃长度是 0，所以永远不可能到达最后一个下标。

解题思路总览：
──────────────────────────────────────────────
解法            时间复杂度    空间复杂度    说明
──────────────────────────────────────────────
回溯（递归）       O(2^n)       O(n)       纯暴力，超时
记忆化 DFS         O(n²)        O(n)       自顶向下 DP
贪心               O(n)         O(1)       ⭐ 最优解法
动态规划（自底向上） O(n²)       O(n)       标准 DP
──────────────────────────────────────────────
"""

from typing import List


# ══════════════════════════════════════════════════════════
# 解法一：回溯法（Backtracking）
# ══════════════════════════════════════════════════════════

def canJump_backtrack(nums: List[int]) -> bool:
    """
    回溯法（纯暴力递归）

    核心思路：
    ────────────────────────────────────────────────────────
    从起点（下标 0）开始，在每个位置上枚举所有可能的跳跃步数
    （从 1 到 nums[start]），递归地尝试每条路径。
    只要有一条路径能到达终点，就返回 True。

    算法步骤：
    1. 从下标 0 开始递归
    2. 如果当前下标 == n-1（终点），标记成功
    3. 否则枚举当前可跳的所有步数（1 ～ nums[start]）
    4. 对每个步数递归到下一个位置

    时间复杂度：O(2^n) — 最坏情况下每个位置都尝试所有步数
    空间复杂度：O(n)   — 递归栈的深度

    ⚠️ 注意：这个解法在 LeetCode 上会超时（TLE），
       仅作为理解题意和回溯思想的参考。
       实际做题请用贪心或 DP 解法。
    """
    possible = False
    n = len(nums)

    # 边界情况：数组长度为 1，起点就是终点
    if n == 1:
        return True

    def backtrack(start: int) -> None:
        nonlocal possible

        # 剪枝：已经找到可行路径，停止搜索
        if possible:
            return

        # 到达终点，标记成功
        if start == n - 1:
            possible = True
            return

        # 当前位置能跳的最大步数
        max_steps = nums[start]

        # 枚举所有可能的步数，从大到小能更快找到可行路径
        for step in range(max_steps, 0, -1):
            next_pos = start + step
            # 如果超出数组范围，跳过
            if next_pos >= n:
                continue
            backtrack(next_pos)

    backtrack(0)
    return possible


# ══════════════════════════════════════════════════════════
# 解法二：记忆化 DFS（自顶向下动态规划）
# ══════════════════════════════════════════════════════════

def canJump_dfs_memo(nums: List[int]) -> bool:
    """
    记忆化 DFS（自顶向下动态规划）⭐⭐

    为什么需要记忆化？
    ────────────────────────────────────────────────────────
    回溯法会重复探索相同位置的不同路径。
    例如 nums = [2, 3, 1, ...]，从下标 0 跳 2 步到下标 2，
    和从 0→1→2 都会到达下标 2，后续的搜索完全重复。
    用一个备忘录记录"从该位置能否到达终点"，避免重复计算。

    状态定义：
    ────────────────────────────────────────────────────────
    memo[i] 表示从下标 i 出发能否到达终点：
        - memo[i] = True  → 能到达终点
        - memo[i] = False → 不能到达终点
        - memo[i] = None  → 还没计算过（初始值）

    算法步骤：
    1. 从下标 0 开始 DFS
    2. 如果当前下标 == n-1（终点），返回 True
    3. 如果当前下标已经计算过（memo[start] != None），直接返回结果
    4. 枚举所有可能的步数，递归搜索
    5. 如果任意一条路径成功，memo[start] = True，否则 False
    6. 返回 memo[start]

    时间复杂度：O(n²) — 每个位置最多计算一次，每次最多枚举 n 种步数
    空间复杂度：O(n)   — 备忘录数组 + 递归栈
    """
    n = len(nums)

    # 边界情况：数组长度为 1，起点就是终点
    if n == 1:
        return True

    # 备忘录：None=未计算, True=能到终点, False=不能到终点
    memo: List[bool | None] = [None] * n
    memo[n - 1] = True  # 终点一定能到达终点

    def dfs(start: int) -> bool:
        # 如果已经计算过，直接返回备忘录中的结果
        if memo[start] is not None:
            return memo[start]

        # 当前位置能跳的最大步数
        max_steps = nums[start]

        # 枚举所有可能的步数
        for step in range(max_steps, 0, -1):
            next_pos = start + step
            # 如果下一步位置在数组范围内，且从那里能到终点
            if next_pos < n and dfs(next_pos):
                memo[start] = True
                return True

        # 所有步数都无法到达终点
        memo[start] = False
        return False

    return dfs(0)


