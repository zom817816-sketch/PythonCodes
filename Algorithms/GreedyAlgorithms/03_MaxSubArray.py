"""
给你一个整数数组 nums ，请你找出一个具有最大和的连续子数组（子数组最少包含一个元素），返回其最大和。

子数组是数组中的一个连续部分。

 示例 1：

 输入：nums = [-2,1,-3,4,-1,2,1,-5,4]
 输出：6
 解释：连续子数组 [4,-1,2,1] 的和最大，为 6 。
 示例 2：

 输入：nums = [1]
 输出：1
 示例 3：

 输入：nums = [5,4,-1,7,8]
 输出：23
"""

from typing import List


def maxSubArray(nums: List[int]) -> int:
    """
    暴力解法：枚举所有可能的子数组，计算和并记录最大值。⭐
    时间复杂度：O(n^2) — 双循环遍历所有起点和终点
    空间复杂度：O(1)   — 只使用了几个变量
    """
    # 注意：如果数组全为负数，最大子数组和也应是负数，因此初始化为负无穷更安全
    result = -float("inf")

    # 枚举子数组的起点 i
    for i in range(len(nums)):
        cur_sum = 0  # 记录从 i 开始的当前连续子数组的和
        # 枚举子数组的终点 j
        for j in range(i, len(nums)):
            cur_sum += nums[j]  # 每次向右扩展一个元素
            result = max(result, cur_sum)  # 更新全局最大和

    return result


# 贪婪解法
"""
贪心解法
贪心贪的是哪里呢？
如果 -2 1 在一起，计算起点的时候，一定是从 1 开始计算，因为负数只会拉低总和，这就是贪心贪的地方！
局部最优：当前“连续和”为负数的时候立刻放弃，从下一个元素重新计算“连续和”，因为负数加上下一个元素 “连续和”只会越来越小。
全局最优：选取最大“连续和”
局部最优的情况下，并记录最大的“连续和”，可以推出全局最优。
从代码角度上来讲：遍历 nums，从头开始用 count 累积，如果 count 一旦加上 nums[i]变为负数，那么就应该从 nums[i+1]开始从 0 累积 count 了，因为已经变为负数的 count，只会拖累总和。
这相当于是暴力解法中的不断调整最大子序和区间的起始位置。
"""


def maxSubArray_Greedy(nums: List[int]) -> int:
    """
    贪心解法：⭐⭐
    核心思想：如果当前累积的和 cur_sum 变为负数，就立即丢弃，从下一个元素重新开始累积。
    因为负数只会拖累后续的和（加负数必然变小）。

    局部最优：遇到负累积和就清零重新开始。
    全局最优：过程中不断更新全局最大和。

    时间复杂度：O(n)
    空间复杂度：O(1)
    """
    result = -float("inf")  # 用负无穷初始化，避免全负数数组得到 0 的错误
    cur_sum = 0  # 当前连续子数组的和

    for i in range(len(nums)):
        cur_sum += nums[i]  # 先累加当前元素

        # 即使加了负数导致 cur_sum 变小，只要它仍大于已记录的 result，就更新
        if cur_sum > result:
            result = cur_sum

        # 贪心的关键：如果当前和变为负数，它对后续和的贡献为负，直接清零
        if cur_sum < 0:
            cur_sum = 0

    return result


"""
动态规划常用于求解最优化问题。最大子数组和问题可以这样定义状态：
状态定义：
dp[i] 表示以第 i 个元素结尾的连续子数组的最大和。
转移方程：
对于 nums[i]，只有两种选择：
要么把它接在前面的子数组后面：dp[i-1] + nums[i]
要么自己单独成为一个新的子数组：nums[i]
取两者的最大值：dp[i] = max(dp[i-1] + nums[i], nums[i])
最终答案：
最大子数组和不一定是 dp[n-1]，因为最大和可能以任何一个元素结尾，所以答案是 max(dp)。
空间优化：
计算 dp[i] 时只用到了 dp[i-1]，因此可以用一个变量代替整个数组，把空间复杂度从 O(n) 降到 O(1)。
"""


def maxSubArray_DP(nums: List[int]) -> int:
    """
    动态规划解法（空间优化版）⭐⭐⭐
    时间复杂度：O(n)
    空间复杂度：O(1)
    """
    # dp 表示以当前元素结尾的最大子数组和
    dp = nums[0]  # 初始化（第一个元素）
    result = dp  # 全局最大和

    # 从第二个元素开始遍历
    for i in range(1, len(nums)):
        # 状态转移：要么延续之前的子数组，要么从当前元素重新开始
        dp = max(dp + nums[i], nums[i])
        # 更新全局最大值
        result = max(result, dp)

    return result
