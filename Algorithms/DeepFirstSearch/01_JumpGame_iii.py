# 解法三：记忆化 DFS（自顶向下动态规划）
# ══════════════════════════════════════════════════════════

def canReach_memo(arr: List[int], start: int) -> bool:
    """
    记忆化 DFS（自顶向下动态规划）⭐⭐

    为什么需要记忆化？
    ────────────────────────────────────────────────────────
    纯 DFS 加 visited 虽然能避免死循环，但如果有环存在，
    会导致进入环之后整条路径被废弃，而无法复用一些中间
    结果。使用 memo 记录"从某个位置能否到达 0"，可以
    把结果缓存下来，避免重复搜索。

    三种状态标记：
    ────────────────────────────────────────────────────────
    memo[i] =  0  → 未探索
    memo[i] =  1  → 正在探索中（用于检测当前递归路径中的环）
    memo[i] =  2  → 从该位置可以到达 0
    memo[i] = -1  → 从该位置无法到达 0

    算法步骤：
    1. 如果下标越界，返回 False
    2. 如果 memo[i] == -1，说明之前已确认此路不通，直接返回 False
    3. 如果 memo[i] == 2，说明之前已确认此路可通，直接返回 True
    4. 如果 memo[i] == 1，说明在当前递归路径中形成了环，返回 False
    5. 标记 memo[i] = 1（正在探索中）
    6. 递归探索 i + arr[i] 和 i - arr[i]
    7. 根据递归结果更新 memo[i] = 2（可通）或 memo[i] = -1（不通）

    为什么需要状态 1（正在探索中）？
    ────────────────────────────────────────────────────────
    只用 visited 标记无法区分"环"和"已确认不可达"：
        - 进入环后，回到环上的某个节点 → visited 标记该节点
          已访问，直接返回 False，但环上的其他节点可能从另一
          条路径到达，如果不加区分会误判
        - 状态 1 能检测在当前递归路径中是否形成了环（死路），
          而状态 -1 和 2 是全局持久化的结论

    示例：arr = [1, 1, 1], start = 0
    ────────────────────────────────────────────────────────
    索引:  0    1    2
    值:   [1,   1,   1]

    DFS 轨迹（memo 变化）：
        dfs(0) → memo[0]=1
        ├─ dfs(0+1=1) → memo[1]=1
        │   ├─ dfs(1+1=2) → memo[2]=1
        │   │   ├─ dfs(2+1=3) → 越界 → False
        │   │   └─ dfs(2-1=1) → memo[1]==1 → 环！→ False
        │   │   → memo[2]=-1
        │   └─ dfs(1-1=0) → memo[0]==1 → 环！→ False
        │   → memo[1]=-1
        └─ dfs(0-1=-1) → 越界 → False
        → memo[0]=-1
    最终返回 False

    时间复杂度：O(n) — 每个下标最多计算一次
    空间复杂度：O(n) — memo 数组 + 递归栈
    """
    n = len(arr)
    # 状态：0 = 未探索，1 = 探索中，2 = 可达，-1 = 不可达
    memo = [0] * n

    def dfs(i: int) -> bool:
        # 越界
        if i < 0 or i >= n:
            return False

        # 之前已确认不可达
        if memo[i] == -1:
            return False
        # 之前已确认可达
        if memo[i] == 2:
            return True
        # 在当前递归路径中形成了环
        if memo[i] == 1:
            return False

        # 找到目标
        if arr[i] == 0:
            memo[i] = 2
            return True

        # 标记为"正在探索中"
        memo[i] = 1

        # 尝试两个方向
        if dfs(i + arr[i]) or dfs(i - arr[i]):
            memo[i] = 2
            return True

        # 两个方向都走不通
        memo[i] = -1
        return False

    return dfs(start)


# ══════════════════════════════════════════════════════════
# 解法四：迭代 DFS + 栈
# ══════════════════════════════════════════════════════════

def canReach_iterative_dfs(arr: List[int], start: int) -> bool:
    """
    迭代 DFS + 栈（手动模拟递归）⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    用显式栈（后进先出）代替递归中的函数调用栈，实现深度
    优先搜索。本质上与递归 DFS 相同，只是避免了递归调用
    栈溢出（Python 默认递归深度限制约 1000）。

    与 BFS 的区别：
    ────────────────────────────────────────────────────────
    BFS：用队列（FIFO），逐层向外探索
    DFS：用栈  （LIFO），沿着一条路径走到黑

    BFS 和迭代 DFS 的代码非常相似，区别仅在于：
        - BFS：queue.pop(0)              → 从头部取出
               queue.append(jump)         → 加入尾部
        - 迭代 DFS：stack.pop()           → 从尾部取出（默认）
                    stack.append(jump)    → 加入尾部

    算法步骤：
    1. 初始化栈为 [start]，visited 标记 start
    2. 循环直到栈为空：
        a. 弹出栈顶元素 i
        b. 如果 arr[i] == 0，返回 True
        c. 将 i + arr[i] 和 i - arr[i]（有效且未访问）压入栈
    3. 栈为空仍未找到，返回 False

    时间复杂度：O(n) — 每个下标最多入栈一次
    空间复杂度：O(n) — 栈 + visited 数组
    """
    n = len(arr)
    visited = [False] * n
    stack = [start]
    visited[start] = True

    while stack:
        i = stack.pop()

        # 找到目标
        if arr[i] == 0:
            return True

        # 先压右跳再压左跳（不影响正确性，只影响遍历顺序）
        # 这样左跳会在栈顶，先被探索（模拟递归中左分支先探索的顺序）
        for jump in (i + arr[i], i - arr[i]):
            if 0 <= jump < n and not visited[jump]:
                visited[jump] = True
                stack.append(jump)

    return False
