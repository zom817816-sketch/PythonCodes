"""
跳跃游戏 III（Jump Game III）

题目描述：
给定一个非负整数数组 arr，你最开始位于该数组的起始下标 start 处。
当你位于下标 i 处时，你可以跳到 i + arr[i] 或者 i - arr[i]。
请你判断自己是否能够跳到对应元素值为 0 的 任一 下标处。
注意，不管是什么情况下，你都无法跳到数组之外。

示例 1：
    输入：arr = [4, 2, 3, 0, 3, 1, 2], start = 5
    输出：true
    解释：
        到达值为 0 的下标 3 有以下可能方案：
        下标 5 -> 下标 4 -> 下标 1 -> 下标 3
        下标 5 -> 下标 6 -> 下标 4 -> 下标 1 -> 下标 3

示例 2：
    输入：arr = [4, 2, 3, 0, 3, 1, 2], start = 0
    输出：true
    解释：
        到达值为 0 的下标 3 有以下可能方案：
        下标 0 -> 下标 4 -> 下标 1 -> 下标 3

示例 3：
    输入：arr = [3, 0, 2, 1, 2], start = 2
    输出：false
    解释：无法到达值为 0 的下标 1 处。

解题思路总览：
──────────────────────────────────────────────────────────────────
解法                    时间复杂度    空间复杂度    说明
──────────────────────────────────────────────────────────────────
DFS + 已访问集合（递归）    O(n)          O(n)       递归回溯，直觉解法
BFS + 已访问集合（迭代）    O(n)          O(n)       ⭐ 最优解，层次遍历
记忆化 DFS（自顶向下 DP）   O(n)          O(n)       带状态缓存的 DFS
迭代 DFS + 栈              O(n)          O(n)       显式栈模拟递归
──────────────────────────────────────────────────────────────────

与跳跃游戏 I / II 的区别：
    跳跃游戏 I：从起点出发，每次可以向右跳 [0, nums[i]] 步，问能否到达终点。
    跳跃游戏 II：从起点出发，每次可以向右跳 [0, nums[i]] 步，问最少跳几次到终点。
    跳跃游戏 III：每次只能跳 exactly arr[i] 步，但可以向左或向右跳（更像图遍历）。
"""

from typing import List


# ══════════════════════════════════════════════════════════
# 解法一：DFS + 已访问集合（递归）
# ══════════════════════════════════════════════════════════

def canReach_dfs(arr: List[int], start: int) -> bool:
    """
    DFS + 已访问集合（递归）⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    把数组看作一个有向图，每个下标 i 有两条出边：
        - i + arr[i]（向右跳）
        - i - arr[i]（向左跳）
    从 start 出发做深度优先搜索，搜索过程中用 visited 集合
    记录已经访问过的下标，避免死循环（因为数组长度有限，
    如果跳回之前访问过的位置，说明形成了环）。

    算法步骤：
    1. 如果当前下标超出数组范围，返回 False
    2. 如果当前下标已经访问过，返回 False（避免死循环）
    3. 如果 arr[i] == 0，到达目标，返回 True
    4. 标记当前下标为已访问
    5. 递归搜索 i + arr[i] 和 i - arr[i]
    6. 只要有任意一条路径能找到 0，就返回 True

    图解：arr = [4, 2, 3, 0, 3, 1, 2], start = 5
    ────────────────────────────────────────────────────────
    索引:  0    1    2    3    4    5    6
    值:   [4,   2,   3,   0,   3,   1,   2]
                               ↑
                             终点

    从 start=5 出发的搜索树：
                    ┌── 5 ──┐
                    ↓       ↓
                  5+1=6    5-1=4
                    ↓       ↓
                  6+2=8 ❌  4+3=7 ❌
                  6-2=4     4-3=1
                    ↓       ↓
                    (已访) 1+2=3 ✅ → 返回 True
                          1-2=-1 ❌

    时间复杂度：O(n) — 每个下标最多访问一次
    空间复杂度：O(n) — visited 数组 + 递归栈深度（最坏 O(n)）
    """
    n = len(arr)
    visited = [False] * n

    def dfs(i: int) -> bool:
        # 超出数组范围
        if i < 0 or i >= n:
            return False
        # 已经访问过，说明陷入循环
        if visited[i]:
            return False
        # 找到值为 0 的下标
        if arr[i] == 0:
            return True

        # 标记已访问
        visited[i] = True

        # 向左右两个方向探索
        # 只要有一条路径能找到 0 就返回 True
        return dfs(i + arr[i]) or dfs(i - arr[i])

    return dfs(start)


# ══════════════════════════════════════════════════════════
# 解法二：BFS + 已访问集合（迭代，推荐解）
# ══════════════════════════════════════════════════════════

def canReach_bfs(arr: List[int], start: int) -> bool:
    """
    BFS + 已访问集合（迭代）⭐⭐⭐

    核心思路：
    ────────────────────────────────────────────────────────
    使用队列进行广度优先搜索，从 start 开始逐层向外探索。
    每次从队列中取出一个下标 i：
        - 如果 arr[i] == 0，返回 True
        - 否则将 i + arr[i] 和 i - arr[i]（在数组范围内且未访问）
          加入队列继续搜索

    BFS 适合这道题的原因：
    ────────────────────────────────────────────────────────
    1. ⭕ 无向图特性：每个下标有两个邻居（左跳、右跳），
       这是一个标准的图可达性问题，BFS 是最直观的解法
    2. ✅ 能找到任意一个目标即可，不需要找最短路径，
       BFS 和 DFS 在效率上没有本质区别
    3. 🚫 不存在权重（步长固定），所以 BFS 天然适合

    图解示例：arr = [4, 2, 3, 0, 3, 1, 2], start = 5
    ────────────────────────────────────────────────────────
    BFS 队列过程（queue = head -> tail）：

    第 0 层：[5]
        弹出 5，arr[5]=1≠0
        入队：5+1=6（有效）、5-1=4（有效）
        队列 → [6, 4]

    第 1 层：[6, 4]
        弹出 6，arr[6]=2≠0
        入队：6+2=8 ❌（越界跳过）、6-2=4（已访问跳过）
        队列 → [4]

        弹出 4，arr[4]=3≠0
        入队：4+3=7 ❌（越界跳过）、4-3=1（有效）
        队列 → [1]

    第 2 层：[1]
        弹出 1，arr[1]=2≠0
        入队：1+2=3 ✅（arr[3]==0，找到！）
        返回 True

    ⚡ 注意：这里 BFS 的好处是遇到目标就立刻返回，
       不需要探索整个图。
       但如果图中有环（从多个路径到达同一节点），
       visited 集合保证每个节点只入队一次。

    时间复杂度：O(n) — 每个下标最多入队一次
    空间复杂度：O(n) — 队列 + visited 数组
    """
    n = len(arr)
    visited = [False] * n
    queue = [start]
    visited[start] = True

    while queue:
        i = queue.pop(0)

        # 找到目标
        if arr[i] == 0:
            return True

        # 向左右两个方向探索
        for jump in (i + arr[i], i - arr[i]):
            # 检查是否在数组范围内且未访问过
            if 0 <= jump < n and not visited[jump]:
                visited[jump] = True
                queue.append(jump)

    return False


